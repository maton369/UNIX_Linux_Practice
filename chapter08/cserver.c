/* socket/cserver.c */
#include <arpa/inet.h>  /* htons(), htonl() を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), perror() を使うためのヘッダ */
#include <stdlib.h>     /* exit() を使うためのヘッダ */
#include <string.h>     /* memset(), strlen() を使うためのヘッダ */
#include <sys/types.h>  /* accept(), bind(), fork(), setsockopt(),
                           socket(), waitpid(), write() に関係する型定義 */
#include <sys/socket.h> /* socket(), setsockopt(), bind(), listen(),
                           accept(), shutdown() を使うためのヘッダ */
#include <sys/uio.h>    /* write() の宣言が入ることがある */
#include <sys/wait.h>   /* waitpid() を使うためのヘッダ */
#include <unistd.h>     /* close(), fork(), write() を使うためのヘッダ */

#define SERVER_ADDR     "127.0.0.1"
#define SERVER_PORT     12345

enum {
    /*
     * listen() に渡す backlog の目安である。
     *
     * サーバへ複数の接続要求が同時に来たとき、
     * accept() される前の要求はカーネル内の待ち行列に一時的に並ぶ。
     *
     * この値は、その待ち行列をどの程度まで受け止めるかの目安になる。
     * ここでは 5 件ぶんとしている。
     */
    NQUEUESIZE = 5,     /* listenのための待ち行列枠数 */
};

/*
 * クライアントへ返す固定メッセージである。
 *
 * このサーバは「接続してきた相手へ固定文を送って切る」簡単な応答サーバであり、
 * 受け取ったデータを解析したり、内容に応じて応答を変えたりはしていない。
 */
char *message = "Hello!\nGood-bye!!\n";

int main(void) {
    /*
     * s:
     *   待ち受け用ソケット (listening socket) である。
     *
     *   これはサーバ全体の「受付窓口」に相当する。
     *   bind() と listen() を経て、接続要求を受け付けるためのソケットになる。
     *
     * ws:
     *   accept() が返す「接続済みソケット (connected socket)」である。
     *
     *   実際のクライアントとの送受信は、この ws を使って行う。
     *   1 接続ごとに 1 個ずつ新しく作られる。
     *
     * soval:
     *   setsockopt() で SO_REUSEADDR を有効にするための設定値。
     *
     * cc:
     *   write() が実際に書き込んだバイト数を受ける変数。
     *
     * sa:
     *   サーバ自身の待受先アドレス情報を入れる sockaddr_in 構造体。
     *
     * ca:
     *   接続してきたクライアント側のアドレス情報を受け取る sockaddr_in 構造体。
     *
     * ca_len:
     *   accept() に渡す、ca 構造体のサイズ。
     *   戻ってきたあとには実際に書き込まれたサイズが入る。
     */
    int s, ws, soval, cc;
    struct sockaddr_in sa, ca;
    socklen_t ca_len;

    /*
     * ------------------------------------------------------------
     * 1. socket() で待ち受け用ソケットを作る
     * ------------------------------------------------------------
     *
     * socket(AF_INET, SOCK_STREAM, 0)
     *
     * 各引数の意味:
     *
     *   AF_INET:
     *     IPv4 用ソケットであることを示す。
     *
     *   SOCK_STREAM:
     *     ストリーム型ソケットであることを示す。
     *     通常これは TCP を意味すると考えてよい。
     *
     *   0:
     *     既定のプロトコルを使う。
     *     AF_INET + SOCK_STREAM の組み合わせでは通常 TCP になる。
     *
     * 戻り値:
     *   成功時:
     *     新しいソケット fd を返す。
     *
     *   失敗時:
     *     -1 を返す。
     *
     * 重要:
     *   この時点では、まだ
     *     - どのポートで待つか
     *     - どのアドレスで待つか
     *     - サーバとして接続を受けるか
     * は決まっていない。
     *
     *   まだ「TCP 用の通信口を 1 個作った」だけである。
     */
    if ((s = socket(AF_INET, SOCK_STREAM, 0)) == -1) {
        perror("socket");
        exit(1);
    }

    /*
     * ------------------------------------------------------------
     * 2. setsockopt() で SO_REUSEADDR を設定する
     * ------------------------------------------------------------
     *
     * setsockopt() はソケットに各種オプションを設定するシステムコールである。
     *
     * 今回使っている SO_REUSEADDR は、
     * 「同じローカルアドレス / ポートの再利用をしやすくする」設定である。
     *
     * サーバを停止してすぐに再起動すると、
     * 以前の接続の名残で bind() が失敗することがある。
     * 開発・実験時には SO_REUSEADDR を有効にしておくのが定石である。
     *
     * 各引数の意味:
     *
     *   s:
     *     設定対象のソケット fd
     *
     *   SOL_SOCKET:
     *     ソケットレベルのオプションであることを示す
     *
     *   SO_REUSEADDR:
     *     再利用許可オプション
     *
     *   &soval:
     *     実際の設定値へのポインタ
     *     ここでは int 型の 1 を渡して有効化する
     *
     *   sizeof(soval):
     *     設定値の大きさ
     */
    soval = 1;
    if (setsockopt(s, SOL_SOCKET, SO_REUSEADDR, &soval, sizeof(soval))
          == -1) {
        perror("setsockopt");
        exit(1);
    }

    /*
     * ------------------------------------------------------------
     * 3. bind() 用の sockaddr_in を組み立てる
     * ------------------------------------------------------------
     *
     * bind() はソケットをローカルのアドレス / ポート番号に結び付ける。
     * そのために sockaddr_in sa を準備する。
     */

    /*
     * 構造体全体を 0 で初期化する。
     *
     * 理由:
     *   sockaddr_in の未使用領域や予約領域にゴミ値が残らないようにするため。
     *   ネットワーク構造体はまず memset でゼロクリアしてから必要項目だけ埋めるのが定石である。
     */
    memset(&sa, 0, sizeof(sa));

    /*
     * BSD 系には sin_len があることがあるが、
     * Linux の struct sockaddr_in には通常存在しない。
     *
     * Linux ではこの行はコンパイルエラーになるため削除する必要がある。
     *
     * そのためここではコメントアウトしておく。
     */
    /* sa.sin_len = sizeof(sa); */     /* sin_len がある場合。Linux では通常不要 */

    /*
     * このアドレスが IPv4 用であることを示す。
     *
     * socket() 側でも AF_INET を指定しているので、
     * ここでも同じアドレスファミリを設定して整合を取る。
     */
    sa.sin_family = AF_INET;

    /*
     * 待ち受けるポート番号を設定する。
     *
     * SERVER_PORT は 12345 である。
     *
     * htons():
     *   host to network short の略。
     *   ホストバイトオーダの 16bit 整数を、ネットワークバイトオーダへ変換する。
     *
     * TCP/IP ではポート番号をネットワークバイトオーダで扱う必要があるため、
     * 必ず htons() を通す。
     */
    sa.sin_port = htons(SERVER_PORT);

    /*
     * 待ち受けアドレスを設定する。
     *
     * INADDR_ANY は
     *   「このホスト上のどのローカル IPv4 アドレス宛てでも受け付ける」
     * ことを表す特別な値である。
     *
     * つまり 127.0.0.1 だけでなく、
     * マシンが持つ他の IPv4 アドレスにも対応できる。
     *
     * htonl():
     *   host to network long の略。
     *   32bit 値をネットワークバイトオーダへ変換する。
     */
    sa.sin_addr.s_addr = htonl(INADDR_ANY);

    /*
     * ------------------------------------------------------------
     * 4. bind() でソケットにローカルアドレスを結び付ける
     * ------------------------------------------------------------
     *
     * bind(s, (struct sockaddr *)&sa, sizeof(sa))
     *
     * bind() は、
     *   「このソケットをこのローカル IP / ポート番号に対応付ける」
     * システムコールである。
     *
     * サーバでは bind() によって初めて
     *   「どのポートで待つサーバなのか」
     * が決まる。
     *
     * ここでは 12345 番ポートで待ち受ける。
     *
     * 戻り値:
     *   成功時 0
     *   失敗時 -1
     */
    if (bind(s, (struct sockaddr *)&sa, sizeof(sa)) == -1) {
        perror("bind");
        exit(1);
    }

    /*
     * ------------------------------------------------------------
     * 5. listen() で待ち受け状態にする
     * ------------------------------------------------------------
     *
     * bind() をしただけではまだ接続要求は受け付けられない。
     * そのソケットを待ち受け用ソケットとして有効化するのが listen() である。
     *
     * listen(s, NQUEUESIZE)
     *
     * これは概念的には
     *   「このソケットをサーバの受付窓口にし、
     *    未 accept の接続要求をある程度キューに貯められるようにする」
     * 操作である。
     *
     * この時点で s は「待ち受けソケット」になる。
     */
    if (listen(s, NQUEUESIZE)) {
        perror("listen");
        exit(1);
    }

    /*
     * 初期化完了。
     *
     * ここまで成功するとサーバは接続待ち可能な状態になっている。
     */
    fprintf(stderr, "Ready.\n");

    /*
     * ------------------------------------------------------------
     * 6. 反復サーバ本体
     * ------------------------------------------------------------
     *
     * このサーバは無限ループで接続要求を受け続ける。
     *
     * ただし iterative server（1接続ずつ順番に処理するサーバ）ではなく、
     * accept() したあと fork() して
     * 子プロセスがその接続を処理する concurrent server（並行サーバ）
     * の形になっている。
     *
     * つまり:
     *
     *   親:
     *     accept() を繰り返し、新しい接続を受け続ける
     *
     *   子:
     *     1件の接続に対して応答して終了する
     *
     * という役割分担である。
     */
    for (;;) {
        /*
         * forkcc:
         *   fork() の戻り値を受け取る変数。
         *
         * status:
         *   waitpid() で回収した子プロセスの終了状態を受ける変数。
         */
        pid_t forkcc;
        int status;

        /*
         * --------------------------------------------------------
         * accept() で待たされる前に、終了済み子プロセスを回収する
         * --------------------------------------------------------
         *
         * 子プロセスが終了すると、親が wait()/waitpid() で回収するまで
         * ゾンビプロセスになる。
         *
         * このコードでは、
         *   while (waitpid(-1, &status, WNOHANG) > 0);
         *
         * とすることで、
         * 「すでに終わっている子がいれば、全部回収しておく」
         * 処理をしている。
         *
         * waitpid() の各引数:
         *
         *   第1引数 -1:
         *     任意の子プロセスを対象にする
         *
         *   第2引数 &status:
         *     終了状態の格納先
         *
         *   第3引数 WNOHANG:
         *     まだ誰も終了していないならブロックせずすぐ戻る
         *
         * 戻り値:
         *   >0 : 回収した子の PID
         *    0 : 今回は回収対象なし
         *   -1 : エラー
         *
         * ここでは >0 の間だけ繰り返して、
         * たまっていたゾンビをまとめて掃除している。
         *
         * つまりこれは
         *   「accept 前の幽霊掃除」
         * である。
         */
        while (waitpid(-1, &status, WNOHANG) > 0);

        /*
         * --------------------------------------------------------
         * 7. accept() で新しい接続を 1 件受け入れる
         * --------------------------------------------------------
         *
         * 親プロセスは待ち受けソケット s に対して accept() を呼び、
         * 新しい接続要求が来るまで待つ。
         */
        fprintf(stderr, "Waiting for a connection...\n");

        /*
         * accept() 呼び出し前に、ca 構造体のサイズを設定しておく。
         *
         * 戻ってきた後は ca にクライアントアドレスが入り、
         * ca_len には実際に使われたサイズが入る。
         */
        ca_len = sizeof(ca);

        /*
         * accept(s, (struct sockaddr *)&ca, &ca_len)
         *
         * これは待ち受けソケット s に届いた接続要求を 1 件受理し、
         * その接続専用の新しいソケット fd を返す。
         *
         * 非常に重要:
         *   s は「待ち受け専用」で残り続ける。
         *   実際の会話は新しい ws で行う。
         *
         * つまり
         *   s  = 受付窓口
         *   ws = 今回のクライアントとの会話用ソケット
         * という分担になる。
         */
        if ((ws = accept(s, (struct sockaddr *)&ca, &ca_len)) == -1) {
            perror("accept");
            exit(1);
        }
        fprintf(stderr, "Connection established.\n");

        /*
         * --------------------------------------------------------
         * 8. fork() して接続処理を子プロセスへ委譲する
         * --------------------------------------------------------
         *
         * concurrent server の要点はここである。
         *
         * 親が accept() したあと fork() し、
         * 子プロセスが今回の接続 ws を処理する。
         *
         * その間、親は ws を閉じてすぐ次の accept() に戻れるため、
         * 別のクライアントを並行して受け付けやすくなる。
         */
        if ((forkcc = fork()) == -1) {
            perror("fork");
            exit(1);
        } else if (forkcc == 0) {
            /* 子プロセス */

            /*
             * 子プロセスは今回の接続 ws を担当する。
             *
             * 子には待ち受けソケット s は不要である。
             * なぜなら新しい接続を accept() する役目は親が持つからである。
             *
             * 不要な fd を閉じるのは、
             *   - 資源節約
             *   - 役割の明確化
             *   - 意図しない fd 共有を避ける
             * ために重要である。
             */
            if (close(s) == -1) {
                perror("child: close listening socket");
                exit(1);
            }

            /*
             * ----------------------------------------------------
             * 9. 子プロセスがこの接続へ応答する
             * ----------------------------------------------------
             *
             * 今回のサーバは固定メッセージを 1 回送るだけである。
             */
            fprintf(stderr, "Sending the message...\n");

            /*
             * write(ws, message, strlen(message))
             *
             * 接続済みソケット ws へ固定文字列を送る。
             *
             * strlen(message) を使っているので、
             * C 文字列終端の '\0' は送らない。
             *
             * 送信内容は:
             *
             *   Hello!
             *   Good-bye!!
             *
             * の 2 行である。
             *
             * 戻り値:
             *   成功時:
             *     実際に書き込んだバイト数
             *
             *   失敗時:
             *     -1
             *
             * なお、TCP はバイトストリームなので、
             * 1 回の write() が相手の 1 回の read() に対応するとは限らない。
             *
             * また、厳密には部分書き込みもありうるので、
             * 実務コードなら全量送信ループが望ましい。
             */
            if ((cc = write(ws, message, strlen(message))) == -1) {
                perror("child: write");
                exit(1);
            }
            fprintf(stderr, "Message sent.\n");

            /*
             * ----------------------------------------------------
             * 10. shutdown() でこの接続の送受信を停止する
             * ----------------------------------------------------
             *
             * shutdown(ws, SHUT_RDWR)
             *
             * SHUT_RDWR は
             *   「このソケットではもう読みも書きもしない」
             * という意味である。
             *
             * close() と違って、
             * shutdown() は通信方向の停止を明示するシステムコールである。
             *
             * 小さなサンプルでは close() だけでもよいことは多いが、
             * shutdown() を入れると
             *   「通信停止」と「fd 解放」が別の概念である
             * ことが分かりやすい。
             */
            if (shutdown(ws, SHUT_RDWR) == -1) {
                perror("child: shutdown");
                exit(1);
            }

            /*
             * ----------------------------------------------------
             * 11. 接続済みソケット ws を閉じる
             * ----------------------------------------------------
             *
             * 子プロセスは今回の 1 接続を処理し終えたので、
             * ws はもう不要である。
             *
             * close() により、この接続専用ソケット fd を解放する。
             */
            if (close(ws) == -1) {
                perror("child: close");
                exit(1);
            }

            /*
             * 子プロセスは今回の接続処理だけを担当するので、
             * 仕事が終わったら終了する。
             *
             * 親は別途 waitpid(WNOHANG) によってこの終了をあとで回収する。
             */
            exit(0);    /* 子プロセスの終了 */
        }

        /* 親プロセス */

        /*
         * 親プロセスは「今回の接続を処理する役」は子に渡した。
         * したがって親では接続済みソケット ws は不要である。
         *
         * ここで親が ws を閉じておくことで、
         *   - 親は次の accept() に専念できる
         *   - 不要な fd を持ち続けない
         * という利点がある。
         *
         * これが concurrent server での典型的な役割分担である。
         */
        if (close(ws) == -1) {
            perror("close");
            exit(1);
        }

        /*
         * ここまでで親は今回の接続を子へ委譲し終えた。
         *
         * ループ先頭へ戻り、次の接続要求をまた accept() する。
         * したがって複数のクライアントを時間的に重ねて処理しやすくなる。
         */
        /* 次の要求の受け入れへ */
    }

    /*
     * 無限ループサーバなので通常ここへは到達しない。
     */
    /* 無限ループ */
}
