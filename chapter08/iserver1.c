/* socket/iserver1.c */
#include <arpa/inet.h>  /* htons(), htonl() を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), perror() を使うためのヘッダ */
#include <stdlib.h>     /* exit() を使うためのヘッダ */
#include <string.h>     /* memset(), strlen() を使うためのヘッダ */
#include <sys/types.h>  /* accept(), bind(), setsockopt(), socket(), write() に関係する型定義 */
#include <sys/socket.h> /* socket(), setsockopt(), bind(), listen(), accept(), shutdown() を使うためのヘッダ */
#include <sys/uio.h>    /* write() の宣言が入ることがある */
#include <unistd.h>     /* close(), write() を使うためのヘッダ */

#define SERVER_ADDR     "127.0.0.1"
#define SERVER_PORT     12345

enum {
    /*
     * listen() が保持する待ち行列（backlog）の大きさである。
     *
     * サーバに対してクライアントが接続要求を送ると、
     * その要求はカーネル内部の待ち行列に一時的に並ぶ。
     *
     * listen(s, NQUEUESIZE) は、
     * 「この待ち行列を最大どれくらい受け止めるか」の目安を与える。
     *
     * ここでは 5 としているので、
     * 同時に到着した複数の接続要求をある程度までは待たせられる。
     */
    NQUEUESIZE = 5,     /* listenのための待ち行列枠数 */
};

/*
 * クライアントへ送る固定メッセージである。
 *
 * このサーバは対話型ではなく、
 * 接続してきたクライアントへ毎回この文字列を送って接続を閉じる
 * シンプルな「一問一答型サーバ」になっている。
 */
char *message = "Hello!\nGood-bye!!\n";

int main(void) {
    /*
     * s:
     *   listen 用の「待ち受けソケット (listening socket)」である。
     *
     *   これはサーバ全体の玄関口に相当する。
     *   このソケット自体で個々のクライアントとデータ交換するのではなく、
     *   「新しい接続要求を受け付けるため」に使う。
     *
     * ws:
     *   accept() によって返される「接続済みソケット」である。
     *
     *   クライアントと実際にデータを送受信するのはこちらである。
     *   1 回の accept() につき 1 個の接続済みソケットが作られる。
     *
     * soval:
     *   setsockopt() で SO_REUSEADDR を有効にするときに渡す値。
     *   1 を入れて「再利用を有効にする」という意味にしている。
     *
     * cc:
     *   write() が実際に書き込んだバイト数を受け取る変数。
     *
     * sa:
     *   サーバ自身のアドレス情報（どの IP / ポートで待ち受けるか）を表す構造体。
     *
     * ca:
     *   接続してきたクライアント側のアドレス情報を受け取る構造体。
     *
     * ca_len:
     *   accept() に渡す「ca 構造体のサイズ」であり、
     *   戻り時には実際に書き込まれたサイズが入る。
     */
    int s, ws, soval, cc;
    struct sockaddr_in sa, ca;
    socklen_t ca_len;

    /*
     * ------------------------------------------------------------
     * 1. socket() で待ち受け用ソケットを作る
     * ------------------------------------------------------------
     *
     * socket(AF_INET, SOCK_STREAM, 0)
     *
     * これは「IPv4 / TCP 用の通信端点」を 1 個作るシステムコールである。
     *
     * 各引数の意味:
     *
     *   AF_INET:
     *     IPv4 アドレス体系を使うことを表す。
     *
     *   SOCK_STREAM:
     *     ストリーム型ソケットを使うことを表す。
     *     通常これは TCP を意味すると考えてよい。
     *
     *   0:
     *     デフォルトプロトコルを使う。
     *     AF_INET + SOCK_STREAM の組み合わせでは実質 TCP になる。
     *
     * 戻り値:
     *   成功時:
     *     新しいソケット fd を返す
     *
     *   失敗時:
     *     -1 を返す
     *
     * 重要:
     *   この時点ではまだ
     *     - どのポートで待つか
     *     - どのアドレスに束縛されるか
     *     - 待ち受け状態にするか
     * は決まっていない。
     *
     *   ただ「TCP 用のソケット fd が 1 個できた」だけである。
     */
    if ((s = socket(AF_INET, SOCK_STREAM, 0)) == -1) {
        perror("socket");
        exit(1);
    }

    /*
     * ------------------------------------------------------------
     * 2. setsockopt() で SO_REUSEADDR を設定する
     * ------------------------------------------------------------
     *
     * setsockopt() は、ソケットに対して各種オプションを設定するシステムコールである。
     *
     * 今回使っているのは
     *
     *   SOL_SOCKET, SO_REUSEADDR
     *
     * であり、これは
     *   「ローカルアドレス再利用を許可する」
     * オプションである。
     *
     * サーバプログラムを再起動した直後などでは、
     * 以前使っていたポートが TIME_WAIT 等の都合で
     * しばらく再利用できず bind() に失敗することがある。
     *
     * SO_REUSEADDR を設定しておくと、
     * 開発中や実験中に同じポートでサーバを立て直しやすくなる。
     *
     * 各引数の意味:
     *
     *   第1引数 s:
     *     設定対象のソケット fd
     *
     *   第2引数 SOL_SOCKET:
     *     ソケットレベルのオプションであることを示す
     *
     *   第3引数 SO_REUSEADDR:
     *     再利用を許可するオプション名
     *
     *   第4引数 &soval:
     *     実際の設定値へのポインタ
     *     ここでは int 型の 1 を渡して「有効化」している
     *
     *   第5引数 sizeof(soval):
     *     設定値のバイト数
     *
     * 戻り値:
     *   成功時 0
     *   失敗時 -1
     */
    soval = 1;
    if (setsockopt(s, SOL_SOCKET, SO_REUSEADDR, &soval, sizeof(soval)) == -1) {
        perror("setsockopt");
        exit(1);
    }

    /*
     * ------------------------------------------------------------
     * 3. bind() 用の sockaddr_in を組み立てる
     * ------------------------------------------------------------
     *
     * bind() は
     *   「このソケットをどのローカルアドレス / ポート番号に結びつけるか」
     * を OS に伝えるシステムコールである。
     *
     * そのために、まず sockaddr_in 構造体 sa を作る。
     */

    /*
     * memset(&sa, 0, sizeof(sa));
     *
     * sockaddr_in 全体を 0 で初期化する。
     *
     * 理由:
     *   構造体の使わない領域や予約領域にゴミ値が残らないようにするため。
     *
     * ネットワーク系の構造体では、まずゼロクリアしてから必要な項目だけ埋めるのが定石である。
     */
    memset(&sa, 0, sizeof(sa));

    /*
     * sa.sin_len = sizeof(sa);
     *
     * これは BSD 系に存在することがあるフィールドで、
     * sockaddr 構造体の長さを表す。
     *
     * ただし Linux の struct sockaddr_in には通常 sin_len が無い。
     * そのため Linux ではこの行は削除する必要がある。
     *
     * つまり、このコードを Linux でコンパイルするなら次のようにする。
     *
     *   // sa.sin_len = sizeof(sa);
     *
     * 学習上のポイント:
     *   socket API は概念は共通でも、
     *   sockaddr 構造体の細部は BSD 系と Linux で差異がある。
     */
    /* sa.sin_len = sizeof(sa); */     /* sin_len がある場合。Linux では通常不要 */

    /*
     * sa.sin_family = AF_INET;
     *
     * このアドレスが IPv4 用であることを表す。
     *
     * socket() 側も AF_INET だったので、
     * ここでも同じアドレスファミリを設定して整合を取る。
     */
    sa.sin_family = AF_INET;

    /*
     * sa.sin_port = htons(SERVER_PORT);
     *
     * 待ち受けるポート番号を設定する。
     *
     * SERVER_PORT は 12345 である。
     *
     * ここで htons() を使う理由:
     *   TCP/IP のヘッダや sockaddr 内の数値は
     *   ネットワークバイトオーダ（ビッグエンディアン）で保持する必要があるため。
     *
     * htons:
     *   host to network short
     *
     * つまり
     *   「ホスト内部表現の 16bit 整数を、ネットワーク用表現へ変換する」
     * 関数である。
     */
    sa.sin_port = htons(SERVER_PORT);

    /*
     * sa.sin_addr.s_addr = htonl(INADDR_ANY);
     *
     * ここはサーバ側で特に重要な部分である。
     *
     * INADDR_ANY は
     *   「このホスト上のどのローカル IP アドレス宛てでも受け付ける」
     * ことを表す特別な値である。
     *
     * つまり 127.0.0.1 だけでなく、
     * マシンが持つ他の IPv4 アドレス宛ての接続要求も受けられる。
     *
     * htonl:
     *   host to network long
     *
     * INADDR_ANY は 32bit 値なので htonl() を通している。
     *
     * なお、SERVER_ADDR というマクロはこのコードでは実際には使っていない。
     * サーバ側は特定アドレスに限定せず INADDR_ANY で待っているためである。
     */
    sa.sin_addr.s_addr = htonl(INADDR_ANY);

    /*
     * ------------------------------------------------------------
     * 4. bind() でソケットに「名前」を付ける
     * ------------------------------------------------------------
     *
     * bind(s, (struct sockaddr *)&sa, sizeof(sa))
     *
     * bind() は、
     *   「このソケット fd を、ローカルのこのアドレス / ポートに対応付ける」
     * システムコールである。
     *
     * サーバにとって bind() は非常に重要で、
     * これを行って初めて
     *   「どのポートで待つサーバなのか」
     * が決まる。
     *
     * ここでいう「ソケットに名前を付ける」とは、
     * UNIX の「名前付きソケット」ではなく、
     * ローカルの IP / ポート番号に結び付けるという意味である。
     *
     * 各引数:
     *
     *   s:
     *     待ち受けに使うソケット
     *
     *   (struct sockaddr *)&sa:
     *     結び付けたいローカルアドレス情報
     *
     *   sizeof(sa):
     *     アドレス構造体のサイズ
     *
     * 戻り値:
     *   成功時 0
     *   失敗時 -1
     *
     * 失敗理由の例:
     *   - そのポートがすでに使われている
     *   - 権限不足
     *   - アドレス構造が不正
     */
    if (bind(s, (struct sockaddr *)&sa, sizeof(sa)) == -1) {
        perror("bind");
        exit(1);
    }

    /*
     * ------------------------------------------------------------
     * 5. listen() で待ち受け状態にする
     * ------------------------------------------------------------
     *
     * bind() をしただけでは、まだ「接続要求を受けるサーバ」にはなっていない。
     * そのソケットを待ち受けソケットとして有効化するのが listen() である。
     *
     * listen(s, NQUEUESIZE)
     *
     * これは概念的には
     *   「このソケットを受け付け窓口にし、未 accept の接続要求を
     *    ある程度キューに貯められるようにする」
     * 操作である。
     *
     * 戻り値:
     *   成功時 0
     *   失敗時 -1
     *
     * ここで重要なのは、
     *   socket -> bind -> listen
     * の順序で初めて「サーバが待受可能」になることだ。
     *
     * つまり listen() は
     *   「このソケットをサーバの玄関口にする」
     * システムコールである。
     */
    if (listen(s, NQUEUESIZE)) {
        perror("listen");
        exit(1);
    }

    /*
     * ここまで成功すると、
     * s は「待ち受けソケット」として完成している。
     *
     * つまりサーバは
     *   - ポート 12345 に束縛済み
     *   - 接続要求を待ち受け中
     * の状態になっている。
     */
    fprintf(stderr, "Ready.\n");

    /*
     * ------------------------------------------------------------
     * 6. accept() による接続受け入れループ
     * ------------------------------------------------------------
     *
     * このサーバは無限ループで
     *
     *   accept() -> write() -> shutdown() -> close()
     *
     * を繰り返し、クライアントが来るたびに同じメッセージを返す。
     *
     * つまり「1接続ごとに応答して切断する」反復サーバである。
     */
    for (;;) {
        /*
         * accept() は待ち受けソケット s に対して届いた
         * 次の接続要求を 1 件取り出し、
         * その接続専用の新しいソケット fd を返す。
         *
         * 非常に重要:
         *   listen 用ソケット s はそのまま残る。
         *   つまり
         *
         *     s  = 受付窓口
         *     ws = 実際の会話用ソケット
         *
         * という役割分担になる。
         *
         * ここを理解すると accept() の本質が分かる。
         */

        fprintf(stderr, "Waiting for a connection...\n");

        /*
         * accept() はクライアントアドレスを書き込むために、
         * 事前に ca_len に「ca のサイズ」を入れておく必要がある。
         *
         * 戻ってきた後は、ca にクライアントのアドレス情報、
         * ca_len には実際に使われたサイズが入る。
         */
        ca_len = sizeof(ca);

        /*
         * accept(s, (struct sockaddr *)&ca, &ca_len)
         *
         * 第1引数:
         *   s
         *   -> 待ち受けソケット
         *
         * 第2引数:
         *   クライアントアドレスを書き込む領域
         *
         * 第3引数:
         *   その領域サイズへのポインタ
         *
         * 戻り値:
         *   成功時:
         *     新しい接続済みソケット fd を返す
         *
         *   失敗時:
         *     -1
         *
         * この新しい fd が ws であり、
         * 今回のクライアントとの実際の通信は ws を使って行う。
         *
         * つまり accept() は、
         *   「待っていた接続要求を 1 件受理し、その会話専用 fd を作る」
         * システムコールである。
         */
        if ((ws = accept(s, (struct sockaddr *)&ca, &ca_len)) == -1) {
            perror("accept");
            exit(1);
        }
        fprintf(stderr, "Connection established.\n");

        /*
         * ------------------------------------------------------------
         * 7. 接続済みソケット ws に対して write() で応答する
         * ------------------------------------------------------------
         *
         * accept() に成功すると ws は接続済み TCP ストリームになる。
         * そのため通常ファイルのように write() が使える。
         *
         * このコードでは固定メッセージを 1 回だけ送っている。
         */
        fprintf(stderr, "Sending the message...\n");

        /*
         * write(ws, message, strlen(message))
         *
         * ws に対して message の内容を送信する。
         *
         * strlen(message) を使っているので、
         * 末尾の '\0' は送らず、
         * 実際の文字列内容だけを送る。
         *
         * 送る内容は
         *
         *   Hello!
         *   Good-bye!!
         *
         * の 2 行である。
         *
         * 戻り値:
         *   成功時:
         *     実際に書き込めたバイト数
         *
         *   失敗時:
         *     -1
         *
         * 注意:
         *   TCP はストリームなので、
         *   1 回の write() が相手側の 1 回の read() に対応するとは限らない。
         *   ただしこの例では小さな固定メッセージなので概念説明には十分である。
         *
         * また、厳密には部分書き込みの可能性もあるので、
         * 実務コードならループで全量送信を確認したい。
         */
        if ((cc = write(ws, message, strlen(message))) == -1) {
            perror("write");
            exit(1);
        }
        fprintf(stderr, "Message sent.\n");

        /*
         * ------------------------------------------------------------
         * 8. shutdown() でこの接続の通信を止める
         * ------------------------------------------------------------
         *
         * shutdown(ws, SHUT_RDWR)
         *
         * これは接続済みソケット ws の
         *   - 読み取り方向
         *   - 書き込み方向
         * の両方を停止する。
         *
         * SHUT_RDWR は
         *   「この接続ではもう送受信しない」
         * ことを明示的に伝える指定である。
         *
         * 単純な短命接続では close() だけでも終われる場合は多いが、
         * shutdown() を挟むことで
         * 「通信を止める」と「fd を捨てる」を分離して見せている。
         */
        if (shutdown(ws, SHUT_RDWR) == -1) {
            perror("shutdown");
            exit(1);
        }

        /*
         * ------------------------------------------------------------
         * 9. close() で接続済みソケット ws を閉じる
         * ------------------------------------------------------------
         *
         * ws は今回のクライアント専用の会話用ソケットである。
         * もう応答は終わったので close() で資源を解放する。
         *
         * 重要:
         *   ここで閉じるのは ws だけであり、
         *   待ち受けソケット s は閉じない。
         *
         *   だからサーバは次のクライアントも accept() できる。
         *
         * これが
         *   「listen 用ソケットは長寿命、接続済みソケットは接続ごとに短命」
         * というサーバプログラムの基本構造である。
         */
        if (close(ws) == -1) {
            perror("close");
            exit(1);
        }

        /*
         * ここまでで 1 接続分の処理が終わった。
         *
         * ループ先頭へ戻り、次の接続要求を accept() してまた同じ処理を行う。
         */
        /* 次の要求の受け入れへ */
    }

    /*
     * このプログラムは無限ループサーバなので通常ここへは到達しない。
     * したがって return 文は省略されている。
     */
    /* 無限ループ */
}
