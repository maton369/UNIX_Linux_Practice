/* fs/showmode.c */
#include <stdio.h>      /* perror, puts を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* stat 関連の型定義が入ることがある */
#include <sys/stat.h>   /* struct stat, stat(), S_IRUSR などの権限ビット定数を使うためのヘッダ */
#include <unistd.h>     /* stat() を使うためのヘッダ */

/*
 * このプログラムの主題は、
 *
 *   「stat() で取得した st_mode から、UNIX 風の権限文字列
 *    rwxrwxrwx 形式を自分で組み立てて表示する」
 *
 * ことである。
 *
 * たとえば ls -l の一部では
 *
 *   rw-r--r--
 *   rwxr-xr-x
 *
 * のような表示を見ることがあるが、
 * このコードはその「権限9文字部分」だけを自前で生成している。
 *
 * つまりこのプログラムは、
 *
 *   1. 対象パスに対して stat() を行う
 *   2. st_mode に入っている権限ビットを調べる
 *   3. 各ビットに対応する文字を perm[] に埋める
 *   4. 完成した文字列を表示する
 *
 * というアルゴリズムで動く。
 *
 * 重要なのは、st_mode は単なる1個の整数ではあるが、
 * その中に
 *
 *   - ファイル種別ビット
 *   - 所有者/グループ/その他の権限ビット
 *   - setuid / setgid / sticky bit
 *
 * などがビット単位で詰め込まれていることである。
 *
 * このコードでは、そのうち主に
 *
 *   S_IRUSR, S_IWUSR, S_IXUSR
 *   S_IRGRP, S_IWGRP, S_IXGRP
 *   S_IROTH, S_IWOTH, S_IXOTH
 *   S_ISUID, S_ISGID, S_ISVTX
 *
 * を読んで、人間が見慣れた
 *
 *   rwxrwxrwx
 *
 * 形式へ変換している。
 *
 * つまりこのプログラムは、
 *
 *   「ビット表現された権限情報を、人間向けの記号列へデコードする」
 *
 * 小さなデコーダである。
 */

enum {
    /*
     * PERMLEN = 9
     *
     * UNIX 風権限文字列の長さである。
     *
     * 内訳は
     *
     *   0 1 2   3 4 5   6 7 8
     *   r w x   r w x   r w x
     *   ^^^^^   ^^^^^   ^^^^^
     *   owner   group   other
     *
     * の9文字で構成されるため、長さは 9。
     *
     * たとえば
     *
     *   rw-r--r--
     *
     * も 9 文字である。
     */
    PERMLEN = 9,        /* rw-r--r-- の長さ */
};

int main(int argc, char **argv) {
    /*
     * sbuf:
     *   stat() が取得したファイル属性を格納する構造体。
     *
     * この中の st_mode に、権限ビットや種別ビットが入る。
     *
     * i:
     *   perm 配列を初期化するときに使うループ変数。
     *
     * perm:
     *   完成させたい権限文字列を入れる配列。
     *
     *   長さ 9 の本体に加え、C文字列として扱うためには末尾に '\0' が必要なので、
     *   PERMLEN+1 を確保している。
     *
     *   つまり最終的には
     *
     *     perm[0..8]  = 表示文字
     *     perm[9]     = '\0'
     *
     * という形になる。
     */
    struct stat sbuf;
    int i;
    char perm[PERMLEN+1];       /* +1 は '\0' のため */

    /*
     * stat(argv[1], &sbuf)
     *
     * argv[1] で与えられたパスに対して stat() を行い、
     * 属性を sbuf に取得する。
     *
     * ここで欲しいのは主に
     *
     *   sbuf.st_mode
     *
     * の中の権限ビットである。
     *
     * 成功時:
     *   0 を返し、sbuf に属性が入る
     *
     * 失敗時:
     *   -1 を返す
     *
     * 失敗理由の例:
     *   - argv[1] が存在しない
     *   - 権限不足
     *   - 引数そのものが与えられていない
     *
     * 注意:
     *   このコードは argc チェックをしていないため、
     *   引数なしで実行すると argv[1] が存在せず危険である。
     *
     *   教材としては stat と権限表示の本体に集中しているが、
     *   実用コードなら最初に argc != 2 を確認したい。
     */
    if (stat(argv[1], &sbuf) == -1) {
        /*
         * stat() に失敗したら、その理由を表示して異常終了する。
         *
         * perror("stat") により、
         * たとえば
         *
         *   stat: No such file or directory
         *
         * のようなメッセージが出る。
         */
        perror("stat");
        exit(1);
    }

    /*
     * まず perm[0..8] を全部 '-' で初期化する。
     *
     * これは重要で、
     * 権限が「無い」場所の表示は '-' だからである。
     *
     * つまりこのアルゴリズムは、
     *
     *   1. 何も権限がない形 "---------"
     *      で開始する
     *   2. 権限がある場所だけを
     *      'r', 'w', 'x', 's', 't'
     *      に上書きする
     *
     * という「全消し初期化 -> 条件付き上書き」になっている。
     *
     * たとえば最終結果が
     *
     *   rw-r--r--
     *
     * になる場合でも、最初は
     *
     *   ---------
     *
     * から始めて、該当する位置を順に変更していく。
     */
    for (i = 0; i < PERMLEN; i++)
        perm[i] = '-';

    /*
     * 文字列終端を入れる。
     *
     * C では文字配列を puts() などで扱うには、
     * 最後に '\0' が必要である。
     *
     * これにより perm は
     *
     *   "---------"
     *
     * のような通常の C 文字列として扱えるようになる。
     */
    perm[PERMLEN] = '\0';

    /*
     * ------------------------------------------------------------
     * 所有者 (user / owner) の権限ビットを反映
     * ------------------------------------------------------------
     *
     * owner 用の位置は
     *
     *   perm[0] = 読み取り (r)
     *   perm[1] = 書き込み (w)
     *   perm[2] = 実行     (x)
     *
     * である。
     *
     * まず通常の rwx ビットを見て埋め、
     * そのあと setuid ビットがあれば perm[2] を 's' に上書きする。
     */

    /*
     * S_IRUSR:
     *   owner に読み取り権限があるかを表すビット。
     *
     * これが立っていれば perm[0] を 'r' にする。
     * 立っていなければ、初期値の '-' のまま。
     */
    if (sbuf.st_mode & S_IRUSR) perm[0] = 'r';

    /*
     * S_IWUSR:
     *   owner に書き込み権限があるかを表すビット。
     *
     * 立っていれば perm[1] を 'w' にする。
     */
    if (sbuf.st_mode & S_IWUSR) perm[1] = 'w';

    /*
     * S_IXUSR:
     *   owner に実行権限があるかを表すビット。
     *
     * 実行可能ファイルや検索可能ディレクトリなどで立つ。
     *
     * 立っていれば perm[2] を 'x' にする。
     */
    if (sbuf.st_mode & S_IXUSR) perm[2] = 'x';

    /*
     * S_ISUID:
     *   set-user-ID ビット。
     *
     * これは通常の rwx の「x の位置」に重ねて表示される特殊ビットであり、
     * owner の実行位置に対して 's' 表示を行う。
     *
     * このコードでは、S_IXUSR を見て 'x' を入れたあと、
     * S_ISUID が立っていれば無条件に 's' で上書きしている。
     *
     * そのため、
     *   実行権限あり + setuid あり
     * なら 's'
     *
     * ただし本格的な ls 互換では、
     *   実行権限なし + setuid あり
     * の場合は 'S'
     * を使うことがある。
     *
     * このコードはそこまでは扱わず、簡略化して常に 's' としている。
     */
    if (sbuf.st_mode & S_ISUID) perm[2] = 's';

    /*
     * ------------------------------------------------------------
     * グループ (group) の権限ビットを反映
     * ------------------------------------------------------------
     *
     * group 用の位置は
     *
     *   perm[3] = 読み取り (r)
     *   perm[4] = 書き込み (w)
     *   perm[5] = 実行     (x)
     *
     * である。
     *
     * owner 部分と同様に、
     * 通常の rwx を見たあと setgid があれば 's' に上書きする。
     */

    /*
     * S_IRGRP:
     *   group に読み取り権限があるかを表すビット。
     */
    if (sbuf.st_mode & S_IRGRP) perm[3] = 'r';

    /*
     * S_IWGRP:
     *   group に書き込み権限があるかを表すビット。
     */
    if (sbuf.st_mode & S_IWGRP) perm[4] = 'w';

    /*
     * S_IXGRP:
     *   group に実行権限があるかを表すビット。
     */
    if (sbuf.st_mode & S_IXGRP) perm[5] = 'x';

    /*
     * S_ISGID:
     *   set-group-ID ビット。
     *
     * group の実行位置に重ねて 's' を表示する。
     *
     * これも setuid と同様に、
     * 厳密な ls 互換なら 'S' / 's' の出し分けがあるが、
     * このコードは簡略化して常に 's' を使っている。
     */
    if (sbuf.st_mode & S_ISGID) perm[5] = 's';

    /*
     * ------------------------------------------------------------
     * その他 (other) の権限ビットを反映
     * ------------------------------------------------------------
     *
     * other 用の位置は
     *
     *   perm[6] = 読み取り (r)
     *   perm[7] = 書き込み (w)
     *   perm[8] = 実行     (x)
     *
     * である。
     *
     * ここでは sticky bit が実行位置に重ねて 't' で表示される。
     */

    /*
     * S_IROTH:
     *   other に読み取り権限があるかを表すビット。
     */
    if (sbuf.st_mode & S_IROTH) perm[6] = 'r';

    /*
     * S_IWOTH:
     *   other に書き込み権限があるかを表すビット。
     */
    if (sbuf.st_mode & S_IWOTH) perm[7] = 'w';

    /*
     * S_IXOTH:
     *   other に実行権限があるかを表すビット。
     */
    if (sbuf.st_mode & S_IXOTH) perm[8] = 'x';

    /*
     * S_ISVTX:
     *   sticky bit。
     *
     * これも other の実行位置に重ねて表示される特殊ビットである。
     *
     * 典型例としては /tmp のようなディレクトリで見られる。
     * その場合、削除制御などに関わる意味を持つ。
     *
     * このコードでは sticky bit が立っていれば、
     * perm[8] を 't' に上書きする。
     *
     * こちらも本格的な ls 互換では、
     * 実行権限なし + sticky bit あり
     * の場合に 'T' を使うことがあるが、
     * このコードでは簡略化して常に 't' を使っている。
     */
    if (sbuf.st_mode & S_ISVTX) perm[8] = 't';

    /*
     * 最後に完成した権限文字列を表示する。
     *
     * 例:
     *   rw-r--r--
     *   rwxr-xr-x
     *   rwsr-sr-t
     *
     * puts() は文字列を表示したあと自動で改行するので、
     * ここで見やすく1行出力できる。
     *
     * この1行が、このプログラムの最終成果物である。
     */
    puts(perm);

    /*
     * ここまで来たということは、
     *   - stat() に成功した
     *   - st_mode をもとに権限文字列を組み立てられた
     *   - それを表示できた
     *
     * ということなので、正常終了を表す 0 を返す。
     */
    return 0;
}
