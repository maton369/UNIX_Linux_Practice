/* fs/stat0.c */
#include <stdio.h>      /* printf, perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* stat 関連の型定義が入ることがある */
#include <sys/stat.h>   /* struct stat, stat() を使うためのヘッダ */
#include <unistd.h>     /* stat() を使うためのヘッダ */

/*
 * このプログラムの主題は「stat() によるファイル属性の取得」である。
 *
 * UNIX / Linux では、ファイルシステム上の各パスには
 *
 *   - ファイルの種類
 *   - パーミッション
 *   - inode 番号
 *   - リンク数
 *   - 所有者 UID / GID
 *   - サイズ
 *
 * など、さまざまなメタデータ（属性情報）が付随している。
 *
 * この「ファイルの中身」ではなく「ファイルに関する説明情報」を取得する
 * 代表的な関数が stat() である。
 *
 * このコードは非常に小さいが、やっていることは本質的で、
 *
 *   1. コマンドライン引数で対象パス名を受け取る
 *   2. stat(path, &sb) を呼ぶ
 *   3. OS に対象ファイルの属性を問い合わせる
 *   4. 結果を struct stat sb に書き込んでもらう
 *   5. その中の主要なフィールドを表示する
 *
 * という流れになっている。
 *
 * つまりこのプログラムは、
 *
 *   「与えられたパス名に対応するファイルシステムオブジェクトの属性を、
 *    struct stat へ取得して可視化する」
 *
 * 最小例である。
 *
 * 重要:
 *   stat() は「パス名をたどって、その先にあるファイルの属性」を取得する。
 *   したがって、対象がシンボリックリンクだった場合、
 *   そのリンク自身ではなく「リンク先」の属性を見るのが基本動作である。
 *
 *   もしリンク自身の属性を見たいなら lstat() を使う。
 *
 * つまり stat() は
 *   「パス解決後の実体の属性を見る関数」
 * と理解すると分かりやすい。
 */

int main(int argc, char *argv[]) {
    /*
     * sb:
     *   stat() が取得したファイル属性を格納する構造体である。
     *
     * struct stat には多くの情報が入るが、
     * このコードでは主に
     *
     *   st_ino   : inode 番号
     *   st_mode  : ファイル種別 + パーミッション
     *   st_nlink : ハードリンク数
     *   st_uid   : 所有者ユーザ ID
     *   st_gid   : 所有グループ ID
     *   st_size  : ファイルサイズ
     *
     * を表示している。
     *
     * つまり sb は、
     *   「対象ファイルの属性情報一式が入る箱」
     * である。
     */
    struct stat sb;

    /*
     * stat(argv[1], &sb)
     *
     * ここがこのプログラムの中心である。
     *
     * 第1引数:
     *   argv[1]
     *   -> 属性を知りたい対象のパス名
     *
     * 第2引数:
     *   &sb
     *   -> 取得した属性を書き込む先の struct stat へのポインタ
     *
     * stat() の意味をアルゴリズムとして書くと、
     *
     *   1. パス argv[1] を解決する
     *   2. そのパスが指すファイルシステムオブジェクトを探す
     *   3. そのオブジェクトの属性を読み出す
     *   4. struct stat に書き込む
     *
     * である。
     *
     * 戻り値:
     *   成功時:
     *     0
     *
     *   失敗時:
     *     0 以外（通常は -1）
     *
     * したがって、このコードでは
     *
     *   != 0
     *
     * でエラー判定している。
     *
     * 失敗理由の例:
     *   - argv[1] が存在しない
     *   - パスの途中にアクセス不能なディレクトリがある
     *   - 権限不足
     *   - argv[1] 自体が与えられていない
     *
     * 注意:
     *   このコードでは argc チェックが無いので、
     *   引数なしで実行すると argv[1] が存在せず不正動作の原因になりうる。
     *   教材としては stat() の本体に集中しているが、
     *   実用コードなら最初に argc != 2 を確認したい。
     */
    if (stat(argv[1], &sb) != 0) {
        /*
         * stat() 失敗時のエラー処理。
         *
         * perror("stat") により、errno に対応した理由を表示する。
         *
         * 例:
         *   stat: No such file or directory
         *   stat: Permission denied
         *
         * など。
         *
         * stat に失敗した時点で sb の内容は信用できないため、
         * ここではそのまま異常終了する。
         */
        perror("stat");
        exit(1);
    }

    /*
     * ここから先は stat() 成功後であり、
     * sb の中には argv[1] が指すファイルの属性情報が入っている。
     *
     * まず、どの対象についての情報か見やすいようにヘッダを表示する。
     */
    printf("Information for %s:\n", argv[1]);

    /*
     * sb.st_ino
     *
     * inode 番号である。
     *
     * UNIX 系ファイルシステムでは、ファイルの実体は inode という管理単位で表現される。
     * ファイル名はその inode を参照するための名前の1つだと考えられる。
     *
     * したがって inode 番号は、
     *   「そのファイルシステム上でその実体を識別する番号」
     * に近い。
     *
     * ハードリンクがある場合、異なる名前でも同じ inode 番号を持つことがある。
     *
     * 例:
     *   file1 と file2 が同じ実体へのハードリンクなら、
     *   stat(file1).st_ino == stat(file2).st_ino
     * になりうる。
     *
     * アルゴリズム的には、
     *   「名前ではなく実体識別子を見ている」
     * フィールドである。
     */
    printf("  st_ino   = %d\n", (int)sb.st_ino);

    /*
     * sb.st_mode
     *
     * これは非常に重要なフィールドで、
 *
     *   - ファイル種別
     *   - パーミッション
     *
     * をまとめて保持しているビット列である。
     *
     * たとえば
     *   通常ファイルか
     *   ディレクトリか
     *   シンボリックリンクか
     *
     * といった種類情報も、この st_mode の中に入っている。
     *
     * また、
     *   読み取り権限
     *   書き込み権限
     *   実行権限
     * もここに含まれる。
     *
     * このコードでは %o（8進数）で表示している。
     * UNIX のパーミッション表現は 0755 や 0644 のような 8 進表現がよく使われるので、
     * 8 進で見ると意味が分かりやすい。
     *
     * 重要:
     *   st_mode 全体をそのまま表示しているため、
     *   パーミッションだけでなくファイル種別ビットも含まれている。
     *
     * つまりここで見えている値は
     *   「単なる rwx 権限値そのもの」
     * ではなく、
     *   「ファイル種別 + 権限」を合わせたビット集合
     * である。
     *
     * 本格的に使うときは
     *   S_ISREG(sb.st_mode)
     *   S_ISDIR(sb.st_mode)
     *   sb.st_mode & 0777
     * などで分解して使うことが多い。
     */
    printf("  st_mode  = %o\n", (int)sb.st_mode);

    /*
     * sb.st_nlink
     *
     * ハードリンク数である。
     *
     * これは
     *   「この inode を参照しているディレクトリエントリの数」
     * と考えると分かりやすい。
     *
     * 通常の普通ファイルでは 1 であることが多いが、
     * ハードリンクを追加すると 2, 3, ... と増える。
     *
     * ディレクトリでは "." や ".." の関係もあって、
     * 値の意味が少し複雑になる。
     *
     * この値を見ることで、
     *   「この実体が何個の名前から参照されているか」
     * の一端が分かる。
     */
    printf("  st_nlink = %d\n", (int)sb.st_nlink);

    /*
     * sb.st_uid
     *
     * このファイルの所有者ユーザ ID（UID）である。
     *
     * UNIX 系ではファイルには所有者があり、
     * その所有者は名前ではなく UID という整数で管理される。
     *
     * たとえば
     *   maton
     * というユーザ名に対応する UID が 1000 なら、
     * そのユーザが所有するファイルの st_uid は 1000 になる可能性がある。
     *
     * ここでは名前解決まではせず、生の数値だけを表示している。
     *
     * 所有者情報はアクセス制御の判断などに使われる重要な属性である。
     */
    printf("  st_uid   = %d\n", (int)sb.st_uid);

    /*
     * sb.st_gid
     *
     * このファイルの所有グループ ID（GID）である。
     *
     * UNIX 系ではユーザだけでなくグループという概念もあり、
     * ファイルには「どのグループに属するか」も設定されている。
     *
     * これもアクセス権限の判定に関係する重要なメタデータである。
     *
     * ここでも人間向けグループ名ではなく、生の数値 GID をそのまま表示している。
     */
    printf("  st_gid   = %d\n", (int)sb.st_gid);

    /*
     * sb.st_size
     *
     * ファイルサイズである。
     *
     * 通常ファイルであれば、
     *   「何バイトの内容を持っているか」
     * を表すことが多い。
     *
     * たとえば 123 バイトのテキストファイルなら、
     * st_size は 123 になる。
     *
     * ただし注意:
     *   ディレクトリや特殊ファイルでは、この値の意味は必ずしも
     *   「人間が直感する中身の大きさ」と一致しないことがある。
     *
     * つまり st_size は
     *   「そのファイルシステムオブジェクトに対して OS が報告するサイズ値」
     * であり、
     * 対象種類によって解釈が変わりうる。
     */
    printf("  st_size  = %d\n", (int)sb.st_size);

    /*
     * ここまで来たということは、
     * stat() に成功し、必要な属性をすべて表示できたということである。
     *
     * return 0 により正常終了を表す。
     */
    return 0;
}
