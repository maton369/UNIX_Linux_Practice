/* fs/fsnav0.c */
#include <limits.h>     /* PATH_MAX を使うためのヘッダ */
#include <errno.h>      /* errno を使うためのヘッダ */
#include <sys/types.h>  /* DIR や readdir 系に関わる型定義が入ることがある */
#include <stdio.h>      /* fgets, fprintf, fputs, getchar, perror,
                           printf, putchar, setbuf を使うためのヘッダ */
#include <stdlib.h>     /* atexit, exit を使うためのヘッダ */
#include <string.h>     /* strchr を使うためのヘッダ */
#include <dirent.h>     /* opendir, readdir, closedir, struct dirent を使うためのヘッダ */
#include <termios.h>    /* tcgetattr, tcsetattr, ICANON, ECHO などを使うためのヘッダ */
#include <unistd.h>     /* chdir, getcwd, STDIN_FILENO を使うためのヘッダ */

/*
 * このプログラムの主題は、
 *
 *   「端末を1文字入力モードに切り替えて、現在ディレクトリを移動・表示する簡易ナビゲータ」
 *
 * である。
 *
 * これは単なるファイルシステム操作の例ではなく、
 * 次の 3 つの仕組みが組み合わさってできている。
 *
 *   1. 端末制御（termios）
 *      -> Enter を押さなくても 1 文字ずつコマンドを受け取れるようにする
 *
 *   2. カレントディレクトリ操作
 *      -> getcwd() で現在位置を表示し、chdir() で移動する
 *
 *   3. ディレクトリ列挙
 *      -> opendir()/readdir()/closedir() でその場の一覧を表示する
 *
 * つまりこのコードは、
 *
 *   「端末を対話用に調整しながら、現在ディレクトリを前後に移動し、
 *    その場のパスや一覧を即時表示する」
 *
 * という対話型プログラムである。
 *
 * 入力コマンドは 1 文字であり、
 *
 *   l : 今いるディレクトリの一覧を表示
 *   u : 親ディレクトリへ移動して、その新しい現在位置を表示
 *   d : 指定したサブディレクトリへ移動して、その新しい現在位置を表示
 *   ? : 現在の作業ディレクトリを表示
 *   q : 終了
 *
 * を意味する。
 *
 * 特に重要なのは端末モード切替である。
 *
 * 通常の端末は canonical mode（行単位入力）なので、
 * getchar() は Enter が押されるまで返ってこない。
 *
 * しかしこのプログラムでは
 *
 *   termcur.c_lflag &= ~(ICANON | ECHO);
 *
 * により、
 *   - ICANON を落として 1 文字即時入力
 *   - ECHO を落として押したキーをそのまま表示しない
 * ようにしている。
 *
 * そのため、'l' を押した瞬間に一覧が表示される。
 *
 * また、終了時や一時的に通常入力へ戻したい場面では、
 * 保存しておいた端末設定 termsave を復元する。
 *
 * これにより、通常のシェルへ戻ったときに
 * 端末が壊れた状態のまま残らないようにしている。
 */

/*
 * 端末設定を復元する関数群の前方宣言。
 *
 * このプログラムは上から順に読まれるので、
 * main() より前に関数名だけ宣言しておくことで、
 * 後で定義されていても main から呼べるようにしている。
 */
void restoreterm(void);
void showlist(void);
void showcwd(void);
void goup(void);
void godown(void);

/*
 * termsave:
 *   プログラム開始時点の端末設定を保存しておく領域である。
 *
 *   これを保存しておくことで、
 *   - 終了時に元の端末状態へ戻せる
 *   - godown() で一時的に通常入力へ戻せる
 *
 * termcur:
 *   プログラム中で使う「変更後の端末設定」を保持する領域である。
 *
 *   基本方針は、
 *     termsave をコピーして
 *     必要なフラグだけ変更する
 * というものである。
 *
 * これにより、
 *   「元設定との差分だけを編集する」
 * 形で安全に端末モードを作れる。
 */
struct termios termsave, termcur;

/*
 * restoreterm():
 *   保存しておいた元の端末設定 termsave を復元する関数。
 *
 * この関数は atexit() に登録されるので、
 * プログラムが通常終了したときに自動で呼ばれる。
 *
 * なぜ必要か:
 *   端末を非 canonical・非 echo にしたまま終了すると、
 *   その後のシェル入力が非常に使いづらくなる。
 *
 * たとえば、
 *   - Enter なしで反応してしまう
 *   - 入力文字が表示されない
 * などの状態が残る可能性がある。
 *
 * そこで、終了時には必ず元に戻す。
 *
 * tcsetattr(STDIN_FILENO, TCSANOW, &termsave)
 * の意味:
 *   - 対象は標準入力の端末
 *   - TCSANOW は「今すぐ適用」
 *   - 適用する内容は termsave
 */
void restoreterm(void) {
    tcsetattr(STDIN_FILENO, TCSANOW, &termsave);
}

/*
 * showlist():
 *   現在ディレクトリ "." を開き、その中のエントリ名を1つずつ表示する関数。
 *
 * この関数のアルゴリズムは次の通り。
 *
 *   1. opendir(".") で現在ディレクトリを開く
 *   2. readdir() でエントリを順番に読む
 *   3. dep->d_name を空白区切りで表示する
 *   4. すべて読み終えたら改行する
 *   5. エラーなら perror する
 *   6. closedir() はこの実装では最後に暗黙的に省略されていないので、
 *      本来は呼ぶべきだが、この関数では明示的には呼んでいない
 *
 * 注意:
 *   元コードでは closedir(dp) が省略されている。
 *   教材としては簡潔だが、厳密にはリソース回収のために呼ぶべきである。
 *   ただしここでは元コードの構造を保ちつつ説明する。
 */
void showlist(void) {
    /*
     * dp:
     *   opendir() によって得られるディレクトリストリーム。
     *
     * dep:
     *   readdir() によって得られる「現在のディレクトリエントリ」へのポインタ。
     */
    DIR *dp;
    struct dirent *dep;

    /*
     * 現在ディレクトリ "." を開く。
     *
     * 成功すれば dp に DIR * が入り、
     * 以後 readdir(dp) によりエントリを先頭から順に読める。
     *
     * 失敗理由の例:
     *   - アクセス権がない
     *   - カレントディレクトリが壊れている
     *   - システム資源不足
     */
    if ((dp = opendir(".")) == NULL) {
        /*
         * 元コードでは perror ではなく独自メッセージにしている。
         * ここでは「開けなかった」という事実だけを簡潔に示している。
         */
        fprintf(stderr, "cannot open directory\n");
        return;
    }

    /*
     * readdir() は
     *   - 正常に1件読めたら struct dirent *
     *   - 末尾でも NULL
     *   - エラーでも NULL
     * を返す。
     *
     * そのため、ループ前に errno を 0 にしておくことで、
     * ループ終了後に
     *
     *   errno == 0 なら正常に最後まで読んだ
     *   errno != 0 ならエラー
     *
     * と判定できるようにしている。
     */
    errno = 0;

    /*
     * readdir(dp) を繰り返し呼び、
     * 現在ディレクトリに含まれる各エントリ名 dep->d_name を表示する。
     *
     * ここでは改行区切りではなく空白区切りで表示しているため、
     * 1 行にまとめて一覧が出る。
     *
     * "." や ".." も特別扱いせず、そのまま表示される。
     */
    while ((dep = readdir(dp)) != NULL)
        printf("%s ", dep->d_name);

    /*
     * ループ終了後の errno を見て、
     * NULL が「正常終端」なのか「エラー」なのかを判定する。
     *
     * 正常終端なら見やすいように最後に改行を1つ出す。
     * エラーなら perror する。
     */
    if (errno == 0)
        putchar('\n');
    else
        perror("\nreaddir");

    /*
     * 本来はここで closedir(dp) を呼ぶのが望ましい。
     * 元コードでは省略されているため、そのままにしている。
     *
     * 教材としては
     *   「ディレクトリ一覧の表示」
     * に主題を絞っていると考えられる。
     */
}

/*
 * showcwd():
 *   現在の作業ディレクトリを絶対パスで表示する関数。
 *
 * アルゴリズム:
 *   1. getcwd() で現在位置を buf に入れる
 *   2. その文字列をそのまま表示する
 *
 * ここでは getcwd() の失敗判定を省略している。
 * 教材としては簡潔だが、厳密には NULL チェックしたい。
 */
void showcwd(void) {
    /*
     * カレントディレクトリの文字列表現を受け取るバッファ。
     *
     * PATH_MAX+1 としているのは、
     * パス文字列の末尾に '\0' を置くためである。
     */
    char buf[PATH_MAX+1];

    /*
     * 現在の作業ディレクトリを buf に書き込む。
     *
     * たとえば
     *   /home/maton/dev
     * が現在位置なら、その絶対パスが buf に入る。
     */
    getcwd(buf, sizeof(buf));

    /*
     * 現在位置をそのまま1行表示する。
     *
     * '?' コマンドや、u/d の移動後に
     * 「今どこにいるか」を確認するために使う。
     */
    printf("%s\n", buf);
}

/*
 * goup():
 *   親ディレクトリへ1段上がる関数。
 *
 * 実体は chdir("..") を呼ぶだけである。
 *
 * ".." は親ディレクトリを表す特別な名前なので、
 * これに移動することで現在位置を1つ上へ変えられる。
 *
 * 例:
 *   /home/maton/dev にいるとき
 *   goup() 後は /home/maton
 *
 * この関数ではエラー判定を省略している。
 * たとえばアクセス不能な状況では本来チェックしたい。
 */
void goup(void) {
    chdir("..");
}

/*
 * godown():
 *   指定したディレクトリ名へ降りる関数。
 *
 * この関数はこのプログラムの中で最も面白い部分の1つである。
 * 理由は、通常時は端末が「1文字即時入力モード」になっているため、
 * ここでディレクトリ名のような通常の文字列入力をしたいときは、
 * 一時的に端末設定を元へ戻す必要があるからである。
 *
 * アルゴリズム:
 *   1. 端末設定を termsave に戻す
 *      -> 通常の行入力・エコーありに戻る
 *   2. "directory? " と表示する
 *   3. fgets() で1行読み取る
 *   4. 末尾改行を削る
 *   5. chdir(buf) でそのディレクトリへ移動する
 *   6. 端末設定を termcur に戻す
 *      -> 再び 1 文字即時入力モードへ戻る
 *
 * つまり godown() は
 *   「一時的に普通の入力モードへ戻して詳細入力を受ける」
 * ための特殊処理である。
 */
void godown(void) {
    /*
     * buf:
     *   移動先ディレクトリ名を入力してもらうためのバッファ。
     *
     * p:
     *   改行文字 '\n' を探して切り落とすためのポインタ。
     */
    char buf[PATH_MAX+1], *p;

    /*
     * まず端末設定を元に戻す。
     *
     * 普段は ICANON と ECHO を切っているため、
     * そのままだと fgets() による通常行入力が扱いづらい。
     *
     * そこで termsave を即時適用し、
     * 通常の「文字が表示され、Enter で確定する」モードへ一時復帰する。
     */
    tcsetattr(STDIN_FILENO, TCSANOW, &termsave);

    /*
     * 入力プロンプトを標準エラーへ表示する。
     *
     * ここで stderr を使っているのは、
     * 端末対話用メッセージとして stdout と区別する意図だと考えられる。
     */
    fputs("directory? ", stderr);

    /*
     * 通常行入力として 1 行読む。
     *
     * 例:
     *   src
     *   ../tmp
     *   /home/maton
     *
     * などが入る。
     */
    fgets(buf, sizeof(buf), stdin);

    /*
     * fgets() は改行 '\n' も含めて読むことがあるため、
     * それを探して '\0' に置き換える。
     *
     * これにより、
     *   "src\n"
     * は
     *   "src"
     * という通常のパス文字列になる。
     */
    if ((p = strchr(buf, '\n')) != NULL)
        *p = '\0';

    /*
     * 入力された文字列をディレクトリ名として chdir() する。
     *
     * 成功すれば現在ディレクトリが変わる。
     * 失敗すれば perror("chdir") で理由を表示する。
     *
     * 失敗理由の例:
     *   - その名前のディレクトリが存在しない
     *   - アクセス権がない
     *   - 実は通常ファイルで、ディレクトリではない
     */
    if (chdir(buf) != 0)
        perror("chdir");

    /*
     * 最後に再び 1 文字入力モードへ戻す。
     *
     * これを忘れると、その後のメインループで
     * 1キー操作 UI が崩れてしまう。
     *
     * つまり godown() は
     *   「通常入力へ戻す -> 入力を受ける -> 即時入力へ戻す」
     * というモード遷移を含む関数である。
     */
    tcsetattr(STDIN_FILENO, TCSANOW, &termcur);
}

int main(void) {
    /*
     * c:
     *   getchar() で受け取る 1 文字コマンド。
     *
     *   'l', 'u', 'd', '?', 'q'
     * などが入る。
     */
    int c;

    /*
     * ------------------------------------------------------------
     * 端末現在設定の保存
     * ------------------------------------------------------------
     *
     * tcgetattr(STDIN_FILENO, &termsave)
     *
     * 標準入力端末の現在設定を termsave に読み込む。
     *
     * これが元の正常な端末状態であり、
     *   - 終了時の復元
     *   - godown() での一時復元
     * に使われる。
     */
    tcgetattr(STDIN_FILENO, &termsave);

    /*
     * termcur は termsave を元に作る。
     *
     * こうすることで、元設定との差分だけを編集すればよい。
     */
    termcur = termsave;

    /*
     * atexit(restoreterm)
     *
     * プログラム終了時に restoreterm() を自動呼び出しする登録である。
     *
     * これにより、
     *   - 通常の q 終了
     *   - exit(1) などの終了
     * のどちらでも、できるだけ端末復元が行われる。
     *
     * 端末対話プログラムでは非常に重要な防御である。
     */
    if (atexit(restoreterm) != 0) {
        /*
         * atexit 登録に失敗したら安全性が落ちるので異常終了する。
         */
        perror("atexit");
        exit(1);
    }

    /*
     * ------------------------------------------------------------
     * 即時 1 文字入力モードの設定
     * ------------------------------------------------------------
     *
     * termcur.c_lflag &= ~(ICANON | ECHO);
     *
     * ICANON を落とす:
     *   行単位入力をやめて、1文字ごとに入力を処理できるようにする
     *
     * ECHO を落とす:
     *   押したキーを端末に自動表示しない
     *
     * これにより、
     *   l を押した瞬間に一覧表示
     *   u を押した瞬間に親へ移動
     * が可能になる。
     */
    termcur.c_lflag &= ~(ICANON | ECHO);

    /*
     * VMIN = 1:
     *   1文字受け取るまで read 系が返らない
     *
     * VTIME = 0:
     *   タイムアウトなし
     *
     * この組み合わせにより、
     *   「キーが1つ押されたら即座に返る」
     * 入力動作になる。
     */
    termcur.c_cc[VMIN] = 1;
    termcur.c_cc[VTIME] = 0;

    /*
     * 編集した端末設定 termcur を即時適用する。
     *
     * ここから先、メインループの getchar() は
     * Enter を待たずに 1 文字で反応するようになる。
     */
    tcsetattr(STDIN_FILENO, TCSANOW, &termcur);

    /*
     * stdin のバッファリングを無効化する。
     *
     * setbuf(stdin, NULL) により、
     * 標準入力はバッファなしモードになる。
     *
     * これも「1文字ごとの即時反応」を実現しやすくする補助である。
     */
    setbuf(stdin, NULL);

    /*
     * ------------------------------------------------------------
     * メインループ
     * ------------------------------------------------------------
     *
     * getchar() で 1 文字ずつコマンドを受け取り、
     * switch で対応する操作を呼ぶ。
     *
     * 1ループ = 1キーコマンド処理
     */
    for (;;) {
        /*
         * 非 canonical モードなので、
         * getchar() は Enter を待たずに1文字入力で返る。
         */
        c = getchar();

        /*
         * 入力コマンドに応じて処理を分岐する。
         */
        switch (c) {
            case 'l':
                /*
                 * list:
                 *   現在ディレクトリの一覧を表示する。
                 */
                showlist();
                break;

            case 'u':
                /*
                 * up:
                 *   親ディレクトリへ移動し、
                 *   移動後の現在位置を表示する。
                 *
                 * つまり
                 *   chdir("..")
                 *   getcwd(...)
                 * の組合せである。
                 */
                goup();
                showcwd();
                break;

            case 'd':
                /*
                 * down:
                 *   指定ディレクトリへ移動し、
                 *   移動後の現在位置を表示する。
                 *
                 * godown() の中では一時的に通常入力モードへ戻して、
                 * ディレクトリ名を文字列で入力させる。
                 */
                godown();
                showcwd();
                break;

            case '?':
                /*
                 * 現在位置の確認だけを行う。
                 */
                showcwd();
                break;

            case 'q':
                /*
                 * 終了要求。
                 *
                 * goto finish によりループを抜けて exit(0) へ進む。
                 * このとき atexit 登録済みの restoreterm() により
                 * 端末設定が復元される。
                 */
                goto finish;

            default:
                /*
                 * 未知のキーが押された場合のエラー表示。
                 *
                 * たとえば 'x' を押したなら
                 *   unknown command 'x'
                 * と出る。
                 *
                 * ここではプログラム自体は終了せず、
                 * 次の1文字入力を待つ。
                 */
                fprintf(stderr, "unknown command '%c'\n", c);
                break;
        }
    }

finish:
    /*
     * 正常終了。
     *
     * 直接 restoreterm() を呼んでいないが、
     * atexit(restoreterm) が登録されているため、
     * exit(0) の過程で自動的に端末が元に戻る。
     *
     * これにより、プログラム終了後もシェルが通常の入力状態で使える。
     */
    exit(0);
}
