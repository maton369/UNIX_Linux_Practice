/* fs/myls0.c */
#include <errno.h>      /* errno を使うためのヘッダ */
#include <sys/types.h>  /* DIR や readdir 系に関わる型定義が入ることがある */
#include <stdio.h>      /* printf, perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <dirent.h>     /* opendir, readdir, closedir, struct dirent を使うためのヘッダ */

/*
 * このプログラムの主題は「ディレクトリを開いて、中に含まれる項目名を1つずつ読む」ことである。
 *
 * UNIX / Linux では、ディレクトリは単なる見た目上の“フォルダ”ではなく、
 * その中に
 *
 *   - ファイル名
 *   - サブディレクトリ名
 *   - その他のエントリ名
 *
 * などの一覧を持つ、特別な種類のファイルだと考えることができる。
 *
 * このコードは、そのディレクトリ内容を読む最小例であり、
 * 概念的には `ls` コマンドのごく小さな一部に相当する。
 *
 * ただし本物の `ls` と違って、このプログラムは
 *
 *   - 詳細情報（権限、サイズ、時刻など）は出さない
 *   - ソートもしない
 *   - 引数も取らず、常に "."（現在ディレクトリ）だけを読む
 *
 * という非常に単純な動きしかしない。
 *
 * その代わり、
 *
 *   1. opendir() でディレクトリを開く
 *   2. readdir() でエントリを1個ずつ読む
 *   3. 読み終わったら closedir() で閉じる
 *
 * という「ディレクトリ走査の基本アルゴリズム」がとても見えやすい。
 *
 * 全体の流れは次のように整理できる。
 *
 *   1. "." を opendir() で開く
 *   2. readdir() を繰り返し呼び、
 *      返ってきた struct dirent の d_name を表示する
 *   3. readdir() が NULL を返したらループ終了
 *   4. その NULL が「正常な終端」なのか「エラー」なのかを errno で判定する
 *   5. 最後に closedir() でディレクトリを閉じる
 *
 * 特に重要なのは readdir() のエラー判定である。
 *
 * readdir() は
 *   - エントリを正常に読めたときは struct dirent* を返す
 *   - エントリがもう無いときも NULL を返す
 *   - エラーのときも NULL を返す
 *
 * ので、単に NULL を見ただけでは
 *   「読み終わった」のか「失敗した」のか
 * を区別できない。
 *
 * そこでこのコードでは、ループ前に errno = 0 にしておき、
 * ループ終了後に errno を見て
 *
 *   errno == 0 なら正常終端
 *   errno != 0 ならエラー
 *
 * と判定している。
 *
 * これがこのコードの最重要ポイントの1つである。
 */

int main(void) {
    /*
     * dirp:
     *   開いたディレクトリを表す「ディレクトリストリーム」へのポインタである。
     *
     * FILE * が通常ファイルのストリームを表すのに似ていて、
     * DIR * はディレクトリを順に読むための状態を表す。
     *
     * opendir() に成功すると、この dirp に
     *   「今どのディレクトリエントリまで読んだか」
     * などの内部状態を持ったオブジェクトへのポインタが入る。
     *
     * 以後、この dirp を readdir() に渡すことで、
     * 同じディレクトリを先頭から順番に読んでいける。
     */
    DIR *dirp;

    /*
     * p:
     *   readdir() が返す「現在読んだディレクトリエントリ」へのポインタである。
     *
     * 型は struct dirent * であり、
     * struct dirent は1個のディレクトリエントリに関する情報を持つ構造体である。
     *
     * このプログラムではその中の
     *
     *   p->d_name
     *
     * だけを使っている。
     *
     * d_name は、そのエントリの名前（ファイル名やディレクトリ名）を表す文字列である。
     *
     * たとえば現在ディレクトリに
     *   foo.txt
     *   bar
     *   baz.c
     * があれば、readdir() を繰り返すことで
     *
     *   p->d_name == "."
     *   p->d_name == ".."
     *   p->d_name == "foo.txt"
     *   p->d_name == "bar"
     *   p->d_name == "baz.c"
     *
     * のような値が順に見える可能性がある。
     *
     * 注意:
     *   "." や ".." も普通にディレクトリエントリとして返ってくることがある。
     *   このプログラムはそれらを特別扱いせず、そのまま表示する。
     */
    struct dirent *p;

    /*
     * opendir(".")
     *
     * "." は現在の作業ディレクトリを表す特別なパスである。
     *
     * したがって、
     *
     *   opendir(".")
     *
     * は
     *   「現在ディレクトリを開いて、その内容を読める状態にする」
     * という意味になる。
     *
     * 戻り値:
     *   成功時:
     *     DIR * を返す
     *
     *   失敗時:
     *     NULL を返す
     *
     * 失敗理由の例:
     *   - 現在ディレクトリにアクセス権がない
     *   - パスが壊れている
     *   - システム資源不足
     *
     * このコードでは、NULL なら perror("opendir") して終了する。
     *
     * アルゴリズム的には、ここが
     *
     *   「ディレクトリ列挙処理の開始点」
     *
     * である。
     */
    if ((dirp = opendir(".")) == NULL) {
        /*
         * ディレクトリを開けなかった場合のエラー処理。
         *
         * perror("opendir") によって、errno に応じた失敗理由を表示する。
         *
         * 例:
         *   opendir: Permission denied
         *
         * など。
         */
        perror("opendir");
        exit(1);
    }

    /*
     * errno = 0;
     *
     * ここが readdir() の理解で非常に重要である。
     *
     * readdir() は、エントリが尽きたときにも NULL を返し、
     * エラーのときにも NULL を返す。
     *
     * つまりループ終了時に
     *
     *   p == NULL
     *
     * だけでは、
     *   - 正常に最後まで読んだ
     *   - 途中で失敗した
     * のどちらか分からない。
     *
     * そこで、readdir() を呼び始める前に errno を 0 にしておく。
     *
     * そうすると、
     *   - 正常に最後まで読んで NULL になった場合は errno は 0 のまま
     *   - エラーなら errno が 0 以外に設定される
     *
     * という区別ができる。
     *
     * 図にするとこうである。
     *
     *   ループ前:
     *     errno = 0
     *
     *   readdir() を繰り返す
     *
     *   ループ終了後:
     *     p == NULL
     *     かつ
     *       errno == 0  -> 正常終端
     *       errno != 0  -> エラー
     *
     * このように errno を「事前初期化」しておくのがポイントである。
     */
    errno = 0;

    /*
     * while ((p = readdir(dirp)) != NULL)
     *
     * ここがディレクトリエントリ走査の中心ループである。
     *
     * readdir(dirp) は、
     *   開かれたディレクトリ dirp から「次の1個のエントリ」を読む。
     *
     * 成功時:
     *   struct dirent * を返す
     *
     * もうエントリが無いとき:
     *   NULL を返す
     *
     * エラー時:
     *   NULL を返す
     *
     * このループは、NULL でない限り
     *   「まだ1個読み取れた」
     * とみなして処理を続ける。
     *
     * つまりアルゴリズムとしては
     *
     *   while (次のエントリが存在する) {
     *       その名前を表示する
     *   }
     *
     * に相当する。
     *
     * 図にするとこうである。
     *
     *   opendir(".")
     *       |
     *       v
     *   readdir() -> 1個目
     *       |
     *       v
     *   readdir() -> 2個目
     *       |
     *       v
     *   readdir() -> 3個目
     *       |
     *       v
     *   ...
     *       |
     *       v
     *   readdir() -> NULL
     *       |
     *       +--> 正常終端 or エラーは後で errno で判定
     *
     * なお、このコードは波括弧を付けずに1文だけループ本体にしている。
     */
    while ((p = readdir(dirp)) != NULL)
        /*
         * p->d_name を表示する。
         *
         * d_name はディレクトリエントリ名を表す文字列である。
         * したがって、各エントリを1行ずつ表示すれば、
         * 結果は簡易的な ls のように見える。
         *
         * 例:
         *   .
         *   ..
         *   mypwd.c
         *   myls0.c
         *   out.txt
         *
         * など。
         *
         * このコードは名前だけを出しており、
         * 詳細情報（権限・サイズ・時刻など）は出さない。
         */
        printf("%s\n", p->d_name);

    /*
     * while ループを抜けた時点では、readdir() が NULL を返している。
     *
     * しかしそれが
     *   「正常に最後まで読んだ結果の NULL」
     * なのか
     *   「エラーによる NULL」
     * なのかは、NULL だけでは分からない。
     *
     * そこで errno を見る。
     *
     * ループ前に errno=0 へ初期化しておいたので、
     *
     *   errno == 0  -> 正常終端
     *   errno != 0  -> エラー
     *
     * と判定できる。
     */
    if (errno != 0) { /* エラーがあると errno != 0 になる */
        /*
         * readdir() に失敗していた場合のエラー処理。
         *
         * perror("readdir") によって失敗理由を表示する。
         *
         * 例:
         *   readdir: Input/output error
         *
         * など。
         *
         * ここで exit(1) しているので、このあと closedir() までは進まない。
         * 教材としては簡潔だが、厳密には失敗時も closedir() したい設計もありうる。
         */
        perror("readdir");
        exit(1);
    }

    /*
     * closedir(dirp)
     *
     * 開いていたディレクトリストリームを閉じる。
     *
     * 通常ファイルで
     *   fopen() -> fread()/fgets() -> fclose()
     * という流れがあるのに対応して、
     * ディレクトリでは
     *
     *   opendir() -> readdir() -> closedir()
     *
     * という流れになる。
     *
     * 成功時:
     *   0
     *
     * 失敗時:
     *   0 以外
     *
     * このコードでは、0 以外ならエラーとして扱っている。
     *
     * アルゴリズム的にはここが
     *   「ディレクトリ列挙処理の後始末」
     * である。
     */
    if (closedir(dirp) != 0) {
        /*
         * ディレクトリを閉じるのに失敗した場合のエラー処理。
         *
         * 例:
         *   closedir: Bad file descriptor
         *
         * など。
         *
         * 普通は大きな問題なく閉じられることが多いが、
         * リソース管理として closedir() の戻り値も確認しているのは丁寧である。
         */
        perror("closedir");
        exit(1);
    }

    /*
     * 正常終了。
     *
     * ここまで来たということは、
     *   - opendir() に成功した
     *   - readdir() で最後まで読み切った
     *   - closedir() にも成功した
     *
     * ということである。
     *
     * return 0 により、呼び出し元（通常はシェル）へ
     * 「正常終了」を伝える。
     */
    return 0;
}
