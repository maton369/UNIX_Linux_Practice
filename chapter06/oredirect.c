#include <sys/types.h>  /* open() に関係する型定義が入ることがある */
#include <stdio.h>      /* fprintf() を使うためのヘッダ */
#include <stdlib.h>     /* exit() を使うためのヘッダ */
#include <sys/stat.h>   /* open() の mode 引数に関係する定義 */
#include <fcntl.h>      /* open(), O_WRONLY, O_CREAT, O_TRUNC を使うためのヘッダ */
#include <unistd.h>     /* dup2(), execvp(), STDOUT_FILENO を使うためのヘッダ */

/*
 * このプログラムの主題は、
 *
 *   「あるファイルを標準出力の行き先に付け替えてから、
 *    指定したコマンドを exec で起動する」
 *
 * ことである。
 *
 * つまりこれは、シェルでいう
 *
 *   command > filename
 *
 * にかなり近い処理を、自分で C で明示的に書いたものだと考えると分かりやすい。
 *
 * ただし、このコードはシェル全体ではなく、
 * 「出力リダイレクトしてからコマンドを実行する」部分だけを切り出した最小例である。
 *
 * 全体のアルゴリズムは次の通りである。
 *
 *   1. コマンドライン引数を確認する
 *   2. argv[1] で指定されたファイルを、書き込み用に open() する
 *   3. dup2(fd, STDOUT_FILENO) により、
 *      標準出力(= fd 1) をそのファイルへ付け替える
 *   4. execvp(argv[2], &argv[2]) により、
 *      argv[2] 以降を新しいコマンドの argv として実行する
 *
 * たとえば次のように起動したとする。
 *
 *   ./a.out out.txt ls -l /tmp
 *
 * このとき各引数は概念的に
 *
 *   argv[0] = "./a.out"
 *   argv[1] = "out.txt"
 *   argv[2] = "ls"
 *   argv[3] = "-l"
 *   argv[4] = "/tmp"
 *   argv[5] = NULL
 *
 * となる。
 *
 * このプログラムは
 *
 *   1. out.txt を開く
 *   2. 標準出力を out.txt に付け替える
 *   3. ls -l /tmp を実行する
 *
 * ので、結果として ls の出力は端末ではなく out.txt に書かれる。
 *
 * つまりこのコードは、
 *
 *   「コマンド自身は何も特別なことを知らないまま、
 *    その標準出力だけを別ファイルへ向けて実行させる」
 *
 * という UNIX 的リダイレクトの本質を示している。
 */

int main(int argc, char *argv[])
{
    /*
     * fd:
     *   open() で開いた出力先ファイルのファイルディスクリプタを保持する変数。
     *
     * UNIX / Linux では、開いたファイルは整数番号で管理される。
     * これがファイルディスクリプタ (file descriptor) である。
     *
     * 典型的には最初から
     *
     *   0 -> 標準入力   (stdin)
     *   1 -> 標準出力   (stdout)
     *   2 -> 標準エラー (stderr)
     *
     * が割り当てられている。
     *
     * このコードでは、open() によって新たに得た fd を
     * dup2() で 1 番 (STDOUT_FILENO) に複製することで、
     * 標準出力の行き先を付け替える。
     */
    int fd;

    /*
     * 引数個数のチェック。
     *
     * このプログラムは少なくとも
     *
     *   argv[0] = プログラム名
     *   argv[1] = 出力先ファイル名
     *   argv[2] = 実行するコマンド名
     *
     * の3つが必要である。
     *
     * したがって argc < 3 なら、
     * 「出力先ファイル名」または「実行コマンド」が不足している。
     *
     * 例:
     *   ./a.out
     *   ./a.out out.txt
     *
     * のような実行では、まだ処理に必要な情報が足りない。
     *
     * アルゴリズム的には、ここは
     *
     *   「前提条件の検査」
     *
     * に相当する。
     */
    if (argc < 3) {
        /*
         * 使い方を標準エラー出力へ表示する。
         *
         * "%s filename command [arg...]\n"
         * の意味は、
         *
         *   program-name  出力先ファイル名  実行コマンド  その引数...
         *
         * という形式で使ってください、ということ。
         *
         * [arg...] は任意個の追加引数を意味している。
         *
         * たとえば
         *
         *   ./a.out out.txt ls -l /tmp
         *
         * のように、コマンド名以降は好きなだけ続けられる。
         */
        fprintf(stderr, "%s filename command [arg...]\n", argv[0]);

        /*
         * 使い方が正しくないので異常終了する。
         *
         * exit(1) の 1 は、
         * 呼び出し元（通常はシェル）に対して
         * 「正常終了ではない」ことを伝える終了コードである。
         */
        exit(1);
    }

    /*
     * open(argv[1], O_WRONLY | O_CREAT | O_TRUNC, 0666)
     *
     * ここで出力先ファイルを開く。
     *
     * 第1引数:
     *   argv[1]
     *   -> 出力先のファイル名
     *
     * 第2引数のフラグ:
     *
     *   O_WRONLY
     *     書き込み専用で開く
     *
     *   O_CREAT
     *     ファイルが存在しなければ新規作成する
     *
     *   O_TRUNC
     *     すでに存在する場合は長さを 0 に切り詰める
     *
     * つまり、出力先ファイルの扱いは
     *
     *   - なければ作る
     *   - あれば中身を空にして上書き用に開く
     *
     * ということになる。
     *
     * 第3引数 0666:
     *   O_CREAT が有効なとき、新規作成時のパーミッション候補である。
     *
     * 実際には umask の影響を受けるので、
     * 必ずしもそのまま 0666 になるわけではない。
     *
     * 重要:
     *   ここで open() に成功すると、fd には
     *   「そのファイルを指す新しいファイルディスクリプタ番号」
     *   が入る。
     *
     * ただし、このコードは open() 失敗時のチェックをしていない。
     * 実務コードなら
     *
     *   if (fd == -1) { perror("open"); exit(1); }
     *
     * のように確認したい。
     *
     * 失敗理由の例:
     *   - 親ディレクトリが存在しない
     *   - 書き込み権限がない
     *   - ディスクや資源に問題がある
     */
    fd = open(argv[1], O_WRONLY | O_CREAT | O_TRUNC, 0666);

    /*
     * dup2(fd, STDOUT_FILENO)
     *
     * ここがこのプログラムの核心である。
     *
     * dup2(oldfd, newfd) は、
     *   oldfd が指している先を newfd に複製する
     * 関数である。
     *
     * ここでは
     *
     *   oldfd = fd              -> open() で開いた出力ファイル
     *   newfd = STDOUT_FILENO   -> 標準出力 (通常は 1)
     *
     * なので、結果として
     *
     *   「標準出力が fd の指すファイルへ向く」
     *
     * ことになる。
     *
     * 図にするとこうである。
     *
     *   dup2 前:
     *     fd 1  -> 端末 (stdout)
     *     fd 3  -> out.txt   (例)
     *
     *   dup2(fd, 1) 後:
     *     fd 1  -> out.txt
     *     fd 3  -> out.txt
     *
     * つまり、標準出力の行き先が端末からファイルへ変わる。
     *
     * これがリダイレクトの本質である。
     *
     * 重要:
     *   dup2() は newfd 側（ここでは 1）を必要なら先に閉じてから、
     *   oldfd と同じ対象を指すようにする。
     *
     * したがって、このコードは
     *
     *   close(1); open(...);
     *
     * という手法よりも、より明示的で安全に
     * 「標準出力を特定ファイルへ付け替える」ことができる。
     *
     * これも戻り値チェックを省略しているが、
     * 実務では
     *
     *   if (dup2(fd, STDOUT_FILENO) == -1) { ... }
     *
     * と確認したい。
     */
    dup2(fd, STDOUT_FILENO);

    /*
     * execvp(argv[2], &argv[2])
     *
     * ここで現在のプロセスを、指定コマンドに置き換える。
     *
     * execvp の意味:
     *
     *   exec:
     *     現在のプロセスイメージを別プログラムへ置き換える
     *
     *   v:
     *     引数を vector（配列）で渡す
     *
     *   p:
     *     PATH を検索してコマンドを見つける
     *
     * 第1引数 argv[2]:
     *   実行するコマンド名
     *
     * 第2引数 &argv[2]:
     *   新しいプログラムに渡す argv 配列の先頭
     *
     * ここがとても重要で、
     *
     *   &argv[2]
     *
     * は「元の argv 配列の 2 番目要素以降を新しい argv として再利用する」
     * という意味になる。
     *
     * たとえば元のコマンドラインが
     *
     *   ./a.out out.txt ls -l /tmp
     *
     * ならメモリ上では概念的に
     *
     *   argv[0] = "./a.out"
     *   argv[1] = "out.txt"
     *   argv[2] = "ls"
     *   argv[3] = "-l"
     *   argv[4] = "/tmp"
     *   argv[5] = NULL
     *
     * である。
     *
     * ここで &argv[2] を渡すと、新しいプログラムから見える引数列は
     *
     *   new_argv[0] = "ls"
     *   new_argv[1] = "-l"
     *   new_argv[2] = "/tmp"
     *   new_argv[3] = NULL
     *
     * になる。
     *
     * 図にするとこうである。
     *
     *   元の argv:
     *     [ "./a.out" ][ "out.txt" ][ "ls" ][ "-l" ][ "/tmp" ][ NULL ]
     *                                  ^
     *                                  |
     *                               &argv[2]
     *
     *   新しいコマンドから見た argv:
     *     [ "ls" ][ "-l" ][ "/tmp" ][ NULL ]
     *
     * つまりこのコードは、
     *   「出力先ファイル名 argv[1] だけを自分で消費し、
     *    argv[2] 以降はそのまま新しいコマンドの引数列として流用する」
     * という設計である。
     *
     * 重要:
     *   execvp() に成功すると、このプログラムのコードには戻ってこない。
     *
     *   その時点で現在プロセスは完全に別のコマンドへ置き換わる。
     *
     * また、exec はファイルディスクリプタ表を基本的に引き継ぐので、
     * ここまでに行った
     *
     *   dup2(fd, STDOUT_FILENO)
     *
     * の効果も新しいプログラムへそのまま引き継がれる。
     *
     * そのため、新しいコマンドは
     *   「普通に標準出力へ書いているだけ」
     * のつもりでも、
     *   実際には argv[1] のファイルへ書いている
     * ことになる。
     *
     * これがシェルの
     *
     *   command > file
     *
     * に相当する動きである。
     *
     * 注意:
     *   このコードには execvp 失敗時の処理が無い。
     *
     *   実務コードなら普通は
     *
     *     execvp(...);
     *     perror("execvp");
     *     exit(1);
     *
     *   のように書く。
     */
    execvp(argv[2], &argv[2]);

    /*
     * 元コードではここに到達した場合のエラー処理が書かれていない。
     *
     * execvp() は成功時には戻らないため、
     * ここに来るのは execvp 失敗時だけである。
     *
     * したがって本来は
     *
     *   perror(argv[2]);
     *   exit(1);
     *
     * などを書くべきである。
     *
     * ただし、ここでは元コードの構造を保ちつつ、
     * 主に「リダイレクト + exec」の本体を解説している。
     */
}
