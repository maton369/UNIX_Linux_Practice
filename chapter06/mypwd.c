/* fs/mypwd.c */
#include <limits.h>     /* PATH_MAX を使うためのヘッダ */
#include <stdio.h>      /* printf, perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <unistd.h>     /* getcwd を使うためのヘッダ */

/*
 * このプログラムの主題は「現在の作業ディレクトリ（current working directory）」である。
 *
 * UNIX / Linux では、各プロセスは
 *
 *   「いま自分がどのディレクトリを基準位置として動いているか」
 *
 * という情報を持っている。
 * これがカレントディレクトリである。
 *
 * たとえばシェルで
 *
 *   cd /home/maton
 *
 * としたあとにプログラムを起動すると、そのプログラムは通常
 *
 *   /home/maton
 *
 * をカレントディレクトリとして開始する。
 *
 * このコードは、そのカレントディレクトリを
 *
 *   getcwd()
 *
 * という関数を使って文字列として取得し、それを表示する最小例である。
 *
 * アルゴリズム全体は非常に単純で、次の3段階である。
 *
 *   1. パス文字列を受け取るためのバッファを用意する
 *   2. getcwd() で「現在の作業ディレクトリ」をそのバッファへ書き込む
 *   3. 成功したらその文字列を表示し、失敗したらエラー終了する
 *
 * つまりこのコードは、
 *
 *   「プロセスが持つ現在位置を OS から問い合わせて、人間が読める絶対パス文字列として表示する」
 *
 * プログラムである。
 */

int main(void) {
    /*
     * buf:
     *   現在の作業ディレクトリの絶対パス文字列を受け取るためのバッファである。
     *
     * PATH_MAX は「パス名の最大長の目安」を表す定数である。
     * ここでは PATH_MAX+1 バイト確保している。
     *
     * +1 している理由:
     *   C の文字列は最後に '\0'（ヌル終端文字）が必要だからである。
     *
     * たとえば current directory が
     *
     *   /home/maton/dev
     *
     * なら、buf の中には最終的に
     *
     *   '/', 'h', 'o', 'm', 'e', ..., 'v', '\0'
     *
     * のように格納される。
     *
     * 注意:
     *   PATH_MAX は環境依存であり、絶対にすべての状況を表し切れるとは限らない。
     *   ただし教材としては「十分大きな固定長バッファを用意して getcwd() に渡す」
     *   という典型例として分かりやすい。
     */
    char buf[PATH_MAX+1];

    /*
     * getcwd(buf, sizeof(buf))
     *
     * getcwd は
     *
     *   get current working directory
     *
     * の略であり、現在の作業ディレクトリを絶対パス文字列として取得する関数である。
     *
     * 第1引数:
     *   buf
     *   -> 結果の文字列を書き込む先頭アドレス
     *
     * 第2引数:
     *   sizeof(buf)
     *   -> そのバッファの大きさ
     *
     * getcwd は、成功すると現在ディレクトリの絶対パスを buf に書き込む。
     *
     * たとえば現在位置が
     *
     *   /tmp
     *
     * なら、成功後に buf は
     *
     *   "/tmp"
     *
     * という C 文字列になる。
     *
     * 戻り値:
     *   成功時:
     *     buf を返す（正確には書き込み先バッファ先頭へのポインタ）
     *
     *   失敗時:
     *     NULL を返す
     *
     * したがって、このコードでは
     *
     *   == NULL
     *
     * でエラー判定している。
     *
     * 失敗理由の例:
     *   - バッファが小さすぎる
     *   - 現在ディレクトリにアクセスできない
     *   - 途中の親ディレクトリ参照に問題がある
     *   - 何らかのシステムエラー
     *
     * アルゴリズムの観点では、この1行が
     *
     *   「OS に対して、今の作業ディレクトリを文字列化して教えてもらう」
     *
     * 処理に相当する。
     */
    if (getcwd(buf, sizeof(buf)) == NULL) {
        /*
         * getcwd が失敗した場合のエラー処理である。
         *
         * perror("getcwd") により、errno に応じた説明付きでエラーメッセージを表示する。
         *
         * 例:
         *   getcwd: Numerical result out of range
         *   getcwd: Permission denied
         *
         * など。
         *
         * このようにシステムコールやライブラリ関数の失敗時に perror を使うと、
         * 失敗した関数名と理由をまとめて出せるので便利である。
         */
        perror("getcwd");

        /*
         * 現在ディレクトリを取得できなかったので異常終了する。
         *
         * exit(1) の 1 は「正常終了ではない」ことを呼び出し元へ伝える終了コードである。
         *
         * このコードでは getcwd に失敗すると、それ以上意味のある処理はできないため、
         * そのまま終了している。
         */
        exit(1);
    }

    /*
     * getcwd が成功したので、buf の中には現在の作業ディレクトリの絶対パス文字列が入っている。
     *
     * それを %s で標準出力へ表示する。
     *
     * たとえば buf が
     *
     *   "/home/maton/dev/UNIX_Linux_Practice"
     *
     * なら、画面には
     *
     *   /home/maton/dev/UNIX_Linux_Practice
     *
     * と1行表示される。
     *
     * 最後に '\n' を付けているので、端末上でも見やすい。
     *
     * この1行が、このプログラムの最終目的である。
     */
    printf("%s\n", buf);

    /*
     * 正常終了。
     *
     * ここまで来たということは、
     *   - getcwd に成功した
     *   - 現在ディレクトリを表示できた
     *
     * ということなので、return 0 によって正常終了を表す。
     *
     * main 関数での return 0 は、呼び出し元（通常はシェル）に
     * 「このプログラムは正常に終わった」と伝える意味を持つ。
     */
    return 0;
}
