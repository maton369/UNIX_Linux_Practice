/* fs/files0.c */
#include <errno.h>      /* errno を使うためのヘッダ */
#include <sys/types.h>  /* DIR や lstat 系に関わる型定義が入ることがある */
#include <stdio.h>      /* fprintf, printf, putchar を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/stat.h>   /* struct stat, lstat, S_IFMT, S_IFDIR などを使うためのヘッダ */
#include <dirent.h>     /* opendir, readdir, closedir, struct dirent を使うためのヘッダ */
#include <unistd.h>     /* chdir, lstat を使うためのヘッダ */

/*
 * このプログラムの主題は、
 *
 *   「あるディレクトリの中に、どの種類のファイルが何個あるかを数え、
 *    その分布を記号列として可視化する」
 *
 * ことである。
 *
 * 一覧表示そのものを行う `ls` 的なプログラムではなく、
 * ディレクトリに含まれる各エントリを走査して、
 *
 *   - ディレクトリ
 *   - 通常ファイル
 *   - キャラクタデバイス
 *   - ブロックデバイス
 *   - FIFO
 *   - ソケット
 *   - シンボリックリンク
 *   - その他
 *
 * に分類し、それぞれの個数だけ対応する記号を出力する。
 *
 * たとえば、対象ディレクトリの中に
 *
 *   ディレクトリ 3 個
 *   通常ファイル 5 個
 *   シンボリックリンク 2 個
 *
 * があるなら、おおまかには
 *
 *   ///-----@@
 *
 * のような文字列が出る。
 *
 * つまりこのコードは、ディレクトリ内容を
 * 「名前一覧」ではなく「型分布ヒストグラム風の記号列」
 * として表現するプログラムである。
 *
 * アルゴリズム全体は次の通りである。
 *
 *   1. コマンドライン引数から対象ディレクトリ名を受け取る
 *   2. chdir() でそのディレクトリへ移動する
 *   3. opendir(".") で現在ディレクトリを開く
 *   4. readdir() で各エントリを1個ずつ読む
 *   5. 各エントリ名に対して lstat() を呼び、属性情報を取る
 *   6. st_mode のファイル種別ビットを見て分類カウンタを増やす
 *   7. すべて読み終えたら、カウンタ数だけ記号を出力する
 *
 * 特に重要なのは、
 *
 *   - `stat()` ではなく `lstat()` を使っていること
 *   - `st_mode & S_IFMT` でファイル種別だけを取り出していること
 *
 * である。
 *
 * `lstat()` を使う理由:
 *   シンボリックリンクを「リンクとして」数えたいからである。
 *
 *   `stat()` を使うとリンク先の属性を見てしまうため、
 *   シンボリックリンク自身を S_IFLNK として数えられない。
 *
 *   一方 `lstat()` はリンクをたどらず、
 *   そのリンクエントリ自身の属性を返す。
 *
 * したがって、このプログラムでは `lstat()` が本質的に重要である。
 */

int main(int argc, char *argv[]) {
    /*
     * sb:
     *   lstat() によって取得した 1 エントリ分の属性情報を受け取る構造体。
     *
     *   各エントリごとに lstat(dep->d_name, &sb) を行い、
     *   その st_mode を見てファイル種別を判定する。
     *
     * dp:
     *   opendir(".") で開いたディレクトリストリーム。
     *
     * dep:
     *   readdir() によって返される現在のディレクトリエントリ。
     *   dep->d_name が、そのエントリ名（ファイル名やディレクトリ名）である。
     *
     * i:
     *   最後に記号を個数分だけ出力するためのループ変数。
     *
     * 各カウンタ:
     *
     *   ndir:
     *     ディレクトリの個数
     *
     *   nreg:
     *     通常ファイル（regular file）の個数
     *
     *   nchr:
     *     キャラクタデバイスファイルの個数
     *
     *   nblk:
     *     ブロックデバイスファイルの個数
     *
     *   nfifo:
     *     FIFO（名前付きパイプ）の個数
     *
     *   nsock:
     *     ソケットファイルの個数
     *
     *   nlnk:
     *     シンボリックリンクの個数
     *
     *   nunknown:
     *     上記のどれにも分類できなかったものの個数
     *
     * つまりこのプログラムは、
     *   「各ファイル種別ごとのカウンタを用意して、
     *    走査しながら該当カテゴリをインクリメントしていく」
     * 集計型アルゴリズムになっている。
     */
    struct stat sb;
    DIR *dp;
    struct dirent *dep;
    int i, ndir, nreg, nchr, nblk, nfifo, nsock, nlnk, nunknown;

    /*
     * 引数個数チェック。
     *
     * このプログラムは
     *
     *   files0 <directory>
     *
     * という形式で、
     * 対象ディレクトリ名を 1 個だけ受け取る設計である。
     *
     * したがって必要な argc は
     *
     *   argv[0] = プログラム名
     *   argv[1] = 対象ディレクトリ名
     *
     * の合計 2 である。
     *
     * ここを確認しないと argv[1] が存在しない場合があり、
     * 不正アクセスの原因になる。
     */
    if (argc != 2) {
        /*
         * 使い方が違っていた場合は usage を表示して異常終了する。
         *
         * stderr に出しているので、
         * 通常出力とは分離されたエラーメッセージになる。
         */
        fprintf(stderr, "usage: files0 <directory>\n");
        exit(1);
    }

    /*
     * 最初に対象ディレクトリ名をそのまま表示している。
     *
     * 出力形式は
     *
     *   <directory-name>: <記号列>
     *
     * の形にしたいので、その前半部分をここで出している。
     *
     * 例:
     *   src: ///----@@
     *
     * のようなイメージである。
     */
    printf("%s: ", argv[1]);

    /*
     * chdir(argv[1])
     *
     * 対象ディレクトリへ現在作業ディレクトリを移動する。
     *
     * なぜわざわざ chdir するのか:
     *
     *   この後の処理では
     *     opendir(".")
     *     lstat(dep->d_name, ...)
     *   のように "." と d_name を使って処理したいからである。
     *
     * もし chdir しなければ、
     *   argv[1] の中を読むには毎回
     *     argv[1] + "/" + dep->d_name
     *   のようなパス連結が必要になる。
     *
     * このコードはそれを避け、
     * 「一度そのディレクトリへ入ってから、相対名で処理する」
     * 方針を取っている。
     *
     * つまりアルゴリズムとしては、
     *
     *   1. 対象ディレクトリへ移動する
     *   2. その場 "." の内容を読む
     *
     * になっている。
     *
     * 失敗理由の例:
     *   - そのディレクトリが存在しない
     *   - アクセス権がない
     *   - 実はディレクトリではない
     */
    if (chdir(argv[1]) != 0) {
        /*
         * chdir 失敗時は簡潔にメッセージだけ出して終了している。
         *
         * ここでは perror ではなく固定文字列を表示しているため、
         * 理由までは出ない。
         *
         * 教材としては「移動できなかった」という事実だけを伝える簡素な形である。
         */
        printf("<cannot chdir>\n");
        return 1;
    }

    /*
     * opendir(".")
     *
     * いま chdir に成功しているので "." は対象ディレクトリを意味する。
     *
     * したがってここで、
     *   「対象ディレクトリをディレクトリストリームとして開く」
     * ことになる。
     *
     * 成功すれば dp に DIR* が入り、
     * readdir(dp) によってエントリを先頭から順に読める。
     */
    if ((dp = opendir(".")) == NULL) {
        /*
         * ディレクトリを開けなかった場合。
         *
         * たとえば権限問題などで起こりうる。
         * ここでも簡潔な固定メッセージだけ出して終了している。
         */
        printf("<cannot list>\n");
        return 1;
    }

    /*
     * readdir() のエラー判定用に errno を 0 に初期化する。
     *
     * readdir() は
     *   - 正常に1件読めたとき -> struct dirent *
     *   - もうエントリが無いとき -> NULL
     *   - エラーのとき -> NULL
     * を返すので、
     * NULL だけでは正常終端かエラーかが分からない。
     *
     * そこでループ前に errno = 0 しておけば、
     * ループ後に
     *
     *   errno == 0 なら正常終端
     *   errno != 0 ならエラー
     *
     * と判定できる。
     *
     * ただしこのコードでは、ループ後に errno を明示確認していない。
     * 教材としては簡略化されている。
     */
    errno = 0;

    /*
     * 各カテゴリのカウンタを 0 で初期化する。
     *
     * ここから先、ディレクトリエントリを1個読むたびに、
     * 該当するカテゴリを 1 増やしていく。
     *
     * つまりこのコードは
     *   「走査しながら集計する」
     * カウントアルゴリズムである。
     */
    ndir = nreg = nchr = nblk = nfifo = nsock = nlnk = nunknown = 0;

    /*
     * readdir(dp) を繰り返し呼び、
     * 対象ディレクトリ内のすべてのエントリを1個ずつ処理するループ。
     *
     * 1回のループでやっていることは、
     *
     *   1. 1個の名前 dep->d_name を得る
     *   2. その名前に対して lstat() する
     *   3. st_mode からファイル種別を取り出す
     *   4. 対応するカウンタを増やす
     *
     * である。
     */
    while ((dep = readdir(dp)) != NULL) {
        /*
         * lstat(dep->d_name, &sb)
         *
         * ここで現在のエントリ名に対して属性取得を行う。
         *
         * stat() ではなく lstat() を使っている理由が非常に重要である。
         *
         *   stat():
         *     シンボリックリンクをたどって、その先の実体属性を見る
         *
         *   lstat():
         *     シンボリックリンクをたどらず、リンク自身の属性を見る
         *
         * このプログラムは
         *   「リンクはリンクとして数えたい」
 * ので、S_IFLNK を正しく数えるために lstat() を使う必要がある。
         *
         * たとえばディレクトリ内に
         *   shortcut -> /tmp/file
         * というシンボリックリンクがあったとして、
         *
         *   stat("shortcut", &sb)
         * だとリンク先 file の型を見てしまう可能性がある。
         *
         *   lstat("shortcut", &sb)
         * なら shortcut 自身が S_IFLNK として見える。
         *
         * これがこのコードで lstat() が不可欠な理由である。
         */
        if (lstat(dep->d_name, &sb) != 0)
            /*
             * lstat に失敗した場合はそのエントリを無視して次へ進む。
             *
             * つまりこのコードは
             *   「個別エントリの失敗は全体の失敗にしない」
             * 方針である。
             *
             * 教材としては簡潔でよいが、
             * 実用コードなら失敗件数を数えたり、エラー表示したりしたくなる。
             */
            continue;   /* エラーを無視 */

        /*
         * sb.st_mode & S_IFMT
         *
         * st_mode には
         *   - ファイル種別
         *   - パーミッション
         * が同居している。
         *
         * そこで S_IFMT というビットマスクを AND することで、
         * 「ファイル種別部分だけ」を取り出している。
         *
         * 図にすると、
         *
         *   st_mode
         *   +--------------------------+
         *   | 種別ビット | 権限ビット |
         *   +--------------------------+
         *
         *   st_mode & S_IFMT
         *   +--------------------------+
         *   | 種別ビットだけ残す       |
         *   +--------------------------+
         *
         * となる。
         *
         * この値を switch で見れば、
         * そのエントリが何者かを判定できる。
         *
         * つまりここは
         *   「属性情報からカテゴリ番号を取り出す」
         * フェーズである。
         */
        switch (sb.st_mode & S_IFMT) {
            case S_IFDIR:
                /*
                 * ディレクトリ。
                 *
                 * "." や ".." もここに含まれる。
                 */
                ndir++;
                break;

            case S_IFREG:
                /*
                 * 通常ファイル（regular file）。
                 *
                 * テキストファイルやバイナリファイルなど、
                 * いわゆる普通のファイルがここに入る。
                 */
                nreg++;
                break;

            case S_IFCHR:
                /*
                 * キャラクタデバイスファイル。
                 *
                 * 例:
                 *   /dev/tty
                 *   /dev/null
                 * など。
                 */
                nchr++;
                break;

            case S_IFBLK:
                /*
                 * ブロックデバイスファイル。
                 *
                 * 例:
                 *   ディスクデバイスに対応する特殊ファイルなど。
                 */
                nblk++;
                break;

            case S_IFIFO:
                /*
                 * FIFO（名前付きパイプ）。
                 *
                 * プロセス間通信のための特殊ファイル。
                 */
                nfifo++;
                break;

            case S_IFSOCK:
                /*
                 * ソケットファイル。
                 *
                 * UNIX ドメインソケットなどがこれに当たる。
                 */
                nsock++;
                break;

            case S_IFLNK:
                /*
                 * シンボリックリンク。
                 *
                 * このコードでは lstat() を使っているため、
                 * リンクはリンクとして正しくここに分類される。
                 */
                nlnk++;
                break;

            default:
                /*
                 * 上記の既知カテゴリに当てはまらないもの。
                 *
                 * 普通の環境ではあまり多くないが、
                 * 未知・特殊なものをここへ逃がしている。
                 *
                 * つまり分類アルゴリズムとしては
                 *   「どれにも当てはまらなければ unknown」
                 * というフォールバックを持っている。
                 */
                nunknown++;
                break;
        }
    }

    /*
     * ndir -= 2;
     *
     * ディレクトリ一覧には通常 "." と ".." が含まれる。
     *
     *   "."  : 自分自身のディレクトリ
     *   ".." : 親ディレクトリ
     *
     * これらは人間に見せる分類結果としては通常の「中身」とは考えにくいので、
     * ここではディレクトリ数から 2 引いて補正している。
     *
     * つまりこのコードのディレクトリ数は
 *   「実質的な内容としてのサブディレクトリ数」
     * を目指している。
     *
     * 注意:
     *   空でないディレクトリでは通常妥当だが、
     *   特殊な状況では常に 2 を引くのが絶対安全とは限らない。
     *   ただし教材としてはとても分かりやすい簡略化である。
     */
    ndir -= 2;  /* . と .. は表示しない */

    /*
     * ここから先は、集計結果を「記号列」として出力するフェーズである。
     *
     * 各カテゴリごとに、個数分だけ対応する記号を putchar() で出す。
     *
     * 記号の対応は以下の通り。
     *
     *   / : ディレクトリ
     *   - : 通常ファイル
     *   % : キャラクタデバイス
     *   # : ブロックデバイス
     *   | : FIFO
     *   = : ソケット
     *   @ : シンボリックリンク
     *   ? : 不明
     *
     * つまり出力文字列は
     *   「カテゴリごとに個数だけ記号を連続表示したヒストグラム風表現」
     * である。
     *
     * たとえば
     *   ndir = 2
     *   nreg = 4
     *   nlnk = 1
     * なら
     *
     *   //----@
     *
     * のように出る。
     */

    /*
     * ディレクトリ 1 個につき '/' を 1 個出す。
     */
    for (i = 0; i < ndir;     i++) putchar('/');

    /*
     * 通常ファイル 1 個につき '-' を 1 個出す。
     */
    for (i = 0; i < nreg;     i++) putchar('-');

    /*
     * キャラクタデバイス 1 個につき '%' を 1 個出す。
     */
    for (i = 0; i < nchr;     i++) putchar('%');

    /*
     * ブロックデバイス 1 個につき '#' を 1 個出す。
     */
    for (i = 0; i < nblk;     i++) putchar('#');

    /*
     * FIFO 1 個につき '|' を 1 個出す。
     */
    for (i = 0; i < nfifo;    i++) putchar('|');

    /*
     * ソケット 1 個につき '=' を 1 個出す。
     */
    for (i = 0; i < nsock;    i++) putchar('=');

    /*
     * シンボリックリンク 1 個につき '@' を 1 個出す。
     *
     * 記号 '@' は「どこかを指している」という印象があり、
     * リンク表現として分かりやすい。
     */
    for (i = 0; i < nlnk;     i++) putchar('@');

    /*
     * 不明カテゴリ 1 個につき '?' を 1 個出す。
     *
     * つまり '?' は
     *   「分類できなかったものがこれだけある」
     * ことを示す。
     */
    for (i = 0; i < nunknown; i++) putchar('?');

    /*
     * 最後に改行を1つ出して、出力行を閉じる。
     *
     * 結果として 1 行の記号列ヒストグラムが完成する。
     */
    putchar('\n');

    /*
     * 開いていたディレクトリストリームを閉じる。
     *
     * アルゴリズム全体としては、
     *
     *   chdir -> opendir -> readdir の繰り返し -> 集計表示 -> closedir
     *
     * という流れになっているので、
     * ここが後始末に相当する。
     *
     * 元コードでは戻り値チェックをしていない。
     * 教材としては簡潔だが、厳密には確認してもよい。
     */
    closedir(dp);

    /*
     * 正常終了。
     *
     * ここまで来たということは、
     *   - 引数形式が正しかった
     *   - chdir / opendir に成功した
     *   - ディレクトリエントリの走査と分類が終わった
     *   - 記号列表示まで完了した
     *
     * ということである。
     */
    return 0;
}
