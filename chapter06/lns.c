/* fs/lns.c */
#include <stdio.h>      /* fprintf, perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <unistd.h>     /* symlink を使うためのヘッダ */

/*
 * このプログラムの主題は「シンボリックリンク（symbolic link, symlink）」である。
 *
 * UNIX / Linux では、ファイル名は単にデータ本体そのものではなく、
 * ファイルシステム上のオブジェクトへ到達するための“名前”として扱われる。
 *
 * その中でシンボリックリンクは、
 *
 *   「別のパス名を指し示す特別なファイル」
 *
 * である。
 *
 * たとえば
 *
 *   original.txt
 *
 * というファイルがあり、そこに対するシンボリックリンク
 *
 *   link.txt
 *
 * を作ると、利用者から見ると
 *
 *   link.txt -> original.txt
 *
 * のような関係になる。
 *
 * 重要なのは、シンボリックリンクは
 *   「元ファイルの中身をコピーする」
 * のではなく、
 *   「元ファイルへのパス文字列を保持する」
 * という点である。
 *
 * つまり symlink は実体ファイルそのものではなく、
 * “参照先の名前”を保存する特殊なエントリだと考えると分かりやすい。
 *
 * このプログラムでは、
 *
 *   1. コマンドライン引数を確認する
 *   2. 第1引数をリンク先（target）
 *   3. 第2引数を新しく作るシンボリックリンク名（linkpath）
 *      として解釈する
 *   4. symlink(target, linkpath) を呼ぶ
 *   5. 成功すれば終了、失敗すれば perror して終了する
 *
 * という流れで動く。
 *
 * たとえば
 *
 *   ./lns original.txt link.txt
 *
 * と実行すると、
 *
 *   link.txt
 *
 * という名前のシンボリックリンクが作られ、その中には
 *
 *   "original.txt"
 *
 * という参照先パスが記録される。
 *
 * その結果、たとえば cat link.txt のようにアクセスすると、
 * OS は link.txt をたどって original.txt に到達しようとする。
 *
 * ただしここで本当に大切なのは、
 * シンボリックリンク作成時点では
 *
 *   「リンク先の実体ファイルが存在している必要は必ずしもない」
 *
 * という点である。
 *
 * つまり symlink() は
 *   “参照文字列を持つリンクオブジェクトを作る”
 * 操作であり、
 *   “リンク先が正しいかを今すぐ保証する”
 * 操作ではない。
 *
 * そのため、存在しないファイルを指すリンクも作れてしまう。
 * そういうリンクは壊れたシンボリックリンク（dangling symlink）などと呼ばれる。
 *
 * このコードは、そのシンボリックリンク作成操作を最小限の形で行うプログラムである。
 */

int main(int argc, char *argv[]) {
    /*
     * argc:
     *   コマンドライン引数の個数。
     *
     * argv:
     *   コマンドライン引数文字列の配列。
     *
     * このプログラムでは、使い方として
     *
     *   argv[0] = 実行したプログラム名（例: "./lns"）
     *   argv[1] = リンク先として記録したいパス（target）
     *   argv[2] = 新しく作るシンボリックリンクの名前（linkpath）
     *
     * を期待している。
     *
     * したがって、必要な引数数は 3 個である。
     *
     * 例:
     *   ./lns original.txt link.txt
     *
     * の場合
     *
     *   argc    = 3
     *   argv[0] = "./lns"
     *   argv[1] = "original.txt"
     *   argv[2] = "link.txt"
     *
     * となる。
     */

    /*
     * 引数個数チェック。
     *
     * このプログラムは
     *
     *   <file> <symbolic link>
     *
     * の 2 引数を要求する。
     *
     * さらに argv[0] にはプログラム名が入るので、
     * argc は合計で 3 でなければならない。
     *
     * つまり、
     *
     *   argc != 3
     *
     * なら使い方が誤っていると判断する。
     *
     * このチェックをしないと、
     * argv[1] や argv[2] が存在しないのにアクセスしてしまい、
     * 不正動作の原因になる。
     *
     * アルゴリズム的には、
     * ここは
     *
     *   「入力前提条件の検査」
     *
     * である。
     */
    if (argc != 3) {
        /*
         * 使い方が間違っているときのエラーメッセージ。
         *
         * fprintf(stderr, ...) を使って標準エラー出力へ表示している。
         * これにより、通常出力とエラーメッセージを区別できる。
         *
         * argv[0] を使うことで、
         * 実際にどの名前で起動されたプログラムかを usage に反映できる。
         *
         * たとえば
         *
         *   ./lns
         *
         * という名前で実行されたなら、
         *
         *   usage: ./lns <file> <symbolic link>
         *
         * のように表示される。
         *
         * ここでの <file> は「リンク先として記録したいパス」、
         * <symbolic link> は「作成するリンク名」を意味する。
         *
         * 注意:
         *   文脈によっては <file> という表記は少し誤解を生みやすい。
         *   実際には第1引数は通常ファイルに限らず、
         *   ディレクトリや相対パス、絶対パスでもよい。
         *   つまりより正確には
         *
         *     <target-path> <symlink-name>
         *
         * に近い意味である。
         */
        fprintf(stderr, "usage: %s <file> <symbolic link>\n", argv[0]);

        /*
         * 引数が正しくないので異常終了する。
         *
         * exit(1) の 1 は、
         * 呼び出し元（通常はシェル）に対して
         * 「正常終了ではなかった」
         * ことを伝える終了コードである。
         */
        exit(1);
    }

    /*
     * symlink(argv[1], argv[2])
     *
     * ここがこのプログラムの核心である。
     *
     * symlink() はシンボリックリンクを作成するシステムコールであり、
     * 意味は
     *
     *   第1引数: target
     *     リンク先として記録するパス文字列
     *
     *   第2引数: linkpath
     *     新しく作るシンボリックリンクそのものの名前
     *
     * である。
     *
     * つまり
     *
     *   symlink("original.txt", "link.txt")
     *
     * は、
     *
     *   link.txt という名前のシンボリックリンクを作り、
     *   そのリンクの中に "original.txt" という参照先を記録する
     *
     * ことを意味する。
     *
     * 図にするとこうである。
     *
     *   作成前:
     *     original.txt   （実体ファイル）
     *
     *   実行:
     *     symlink("original.txt", "link.txt")
     *
     *   作成後:
     *     link.txt  ---->  "original.txt"
     *
     * 重要なのは、link.txt の中に「original.txt への参照情報」が入るのであって、
     * original.txt の内容そのものがコピーされるわけではないことだ。
     *
     * さらに、
     * 第1引数 argv[1] は“今この瞬間に存在している必要がない”ことにも注意が必要である。
     *
     * たとえば
     *
     *   symlink("nosuchfile", "badlink")
     *
     * も、多くの場合はリンク作成自体は成功する。
     * その場合 badlink は「存在しないものを指すリンク」になる。
     *
     * つまり symlink() は
     *
     *   「参照先の妥当性を保証する」
     *
     * のではなく、
     *
     *   「参照先文字列を持つリンクオブジェクトを作る」
     *
     * 関数だと理解するのが正しい。
     *
     * 戻り値:
     *   成功時:
     *     0
     *
     *   失敗時:
     *     -1
     *
     * このコードでは、-1 をエラーとして検出している。
     *
     * 失敗理由の例:
     *   - argv[2] で指定したリンク名がすでに存在している
     *   - その場所に作成権限がない
     *   - 親ディレクトリが存在しない
     *   - パスが不正
     *   - ファイルシステム上の制約に反している
     *
     * アルゴリズム的には、この1行が
     *
     *   「与えられた target-path と link-name をもとに、
     *    シンボリックリンクエントリをファイルシステムへ追加する」
     *
     * 操作に相当する。
     */
    if (symlink(argv[1], argv[2]) == -1) {
        /*
         * symlink() が失敗した場合のエラー処理。
         *
         * perror("symlink") により、errno に応じた理由を表示する。
         *
         * 例:
         *   symlink: File exists
         *   symlink: Permission denied
         *   symlink: No such file or directory
         *
         * など。
         *
         * ここで "No such file or directory" が出る場合は、
         * 多くは「リンク先 argv[1] が存在しない」ではなく、
         * 「リンクを作ろうとしている場所 argv[2] 側の親ディレクトリなどが問題」
         * であることが多い。
         *
         * なぜなら、前述の通り symlink は
         * target の存在確認そのものは必須ではないからである。
         */
        perror("symlink");

        /*
         * シンボリックリンクを作れなかったので異常終了する。
         *
         * exit(1) により、
         * 呼び出し元に「処理失敗」を伝える。
         */
        exit(1);
    }

    /*
     * ここまで来たということは、
     *   - 引数個数が正しかった
     *   - symlink() に成功した
     *
     * ということである。
     *
     * したがって return 0 により正常終了を表す。
     *
     * シェルなどから見ると、
     * 終了ステータス 0 は「リンク作成成功」を意味する。
     */
    return 0;
}
