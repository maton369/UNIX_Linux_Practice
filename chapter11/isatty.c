#include <unistd.h>     /* isatty(), STDOUT_FILENO を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「標準出力が端末（tty）に接続されているかどうかを isatty() で判定する」
 *
 * ことである。
 *
 * たった 1 行の本体しかないが、
 * UNIX / Linux の入出力モデルを理解するうえでかなり重要な題材である。
 *
 * ----------------------------------------
 * まず isatty() とは何か
 * ----------------------------------------
 *
 * isatty(fd) は、
 *
 *   「ファイル記述子 fd が、端末デバイスに接続されているか？」
 *
 * を調べる関数である。
 *
 * 返り値は概念的に
 *
 *   1  : 端末である
 *   0  : 端末ではない
 *
 * である。
 *
 * つまりこれは、
 *
 *   「この出力先は、人が直接見ている対話端末か？」
 *
 * を簡単に判定する関数だと考えると分かりやすい。
 *
 * ----------------------------------------
 * なぜそんな判定が必要なのか
 * ----------------------------------------
 *
 * UNIX では、標準出力はいつも画面とは限らない。
 *
 * たとえば次の 3 つは全部「標準出力を使っている」が意味が違う。
 *
 * 1. 端末へそのまま出す
 *
 *      ./a.out
 *
 *    この場合、標準出力は通常ターミナル画面につながっている。
 *
 * 2. ファイルへリダイレクトする
 *
 *      ./a.out > out.txt
 *
 *    この場合、標準出力はファイルにつながる。
 *
 * 3. パイプで別プロセスへ渡す
 *
 *      ./a.out | cat
 *
 *    この場合、標準出力はパイプにつながる。
 *
 * 同じ STDOUT_FILENO でも、
 *
 *   - 端末
 *   - ファイル
 *   - パイプ
 *
 * のどれにつながっているかは実行状況次第で変わる。
 *
 * そのためプログラム側で
 *
 *   「今の stdout は本当に端末なのか？」
 *
 * を知りたい場面がある。
 *
 * それを調べるのが isatty() である。
 *
 * ----------------------------------------
 * tty とは何か
 * ----------------------------------------
 *
 * tty は歴史的には teletypewriter に由来するが、
 * 現代的には大ざっぱに
 *
 *   「対話型の端末インタフェース」
 *
 * と捉えるとよい。
 *
 * 端末エミュレータ上で動くシェルから起動したプログラムなら、
 * 標準入力・標準出力・標準エラー出力が tty につながっていることが多い。
 *
 * 一方でリダイレクトやパイプを使うと、
 * それらは tty ではなくなる。
 *
 * だから isatty() は、
 *
 *   「この fd は対話端末か？」
 *
 * を判定する関数だと理解するとよい。
 */

int main(void)
{
    /*
     * STDOUT_FILENO:
     *   標準出力のファイル記述子番号を表す定数である。
     *
     * UNIX では慣例的に
     *
     *   0 -> 標準入力  (stdin)
     *   1 -> 標準出力  (stdout)
     *   2 -> 標準エラー (stderr)
     *
     * となっている。
     *
     * したがって
     *
     *   STDOUT_FILENO
     *
     * は通常 1 を意味する。
     *
     * ----------------------------------------
     * isatty(STDOUT_FILENO) の意味
     * ----------------------------------------
     *
     * この式は、
     *
     *   「標準出力が端末かどうか」
     *
     * を調べている。
     *
     * 実行例を概念的に見ると:
     *
     *   端末でそのまま実行:
     *     ./a.out
     *     -> stdout は端末
     *     -> isatty(STDOUT_FILENO) は 1 になりやすい
     *
     *   ファイルへリダイレクト:
     *     ./a.out > out.txt
     *     -> stdout はファイル
     *     -> isatty(STDOUT_FILENO) は 0
     *
     *   パイプ:
     *     ./a.out | cat
     *     -> stdout はパイプ
     *     -> isatty(STDOUT_FILENO) は 0
     *
     * つまりこの 1 行だけで、
     *
     *   「今の標準出力が対話端末か否か」
     *
     * を返している。
     *
     * ----------------------------------------
     * return にそのまま使っている意味
     * ----------------------------------------
     *
     * main() の return 値はプロセスの終了ステータスになる。
     *
     * ここでは
     *
     *   return isatty(STDOUT_FILENO);
     *
     * なので、
     *
     *   stdout が tty なら 1 を返して終了
     *   stdout が tty でなければ 0 を返して終了
     *
     * となる。
     *
     * ただしここは少し注意が必要である。
     *
     * UNIX コマンドの慣習では、
     *
     *   0 = 成功
     *   非0 = 何らかの失敗や特別な状態
     *
     * と扱うことが多い。
     *
     * そのため、このコードは
     *
     *   「tty なら成功」
     *
     * という意味の終了ステータスにはなっていない。
     *
     * むしろ
     *
     *   tty なら終了ステータス 1
     *   tty でなければ終了ステータス 0
     *
     * なので、シェル的な真偽感覚とは少し逆に見えることがある。
     *
     * たとえばシェルで
     *
     *   ./a.out
     *   echo $?
     *
     * をすると、
     * 端末で直接実行した場合に 1 が出るかもしれない。
     *
     * ----------------------------------------
     * もし「成功/失敗」の慣習に合わせたいなら
     * ----------------------------------------
     *
     * もし
     *
     *   「stdout が tty なら終了ステータス 0 にしたい」
     *
     * なら、たとえば
     *
     *   return !isatty(STDOUT_FILENO);
     *
     * のように逆転させる設計もある。
     *
     * ただしこのサンプルの主題は終了コード設計ではなく、
     *
     *   isatty() そのものの動作確認
     *
     * なので、返り値をそのまま返しているのだと考えるとよい。
     *
     * ----------------------------------------
     * このコードが教えている本質
     * ----------------------------------------
     *
     * このコードで学ぶべきことは、
     *
     *   「stdout は常に画面とは限らない」
     *
     * という UNIX 的な入出力の考え方である。
     *
     * プログラムは stdout に書いているだけでも、
     * 実行環境によってその先が
     *
     *   - 端末
     *   - ファイル
     *   - パイプ
     *
     * のどれになるかが変わる。
     *
     * だからこそ
     *
     *   isatty()
     *
     * のような判定が意味を持つ。
     *
     * 実務でも、
     *
     *   - 端末相手なら色を付ける
     *   - 端末相手なら進捗バーを出す
     *   - ファイルやパイプ相手なら制御文字を出さない
     *
     * といった分岐に使われることが多い。
     *
     * つまり isatty() は、
     *
     *   「対話向けの表示をしてよいか？」
     *
     * を判断するための基本関数である。
     */
    return isatty(STDOUT_FILENO);
}
