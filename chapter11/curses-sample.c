#include <curses.h>     /* initscr(), endwin(), noecho(), getnstr(), move(),
                           attron(), printw(), refresh(), getch(), A_REVERSE を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「curses ライブラリを使って端末画面を“単なる文字列の流し先”ではなく、
 *    座標付きの画面バッファとして扱い、入力・表示・属性変更を行う」
 *
 * ことである。
 *
 * 以前の printf() ベースのコードでは、
 * 端末は基本的に
 *
 *   「前へ前へ文字が流れていく出力先」
 *
 * として扱われていた。
 *
 * しかし curses ライブラリを使うと、
 *
 *   - 画面全体を管理する
 *   - カーソル位置を座標で指定する
 *   - 文字属性（反転表示など）を変える
 *   - 入力のエコーを制御する
 *   - 実画面への反映タイミングを自分で決める
 *
 * といったことができる。
 *
 * このコードはその最小例として、
 *
 *   1. curses モードを初期化する
 *   2. 入力エコーを切る
 *   3. 文字列入力を受け取る
 *   4. 画面上の別位置へ移動する
 *   5. 反転表示属性を有効にして結果を描く
 *   6. refresh() で実画面へ反映する
 *   7. キー入力を1回待つ
 *   8. curses モードを終了して端末を元へ戻す
 *
 * という流れを示している。
 *
 * つまりこのプログラムは、
 *
 *   「curses による画面初期化、入力制御、座標移動、属性付き描画、画面更新、終了復元」
 *
 * という curses 利用の基本パターンをまとめた教材である。
 */

int main(void)
{
    /*
     * buf:
     *   ユーザが入力した文字列を格納するバッファである。
     *
     * getnstr() がこの配列へ文字列を書き込む。
     *
     * サイズは 1024 バイトあり、
     * 教材としては十分大きい。
     *
     * ただし一般論としては、
     *
     *   「入力上限を明示し、バッファ長を超えないようにする」
     *
     * のが重要であり、
     * このコードでも getnstr() に長さを渡している点が安全上の要となっている。
     */
    char buf[1024];

    /*
     * initscr();
     *
     * ここが curses 利用の出発点である。
     *
     * initscr() は、
     *
     *   「curses ライブラリを初期化し、
     *    端末を curses が管理できる画面モードへ入れる」
     *
     * 関数である。
     *
     * これを呼ぶことで、
     *
     *   - 画面サイズ情報の取得
     *   - 画面バッファの準備
     *   - curses 内部状態の初期化
     *   - 端末制御に必要なセットアップ
     *
     * などが行われる。
     *
     * ----------------------------------------
     * curses ライブラリとは何か
     * ----------------------------------------
     *
     * curses は、
     *
     *   「端末画面を行・列の2次元スクリーンとして扱うためのライブラリ」
     *
     * と考えると分かりやすい。
     *
     * 通常の printf() では、
     *
     *   文字列を順番に前へ出していく
     *
     * だけだが、
     * curses では
     *
     *   - 今どの座標へ書くか
     *   - どんな属性で書くか
     *   - いつ実画面へ反映するか
     *
     * をライブラリ経由で制御できる。
     *
     * つまり initscr() は、
     *
     *   「これ以降は curses 流儀で端末を扱います」
     *
     * という開始宣言に相当する。
     *
     * 注意:
     *   実務では initscr() の失敗や、より細かな初期化関数
     *   （cbreak(), keypad(), raw(), etc.）を組み合わせることも多い。
     *   このサンプルは最小構成で本質を見せている。
     */
    initscr();

    /*
     * noecho();
     *
     * ここで curses に対して
     *
     *   「ユーザ入力を自動では画面へエコーしない」
     *
     * よう指示する。
     *
     * 通常の端末では、ユーザがキーを打つとその文字は自動的に画面へ表示される。
     * しかし curses では入力表示を自前で制御したいことが多い。
     *
     * noecho() を呼ぶと、
     *
     *   入力は受け取るが、ライブラリが自動では表示しない
     *
     * 状態になる。
     *
     * ----------------------------------------
     * termios の ECHO との関係
     * ----------------------------------------
     *
     * 以前の termios の例では
     *
     *   ECHO ビットを直接落としていた
     *
     * が、
     * curses では noecho() のような高水準 API で同様の意図を表現できる。
     *
     * つまり curses は、
     *
     *   「端末の低レベル設定を、画面ライブラリとして扱いやすく抽象化している」
     *
     * とも言える。
     *
     * ----------------------------------------
     * このコードで noecho() が重要な理由
     * ----------------------------------------
     *
     * このあと getnstr() で文字列入力を受けるが、
     * noecho() にしているので、
     *
     *   入力中の文字列がそのまま見えるわけではない
     *
     * 状態になる。
     *
     * つまりこのプログラムは、
     *
     *   「入力結果をあとで別の場所に表示する」
     *
     * のであって、
     *
     *   「入力中にその場で見せる」
     *
     * 設計ではない。
     */
    noecho();

    /*
     * getnstr(buf, sizeof(buf));
     *
     * ここでユーザから文字列入力を受け取り、buf に格納する。
     *
     * getnstr() は curses の入力関数であり、
     *
     *   「最大 n-1 文字までの文字列を読み込む」
     *
     * と考えるとよい。
     *
     * このコードでは
     *
     *   sizeof(buf)
     *
     * を上限にしているので、
     * バッファ長に応じた制限付き入力になっている。
     *
     * ----------------------------------------
     * curses における入力の意味
     * ----------------------------------------
     *
     * curses では、入力も単なる getchar() ではなく、
     * 画面管理と整合する形で扱うことが多い。
     *
     * getnstr() はその中でも
     *
     *   「1行の文字列を読む」
     *
     * 高水準 API である。
     *
     * ユーザが文字を打ち、通常は Enter で確定すると、
     * その文字列が buf に入る。
     *
     * ただし noecho() の影響で、
     *
     *   入力中の文字は自動表示されない
     *
     * ことに注意したい。
     *
     * つまりこの行の意味は、
     *
     *   「画面ライブラリの入力 API を使って、
     *    1 本の文字列を安全に受け取る」
     *
     * である。
     *
     * 注意:
     *   実務では戻り値確認や入力キャンセル処理などを考えることもある。
     *   このサンプルでは本質を簡潔に示している。
     */
    getnstr(buf, sizeof(buf));

    /*
     * move(2, 10);
     *
     * ここで curses の現在カーソル位置を
     *
     *   2 行目, 10 列目
     *
     * へ移動する。
     *
     * curses では画面上の書き込み位置を座標で扱える。
     * move(y, x) の形式で、
     *
     *   第1引数 = 行 (y)
     *   第2引数 = 列 (x)
     *
     * である。
     *
     * ----------------------------------------
     * この move() の役割
     * ----------------------------------------
     *
     * getnstr() で入力を受けた直後の位置にそのまま結果を書くのではなく、
     * 画面の別位置へ結果を表示したい。
     *
     * そのために move() で書き込み位置を変えている。
     *
     * つまりこの行は、
     *
     *   「入力欄とは別の場所へ結果表示する」
     *
     * 構成を作っている。
     *
     * これが curses 的な画面設計の入り口である。
     *
     * printf() ベースの世界では、
     * 「次に出す文字列は今のカーソル位置の続き」に出ることが多い。
     *
     * しかし curses では、
     *
     *   「画面のここに書く」
     *
     * を明示できる。
     *
     * つまり move() は、
     *
     *   端末画面を座標付きの2次元領域として扱う
     *
     * curses らしさを最も分かりやすく示している。
     */
    move(2, 10);

    /*
     * attron(A_REVERSE);
     *
     * ここで現在の描画属性として
     *
     *   A_REVERSE
     *
     * を有効にしている。
     *
     * A_REVERSE は「反転表示」を意味する属性であり、
     * 多くの端末では
     *
     *   前景色と背景色を入れ替えたような見た目
     *
     * になる。
     *
     * attron() は、
     *
     *   「これ以降の描画にこの属性を乗せる」
     *
     * 関数である。
     *
     * ----------------------------------------
     * curses における属性とは何か
     * ----------------------------------------
     *
     * curses では単に文字を置くだけでなく、
     *
     *   - 反転
     *   - 太字
     *   - 下線
     *   - 色
     *
     * などを属性として文字に付与できる。
     *
     * つまり curses は
     *
     *   「どの文字を」
     *   「どこへ」
     *   「どんな見た目で」
     *
     * 描くかを管理できる。
     *
     * このコードではその最小例として、
     *
     *   入力結果を反転表示で目立たせる
     *
     * ことをしている。
     *
     * 注意:
     *   本格的には attroff() で属性を戻すことも多い。
     *   このサンプルではこの後すぐ終了フローへ進むので省略している。
     */
    attron(A_REVERSE);

    /*
     * printw("input = %s\n", buf);
     *
     * curses 版の printf() のような関数である。
     *
     * printw() は、
     *
     *   「現在の curses カーソル位置へ書式付き文字列を書き込む」
     *
     * 関数である。
     *
     * このコードでは直前に move(2, 10) をしているので、
     * 2 行目 10 列目から
     *
     *   input = <ユーザ入力>
     *
     * が描かれる。
     *
     * さらに attron(A_REVERSE) の効果が有効なままなので、
     * この文字列は反転表示属性付きで描かれる。
     *
     * ----------------------------------------
     * 重要: まだ画面へ「確定表示」されたとは限らない
     * ----------------------------------------
     *
     * curses では printw() を呼んでも、
     * 必ずしもその瞬間に端末実画面が更新されるとは限らない。
     *
     * 多くの場合、
     *
     *   まず curses の内部画面バッファへ描く
     *
     * だけであり、
     * 実際の端末画面へ反映するには refresh() が必要である。
     *
     * つまりこの行は、
     *
     *   「画面バッファへ反転付きの文字列を描いている」
     *
     * 段階だと考えるとよい。
     *
     * ここが curses の重要な考え方で、
     *
     *   描画命令
     *   と
     *   実画面更新
     *
     * が分離されている。
     */
    printw("input = %s\n", buf);

    /*
     * refresh();
     *
     * ここで curses の内部画面バッファを、
     * 実際の端末画面へ反映する。
     *
     * refresh() は、
     *
     *   「いままで画面バッファへ行った変更を、
     *    実端末へ同期する」
     *
     * 関数である。
     *
     * したがって、
     *
     *   move()
     *   attron()
     *   printw()
     *
     * などで準備した画面状態が、
     * ここで初めてユーザに見える形になる。
     *
     * ----------------------------------------
     * curses の重要な考え方
     * ----------------------------------------
     *
     * curses は一般に
     *
     *   - まず仮想画面（内部バッファ）へ変更を加える
     *   - 最後に refresh() でまとめて反映する
     *
     * という流れで動く。
     *
     * これにより、
     *
     *   画面チラつきを抑える
     *   必要最小限の更新だけ行う
     *   複雑なレイアウト変更をまとめて見せる
     *
     * といった利点がある。
     *
     * つまり refresh() は、
     *
     *   curses の描画を「実画面へコミットする」操作
     *
     * だと言える。
     */
    refresh();

    /*
     * getch();
     *
     * ここで 1 文字入力を待つ。
     *
     * この関数の役割は主に
     *
     *   「結果画面が一瞬で消えないように、一旦停止する」
     *
     * ことである。
     *
     * もしこの行が無いと、
     * refresh() で画面を表示した直後に endwin() が呼ばれて
     * すぐ curses モード終了になってしまい、
     * 結果を落ち着いて見られない。
     *
     * つまり getch() は、
     *
     *   「ユーザに画面を見せるための待機」
     *
     * として使われている。
     *
     * curses では getch() は単なる 1 文字入力 API だが、
     * 簡易デモでは「キー待ち」の用途で非常によく使われる。
     */
    getch();

    /*
     * endwin();
     *
     * ここで curses モードを終了し、
     * 端末を通常状態へ戻す。
     *
     * initscr() で curses の管理下へ入った以上、
     * 最後には endwin() で終了処理を行うのが基本である。
     *
     * これにより、
     *
     *   - 端末表示モードの復元
     *   - 画面管理状態の終了
     *   - 入力設定の復元
     *
     * などが行われる。
     *
     * ----------------------------------------
     * なぜ endwin() が重要か
     * ----------------------------------------
     *
     * curses を使うプログラムで endwin() を忘れると、
     * 端末が不自然な状態のまま残ることがある。
     *
     * たとえば
     *
     *   - エコー設定
     *   - カーソル状態
     *   - 画面表示
     *
     * などが期待通りに戻らず、シェルへ戻ったあと困る可能性がある。
     *
     * したがって curses の基本パターンは、
     *
     *   initscr() で始めて
     *   endwin() で終える
     *
     * である。
     *
     * このコードはその最小の開始・終了対をきちんと含んでいる。
     */
    endwin();

    /*
     * main() の終わり。
     *
     * このプログラム全体をまとめると、
     *
     *   1. initscr() で curses 画面管理を開始
     *   2. noecho() で入力エコーを抑制
     *   3. getnstr() で文字列入力を取得
     *   4. move() で表示位置を変更
     *   5. attron(A_REVERSE) で反転属性を有効化
     *   6. printw() で結果を仮想画面へ描画
     *   7. refresh() で実画面へ反映
     *   8. getch() で確認待ち
     *   9. endwin() で curses 終了
     *
     * という流れである。
     *
     * つまりこのコードは、
     *
     *   「curses ライブラリによって、
     *    入力・位置指定・属性付き表示・画面更新を
     *    高水準に扱う基本例」
     *
     * と言える。
     */
}
