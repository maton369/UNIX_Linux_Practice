#include <stdio.h>      /* printf(), fflush(), stdout を使うためのヘッダ */
#include <stdlib.h>     /* atoi() を使うためのヘッダ */
#include <unistd.h>     /* sleep() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「端末に対してエスケープシーケンスを出力することで、
 *    カーソル位置を制御し、同じ場所の文字を描き替えている」
 *
 * ことである。
 *
 * 一見するとこのプログラムは、
 *
 *   '.', 'o', 'O'
 *
 * を順番に表示しているだけに見える。
 *
 * しかし本質は単なる printf() ではなく、
 *
 *   "\033[D"
 *
 * というエスケープシーケンスを使って、
 * 出力した直後にカーソルを左へ1文字戻している点にある。
 *
 * その結果、次のループで同じ位置に別の文字を書けるので、
 *
 *   . -> o -> O -> . -> o -> O ...
 *
 * のように、
 * 「同じ1文字分の場所が変化している」ように見える。
 *
 * つまりこのコードは、
 *
 *   「端末を単なる文字の流れとして使うのではなく、
 *    カーソル付きの表示装置として使う」
 *
 * 最小例である。
 *
 * ----------------------------------------
 * エスケープシーケンスとは何か
 * ----------------------------------------
 *
 * エスケープシーケンスとは、
 *
 *   「画面にそのまま表示する文字」ではなく、
 *    端末に対する命令として解釈される特殊な文字列
 *
 * のことである。
 *
 * たとえば
 *
 *   \n
 *
 * は改行としてよく知られているが、
 * 端末制御ではさらに
 *
 *   ESC [ D
 *
 * のような並びを使って、
 *
 *   - カーソルを動かす
 *   - 画面を消す
 *   - 文字色を変える
 *
 * などができる。
 *
 * このコードで使っている
 *
 *   "\033[D"
 *
 * は、
 *
 *   ESC [ D
 *
 * を C の文字列表現で書いたものであり、
 *
 *   「カーソルを左に1文字戻す」
 *
 * という意味を持つ。
 *
 * この「出力したあとにカーソルを戻す」という動きによって、
 * 次の文字が同じ場所へ上書きされるように見える。
 */

int main(int argc, char *argv[])
{
    /*
     * i:
     *   for ループのカウンタである。
     *
     *   0 から n-1 まで増えていく。
     *
     * n:
     *   何回表示を更新するかを表す回数である。
     *
     *   コマンドライン引数 argv[1] を atoi() で整数へ変換している。
     *
     * たとえば
     *
     *   ./a.out 10
     *
     * のように実行したなら、
     *
     *   n = 10
     *
     * となり、表示更新を 10 回行う。
     *
     * 注意:
     *   このコードは argv[1] が必ず存在する前提で書かれている。
     *   つまり引数不足チェックはしていない。
     *
     *   教材としては簡潔だが、実務では
     *
     *     if (argc < 2) ...
     *
     *   のような確認を入れたい。
     */
    int i = 0, n = atoi(argv[1]);

    /*
     * c[]:
     *   表示に使う文字の候補配列である。
     *
     *   c[0] = '.'
     *   c[1] = 'o'
     *   c[2] = 'O'
     *
     * となっている。
     *
     * ループ中では
     *
     *   c[i % 3]
     *
     * を使うので、
     *
     *   i = 0 -> '.'
     *   i = 1 -> 'o'
     *   i = 2 -> 'O'
     *   i = 3 -> '.'
     *   i = 4 -> 'o'
     *   i = 5 -> 'O'
     *
     * のように 3 文字が周期的に繰り返される。
     *
     * したがって表示としては、
     *
     *   . o O . o O ...
     *
     * が同じ場所に順番に描かれていくイメージになる。
     */
    char c[] = {'.', 'o', 'O'};

    /*
     * for (i = 0; i < n; i++)
     *
     * 表示更新を n 回繰り返すループである。
     *
     * 各反復でやっていることは次の 3 ステップである。
     *
     *   1. 文字を1つ表示する
     *   2. エスケープシーケンスでカーソルを左に1文字戻す
     *   3. fflush() で今すぐ画面へ反映させる
     *   4. 1秒待つ
     *
     * この繰り返しによって、
     *
     *   「同じ1マスの表示が時間とともに変わる」
     *
     * アニメーションのような見え方になる。
     */
    for (i = 0; i < n; i++) {
        /*
         * printf("%c\033[D", c[i % 3]);
         *
         * ここがこのコードの核心であり、
         * 特にエスケープシーケンスの理解が重要である。
         *
         * ----------------------------------------
         * まず "%c" の部分
         * ----------------------------------------
         *
         *   %c
         *
         * は 1 文字表示するための書式指定子である。
         *
         * ここでは
         *
         *   c[i % 3]
         *
         * が渡されるので、
         *
         *   '.', 'o', 'O'
         *
         * が順番に 1 文字ずつ表示される。
         *
         * ----------------------------------------
         * 次に "\033[D" の部分
         * ----------------------------------------
         *
         * これは C の文字列中で書かれたエスケープシーケンスである。
         *
         * 分解するとこうなる。
         *
         *   \033
         *     -> 8進数 033
         *     -> 10進数 27
         *     -> ASCII の ESC (escape) 文字
         *
         *   [
         *     -> CSI（Control Sequence Introducer）の一部として使われる
         *
         *   D
         *     -> カーソルを左へ移動する命令
         *
         * つまり
         *
         *   "\033[D"
         *
         * は、
         *
         *   ESC [ D
         *
         * という並びであり、
 *
         *   「カーソルを左に1文字戻す」
         *
         * 命令として端末に解釈される。
         *
         * ----------------------------------------
         * 実際に何が起きるか
         * ----------------------------------------
         *
         * たとえば最初の反復で '.' を出したとする。
         *
         *   1. '.' が表示される
         *   2. カーソルは通常、その右隣へ進む
         *   3. しかし直後の "\033[D" によりカーソルが左へ1つ戻る
         *
         * その結果、カーソルは再び '.' の位置に戻る。
         *
         * 次の反復で 'o' を表示すると、
         *
         *   - カーソル位置は同じ場所
         *   - そこへ 'o' が書かれる
         *
         * ので、見た目としては '.' が 'o' に置き換わる。
         *
         * 図にするとこうである。
         *
         *   初回:
         *     位置Xに '.' を表示
         *     カーソルは一旦右へ進む
         *     ESC[D で左に戻る
         *     -> カーソルは再び位置X
         *
         *   次回:
         *     位置Xに 'o' を表示
         *     カーソルは一旦右へ進む
         *     ESC[D で左に戻る
         *     -> カーソルは再び位置X
         *
         *   次々回:
         *     位置Xに 'O' を表示
         *
         * つまり、
         *
         *   「1文字書いては、カーソルを同じ場所へ戻す」
         *
         * を繰り返している。
         *
         * ----------------------------------------
         * なぜ上書きのように見えるのか
         * ----------------------------------------
         *
         * 端末は「画面上の現在カーソル位置に文字を書く」装置である。
         * このコードでは毎回同じ位置にカーソルを戻しているため、
         * そこへ新しい文字を書けば、
         *
         *   前の文字が見た目として置き換わる
         *
         * ことになる。
         *
         * だからこのコードは、
         *
         *   '.' -> 'o' -> 'O'
         *
         * と1文字が変化するアニメーションのように見える。
         *
         * ----------------------------------------
         * エスケープシーケンスの一般化
         * ----------------------------------------
         *
         * この例では ESC[D だけを使っているが、
         * 端末エスケープシーケンスには他にも多くの種類がある。
         *
         * たとえば
         *
         *   ESC[A  -> 上へ移動
         *   ESC[B  -> 下へ移動
         *   ESC[C  -> 右へ移動
         *   ESC[D  -> 左へ移動
         *   ESC[2J -> 画面クリア
         *
         * などがある。
         *
         * このコードはその中でも最も単純な
         *
         *   「カーソルを1文字戻す」
         *
         * 例であり、端末制御の入口として非常に分かりやすい。
         */
        printf("%c\033[D", c[i % 3]);

        /*
         * fflush(stdout);
         *
         * ここも重要である。
         *
         * stdout は通常バッファリングされる。
         * つまり printf() しても、その内容がすぐ端末へ出るとは限らない。
         *
         * 特に改行 '\n' が無い場合、
         * 出力がバッファに溜まったまましばらく見えないことがある。
         *
         * このコードでは毎回
         *
         *   "%c\033[D"
         *
         * を出しているが、改行していない。
         *
         * そのため fflush(stdout) を入れないと、
         *
         *   文字の更新がリアルタイムに見えない
         *
         * 可能性がある。
         *
         * fflush(stdout) により、
         *
         *   「今出した表示命令を、今すぐ端末へ送る」
         *
         * ことができる。
         *
         * つまりこの1行は、
         *
         *   エスケープシーケンスを使った表示変化を
         *   その場で観察できるようにするため
         *
         * に必要である。
         *
         * 図にするとこうである。
         *
         *   printf() だけ:
         *     文字列が stdout バッファに溜まるかもしれない
         *
         *   fflush(stdout):
         *     バッファ内容を今すぐ端末へ押し出す
         *
         * このコードではアニメーション的に見せたいので、
         * fflush() は事実上必須である。
         */
        fflush(stdout);

        /*
         * sleep(1);
         *
         * 1 秒待つ。
         *
         * これにより文字の変化がゆっくりになり、
         *
         *   '.' -> 'o' -> 'O'
         *
         * の変化を人間が視認できる。
         *
         * もし sleep() が無ければ、
         * 文字が非常に高速に入れ替わり、
         * ほとんど一瞬で終わってしまう。
         *
         * つまり sleep(1) は、
         *
         *   「エスケープシーケンスによる表示更新を
         *    アニメーションとして見せるための時間調整」
         *
         * である。
         */
        sleep(1);
    }

    /*
     * ループが終わると main() も終わる。
     *
     * このコードでは最後に改行を出していないため、
     * シェルのプロンプトが同じ行に出ることがある。
     *
     * 教材としては
     *
     *   「同じ位置に文字を書き換える」
     *
     * ことが主題なのでこれでも十分である。
     *
     * 実用上もう少し整えるなら、
     * 最後に改行を1つ出して画面をきれいに終えることもある。
     *
     * ----------------------------------------
     * このコード全体のまとめ
     * ----------------------------------------
     *
     * このプログラムは、
     *
     *   1. 文字を1つ表示する
     *   2. ESC[D でカーソルを左へ戻す
     *   3. fflush() で即時反映する
     *   4. sleep() でゆっくり見せる
     *
     * を繰り返すことで、
     *
     *   「同じ場所の文字が変化する」
     *
     * ことを端末上で実現している。
     *
     * つまりこのコードは、
     *
     *   「エスケープシーケンスを使うと、
     *    端末は単なる文字列出力先ではなく、
     *    カーソル付きの表示装置として扱える」
     *
     * ことを示す最小例である。
     */
}
