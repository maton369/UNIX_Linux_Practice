#include <curses.h>     /* putp(), setupterm(), tigetstr(), tparm() を使うためのヘッダ */
#include <term.h>       /* terminfo 関連定義。環境によってはこちらが本体側 */

/*
 * このコードの主題は、
 *
 *   「terminfo ライブラリを使って、
 *    端末依存の制御文字列をデータベースから取得し、
 *    その端末に合ったカーソル移動命令を生成して出力する」
 *
 * ことである。
 *
 * 以前のエスケープシーケンスの例では、
 *
 *   "\033[D"
 *
 * のように、端末制御列を文字列として直接コード中に埋め込んでいた。
 *
 * これは学習用としては分かりやすいが、
 * 本質的には
 *
 *   「その端末が ANSI/VT 系のエスケープシーケンスを理解する」
 *
 * ことを前提にしている。
 *
 * しかし UNIX 系では、端末の種類は一様ではない。
 * 端末エミュレータや実機端末によって、
 *
 *   - カーソル移動の表現
 *   - 画面クリアの表現
 *   - 色変更の表現
 *
 * などが異なることがある。
 *
 * そこで使うのが terminfo ライブラリである。
 *
 * terminfo の考え方は、
 *
 *   「端末制御列をコードに直書きするのではなく、
 *    端末種別ごとのデータベースから capabilities（能力）を引く」
 *
 * というものである。
 *
 * このコードではその最小例として、
 *
 *   1. setupterm() で現在端末の情報を初期化する
 *   2. tigetstr("cup") で「カーソル位置指定」能力の文字列テンプレートを得る
 *   3. tparm() でそのテンプレートへ行番号・列番号を埋め込む
 *   4. putp() で実際に端末へ送る
 *
 * という流れを示している。
 *
 * ここで "cup" は terminfo capability 名であり、
 *
 *   cursor position
 *
 * を意味する。
 *
 * つまりこのコードは、
 *
 *   「カーソルを 10 行 20 列へ動かすための命令列を、
 *    その端末に合う形で terminfo から生成して出力する」
 *
 * 例である。
 *
 * ----------------------------------------
 * terminfo ライブラリとは何か
 * ----------------------------------------
 *
 * terminfo は、
 *
 *   「端末の能力(capability)を名前付きで管理するデータベース」
 *
 * である。
 *
 * たとえば代表的な能力としては、
 *
 *   clear
 *     -> 画面クリア
 *
 *   cup
 *     -> カーソル移動
 *
 *   civis
 *     -> カーソル非表示
 *
 *   cnorm
 *     -> カーソル通常表示
 *
 *   bold
 *     -> 太字
 *
 * などがある。
 *
 * 各端末種別ごとに、
 *
 *   「その能力を実現するためにどんな制御列を送ればよいか」
 *
 * が terminfo データベースに登録されている。
 *
 * したがってプログラム側は
 *
 *   "cup"
 *
 * のような capability 名だけ知っていれば、
 * 実際の端末制御列はライブラリが引いてくれる。
 *
 * このおかげで、
 *
 *   端末依存のエスケープシーケンスを直接コードへ埋め込まなくてよい
 *
 * という利点がある。
 */

int main(void)
{
    /*
     * p:
     *   terminfo から取得・加工した制御文字列を保持するポインタである。
     *
     * このコードでは最終的に
     *
     *   「カーソルを特定位置へ移動するための文字列」
     *
     * を指すようになる。
     *
     * 途中で
     *
     *   tigetstr("cup")
     *   tparm(...)
     *
     * の返り値を順に受けるため、
     * 1 本の char* として使っている。
     */
    char *p;

    /*
     * setupterm(NULL, 0, NULL);
     *
     * ここが terminfo 利用の出発点である。
     *
     * setupterm() は、
     *
     *   「現在使っている端末に対応する terminfo 情報を初期化する」
     *
     * 関数である。
     *
     * 引数の意味:
     *
     *   第1引数 NULL:
     *     端末種別名を明示しない。
     *
     *     この場合、多くの環境では TERM 環境変数を見て、
     *     現在の端末種別を決める。
     *
     *     たとえば TERM が
     *
     *       xterm-256color
     *
     *     なら、その端末種別の terminfo エントリを使う。
     *
     *   第2引数 0:
     *     出力先ファイル記述子。
     *
     *     ここでは 0、つまり標準入力の fd を渡している。
     *     実務では 1（標準出力）や明示的な端末 fd を使う例も多い。
     *
     *     terminfo は「どの端末へ制御を送るか」に関わるので、
     *     端末に接続された fd を指定する必要がある。
     *
     *   第3引数 NULL:
     *     初期化成否を受け取る int* を省略している。
     *
     *     本来はここにポインタを渡してエラー確認することが多い。
     *     このサンプルでは簡潔さを優先している。
     *
     * ----------------------------------------
     * setupterm() がなぜ必要か
     * ----------------------------------------
     *
     * tigetstr() などで capability を引く前に、
     *
     *   「どの端末種別について引くのか」
     *
     * を決めなければならない。
     *
     * その準備をするのが setupterm() である。
     *
     * 図にするとこうである。
     *
     *   TERM 環境変数
     *      |
     *      v
     *   setupterm()
     *      |
     *      v
     *   その端末種別に対応する terminfo をロード
     *
     * つまりこの1行によって、
     *
     *   「この端末向けの capability を問い合わせる準備が整う」
     *
     * のである。
     *
     * 注意:
     *   実務コードなら戻り値や errret を見て、
     *   terminfo の初期化に失敗していないか確認したい。
     */
    setupterm(NULL, 0, NULL);

    /*
     * p = tigetstr("cup");
     *
     * ここで terminfo データベースから
     *
     *   "cup"
     *
     * という capability を文字列として取得する。
     *
     * ----------------------------------------
     * "cup" とは何か
     * ----------------------------------------
     *
     * "cup" は terminfo capability 名で、
     *
     *   cursor position
     *
     * を表す。
     *
     * つまり
     *
     *   「カーソルを指定した行・列へ移動する能力」
     *
     * である。
     *
     * ただし tigetstr("cup") が返すのは、
     *
     *   すでに完成した制御列
     *
     * ではなく、
     *
     *   「行と列を埋め込めるテンプレート文字列」
     *
     * と考えるのがよい。
     *
     * たとえば概念的には、
     *
     *   "\033[%i%p1%d;%p2%dH"
     *
     * のような parameterized string が返ることがある。
     *
     * これは人間がそのまま使うものではなく、
     *
     *   「あとで行番号・列番号を与えて完成形へ展開する」
     *
     * ためのテンプレートである。
     *
     * ----------------------------------------
     * なぜ直接 ESC シーケンスを書かないのか
     * ----------------------------------------
     *
     * 端末によってカーソル移動の制御列が違う可能性があるからである。
     *
     * もしコードに
     *
     *   "\033[10;20H"
     *
     * のようなものを直書きすると、
     * ANSI/VT 互換端末では動いても、
     * 別方式では動かないかもしれない。
     *
     * terminfo を使えば、
     *
     *   「この端末の cup は何か」
     *
     * をデータベースから引けるので、
     * より可搬性が高くなる。
     *
     * つまりこの行は、
     *
     *   「カーソル移動能力のテンプレートを端末依存データベースから取得する」
     *
     * 処理である。
     *
     * 注意:
     *   実務では返り値が有効かどうか（capability が存在するか）を
     *   確認したい。
     */
    p = tigetstr("cup");

    /*
     * p = tparm(p, 10, 20);
     *
     * ここで先ほど取得した parameterized capability へ
     * 実際の引数を埋め込む。
     *
     * tparm() は、
     *
     *   「terminfo のパラメータ付き文字列テンプレートに
     *    実引数を与えて、完成した制御文字列へ展開する」
     *
     * 関数である。
     *
     * 今回の引数:
     *
     *   第1引数 p:
     *     tigetstr("cup") で得たカーソル移動テンプレート
     *
     *   第2引数 10:
     *     行番号
     *
     *   第3引数 20:
     *     列番号
     *
     * したがって意味としては、
     *
     *   「カーソルを 10 行 20 列へ動かすための
     *    この端末用の完成済み制御列を作る」
     *
     * である。
     *
     * ----------------------------------------
     * parameterized string のイメージ
     * ----------------------------------------
     *
     * たとえば cup テンプレートが概念的に
     *
     *   "ESC[%p1;%p2H"
     *
     * のようなものだとすると、
     * tparm(..., 10, 20) はそれを
     *
     *   "ESC[10;20H"
     *
     * のような完成形にするイメージである。
     *
     * 実際の表現は端末種別次第なので、
     * 呼び出し側はその詳細を知らなくてよい。
     *
     * ここが terminfo の利点である。
     *
     * ----------------------------------------
     * 行番号・列番号について
     * ----------------------------------------
     *
     * 端末によっては 0-origin / 1-origin の違いがあったり、
     * terminfo テンプレート内で %i によって補正したりすることがある。
     *
     * つまり利用側が
     *
     *   「この端末では +1 が必要か」
     *
     * を自前で考えなくて済むよう、
     * テンプレート側にその知識が埋まっていることがある。
     *
     * これも terminfo を使う利点の 1 つである。
     *
     * つまりこの行は、
     *
     *   「端末依存テンプレートへ、位置引数 10,20 を埋め込んで
     *    実際に送れる制御列へ変換する」
     *
     * 処理である。
     */
    p = tparm(p, 10, 20);

    /*
     * putp(p);
     *
     * ここで完成した制御文字列を端末へ出力する。
     *
     * putp() は、
     *
     *   「terminfo 文字列を端末へ送るための出力関数」
     *
     * である。
     *
     * 一般の printf("%s", p) と似ているように見えるが、
     * terminfo 系では putp() を使う流儀がある。
     *
     * putp() は内部的に必要な形で文字列を出力し、
     * capability 文字列を端末制御用として扱うための関数である。
     *
     * したがってこの行が実行されると、
     *
     *   「カーソルを 10 行 20 列へ動かすための
     *    この端末用制御列」
     *
     * が端末へ送られる。
     *
     * 結果として、端末のカーソル位置が移動する。
     *
     * ----------------------------------------
     * ここで実際に画面に何が起こるか
     * ----------------------------------------
     *
     * putp(p) 自体は「可視文字を表示する」とは限らない。
     * むしろ多くの場合、
     *
     *   画面に直接見えない制御列
     *
     * を送っている。
     *
     * 今回は cup なので、
     *
     *   カーソル位置だけが変わる
     *
     * のが本質である。
     *
     * つまりこのコードは
     *
     *   「何か文字を描く」
     *
     * のではなく、
     *
     *   「カーソルを動かす」
     *
     * 例である。
     *
     * その後に printf() 等で文字を出せば、
     * その新しい位置へ描画できる。
     *
     * したがってこの1行は、
     *
     *   「terminfo から生成した制御列を端末へ送って、
     *    実際の端末状態を変化させる」
     *
     * 最後の実行段階である。
     */
    putp(p);

    /*
     * main() の終わり。
     *
     * このコード全体をまとめると、
     *
     *   1. setupterm() で現在端末の terminfo を初期化
     *   2. tigetstr("cup") でカーソル位置指定 capability を取得
     *   3. tparm(..., 10, 20) で行列引数を埋め込み
     *   4. putp() で制御列を実際に端末へ送る
     *
     * という流れである。
     *
     * 主に学ぶべき点は、
     *
     *   - 端末制御列を直接コードへ埋め込まなくてもよい
     *   - terminfo データベースから capability を引ける
     *   - パラメータ付き能力は tparm() で具体化する
     *   - putp() で端末へ送る
     *
     * ということだ。
     *
     * つまりこのプログラムは、
     *
     *   「端末依存制御をデータベース経由で抽象化する」
     *
     * terminfo ライブラリの最小使用例である。
     */
}
