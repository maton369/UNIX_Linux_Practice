/* proc/showenv.c */
#include <stdio.h>      /* printf を使うためのヘッダ */

/*
 * environ は、プロセスが持っている「環境変数の一覧」を指す外部変数である。
 *
 * 型は
 *
 *   char **environ;
 *
 * であり、これは
 *   「文字列へのポインタの配列の先頭」
 * を指していると考えると分かりやすい。
 *
 * ここで各文字列は、典型的には
 *
 *   "PATH=/usr/bin:/bin"
 *   "HOME=/home/user"
 *   "LANG=ja_JP.UTF-8"
 *
 * のような
 *
 *   名前=値
 *
 * 形式になっている。
 *
 * つまり environ は概念的には次のような配列を指している。
 *
 *   environ[0] -> "PATH=..."
 *   environ[1] -> "HOME=..."
 *   environ[2] -> "LANG=..."
 *   ...
 *   environ[n] -> NULL
 *
 * 最後は NULL ポインタで終端される。
 *
 * このコードでは、その NULL まで順にたどることで、
 * 現在プロセスの環境変数を全部表示している。
 *
 * 「外部変数 environ を参照するため」とコメントされている通り、
 * environ 自体はこのファイル内で定義しているわけではない。
 * プロセス起動時に OS / C ランタイム側から渡される環境情報を、
 * 外部変数として参照している。
 */
extern char **environ;  /* 外部変数 environ を参照するため */

int main(void) {
    /*
     * p:
     *   environ を走査するためのポインタである。
     *
     * environ 自体は
     *   「環境文字列へのポインタの並び」
     * の先頭を指しているので、
     * それをそのまま p に入れれば、p を1つずつ進めながら
     * 各環境変数文字列を見ていける。
     *
     * 型が char ** なのは、
     *   - 各要素が char * （文字列へのポインタ）
     *   - それらが並んだ配列をたどる
     * からである。
     *
     * 図にすると概念的にはこうである。
     *
     *   p
     *   |
     *   v
     *   +---------+---------+---------+-----+------+
     *   | char *  | char *  | char *  | ... | NULL |
     *   +---------+---------+---------+-----+------+
     *        |         |         |
     *        v         v         v
     *    "PATH=..." "HOME=..." "LANG=..."
     *
     * つまり p は「環境変数文字列ポインタ列」を走査するカーソルの役割を持つ。
     */
    char **p = environ;

    /*
     * while (*p != NULL)
     *
     * ここがこのコードの中心である。
     *
     * p は char ** なので、
     *
     *   p     : 文字列ポインタ配列の現在位置
     *   *p    : 現在位置にある 1 個の char *（= 1 個の環境変数文字列）
     *
     * を意味する。
     *
     * 環境変数配列は NULL 終端になっているので、
     *
     *   *p != NULL
     *
     * の間はまだ有効な環境変数文字列がある、という意味になる。
     *
     * つまりこのループは、
     *
     *   「環境変数文字列が NULL にぶつかるまで順番に処理する」
     *
     * という典型的な NULL 終端配列の走査アルゴリズムである。
     *
     * 配列インデックスで書くと概念的には
     *
     *   for (i = 0; environ[i] != NULL; i++)
     *
     * と似た意味であり、
     * このコードはそれをポインタで書いている。
     */
    while (*p != NULL) {

        /*
         * printf("%s\n", *p);
         *
         * 現在の環境変数文字列を 1 行表示する。
         *
         * *p は char * であり、C文字列を指しているので、
         * %s で表示できる。
         *
         * たとえば *p が
         *
         *   "PATH=/usr/bin:/bin"
         *
         * なら、その1行がそのまま出力される。
         *
         * ここで "\n" を付けているので、
         * 各環境変数が 1 行ずつ見やすく表示される。
         *
         * したがって出力全体は概念的にこうなる。
         *
         *   PATH=/usr/bin:/bin
         *   HOME=/home/user
         *   LANG=ja_JP.UTF-8
         *   ...
         *
         * つまり「このプロセスが持っている環境変数一覧のダンプ」である。
         */
        printf("%s\n", *p);

        /*
         * p++;
         *
         * 次の環境変数文字列ポインタへ進む。
         *
         * p は char ** なので、p++ により
         * 「次の char * 要素」へ進む。
         *
         * 重要:
         *   これは 1 バイトだけ進むのではなく、
         *   char * 1 個分だけ進む。
         *
         * つまり概念的には、
         *
         *   p = &environ[0]
         *   p = &environ[1]
         *   p = &environ[2]
         *   ...
         *
         * のように順番に配列要素をたどっていくイメージである。
         *
         * 図にすると:
         *
         *   最初:
         *     p -> environ[0]
         *
         *   1回進める:
         *     p -> environ[1]
         *
         *   さらに進める:
         *     p -> environ[2]
         *
         * というように動く。
         */
        p++;
    }

    /*
     * ここへ来たということは、
     *
     *   *p == NULL
     *
     * になった、つまり環境変数配列の終端まで走査し終えたということである。
     *
     * したがって、すべての環境変数を表示し終えた。
     */

    /*
     * 正常終了。
     *
     * このコードでは特別な後始末は不要である。
     * environ は自分で確保したメモリではなく、
     * プロセス環境としてすでに与えられている情報だからである。
     */
    return 0;
}

/*
 * このコードから学ぶべきことのまとめ
 *
 * 1. 環境変数はプロセスごとに持たれる
 *
 *    PATH や HOME などは、実行中プロセスが持つ環境情報の一部である。
 *
 * 2. environ は char ** で表現される
 *
 *    つまり、
 *      「char * 文字列ポインタの並び」
 *    を指している。
 *
 * 3. 各要素は "名前=値" の形の C文字列である
 *
 *    例:
 *      "PATH=/usr/bin:/bin"
 *
 * 4. 配列は NULL 終端である
 *
 *    したがって
 *
 *      while (*p != NULL)
 *
 *    のように走査できる。
 *
 * 5. このコードはポインタによる走査の基本例でもある
 *
 *    配列添字を使わず、
 *    char ** のポインタを1つずつ進めている点が重要である。
 *
 *
 * 図でまとめると:
 *
 *   environ
 *      |
 *      v
 *   +---------+---------+---------+-----+------+
 *   | char *  | char *  | char *  | ... | NULL |
 *   +---------+---------+---------+-----+------+
 *        |         |         |
 *        v         v         v
 *    "PATH=..." "HOME=..." "LANG=..."
 *
 *   走査:
 *     p = environ;
 *     while (*p != NULL) {
 *         printf("%s\n", *p);
 *         p++;
 *     }
 *
 *
 * このコードの本質を一言で言うと、
 *
 *   「プロセスの環境変数一覧を表す NULL 終端の文字列ポインタ配列 environ を、先頭から終端まで順にたどって表示している」
 *
 * である。
 */
