// proc/exec1.c
#include <limits.h>     /* PATH_MAX を使うためのヘッダ */
#include <stdio.h>      /* printf, fflush, perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <unistd.h>     /* execl を使うためのヘッダ */
#include "mysub.h"      /* getstr を使うための自作ヘッダ */

/*
 * このコードの主題は「exec 系関数のうち execl() の使い方」であり、
 * 特に
 *
 *   「リスト引数 (list arguments)」
 *
 * で新しいプログラムに渡す引数列を指定する点が重要である。
 *
 * exec 系関数は、
 *   「現在のプロセスの実行内容を別のプログラムに置き換える」
 * 関数群である。
 *
 * したがって、fork() のように新しいプロセスを追加で作るのではなく、
 * 今動いているこのプロセス自身が、別プログラムへ変身する。
 *
 * このプログラムでは最終的に
 *
 *   /bin/ls -l <directory>
 *
 * を実行したい。
 *
 * ただしここで大切なのは、
 * 単に ls を呼びたいのではなく、
 * execl() の
 *
 *   "l" = list
 *
 * つまり「引数を配列ではなく、1個ずつ順番に並べて渡す」
 * 形式を理解することである。
 *
 * 全体の流れは次の通りである。
 *
 *   1. ユーザにディレクトリ名を入力してもらう
 *   2. そのディレクトリ名を buf に入れる
 *   3. これからそのディレクトリの ls -l を実行する、と表示する
 *   4. fflush(stdout) でその表示を確実に出す
 *   5. execl("/bin/ls", "ls", "-l", buf, (char *)0) を呼ぶ
 *   6. 成功すれば現在プロセスは ls に置き換わる
 *   7. 失敗した場合だけ perror("execl") に進む
 *
 * つまりこのコードは、
 *
 *   「ユーザ入力したディレクトリを ls -l の引数として、
 *    execl() のリスト形式で渡して実行する」
 *
 * サンプルである。
 */

int main(void) {
    /*
     * buf:
     *   ユーザが入力したディレクトリ名を格納するバッファである。
     *
     * PATH_MAX は「パス名の最大長」の目安として使われる定数であり、
     * ここでは十分大きな文字列バッファとして利用している。
     *
     * たとえば
     *   "."
     *   ".."
     *   "/home/maton/dev"
     *   "/tmp"
     * などのディレクトリ名がここに入る。
     *
     * この buf の内容は、あとで execl() の引数の1つとして渡される。
     */
    char buf[PATH_MAX];

    /*
     * getstr("directory? ", buf, sizeof(buf));
     *
     * ユーザに対象ディレクトリを入力させる。
     *
     * ここで入力された文字列は buf に入る。
     * getstr() 側で末尾改行は除去される想定なので、
     * buf はそのまま普通の C 文字列として使える。
     *
     * 例:
     *   ユーザ入力が
     *     /etc
     *   なら
     *     buf = "/etc"
     *
     * となる。
     *
     * この時点ではまだ ls は実行していない。
     * 単に「あとで ls に渡す対象ディレクトリ文字列」を用意しているだけである。
     */
    getstr("directory? ", buf, sizeof(buf));

    /*
     * printf("Files in %s:\n", buf);
     *
     * これからそのディレクトリの内容を表示する、という案内を出している。
     *
     * たとえば buf が "/etc" なら
     *
     *   Files in /etc:
     *
     * のように表示される。
     *
     * この出力は exec 前の「元のプログラム」の出力である。
     * これ以降、execl() が成功すると ls の出力へ切り替わるため、
     * どこまでが元プログラムのメッセージか分かるようにしている。
     */
    printf("Files in %s:\n", buf);

    /*
     * fflush(stdout);
     *
     * これは重要である。
     *
     * stdout はバッファリングされることがあるため、
     * 直前の
     *
     *   printf("Files in %s:\n", buf);
     *
     * の出力がまだ画面や端末に完全には出ていない可能性がある。
     *
     * その状態で execl() に成功すると、
     * 実行主体が ls に置き換わってしまい、
     * 観察上どこまでが元プログラムの出力なのか分かりにくくなることがある。
     *
     * そこで exec 前に stdout を flush し、
     *   「これから ls に置き換わる前の最後の表示」
     * を確実に出している。
     *
     * つまりこの fflush は、
     *   exec 前後の出力境界を見やすくするための処理
     * である。
     */
    fflush(stdout);

    /*
     * execl("/bin/ls", "ls", "-l", buf, (char *)0);
     *
     * ここがこのコードの核心であり、
     * 特に「リスト引数」の理解が大切である。
     *
     * ------------------------------------------------------------------
     * 1. execl() とは何か
     * ------------------------------------------------------------------
     *
     * execl は exec 系関数の1つであり、
     * 現在プロセスの実行内容を別プログラムへ置き換える。
     *
     * 関数名の意味を分解すると、
     *
     *   exec  : プロセス内容を置き換える
     *   l     : 引数を list（リスト）形式で渡す
     *
     * である。
     *
     * ここでいう「リスト形式」とは、
     * 配列 argv を渡すのではなく、
     * 可変長引数として 1 個ずつ順番に並べる形式である。
     *
     * たとえばこの呼び出しでは
     *
     *   "ls"
     *   "-l"
     *   buf
     *   (char *)0
     *
     * を1つずつ書いている。
     *
     * ------------------------------------------------------------------
     * 2. 各引数の意味
     * ------------------------------------------------------------------
     *
     * 第1引数:
     *   "/bin/ls"
     *
     *   実行したいプログラムの実体ファイルのパスである。
     *   ここではフルパスを指定しているため、PATH 検索は行わない。
     *
     *   つまり、明示的に /bin/ls を起動する。
     *
     * 第2引数:
     *   "ls"
     *
     *   新しいプログラムから見た argv[0] に相当する。
     *
     *   C の main(int argc, char *argv[]) で受け取る argv[0] は、
     *   慣習的にはコマンド名自身を入れることが多い。
     *
     *   ここでは "ls" を入れているので、
     *   新しい /bin/ls プロセスは
     *
     *     argv[0] = "ls"
     *
     * として起動される。
     *
     * 第3引数:
     *   "-l"
     *
     *   新しいプログラムから見た argv[1] に相当する。
     *
     *   つまり ls コマンドに対するオプションであり、
     *   詳細表示形式（long format）を意味する。
     *
     * 第4引数:
     *   buf
     *
     *   新しいプログラムから見た argv[2] に相当する。
     *
     *   これはユーザが入力したディレクトリ名であり、
     *   ls -l の対象ディレクトリになる。
     *
     *   たとえば buf = "/etc" なら、
     *   実質的に
     *
     *     ls -l /etc
     *
     * を実行することになる。
     *
     * 第5引数:
     *   (char *)0
     *
     *   これは引数リストの終端を表す NULL ポインタである。
     *
     *   execl() は可変長引数関数なので、
     *   どこまでが argv なのかを知るために、
     *   最後を NULL で終えなければならない。
     *
     *   この NULL が無いと、execl は引数列の終わりを判定できない。
     *
     * ------------------------------------------------------------------
     * 3. リスト引数の実体
     * ------------------------------------------------------------------
     *
     * この execl 呼び出しを、配列 argv の形に直すと概念的にはこうなる。
     *
     *   argv[0] = "ls"
     *   argv[1] = "-l"
     *   argv[2] = buf
     *   argv[3] = NULL
     *
     * つまり、execl は
     *   「配列を自分で用意して渡す」のではなく、
     *   その配列要素を1つずつ並べて書く形だと理解するとよい。
     *
     * 図にするとこうである。
     *
     *   execl("/bin/ls",
     *         "ls",     <- argv[0]
     *         "-l",     <- argv[1]
     *         buf,      <- argv[2]
     *         NULL);    <- argv[3] の終端
     *
     * これが「リスト引数」の意味である。
     *
     * ------------------------------------------------------------------
     * 4. 実際に何が実行されるか
     * ------------------------------------------------------------------
     *
     * たとえば buf = "/tmp" なら、
     * 概念的には新しいプログラムは
     *
     *   /bin/ls -l /tmp
     *
     * として起動される。
     *
     * ただし重要なのは、
     *   「このプログラムが /bin/ls を子プロセスとして新規起動する」
     * のではなく、
     *   「このプログラム自身が /bin/ls に置き換わる」
     * ことである。
     *
     * つまりプロセス数は増えない。
     * 同じプロセスが途中から ls になる。
     *
     * ------------------------------------------------------------------
     * 5. 成功時は戻らない
     * ------------------------------------------------------------------
     *
     * execl に成功すると、現在のプロセスイメージは /bin/ls に完全に置換される。
     * そのため、この関数呼び出しは成功時には戻らない。
     *
     * つまり、この行の次にある
     *
     *   perror("execl");
     *   exit(1);
     *
     * へ進むのは、execl が失敗した場合だけである。
     *
     * これが普通の関数呼び出しと大きく違う点である。
     */
    execl("/bin/ls", "ls", "-l", buf, (char *)0);

    /*
     * perror("execl");
     *
     * ここに到達するのは execl が失敗した場合だけである。
     *
     * つまり、
     *   もし execl に成功していれば、
     *   この時点ではもうこのプロセスは /bin/ls になっているので、
     *   ここへ戻ってこない。
     *
     * 失敗理由の例:
     *   - /bin/ls が存在しない
     *   - 実行権限がない
     *   - メモリ不足
     *   - システム資源不足
     *
     * perror("execl") は errno に対応するエラーメッセージを表示する。
     *
     * 例:
     *   execl: No such file or directory
     *   execl: Permission denied
     *
     * など。
     *
     * このように、
     *   exec 呼び出しの直後に perror を置く
     * のは典型的な書き方である。
     *
     * 理由:
     *   exec は成功時には戻らないので、
     *   直後のコードは「失敗処理専用」にできるからである。
     */
    perror("execl");

    /*
     * exec に失敗したので異常終了する。
     *
     * exit(1) により、
     *   「このプログラムは正常には完了できなかった」
     * ことを呼び出し元へ伝える。
     *
     * 成功時にはここへ来ないので、
     * この exit(1) は失敗経路専用の終了処理である。
     */
    exit(1);
}
