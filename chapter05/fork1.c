/* proc/fork1.c */
#include <stdio.h>      /* printf, fflush, perror, putchar を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型を使うためのヘッダ */
#include <unistd.h>     /* fork, sleep を使うためのヘッダ */

/*
 * このコードの主題は「fork() 後に親プロセスと子プロセスが並行に動くこと」である。
 *
 * 前の fork0.c では、
 *   - fork() によってプロセスが2つになる
 *   - 親と子で PID が異なる
 *   - 変数の値は fork 時点のものを引き継ぐ
 * といった基本を観察した。
 *
 * この fork1.c では、そこから一歩進んで、
 *
 *   - 親と子で「別々の処理」をさせる
 *   - それぞれが独立したタイミングで動く
 *   - 出力が時間的に交互に混ざることがある
 *
 * という「並行実行らしさ」を観察することが目的である。
 *
 * このプログラムの全体像は次の通りである。
 *
 *   1. fork() を呼ぶ
 *   2. fork() の戻り値で、親プロセスか子プロセスかを判定する
 *   3. 子プロセスは 0 から 9 までを "<0><1>..." の形で1秒ごとに表示する
 *   4. 親プロセスは 9 から 0 までを "[9][8]..." の形で2秒ごとに表示する
 *   5. 親子は同時に存在するので、出力は時間的に交錯する
 *
 * つまりこのコードは、
 *
 *   「fork() によって生まれた親子2プロセスが、別々のループを独立に進める」
 *
 * ことを実際の表示結果で確かめるサンプルである。
 *
 * 重要:
 *   親と子の出力順序は完全には固定されない。
 *   OS のスケジューラがどちらをいつ走らせるかによって、
 *   表示順は多少前後する。
 *
 * ただし sleep() を使って速度差をつけているため、
 *   - 子は毎秒
 *   - 親は2秒ごと
 * に進む、という傾向は見やすい。
 */

int main(void) {
    /*
     * i:
     *   ループカウンタとして使う変数である。
     *
     * 子プロセス側では
     *   0,1,2,...,9
     * の昇順表示に使う。
     *
     * 親プロセス側では
     *   9,8,7,...,0
     * の降順表示に使う。
     *
     * ここで重要なのは、fork() 後には親と子がそれぞれ
     * 「自分専用の i」を持つことだ。
 *
     * つまり見た目は同じ変数名 i でも、
     * fork() 後は
     *
     *   親プロセスの i
     *   子プロセスの i
     *
     * が別々に存在する。
     *
     * したがって、子が i を 0 から増やしても、
     * 親の i がそれに引きずられて変化することはない。
     */
    int i;

    /*
     * cpid:
     *   fork() の戻り値を受ける変数である。
     *
     * 型は pid_t であり、PID を表すための型である。
     *
     * fork() の戻り値の意味は非常に重要で、次のようになる。
     *
     *   -1 : fork 失敗
     *    0 : 子プロセス側での戻り値
     *   >0 : 親プロセス側での戻り値（その値は子プロセスの PID）
     *
     * つまり cpid を見れば、
     *   「今自分は親なのか子なのか」
     * が分かる。
     *
     * さらに親側では
     *   「生成された子の PID はいくつか」
     * も分かる。
     *
     * このコードでは子の PID 自体を表示していないが、
     * cpid に入っている意味はそこにある。
     */
    pid_t cpid;

    /*
     * fork() を呼び、その戻り値を cpid に入れている。
     *
     * ここがこのプログラムの分岐点である。
     *
     * fork() を呼ぶと、現在のプロセスが複製され、
     * 以後は
     *
     *   - 親プロセス
     *   - 子プロセス
     *
     * の2つが存在するようになる。
     *
     * そして、この if 文全体は親と子の両方で評価される。
     * ただし cpid の値が違うため、入る分岐が変わる。
     *
     * 図にするとこうである。
     *
     *   fork()
     *     |
     *     +--> 親: cpid = 子PID (>0)
     *     |
     *     +--> 子: cpid = 0
     *
     * 失敗時だけ cpid = -1 になる。
     */
    if ((cpid = fork()) == -1) {
        /* エラー */

        /*
         * fork() が失敗した場合の処理。
         *
         * fork 失敗の理由としては、
         *   - プロセス数制限
         *   - メモリ不足
         *   - システム資源不足
         * などがありうる。
         *
         * perror("fork") により、errno に対応した人間向けエラーメッセージを表示する。
         *
         * 例:
         *   fork: Resource temporarily unavailable
         *
         * など。
         */
        perror("fork");

        /*
         * fork に失敗したら親子の分岐そのものが成立しないため、
         * 異常終了する。
         */
        exit(1);

    } else if (cpid == 0) {
        /* 子プロセス */

        /*
         * ここへ来るのは「子プロセス側」である。
         *
         * fork() は子プロセス側では 0 を返すので、
         * cpid == 0 なら今自分は子だと分かる。
         *
         * この分岐では、子プロセスに特有の仕事として
         *   "<0><1><2>...<9>"
         * を1秒ごとに表示させる。
         */

        /*
         * for (i = 0; i < 10; i++)
         *
         * 子プロセスは 0 から 9 まで昇順で動く。
         *
         * 1回のループで
         *   1. "<i>" を表示する
         *   2. fflush(stdout) で即表示させる
         *   3. sleep(1) で1秒待つ
         *
         * ことを行う。
         *
         * つまり子プロセスのアルゴリズムは
         *
         *   0,1,2,...,9 を 1 秒おきに表示する
         *
         * である。
         */
        for (i = 0; i < 10; i++) {
            /*
             * printf("<%d>", i);
             *
             * 現在の i を "<数字>" の形で表示する。
             *
             * たとえば i=3 のときは
             *
             *   <3>
             *
             * が出る。
             *
             * あえて改行を付けていないのは、
             * 親プロセスの出力 "[...]" と1行上で混ざる様子を観察したいからである。
             */
            printf("<%d>", i);

            /*
             * fflush(stdout);
             *
             * これも重要である。
             *
             * printf() しただけでは stdout にバッファリングされ、
             * すぐ画面に出ないことがある。
             * それでは「1秒ごとに出る」観察がしづらい。
             *
             * そこで毎回 fflush(stdout) を呼んで、
             * 各 "<i>" をその場で表示装置まで押し出している。
             *
             * このおかげで、親子の出力が時間順に見えやすくなる。
             */
            fflush(stdout);

            /*
             * sleep(1);
             *
             * 子プロセスは 1 秒ごとに1回進む。
             *
             * これにより、親の 2 秒周期より速く進む。
             * そのため、表示結果では子の出力の方が高頻度になる。
             */
            sleep(1);  /* 1秒ごとに表示 */
        }

        /*
         * 子プロセスは自分の仕事を終えたので正常終了する。
         *
         * ここで exit(0) するのは「子だけを終わらせる」操作である。
         * 親プロセスは別プロセスなので、これでは終わらない。
         *
         * つまり親子は独立に終了できる。
         */
        exit(0);  /* 子プロセスのexit */
    }

    /* 親プロセス */

    /*
     * ここへ来るのは親プロセス側である。
     *
     * 理由:
     *   fork() の戻り値 cpid が
     *     -1 でも 0 でもない
     *   つまり「子 PID (>0)」
     *   だからである。
     *
     * 親プロセスでは、子とは別の仕事として
     *   "[9][8][7]...[0]"
     * を 2 秒ごとに表示する。
     *
     * つまり親プロセスのアルゴリズムは
     *
     *   9,8,7,...,0 を 2 秒おきに表示する
     *
     * である。
     */

    /*
     * for (i = 9; i >= 0; i--)
     *
     * 親は子とは逆に 9 から 0 へ向かって進む。
     *
     * これにより、表示結果だけ見ても
     *   親の出力か
     *   子の出力か
     * を区別しやすい。
     *
     *   子: <0><1><2>...
     *   親: [9][8][7]...
     *
     * という2系統が同時に進行する。
     */
    for (i = 9; i >= 0; i--) {
        /*
         * 現在の i を "[数字]" の形で表示する。
         *
         * たとえば i=7 なら
         *
         *   [7]
         *
         * が出る。
         *
         * 子と同じく改行は付けないので、
         * 同一行上で親子の出力が混ざる。
         */
        printf("[%d]", i);

        /*
         * 各 "[i]" をその場で可視化するために flush する。
         *
         * これをしないと、親プロセスの出力もバッファにたまり、
         * 2秒ごとの動きが見えにくくなる。
         */
        fflush(stdout);

        /*
         * 親は 2 秒ごとに1回進む。
         *
         * 子の 1 秒周期より遅いので、
         * 実行結果では
         *   子の方が親のあいだに何回か進む
         * ように見える。
         *
         * つまり、親子の速度差を意図的に作ることで、
         * 並行実行の交錯を観察しやすくしている。
         */
        sleep(2);  /* 2秒ごとに表示 */
    }

    /*
     * putchar('\n');
     *
     * 親プロセスのループが終わったあと、最後に改行を1文字出す。
     *
     * これにより、親子の "[...]" "<...>" が1行上に続いた出力のあとで
     * 端末表示を次の行へ整えることができる。
     *
     * ただし注意:
     *   子プロセス側は最後に改行を出していない。
     *   この改行は親プロセスだけが行う。
     *
     * したがって、タイミングによっては子の終了時点ではまだ行末改行がなく、
     * 最後に親が改行して見た目を整える形になる。
     */
    putchar('\n');

    /*
     * 親プロセスも仕事を終えたので正常終了する。
     *
     * このコードでは wait() を使っていないので、
     * 親は子の終了を待たずに自分のループを進める。
     *
     * ただし今回の設定では、
     *   子: 10回 × 1秒 = 約10秒
     *   親: 10回 × 2秒 = 約20秒
     * なので、多くの場合は子の方が先に終わり、
     * 親があとから終了する。
     *
     * ここから先の章では、
     *   wait() による子プロセス回収
     *   ゾンビプロセス
     * などが話題になることが多い。
     *
     * このコード自体の主題は、
     *   「親子が独立に動く」
     * ところまでである。
     */
    exit(0);  /* 親プロセスのexit */
}
