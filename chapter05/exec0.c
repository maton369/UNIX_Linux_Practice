/* proc/exec0.c */
#include <stdio.h>      /* printf, fflush を使うためのヘッダ */
#include <unistd.h>     /* execlp を使うためのヘッダ */

/*
 * このコードの主題は「exec 系関数によるプロセス内容の置き換え」である。
 *
 * UNIX / Linux では、fork() が
 *   「新しい子プロセスを作る」
 * 関数であるのに対し、
 *
 * exec 系関数は
 *   「現在のプロセスの中身を、別のプログラムに置き換える」
 * 関数である。
 *
 * ここで非常に重要なのは、
 * exec は「新しいプロセスを追加で作る」のではない、という点である。
 *
 * つまり、
 *
 *   fork():
 *     1個のプロセス -> 親と子の2個になる
 *
 *   exec():
 *     いま動いている1個のプロセスの実行内容が、
 *     別プログラムに差し替わる
 *
 * という違いがある。
 *
 * このプログラムでは
 *
 *   1. "Invoking ls." を表示する
 *   2. fflush(stdout) でその出力を確実に出す
 *   3. execlp("ls", "ls", (char *)0) を呼ぶ
 *   4. 成功すればこのプロセスは ls プログラムに置き換わる
 *   5. そのため、その後ろの printf("Done.\\n") は実行されない
 *
 * という流れになっている。
 *
 * つまりこのコードは、
 *
 *   「exec に成功すると、元のプログラムの続きはもう実行されない」
 *
 * ことを示す最小例である。
 *
 * さらにこのコードは execlp を使っているので、
 * PATH 環境変数を使って "ls" コマンドを探索して実行する。
 *
 * そのため、
 *   /bin/ls
 * のようにフルパスを書かなくても、
 * 通常のシェルで ls が見つかるのと同じ感覚で実行できる。
 */

int main(void) {
    /*
     * printf("Invoking ls.\\n");
     *
     * これから ls を起動する、という案内メッセージを標準出力へ出している。
     *
     * この行は exec 前の「元のプログラム proc/exec0.c の処理」である。
     *
     * 実行時のイメージ:
     *
     *   Invoking ls.
     *
     * のあとに ls の出力（カレントディレクトリの一覧など）が続く。
     *
     * このメッセージを出しておくことで、
     *   - どこまでが元のプログラムの出力か
     *   - どこからが ls に置き換わった後の出力か
     * を見分けやすくしている。
     */
    printf("Invoking ls.\n");

    /*
     * fflush(stdout);
     *
     * これは重要である。
     *
     * stdout は環境によってバッファリングされることがある。
     * もし printf("Invoking ls.\\n") の出力がまだ stdout のバッファに残ったまま
     * execlp() を呼ぶと、
     * 実行環境によっては表示タイミングが分かりにくくなることがある。
     *
     * そこで fflush(stdout) によって、
     *   "Invoking ls.\n"
     * を exec 前に確実に出力している。
     *
     * 図にするとこうである。
     *
     *   printf 後:
     *     stdout バッファに "Invoking ls.\n" が残る可能性がある
     *
     *   fflush 後:
     *     その内容を実際の出力先へ押し出す
     *
     * これにより、
     *   「元のプログラムの最後の出力」
     * を確実に見せてから exec へ進める。
     *
     * fork の前に fflush したのと同様に、
     * exec 前も出力観察を安定させるために flush が有効である。
     */
    fflush(stdout);

    /*
     * execlp("ls", "ls", (char *)0);
     *
     * ここがこのプログラムの核心である。
     *
     * execlp は exec 系関数の1つであり、
     * 現在のプロセスイメージを別プログラムで置き換える。
     *
     * 関数名 execlp の意味を分けて考えると理解しやすい。
     *
     *   exec
     *     -> 現在プロセスの実行内容を置き換える
     *
     *   l
     *     -> 引数を「リスト形式」で渡す
     *        （可変長引数として1個ずつ並べる）
     *
     *   p
     *     -> PATH を使って実行ファイルを探索する
     *
     * つまり execlp は
     *
     *   「PATH を使って実行ファイルを探し、
     *    引数を個別に並べて指定して、
     *    現在プロセスをそのプログラムで置き換える」
     *
     * 関数である。
     *
     * 各引数の意味:
     *
     * 第1引数:
     *   "ls"
     *   実行したいプログラム名
     *
     *   これは PATH 検索の対象になる。
     *   たとえば PATH の中に /bin が含まれていれば、
     *   通常 /bin/ls が見つかる。
     *
     * 第2引数:
     *   "ls"
     *   新しいプログラムから見た argv[0]
     *
     *   C の main(int argc, char *argv[]) に渡される argv[0] に相当する。
     *   通常はコマンド名自身を入れることが多い。
     *
     * 第3引数:
     *   (char *)0
     *   引数リストの終端を表す NULL ポインタ
     *
     *   execlp は可変長引数で argv を受け取るため、
     *   最後は NULL で終わる必要がある。
     *
     * つまり、この呼び出しは概念的には
     *
     *   ls
     *
     * を引数なしで実行するのと同じである。
     *
     * ここで最重要なのは「成功時の制御の戻り方」である。
     *
     * 普通の関数呼び出しなら、
     *   呼び出し -> 関数実行 -> 戻る
     * となる。
     *
     * しかし exec 系関数は成功すると、
     *   現在のプロセス全体が別プログラムに置き換わる
     * ため、
     *   「呼び出し元へ戻る」
     * ということが起こらない。
     *
     * つまり、
     *
     *   execlp が成功
     *     -> 以後このソースコード上の後続行は実行されない
     *
     * のである。
     *
     * 逆に、execlp が「戻ってくる」のは失敗したときだけである。
     *
     * 失敗原因の例:
     *   - ls が PATH 上で見つからない
     *   - 実行権限がない
     *   - メモリ不足
     *
     * このコードでは戻り値チェックをしていないが、
     * 本来は失敗時に perror("execlp") などを出すのが望ましい。
     */
    execlp("ls", "ls", (char *)0);

    /*
     * printf("Done.\\n");
     *
     * コメントにもある通り、
     * execlp に成功したらこの行は実行されない。
     *
     * なぜなら、execlp 成功時には
     *   現在のプロセスの中身が ls に完全に置き換わっている
     * からである。
     *
     * つまり制御の流れはこうなる。
     *
     *   元のプログラム:
     *     printf("Invoking ls.\\n");
     *     fflush(stdout);
     *     execlp(...);
     *
     *   成功すると:
     *     ここで ls プログラムへ切り替わる
     *     -> ls の main 相当の処理が始まる
     *     -> この printf("Done.\\n") へは戻らない
     *
     * したがって、この行が実際に表示されたなら、
     * それは
     *   「execlp が失敗した」
     * ことを意味する。
     *
     * つまりこの1行は、
     *   「exec 成功時には到達不能な行」
     * をあえて置いて、exec の性質を示す教材的な役割を持っている。
     */
    printf("Done.\n");  /* execlp に成功したら実行されない */

    /*
     * return 0;
     *
     * これも execlp 成功時には実行されない。
     *
     * ここまで来るのは execlp が失敗した場合だけである。
     *
     * ただし、このコードでは失敗を明示的に検出していないため、
     * 失敗しても "Done." を出して正常終了してしまう。
     *
     * 教材としては
     *   「exec に成功したら後続は実行されない」
     * ことを示す簡潔な例になっているが、
     * 実務コードなら例えば次のようにする方がよい。
     *
     *   execlp(...);
     *   perror("execlp");
     *   return 1;
     *
     * そうすると、成功時は戻らず、失敗時だけ perror を出して異常終了できる。
     */
    return 0;
}
