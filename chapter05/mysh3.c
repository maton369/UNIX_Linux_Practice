/* proc/mysh3.c */
#include <errno.h>      /* errno を使うためのヘッダ */
#include <stdio.h>      /* feof, ferror, fprintf, fputs, perror, stderr, stdin を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <string.h>     /* strcmp を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型, fork/waitpid に関連する型定義 */
#include <sys/wait.h>   /* waitpid, WNOHANG を使うためのヘッダ */
#include <unistd.h>     /* fork, execvp を使うためのヘッダ */
#include "mysub.h"
#include "myvec.h"      /* strtovec のため */

/*
 * このコードの主題は「簡易シェルにおけるバックグラウンド実行」である。
 *
 * 以前の mysh0.c では、
 *   - コマンドを1つ入力する
 *   - fork() で子プロセスを作る
 *   - 子で exec する
 *   - 親は wait() して、子が終わるまで待つ
 * という、前面実行だけの最小シェルだった。
 *
 * それに対してこの mysh3.c では、さらに次の機能が追加されている。
 *
 *   1. コマンドラインを空白で分割し、複数引数を扱える
 *   2. 行末に "&" があれば、そのコマンドをバックグラウンド実行する
 *   3. バックグラウンド実行した子は親がその場では待たない
 *   4. ただし終了済みの子を放置するとゾンビになるので、
 *      ループ先頭で waitpid(..., WNOHANG) により回収する
 *
 * つまりこのコードは、
 *
 *   「前面実行では待つ。バックグラウンド実行では待たない。
 *    しかし待たない子も、後で非ブロッキングに回収する」
 *
 * というシェルの基本挙動を示している。
 *
 * 特に重要なのは waitpid() の2つの使い方である。
 *
 *   (1) waitpid(-1, &status, WNOHANG)
 *       -> どの子でもよいから、すでに終了した子がいれば回収する
 *          ただし1つも終わっていなければ待たずにすぐ戻る
 *
 *   (2) waitpid(cpid, &status, 0)
 *       -> 特定の前面実行子 cpid が終わるまで、親をブロックして待つ
 *
 * この2つを使い分けることで、
 *   - バックグラウンドジョブはシェルを止めない
 *   - 前面ジョブはシェルが終わるまで待つ
 * を実現している。
 */

enum {
    /*
     * このシェルが扱う引数個数の上限である。
     *
     * たとえば
     *   ls -l /tmp
     * なら
     *   "ls", "-l", "/tmp", NULL
     * のような argv ベクタになる。
     *
     * その最大長を MAXARGV としている。
     *
     * 注意:
     *   実際には最後に NULL 終端も必要なので、
     *   av 配列は MAXARGV 要素持っており、
     *   strtovec() はその中に
     *     引数列 + 終端 NULL
     *   を入れる前提で使われている。
     */
    MAXARGV = 100,
};

int main(void) {
    /*
     * cmd:
     *   ユーザが1行入力したコマンドライン全体を入れる文字列バッファ。
     *
     *   例:
     *     "ls -l /tmp"
     *     "sleep 10 &"
     *     "echo hello"
     *
     *   のような生の入力文字列がここに入る。
     *
     * av:
     *   strtovec() によって作られる argv ベクタ。
     *
     *   例:
     *     cmd = "ls -l /tmp"
     *   なら概念的に
     *     av[0] = "ls"
     *     av[1] = "-l"
     *     av[2] = "/tmp"
     *     av[3] = NULL
     *   となる。
     *
     * ac:
     *   引数個数に相当する値。
     *   ただし strtovec() の返し方の都合で、最初は終端 NULL 分を含む値になり、
     *   後で ac-- して調整している。
     *
     * status:
     *   waitpid() が子プロセスの終了状態を書き込む先。
     *
     * bg:
     *   バックグラウンド実行フラグ。
     *
     *   bg = 1 ならバックグラウンド実行
     *   bg = 0 なら前面実行
     *
     * cpid:
     *   その回の fork() で生成された子プロセスの PID。
     *
     * zpid:
     *   ループ先頭で「終了済みの子を回収する」ために waitpid() が返した PID。
     *   どの子が回収されたかを知るために使っている。
     */
    char cmd[1024];
    char *av[MAXARGV];
    int ac, status, bg;
    pid_t cpid, zpid;

    /*
     * 無限ループであり、このシェルは
     *
     *   入力 -> 解析 -> 実行 -> 必要なら待機 -> 再び入力
     *
     * を繰り返す。
     *
     * 1回のループが「1コマンド処理」に相当する。
     */
    for (;;) {
        /*
         * --------------------------------------------------------------
         * ループ先頭: 幽霊(= ゾンビ)がいたら回収する
         * --------------------------------------------------------------
         *
         * バックグラウンド実行では、親プロセスはその場で子を wait しない。
         * そのため、子が先に終了すると、親が終了状態を回収するまで
         * 子はゾンビプロセスとして残る可能性がある。
         *
         * そこで新しいコマンドを読む前に、
         * 「すでに終了している子がいないか」を非ブロッキングで確認して回収する。
         *
         * この処理はバックグラウンド実行シェルでは非常に重要であり、
         * wait() を単に前面ジョブにだけ使うだけでは不十分であることを示している。
         */

        /*
         * while ((zpid = waitpid(-1, &status, WNOHANG)) > 0)
         *
         * ここがバックグラウンド実行理解の最重要ポイントである。
         *
         * waitpid() は wait() の拡張版であり、
         * 「どの子を待つか」と「どんな待ち方をするか」を細かく指定できる。
         *
         * 引数の意味:
         *
         * 第1引数: -1
         *   -> 「どの子でもよいから」対象にする
         *
         * 第2引数: &status
         *   -> 終了状態を status に受け取る
         *
         * 第3引数: WNOHANG
         *   -> もし対象の子がまだ誰も終了していなければ、
         *      待たずにすぐ戻る
         *
         * 戻り値:
         *   >0 :
         *     1つの終了済み子を回収した。その PID を返す
         *
         *    0 :
         *     対象の子は存在するが、今はまだ誰も終了していない
         *     （WNOHANG なので待たずに戻った）
         *
         *   -1 :
         *     エラー。たとえば子がそもそもいない場合など
         *
         * この while ループは、
         *   「終了済みの子がいる限り、次々回収する」
         * というアルゴリズムになっている。
         *
         * 図にするとこうである。
         *
         *   親プロセス
         *       |
         *       | waitpid(-1, &status, WNOHANG)
         *       v
         *   +--------------------------------------+
         *   | 終了済みの子がいる?                  |
         *   +-------------------+------------------+
         *                       |
         *          +------------+------------+
         *          |                         |
         *         Yes                       No
         *          |                         |
         *          v                         v
         *   1つ回収して zpid を返す        0 を返す
         *          |
         *          +--> while で次の終了済み子も探す
         *
         * つまりこの1行によって、
         *   「バックグラウンドで終わっていた子を全部掃除してから、
         *    次の入力へ進む」
         * ことができる。
         *
         * コメントの「幽霊」はゾンビプロセスの意味である。
         */
        while ((zpid = waitpid(-1, &status, WNOHANG)) > 0)      /* (1) */
            /*
             * 終了済みの子を1つ回収するたびに、その PID を表示する。
             *
             * 例:
             *   process 12345 salvaged
             *
             * ここで "salvaged" は
             *   「回収した」「引き取った」
             * くらいの意味で使われており、
             * ゾンビ化しかけた（あるいはしていた）子を waitpid が処理したことを示す。
             *
             * これにより、
             *   「バックグラウンドの子がいつ終わって回収されたか」
             * を観察できる。
             */
            fprintf(stderr, "process %d salvaged\n", zpid);

        /*
         * while ループを抜けたあとの zpid の値には次の意味がある。
         *
         *   zpid > 0 ではないので、
         *     0 か -1 のどちらか
         *
         *   0:
         *     子はいるが、今は終了済みの子がいない
         *
         *  -1:
         *     エラー
         *
         * ただし -1 でも、
         *   errno == ECHILD
         * の場合は「待つべき子がいない」というだけであり、
         * ここでは正常な状況として許容している。
         *
         * たとえば
         *   - まだ一度も子を作っていない
         *   - すべての子をすでに回収済み
         * のような場合に ECHILD になりうる。
         */
        if (zpid == -1 && errno != ECHILD) {
            /*
             * ECHILD 以外のエラーは異常として扱う。
             *
             * ここでの perror は waitpid(2) というシステムコール名を
             * ラベルにして表示している。
             *
             * 例:
             *   waitpid(2): Interrupted system call
             *
             * など。
             */
            perror("waitpid(2)");
            exit(1);
        }

        /*
         * --------------------------------------------------------------
         * コマンドライン入力
         * --------------------------------------------------------------
         */

        /*
         * プロンプト "@ " を出して、1行のコマンドラインを読む。
         *
         * ここで入力されるのは、
         *   "ls -l /tmp"
         *   "sleep 10 &"
         *   "echo hello"
         * のような文字列全体である。
         */
        getstr("@ ", cmd, sizeof(cmd));

        /*
         * 入力後に feof(stdin), ferror(stdin) を見ているのは、
         * getstr() の結果として標準入力が EOF になったか、
         * エラーになったかを判定するためである。
         *
         * たとえば Ctrl-D で EOF を入れたら、
         * このシェルを正常終了したい。
         */
        if (feof(stdin)) {
            /*
             * EOF ならシェルを正常終了する。
             *
             * たとえば端末で Ctrl-D を入力した場合がこれに相当する。
             */
            exit(0);
        } else if (ferror(stdin)) {
            /*
             * 入力ストリームに異常が起きたならエラー終了する。
             */
            perror("getstr");
            exit(1);
        }

        /*
         * --------------------------------------------------------------
         * コマンドラインを argv ベクタへ分割
         * --------------------------------------------------------------
         *
         * strtovec(cmd, av, MAXARGV) は、
         * cmd の文字列を空白などで分割し、
         * execvp() に渡せる argv ベクタを av に作る補助関数だと考えられる。
         *
         * 例:
         *   cmd = "ls -l /tmp"
         * なら
         *   av[0] = "ls"
         *   av[1] = "-l"
         *   av[2] = "/tmp"
         *   av[3] = NULL
         *
         * のようになる。
         *
         * 戻り値 ac は終端 NULL を含めた要素数として返る設計らしいため、
         * 後で ac-- で調整している。
         */
        if ((ac = strtovec(cmd, av, MAXARGV)) > MAXARGV) {
            /*
             * 引数が多すぎる場合はこのコマンドを実行しない。
             *
             * つまり「argv ベクタに収まり切らない入力」は拒否する。
             */
            fputs("argument list too long\n", stderr);
            continue;
        }

        /*
         * strtovec が返した ac には終端 NULL も含まれている想定なので、
         * ここで 1 減らして「実引数個数」に直している。
         *
         * 例:
         *   av = {"ls", "-l", NULL}
         * なら元の ac は 3 で、
         *   ac-- 後は 2 になる。
         */
        ac--;   /* 最後のヌルポインタの分を省く */

        /*
         * もし入力が空行なら、引数は何もない。
         * その場合は何もせず次のループへ進む。
         *
         * つまり空 Enter は無視する。
         */
        if (ac == 0)    /* 入力がなかった */
            continue;

        /*
         * --------------------------------------------------------------
         * バックグラウンド実行記号 "&" の判定
         * --------------------------------------------------------------
         *
         * ここがこのコードのもう1つの核心である。
         *
         * シェルでは、行末に "&" があると
         *   「このコマンドはバックグラウンドで実行する」
         * という意味になる。
         *
         * 例:
         *   sleep 10 &
         *
         * なら、
         *   sleep 10 を起動したあと、
         *   親シェルはその終了を待たずにすぐ次のプロンプトへ戻る。
         *
         * つまり "&" は
         *   「wait するかどうか」
         * を決める記号だと考えると分かりやすい。
         */
        if (!strcmp(av[ac-1], "&")) {
            /*
             * 最後の実引数が "&" ならバックグラウンド実行とみなす。
             *
             * strcmp(...) == 0 は文字列一致を意味する。
             */

            /*
             * "&" 自体は実際に execvp() へ渡すべきコマンド引数ではない。
             * それはシェルの制御記号である。
             *
             * したがって argv ベクタから取り除く必要がある。
             *
             * ここでは最後の "&" が入っていた場所を NULL にして、
             * 引数列終端に置き換えている。
             */
            av[ac-1] = NULL;

            /*
             * 実引数個数も 1 減る。
             */
            ac--;

            /*
             * バックグラウンド実行フラグを立てる。
             *
             * このあと親プロセス側で
             *   if (!bg) waitpid(...)
             * と分岐するため、
             * bg = 1 が
             *   「親はこの子をその場では待たない」
             * を意味する。
             */
            bg = 1;

            /*
             * もし入力が "&" だけだった場合、
             * "&" を取り除いたらコマンド本体が空になる。
             *
             * たとえば
             *   &
             * だけ入力したケースである。
             *
             * それは無効な空コマンドなのでエラーにする。
             */
            if (ac == 0) {
                fputs("invalid null command\n", stderr);
                continue;
            }
        } else {
            /*
             * 行末が "&" でなければ前面実行である。
             *
             * つまり親はあとで waitpid(cpid, &status, 0) により、
             * そのコマンドが終わるまで待つ。
             */
            bg = 0;
        }

        /*
         * --------------------------------------------------------------
         * fork() して子で exec、親は必要なら wait
         * --------------------------------------------------------------
         */

        /*
         * fork() によりシェル本体を親として残しつつ、
         * 実際にコマンドを実行する子プロセスを作る。
         *
         * 戻り値:
         *   -1 : 失敗
         *    0 : 子プロセス側
         *   >0 : 親プロセス側（その値は子PID）
         */
        if ((cpid = fork()) == -1) {
            /*
             * fork 失敗時は異常終了する。
             *
             * 子を作れなければコマンド実行そのものが成り立たない。
             */
            perror("fork");
            exit(1);
        } else if (cpid == 0) {
            /* 子プロセス */

            /*
             * 子プロセス側では execvp(av[0], av) を呼んで、
             * 入力されたコマンドへ自分自身を置き換える。
             *
             * execvp の "p":
             *   PATH を検索してコマンドを探す
             *
             * execvp の "v":
             *   引数を argv ベクタ（配列）で渡す
             *
             * たとえば
             *   av = {"ls", "-l", "/tmp", NULL}
             * なら、
             *   ls -l /tmp
             * を実行するのと同じである。
             *
             * 重要:
             *   バックグラウンド実行か前面実行かは、
             *   この子プロセスにとっては基本的に関係ない。
             *   子はただコマンドを実行するだけである。
             *
             *   「待つか待たないか」は親側の方針で決まる。
             */
            execvp(av[0], av);

            /*
             * ここへ来るのは execvp が失敗した場合だけである。
             *
             * 成功すれば子プロセスは完全に新しいプログラムに置き換わるため、
             * 戻ってこない。
             */
            perror(av[0]);
            exit(1);
        }

        /* 親プロセス */

        /*
         * ここへ来るのは親プロセス側である。
         *
         * 親の仕事は bg フラグに応じて2通りに分かれる。
         *
         *   bg == 0:
         *     前面実行
         *     -> この子 cpid が終わるまで待つ
         *
         *   bg == 1:
         *     バックグラウンド実行
         *     -> この場では待たず、すぐ次の入力へ戻る
         *
         * これがこのコードでバックグラウンド実行を実現している本質である。
         */
        if (!bg) {
            /*
             * ----------------------------------------------------------
             * 前面実行: 特定の子 cpid が終わるまで待つ
             * ----------------------------------------------------------
             *
             * waitpid(cpid, &status, 0)
             *
             * 第1引数 cpid:
             *   -> 今まさに作った、この特定の子プロセスだけを待つ
             *
             * 第2引数 &status:
             *   -> 終了状態を受け取る
             *
             * 第3引数 0:
             *   -> オプションなし。つまり終了するまでブロックして待つ
             *
             * これにより前面実行のコマンドは、
             *   「終わるまでシェルを占有する」
             * 形になる。
             *
             * これは通常のシェルで
             *   ls
             *   cat file
             *   sleep 3
             * などを実行したとき、
             * それが終わるまで次のプロンプトが出ないのと同じ挙動である。
             */
            if (waitpid(cpid, &status, 0) == (pid_t)-1) {       /* (2) */
                /*
                 * waitpid 失敗時は異常終了する。
                 */
                perror("waitpid(1)");
                exit(1);
            }

            /*
             * ここへ来たということは、
             * 指定した前面実行子 cpid が終了し、その終了状態も回収された。
             *
             * つまり前面のジョブが終わった、ということである。
             */
            fprintf(stderr, "process %d finished\n", cpid);
        }

        /*
         * bg == 1 のときはこの if (!bg) に入らない。
         *
         * つまりバックグラウンド実行では、
         * 親は fork 直後にその子を待たず、
         * そのまま次のループへ進んで新しいプロンプトを出す。
         *
         * これがバックグラウンド実行の本質である。
         *
         * 図にすると:
         *
         *   前面実行:
         *     fork -> 子で exec -> 親は waitpid(cpid, ...)
         *          -> 子終了後に次の @
         *
         *   バックグラウンド実行:
         *     fork -> 子で exec
         *          -> 親は待たずにすぐ次の @
         *          -> 終了した子は次回ループ先頭で WNOHANG 付き waitpid で回収
         */
    }

    /*
     * for(;;) 無限ループなので通常ここへは到達しない。
     * ただし構文上の終了点として書かれている。
     */
    exit(0);
}
