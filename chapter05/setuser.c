/* proc/setuser.c */
#include <stdio.h>      /* printf, perror を使うためのヘッダ */
#include <stdlib.h>     /* getenv, setenv, exit を使うためのヘッダ */

/*
 * このコードの主題は「環境変数の参照と書き換え」である。
 *
 * UNIX / Linux では、各プロセスは
 *
 *   環境変数 (environment variables)
 *
 * を持っている。
 *
 * 環境変数とは、プロセスが動作するときに参照できる
 *   名前 = 値
 * の形の文字列情報であり、典型的には
 *
 *   USER=maton
 *   HOME=/home/maton
 *   PATH=/usr/bin:/bin
 *
 * のようなものがある。
 *
 * このプログラムでは、代表例として USER 環境変数を扱っている。
 *
 * 全体のアルゴリズムは次の通りである。
 *
 *   1. getenv("USER") で現在の USER 環境変数の値を読む
 *   2. その値を表示する
 *   3. setenv("USER", "johndoe", 1) で USER を書き換える
 *   4. もう一度 getenv("USER") で値を読む
 *   5. 書き換わった結果を表示する
 *
 * つまりこのコードは、
 *
 *   「現在プロセスが持つ環境変数 USER を読み、
 *    その後このプロセスの中で USER を別の値へ更新し、
 *    更新結果を確認する」
 *
 * ための最小例である。
 *
 * 重要:
 *   ここで setenv() が変更するのは
 *   「このプロセスが持っている環境」
 * である。
 *
 *   親プロセスの環境や、すでに終了した別プロセスの環境は変わらない。
 *
 * たとえばシェルからこのプログラムを実行しても、
 * プログラム内で USER を johndoe に変更したからといって、
 * 実行後にシェル側の USER が書き換わるわけではない。
 *
 * つまり setenv() は
 *   「現在実行中の自分のプロセス空間内の環境」
 * を変更する関数である。
 */

int main(void) {
    /*
     * getenv("USER")
     *
     * getenv() は、指定した名前の環境変数の値を取得する関数である。
     *
     * 第1引数:
     *   "USER"
     *   読みたい環境変数名
     *
     * 戻り値:
     *   成功時:
     *     その環境変数の値を指す char * を返す
     *
     *   見つからない場合:
     *     NULL
     *
     * このコードでは、現在の USER が存在することを前提に
     * そのまま %s で表示している。
     *
     * たとえば現在 USER=maton なら、
     *
     *   getenv("USER")
     *
     * は "maton" を指すポインタを返し、
     *
     *   printf("USER is %s\n", ...)
     *
     * によって
     *
     *   USER is maton
     *
     * のように表示される。
     *
     * 注意:
     *   getenv() は環境変数の「値そのもののコピー」を返すのではなく、
     *   プロセスが保持している環境領域内の文字列を指すポインタを返す。
     *
     *   したがって、その文字列の寿命や内容は
     *   後続の setenv()/putenv()/unsetenv() などの影響を受けうる。
     *
     *   また、もし USER が存在しない環境でこのコードを動かすと、
     *   getenv("USER") は NULL を返す可能性がある。
     *   その状態で %s に渡すと未定義動作になりうる。
     *
     *   このサンプルは「USER がある」ことを前提にした簡潔な教材である。
     */
    printf("USER is %s\n", getenv("USER"));

    /*
     * setenv("USER", "johndoe", 1)
     *
     * setenv() は、環境変数を設定する関数である。
     *
     * 第1引数:
     *   "USER"
     *   設定したい環境変数名
     *
     * 第2引数:
     *   "johndoe"
     *   設定したい新しい値
     *
     * 第3引数:
     *   1
     *   既に同名の環境変数が存在する場合に上書きしてよいかどうかを指定する
     *
     *   - 0 なら「既に存在するなら変更しない」
     *   - 1 なら「既に存在していても上書きする」
     *
     * このコードでは 1 を指定しているので、
     * もともと USER が存在していても
     *
     *   USER = johndoe
     *
     * に更新する。
     *
     * 戻り値:
     *   成功時:
     *     0
     *
     *   失敗時:
     *     -1
     *     errno に理由が入る
     *
     * したがって、ここでは -1 かどうかを見てエラー判定している。
     *
     * 重要:
     *   ここで変わるのは「このプロセスが持つ環境」である。
     *
     * たとえば概念的には、
     *
     *   変更前:
     *     USER=maton
     *
     *   変更後:
     *     USER=johndoe
     *
     * となる。
     *
     * ただしその変更は、このプロセス自身に対して行われる。
     * 親シェルや他の無関係なプロセスには影響しない。
     *
     * さらに言えば、このプログラムから fork/exec して子プロセスを起動した場合、
     * その子プロセスには変更後の環境が引き継がれる可能性がある。
     * つまり setenv() は「以後このプロセスから派生する子に渡す環境」を
     * 整える用途にも使われる。
     */
    if (setenv("USER", "johndoe", 1) == -1) {
        /*
         * setenv() に失敗した場合のエラー処理。
         *
         * perror("setenv") により、errno に対応した人間向けメッセージを表示する。
         *
         * 例:
         *   setenv: Invalid argument
         *   setenv: Cannot allocate memory
         *
         * など。
         *
         * setenv() の失敗は通常それほど多くはないが、
         * 引数が不正だったり、メモリ確保に失敗した場合などには起こりうる。
         */
        perror("setenv");

        /*
         * 環境変数の更新に失敗したので、異常終了する。
         *
         * 0 以外の終了コードを返すことで、
         * 呼び出し元（たとえばシェル）に「正常には終わらなかった」ことを伝える。
         */
        exit(1);
    }

    /*
     * 再び getenv("USER") を呼んで、変更後の値を確認する。
     *
     * もし setenv() が成功していれば、
     *
     *   getenv("USER")
     *
     * は "johndoe" を指すようになる。
     *
     * したがって、ここでは典型的に
     *
     *   USER is now johndoe
     *
     * のように表示される。
     *
     * この出力により、
     *   「setenv() によって現在プロセスの環境が確かに更新された」
     * ことを目で確認できる。
     *
     * つまりこのプログラムは、
     *   読む -> 書く -> もう一度読む
     * という手順で、環境変数更新の効果を検証している。
     */
    printf("USER is now %s\n", getenv("USER"));

    /*
     * 正常終了。
     *
     * ここで return 0; することで、
     * 「プログラムは正常に完了した」と呼び出し元へ伝える。
     *
     * なお、このプログラムが終了すると、
     * そのプロセス自体も終了するため、
     * ここで変更した USER=johndoe という環境も一緒に消える。
     *
     * つまり、この変更は永続設定ではなく、
     * あくまで「この実行中プロセスの寿命の間だけの環境変更」である。
     */
    return 0;
}
