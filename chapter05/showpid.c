/* proc/showpid.c */
#include <stdio.h>      /* printf を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* pid_t や getpid に関連する型定義が入ることがある */
#include <unistd.h>     /* getpid を使うためのヘッダ */

/*
 * このコードの主題は「プロセス ID (PID)」である。
 *
 * UNIX / Linux では、実行中の各プロセスには
 *   そのプロセスを識別するための番号
 * が割り当てられている。
 * これが PID（Process ID）である。
 *
 * PID は、OS が
 *   - どのプロセスにシグナルを送るか
 *   - どのプロセスを wait するか
 *   - どのプロセスの状態を調べるか
 * を区別するための基本的な識別子である。
 *
 * このプログラムは、
 *   getpid()
 * を呼び出して、
 *   「今このプログラム自身がどの PID で動いているか」
 * を表示する最小例である。
 *
 * つまりアルゴリズムとしては非常に単純で、
 *
 *   1. カーネルに「自分の PID を教えてほしい」と問い合わせる
 *   2. 返ってきた PID を表示する
 *   3. 終了する
 *
 * という流れである。
 *
 * しかし意味は重要であり、
 * PID は UNIX / Linux のプロセス管理を理解する入口になる。
 */

int main(void) {
    /*
     * printf("pid = %d\n", getpid());
     *
     * ここで getpid() を呼んでいる。
     *
     * getpid() は、
     *   「この関数を呼んでいる現在のプロセス自身の PID」
     * を返す関数である。
     *
     * 返ってくる値は概念的には
     *   「今実行中の自分の番号」
     * である。
     *
     * たとえば実行結果は
     *
     *   pid = 12345
     *
     * のようになる。
     *
     * 重要:
     *   この PID は毎回固定とは限らない。
     *   プロセスを起動するたびに、OS がその時点で利用可能な PID を割り当てるため、
     *   実行ごとに違う値になるのが普通である。
     *
     * さらに重要なのは、
     * getpid() は「親プロセスの PID」ではなく、
     * 「自分自身の PID」を返す点である。
     *
     * 親プロセスの PID を知りたいなら、
     *   getppid()
     * を使う。
     *
     * printf 側では
     *
     *   "pid = %d\n"
     *
     * という形式で整数として表示している。
     *
     * 最後に '\n' を付けているので、
     * 標準出力 stdout が行バッファリングされる環境でも、
     * この1行はそこで flush されやすい。
     */
    printf("pid = %d\n", getpid());

    /*
     * exit(0);
     *
     * プログラムを正常終了する。
     *
     * 0 は「正常終了」を表す終了コードである。
     *
     * C では
     *
     *   return 0;
     *
     * と書いて main を終えても同様の意味になる。
     *
     * ここでは明示的に exit(0) を呼んで、
     *   「ここでプロセスを終了する」
     * ことを分かりやすく書いている。
     *
     * exit() は単に main を抜けるだけではなく、
     * 標準 I/O バッファの flush や atexit ハンドラの実行など、
     * 終了処理も行う。
     *
     * このプログラムでは大きな違いは見えにくいが、
     * 明示的な終了関数として理解しておくとよい。
     */
    exit(0);
}
