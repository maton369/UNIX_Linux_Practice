/* sub/mysub.c */
#include <stdlib.h> /* atof, atoi, strtod, strtol を使うためのヘッダ */
#include <stdio.h>  /* fgets, fprintf, fputs, getc, stdin, stderr を使うためのヘッダ */
#include <limits.h> /* strtol に関連する定数や型のためのヘッダ（このコードでは直接は未使用） */
#include <string.h> /* strchr, strcpy, strlen, strncpy を使うためのヘッダ */
#include "mysub.h"

/*
 * このファイルの主題は、
 *   - 標準入力 stdin から1行入力を受け取る
 *   - それを int / double / 文字列へ変換する
 *   - 長すぎる入力行の残りを捨てて、次の入力に悪影響を残さない
 * という「対話入力用の補助関数群」である。
 *
 * このファイルには主に次の関数がある。
 *
 *   discardline(FILE *fp)
 *     -> 現在の行の残りを捨てる
 *
 *   getint(char *prompt)
 *     -> プロンプトを表示して整数を1つ読む
 *
 *   getdouble(char *prompt)
 *     -> プロンプトを表示して実数を1つ読む
 *
 *   getstr(char *prompt, char *s, int slen)
 *     -> プロンプトを表示して文字列を1つ読む
 *
 * 重要な設計方針:
 *   これらの関数は、基本的に「1行入力してから変換する」方式を取っている。
 *
 * たとえば整数を読むときでも、いきなり scanf("%d", ...) のようにはせず、
 *
 *     1. fgets() で1行を文字列として受け取る
 *     2. その文字列を strtol()/strtod() で数値へ変換する
 *
 * という流れである。
 *
 * この方式には利点がある。
 *
 *   - 入力の境界が「行」で明確になる
 *   - 残り入力の扱いを自分で制御しやすい
 *   - 長すぎる入力行の後始末ができる
 *
 * 特にこのコードでは、
 *   「fgets でバッファに収まり切らなかった長い行の残りを discardline で捨てる」
 * という処理が重要である。
 *
 * これをしないと、次の入力関数が「前回の入力の残り」を読んでしまい、
 * 対話型プログラムが崩れることがある。
 */

/*
 * discardline はこのファイル内だけで使う内部関数なので static にしている。
 *
 * static を付けることで、
 *   この翻訳単位（この .c ファイル）以外からは見えない
 * 内部関数になる。
 *
 * つまり「外部公開 API ではなく、実装詳細である」ことを示している。
 */
static void discardline(FILE *);

/*
 * 1行の最大一時バッファ長。
 *
 * ここでは 4096 バイトを上限として、
 * getint/getdouble/getstr の内部で入力行を一時的に保持する。
 *
 * 注意:
 *   これは「ユーザ入力の真の最大長」ではない。
 *   これより長い行が来た場合でもプログラムは完全には壊れず、
 *   収まり切らなかった残りを discardline で捨てる設計になっている。
 */
enum {
    MAXLINE = 4096,
};

/*
 * discardline:
 *   ストリーム fp から、現在行の残りを捨てる補助関数である。
 *
 * 何をしているか:
 *   - 1文字ずつ getc(fp) で読む
 *   - EOF に達するか
 *   - 改行 '\n' に出会うまで
 *   読み捨てる
 *
 * なぜ必要か:
 *   fgets(buf, MAXLINE, stdin) は、
 *   入力行が MAXLINE-1 文字より長いと、その行全体を1回では読み切れない。
 *
 * すると、
 *   - 先頭部分だけ buf に入る
 *   - 行末改行 '\n' はまだ stdin 側に残る
 *   - さらにその後ろの文字も残る
 *
 * という状態になる。
 *
 * このまま次の入力関数へ進むと、
 * 「ユーザが新しく入力したつもりの行」ではなく、
 * 「前回の長すぎた行の残り」が読まれてしまう。
 *
 * そこで discardline を使って、
 *   現在行の残りを改行まで全部読み捨てる
 * ことで、次の入力をきれいな行境界から始められるようにしている。
 *
 * 図:
 *
 *   stdin に残っているもの:
 *     [まだ buf に入っていない文字列..............]\n[次の行...]
 *
 *   discardline(stdin) 後:
 *     [次の行...]
 *
 * つまり discardline は、
 *   「長すぎた入力行の残骸を掃除する関数」
 * と考えると分かりやすい。
 */
static void discardline(FILE *fp) {
    /*
     * getc() は通常の文字も EOF も返しうるため、
     * 戻り値は int で受けるのが正しい。
     */
    int c;

    /*
     * 改行にぶつかるまで1文字ずつ捨てる。
     *
     * getc(fp) != EOF の間読み続け、
     * もし読んだ文字 c が '\n' なら、
     * その行の終端まで来たと判断して break する。
     *
     * したがって、
     *   - 改行がある通常の長い行
     *   - EOF まで改行が来ない入力
     * のどちらにも対応している。
     */
    while ((c = getc(fp)) != EOF)
        if (c == '\n') break;
}

/*
 * getint:
 *   プロンプトを表示し、標準入力から1行読み、
 *   その内容を整数 int に変換して返す関数である。
 *
 * アルゴリズム:
 *   1. プロンプトを stderr に出す
 *   2. stdin から1行 fgets() で読む
 *   3. 改行が buf 内に無ければ、長い行と判断して残りを discardline(stdin)
 *   4. strtol() で整数へ変換する
 *   5. int にキャストして返す
 *
 * 重要:
 *   この関数は「入力が本当に正しい整数か」を厳密には検証していない。
 *
 * たとえば
 *     "123abc"
 * のような入力でも、strtol は先頭の数値部分を読んで 123 を返す。
 *
 * また、
 *     "abc"
 * のような入力では 0 になる可能性がある。
 *
 * つまりこれは「簡易入力関数」であり、
 * 厳密なバリデーションよりも、行入力と簡易変換を目的としている。
 */
int getint(char *prompt) {
    /*
     * d:
     *   変換後の整数値を入れる。
     *
     * buf:
     *   fgets で1行受け取るための一時バッファ。
     */
    int d;
    char buf[MAXLINE];

    /*
     * fputs(prompt, stderr)
     *
     * プロンプト文字列を標準エラー出力 stderr に表示する。
     *
     * なぜ stderr を使うのか:
     *   対話型プログラムでは、通常出力 stdout を「本来の出力結果」に使い、
     *   プロンプトや診断を stderr に出す設計がある。
     *
     * こうしておくと、stdout をファイルへリダイレクトしても、
     * プロンプトは端末に見えやすい。
     *
     * ただし、環境によっては stderr/stdout の扱い差を強く意識しない場合もある。
     * このコードでは「対話用メッセージは stderr」という流儀を採用している。
     */
    fputs(prompt, stderr);

    /*
     * fgets(buf, MAXLINE, stdin)
     *
     * stdin から1行読む。
     *
     * ここでは戻り値 NULL のチェックをしていない。
     * したがって EOF や入力エラー時には厳密には脆い。
     *
     * 教材としては、主に
     *   「1行読んでから変換する」
     *   「長い行なら残りを捨てる」
     * という構造を示している。
     */
    fgets(buf, MAXLINE, stdin);

    /*
     * strchr(buf, '\n')
     *
     * buf の中に改行文字 '\n' が含まれているかを調べる。
     *
     * fgets は、
     *   行がバッファに収まれば改行も読み込む
     * が、
     *   行が長すぎると途中までしか入らないため改行は buf に入らない
     *
     * したがって
     *
     *   if (!strchr(buf, '\n'))
     *
     * は
     *   「この入力行は長すぎて、まだ続きが stdin に残っている」
     * ことを検出するための判定である。
     */
    if (!strchr(buf, '\n')) discardline(stdin);

    /*
     * strtol(buf, (char**)NULL, 10)
     *
     * buf に入っている文字列を 10 進整数へ変換する。
     *
     * 第1引数:
     *   buf
     *   変換元文字列
     *
     * 第2引数:
     *   (char**)NULL
     *   どこまで読んだかの終端位置を受け取らない
     *
     * 第3引数:
     *   10
     *   10進数として解釈する
     *
     * 戻り値は long 型なので、ここでは int にキャストしている。
     *
     * コメントにあるように atoi(buf) でも書けるが、
     * strtol の方がより汎用的で、厳密なエラー処理にも発展させやすい。
     *
     * ただしこのコードでは endptr も errno も見ていないため、
     * 「strtol の厳密さ」をフルには使っていない。
     */
    d = (int)strtol(buf, (char**)NULL, 10);     /* または atoi(buf) */

    /*
     * 変換結果を返す。
     */
    return d;
}

/*
 * getdouble:
 *   プロンプトを表示し、標準入力から1行読み、
 *   その内容を double に変換して返す関数である。
 *
 * 基本構造は getint とほぼ同じで、
 * 違うのは変換関数が strtod() である点だけである。
 *
 * アルゴリズム:
 *   1. プロンプトを stderr に出す
 *   2. stdin から1行 fgets() で読む
 *   3. 改行が buf 内に無ければ残りを discardline(stdin)
 *   4. strtod() で実数へ変換する
 *   5. 返す
 *
 * これも厳密な入力検証はしていない簡易関数である。
 */
double getdouble(char *prompt) {
    /*
     * d:
     *   変換後の double 値
     *
     * buf:
     *   一時バッファ
     */
    double d;
    char buf[MAXLINE];

    /*
     * プロンプトを stderr に表示する。
     */
    fputs(prompt, stderr);

    /*
     * stdin から1行読む。
     */
    fgets(buf, MAXLINE, stdin);

    /*
     * 行が長すぎて改行が buf 内に無いなら、
     * 残りを捨てて入力状態を次行へそろえる。
     */
    if (!strchr(buf, '\n')) discardline(stdin);

    /*
     * strtod(buf, (char**)NULL)
     *
     * 文字列を double に変換する。
     *
     * 第2引数の endptr を捨てているので、
     * どこまで正しく読めたかは見ていない。
     *
     * コメントにあるように atof(buf) でも書けるが、
     * strtod の方が一般により望ましい。
     */
    d = strtod(buf, (char**)NULL);              /* または atof(buf) */

    /*
     * 変換結果を返す。
     */
    return d;
}

/*
 * getstr:
 *   プロンプトを表示し、標準入力から1行読み、
 *   それをユーザ指定バッファ s に安全に格納する関数である。
 *
 * ここでは数値変換は行わず、「文字列そのもの」を受け取る。
 *
 * アルゴリズム:
 *   1. 必要なら警告を出す（slen が MAXLINE を超えるとき）
 *   2. プロンプトを stderr に出す
 *   3. stdin から1行を buf に読む
 *   4. 改行が buf 内にあれば '\0' に置き換えて末尾改行を削る
 *   5. 改行が無ければ長い行とみなし、残りを discardline(stdin)
 *   6. buf の長さが s に収まるなら strcpy
 *   7. 収まらなければ strncpy で切り詰め、最後に '\0' を保証
 *
 * つまりこの関数は、
 *   「1行文字列入力 + 改行除去 + 必要なら切り詰め」
 * をまとめた関数である。
 */
void getstr(char *prompt, char *s, int slen) {
    /*
     * p:
     *   strchr の結果として改行文字の位置を受けるポインタ
     *
     * buf:
     *   一時的に入力行を受ける内部バッファ
     */
    char *p, buf[MAXLINE];

    /*
     * slen > MAXLINE のときの警告。
     *
     * この関数は内部で buf[MAXLINE] に一度入力を受けるので、
     * たとえ呼び出し側がもっと大きな出力バッファ s を渡しても、
     * 1回に読めるのは MAXLINE-1 文字までである。
     *
     * そのため、
     *   「出力先バッファの方が内部一時バッファより大きい」
     * 状況は少し不自然であり、警告している。
     *
     * ただし「致命的エラー」とはせず、処理は続行する。
     *
     * つまりこの警告は、
     *   呼び出し側設計が想定とズレているかもしれない
     * ことを知らせるためのものである。
     */
    if (slen > MAXLINE)
        fprintf(stderr,
          "getstr: buffer length must be <= %d (%d specified);"
          " proceeding anyway\n", MAXLINE, slen);

    /*
     * プロンプトを stderr に表示する。
     */
    fputs(prompt, stderr);

    /*
     * stdin から1行を buf に読む。
     *
     * 戻り値チェックは省略されているので、
     * EOF やエラー時には厳密には弱い。
     *
     * 教材としては、主に
     *   - 改行除去
     *   - 長い行の処理
     *   - 安全コピー
     * が主題になっている。
     */
    fgets(buf, MAXLINE, stdin);

    /*
     * strchr(buf, '\n')
     *
     * buf 内に改行があるかを探す。
     *
     * 結果が NULL でなければ、
     *   行全体が buf に収まり、改行も読み込めた
     * という意味である。
     *
     * 結果が NULL なら、
     *   行が長すぎて buf に収まり切らず、
     *   まだ stdin に続きが残っている
     * と判断できる。
     */
    if ((p = strchr(buf, '\n')) == NULL)
        /*
         * 改行が無いので、入力行の残りを捨てる。
         *
         * これにより、次の入力関数が前回の残骸を読まないようにする。
         */
        discardline(stdin);
    else
        /*
         * 改行が見つかった場合、
         * その改行文字を '\0' に置き換える。
         *
         * つまり入力された行末の改行を削除して、
         * buf を「改行無しの普通の C 文字列」に整える。
         *
         * 例:
         *   "hello\n\0"
         * を
         *   "hello\0"
         * に変えるイメージである。
         *
         * これは文字列入力関数として非常に一般的な後処理である。
         */
        *p = '\0';

    /*
     * strlen(buf) < slen
     *
     * buf の文字列長が出力先 s の容量より小さいなら、
     * 終端 '\0' も含めて安全に収まる。
     *
     * 注意:
     *   「< slen」なのは、文字列本体だけでなく '\0' も必要だからである。
     *
     * たとえば slen = 10 なら、
     *   9文字までなら '\0' を含めて収まる。
     */
    if (strlen(buf) < slen) {
        /* s に収まる */

        /*
         * strcpy(s, buf)
         *
         * buf は十分短いと分かっているので、そのままコピーしてよい。
         *
         * strcpy は '\0' まで含めてコピーするため、
         * s には正しい C 文字列が入る。
         */
        strcpy(s, buf);
    } else {
        /* 収まらないので後ろを切る */

        /*
         * strncpy(s, buf, slen-1)
         *
         * 出力先 s の最後の1文字分を '\0' 用に残しておき、
         * それ以外の最大 slen-1 文字だけをコピーする。
         *
         * こうすることで、入力が長すぎても s の外へはみ出さない。
         *
         * ただし strncpy は場合によっては終端 '\0' を自動で付けないことがあるので、
         * その次の行で必ず明示的に '\0' を入れている。
         */
        strncpy(s, buf, slen-1);

        /*
         * s[slen-1] = '\0';
         *
         * これにより、たとえ切り詰めコピーになっても
         * s が必ず '\0' 終端文字列になることを保証する。
         *
         * これは安全な文字列処理で非常に重要な一手である。
         */
        s[slen-1] = '\0';
    }
}
