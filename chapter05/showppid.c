/* proc/showppid.c */
#include <stdio.h>      /* printf を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型, fork/getpid/getppid に関連する型定義 */
#include <unistd.h>     /* fork, getpid, getppid, sleep を使うためのヘッダ */

/*
 * このコードの主題は「親プロセス ID (PPID)」である。
 *
 * UNIX / Linux では、プロセスは単独で存在するだけでなく、
 * しばしば「どのプロセスがどのプロセスから生まれたか」という
 * 親子関係を持つ。
 *
 * ここで重要な識別子は 2 つある。
 *
 *   PID   : そのプロセス自身の ID
 *   PPID  : そのプロセスの親プロセスの ID
 *
 * このプログラムでは fork() を使って子プロセスを作り、
 *
 *   - 親プロセス側では
 *       自分の PID と、fork() が返した子プロセスの PID を表示する
 *
 *   - 子プロセス側では
 *       getppid() により親の PID を取得し、
 *       getpid() により自分の PID を取得して表示する
 *
 * ということを行っている。
 *
 * つまりこのコードは、
 *
 *   「fork() で生まれた子が、自分の親が誰かを getppid() で知る」
 *
 * ことを確かめるための最小例である。
 *
 * さらに、このコードでは親プロセスが無限ループで生き続けるため、
 * 子プロセスが実行される時点で親がまだ存在していることが保証されやすい。
 * その結果、子プロセスの getppid() は親の PID をきれいに観察しやすい。
 *
 * もし親がすぐ終了してしまうと、環境によっては
 * 子の親が init/systemd 側へ付け替わるような現象も観察対象になるが、
 * このコードではそれを避け、まず純粋な親子関係を見ることを狙っている。
 */

int main(void) {
    /*
     * pid:
     *   fork() の戻り値を受ける変数である。
     *
     * 型は pid_t であり、PID を表すための型である。
     *
     * fork() の戻り値の意味:
     *
     *   -1 : fork 失敗
     *    0 : 子プロセス側での戻り値
     *   >0 : 親プロセス側での戻り値（その値は子プロセスの PID）
     *
     * したがって、この1つの変数 pid を見れば、
     *   - 今の実行主体が親か子か
     *   - 親なら子の PID がいくつか
     * を判断できる。
     */
    pid_t pid;

    /*
     * if ((pid = fork()) == 0)
     *
     * ここがプロセス分岐の中心である。
     *
     * fork() を呼ぶと、現在のプロセスが複製され、
     * 以後は
     *
     *   - 親プロセス
     *   - 子プロセス
     *
     * の2つが存在するようになる。
     *
     * そしてこの if 文は、親と子の両方で評価される。
     * ただし fork() の戻り値が違うので、入る分岐が変わる。
     *
     *   子プロセス側:
     *     pid == 0
     *     -> if 節に入る
     *
     *   親プロセス側:
     *     pid == 子PID (>0)
     *     -> else 節に入る
     *
     * このコードでは fork 失敗時の -1 チェックは省略されている。
     * 教材としては親子関係の観察に主題を絞っているが、
     * 実務コードでは
     *
     *   if ((pid = fork()) == -1) { ... }
     *
     * のようなエラー処理を入れるのが望ましい。
     */
    if ((pid = fork()) == 0) {
        /* 子プロセス */

        /*
         * ここへ来るのは子プロセス側である。
         *
         * 子プロセスは fork() から 0 を受け取るので、
         * if 条件が真になってこの分岐へ入る。
         *
         * この分岐の目的は、
         *   「子から見て、自分の親 PID と自分自身の PID を表示する」
         * ことである。
         *
         * ここで使う2つの関数は重要である。
         *
         *   getppid()
         *     -> 自分の親プロセスの PID を返す
         *
         *   getpid()
         *     -> 自分自身の PID を返す
         *
         * これにより、
         *   「自分は誰か」
         *   「自分の親は誰か」
         * の両方を子視点で確認できる。
         */

        /*
         * printf("child: parent PID = %d, my PID = %d\n", getppid(), getpid());
         *
         * 子プロセス視点の親子関係を表示する。
         *
         * 典型例:
         *
         *   child: parent PID = 12345, my PID = 12346
         *
         * ここから分かること:
         *
         *   - 親 PID = 12345
         *   - 子 PID = 12346
         *
         * つまり「自分の親は 12345 である」と子が認識している。
         *
         * これを親側出力
         *
         *   parent: my PID = 12345, child PID = 12346
         *
         * と照らし合わせると、
         * 親子関係がきれいに対応していることが分かる。
         *
         * 重要:
         *   getppid() は常に「fork() を呼んだ元の親」を返すとは限らない。
         *   親がすでに終了していれば、子は別のプロセスに引き取られ、
         *   その PID が返ることがある。
         *
         * しかしこのコードでは、親が後で無限ループに入って生き続けるので、
         * 通常は「本来の親の PID」がそのまま観察される。
         */
        printf("child: parent PID = %d, my PID = %d\n",
                                      getppid(), getpid());

        /*
         * 子プロセスは表示だけ終えたら正常終了する。
         *
         * ここで exit(0) しても、親プロセスは別プロセスなので終了しない。
         * 親は else 節でそのまま動き続ける。
         *
         * つまり親子はそれぞれ独立に終了できる。
         */
        exit(0);
    } else {
        /* 親プロセス */

        /*
         * ここへ来るのは親プロセス側である。
         *
         * fork() の戻り値 pid には、
         * 親から見た「新しく作られた子プロセスの PID」が入っている。
         *
         * したがって親は
         *
         *   getpid()
         *     -> 自分自身の PID
         *
         *   pid
         *     -> 子プロセスの PID
         *
         * を組み合わせて、
         * 親視点の親子関係を表示できる。
         */

        /*
         * printf("parent: my PID = %d, child PID = %d\n", getpid(), pid);
         *
         * 親プロセス視点で、
         *   - 自分の PID
         *   - 子の PID
         * を表示する。
         *
         * 典型例:
         *
         *   parent: my PID = 12345, child PID = 12346
         *
         * これと子側出力
         *
         *   child: parent PID = 12345, my PID = 12346
         *
         * を比べると、
         *
         *   親の「my PID」  == 子の「parent PID」
         *   親の「child PID」== 子の「my PID」
         *
         * となり、親子関係が対応していることが分かる。
         */
        printf("parent: my PID = %d, child PID = %d\n", getpid(), pid);

        /*
         * for (;;) sleep(1);
         *
         * 親プロセスを終了させず、そのまま生かしておくための無限ループである。
         *
         * なぜこれが必要か:
         *
         *   このコードの主題は「子プロセスが getppid() で親 PID を見る」ことなので、
         *   子が getppid() を呼ぶ時点で親がまだ存在していてほしい。
         *
         * もし親が printf の直後にすぐ終了すると、
         * 子の実行タイミングによっては
         *
         *   - すでに親が終わっている
         *   - 子が別プロセスに引き取られている
         *
         * という状況が起こりうる。
         *
         * すると getppid() の結果が「本来の親 PID」ではなくなる可能性がある。
         *
         * そこでこの無限ループにより、
         * 親はしばらく（というより永久に）生き続けるので、
         * 子が親 PID を観察する間、親子関係が保たれやすい。
         *
         * sleep(1) を入れている理由:
         *
         *   無限ループを単なる
         *
         *     for (;;)
         *         ;
         *
         * のようなビジーループにすると CPU を無駄に消費する。
         *
         *   sleep(1) を入れることで、
         *   親は1秒ごとに眠りながら存在し続けるだけになり、
         *   CPU 負荷を大きく下げられる。
         *
         * つまりこのループは、
         *
         *   「親を生かしたまま、しかし無駄に CPU を回さず待機させる」
         *
         * ための仕掛けである。
         *
         * 注意:
         *   このコードでは wait() を使っていないため、
         *   子が終了したあと親はそれを回収しない。
         *
         *   そのため、親が生き続ける限り、子はゾンビ状態になる可能性がある。
         *
         *   ただしこのサンプルの主題は wait/waitpid による回収ではなく、
         *   PPID 観察なので、そこまでは扱っていない。
         */
        for (;;)
            sleep(1);
    }
}
