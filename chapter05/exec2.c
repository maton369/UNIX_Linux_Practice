/* proc/exec2.c */
#include <limits.h>     /* PATH_MAX を使うためのヘッダ */
#include <stdio.h>      /* printf, fflush, perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <unistd.h>     /* execv を使うためのヘッダ */
#include "mysub.h"      /* getstr を使うための自作ヘッダ */

/*
 * このコードの主題は「execv() によるプロセス置換」と、
 * 特に
 *
 *   「ベクタ引数 (vector arguments)」
 *
 * で新しいプログラムに渡す引数列を指定する点である。
 *
 * 前の execl() では、
 *   引数を
 *
 *     "ls", "-l", buf, NULL
 *
 * のように1個ずつリスト形式で並べていた。
 *
 * それに対して execv() では、
 *   引数列全体を char * 配列として先に用意し、
 *   その配列へのポインタをまとめて渡す。
 *
 * つまりこのコードは、
 *
 *   execl("/bin/ls", "ls", "-l", buf, NULL);
 *
 * に相当することを、
 *
 *   char *av[] = { "ls", "-l", buf, NULL };
 *   execv("/bin/ls", av);
 *
 * という「配列版」で書き直したものと考えると理解しやすい。
 *
 * 全体の流れは次の通りである。
 *
 *   1. ユーザにディレクトリ名を入力してもらう
 *   2. そのディレクトリ名を buf に格納する
 *   3. av[2] が buf を指しているので、引数列が完成する
 *   4. 「Files in <directory>:」と表示する
 *   5. fflush(stdout) で表示を確実に出す
 *   6. execv("/bin/ls", av) を呼ぶ
 *   7. 成功すれば現在プロセスは /bin/ls に置き換わる
 *   8. 失敗した場合だけ perror("execv") に進む
 *
 * つまりこのコードは、
 *
 *   「ユーザ入力したディレクトリを argv 配列の一部として組み込み、
 *    execv() で /bin/ls -l <directory> を実行する」
 *
 * サンプルである。
 *
 * 重要:
 *   exec は新しい子プロセスを増やすのではなく、
 *   現在のプロセス自身の中身を別プログラムに置き換える。
 *
 *   したがって、成功したらこのプログラムの続きは実行されない。
 */

int main(void) {
    /*
     * buf:
     *   ユーザが入力したディレクトリ名を格納する文字列バッファである。
     *
     * PATH_MAX は「パス名の最大長の目安」として使われる定数であり、
     * ここでは十分大きな入力バッファとして利用している。
     *
     * たとえば、ユーザが
     *
     *   /tmp
     *   /etc
     *   .
     *   ..
     *
     * などを入力すると、その文字列がここに入る。
     *
     * この buf は、後で av[2] を通して新しい ls プログラムの
     * 引数 argv[2] になる。
     */
    char buf[PATH_MAX];

    /*
     * av:
     *   新しいプログラム /bin/ls に渡す引数ベクタ（argv 配列）である。
     *
     * ここが execv() の最大のポイントである。
     *
     * execv の "v" は vector を意味し、
     *   「引数を配列（ベクタ）として渡す」
 * 関数である。
     *
     * この配列の意味は次の通り。
     *
     *   av[0] = "ls"
     *     新しいプログラムから見た argv[0]
     *     慣習的にはコマンド名自身を入れる
     *
     *   av[1] = "-l"
     *     ls の long format オプション
     *
     *   av[2] = buf
     *     対象ディレクトリ
     *     まだこの時点では buf の中身は未入力だが、
     *     「buf という配列の先頭アドレス」を av[2] に入れておくことで、
     *     後から buf の中身が書き換われば、その内容がそのまま引数になる
     *
     *   av[3] = NULL
     *     引数配列の終端
     *
     * つまり、この配列は概念的に
     *
     *   argv = { "ls", "-l", <directory>, NULL }
     *
     * を表す。
     *
     * 重要:
     *   av[2] に「buf の中身のコピー」を入れているのではなく、
     *   buf という文字列領域へのポインタを入れている。
     *
     *   そのため、後で getstr() が buf に "/tmp" を書き込めば、
     *   av[2] は自動的に "/tmp" を指す引数として機能する。
     *
     * 図にするとこうである。
     *
     *   av
     *   +-------+-------+-------+------+
     *   | "ls"  | "-l"  |  buf  | NULL |
     *   +-------+-------+-------+------+
     *                         |
     *                         v
     *                      [buf の先頭]
     *                      まだ空、あとで getstr で書き込まれる
     */
    char *av[4] = { "ls", "-l", buf, NULL };

    /*
     * getstr("directory? ", buf, sizeof(buf));
     *
     * ユーザに対象ディレクトリを入力させる。
     *
     * ここで入力された文字列が buf に格納される。
     * getstr() 側で末尾改行は除去される想定なので、
     * buf はそのまま C 文字列として利用できる。
     *
     * たとえばユーザが
     *
     *   /etc
     *
     * と入力すると、
     *
     *   buf = "/etc"
     *
     * となる。
     *
     * この瞬間、av[2] はもともと buf を指しているので、
     * 引数ベクタ全体は概念的に
     *
     *   av[0] = "ls"
     *   av[1] = "-l"
     *   av[2] = "/etc"
     *   av[3] = NULL
     *
     * として完成したことになる。
     *
     * つまりこのコードでは、
     *   先に argv 配列の「構造」を作っておき、
     *   後から buf を埋めることで argv 全体を完成させている。
     */
    getstr("directory? ", buf, sizeof(buf));

    /*
     * printf("Files in %s:\n", buf);
     *
     * これから対象ディレクトリの内容を表示する、という案内メッセージを出す。
     *
     * たとえば buf が "/etc" なら、
     *
     *   Files in /etc:
     *
     * のように表示される。
     *
     * これは exec 前の「元のプログラム」の最後の案内であり、
     * このあと execv() が成功すると出力主体は /bin/ls に変わる。
     *
     * したがってこのメッセージは、
     *   どこまでが元のプログラムの出力か
     * を区別するための目印でもある。
     */
    printf("Files in %s:\n", buf);

    /*
     * fflush(stdout);
     *
     * これは重要である。
     *
     * stdout は行バッファリングや完全バッファリングされることがあり、
     * 直前の printf の出力がまだ内部バッファに残っている可能性がある。
     *
     * その状態で execv() に成功すると、
     * 観察上、元プログラムの出力と ls の出力の境界が分かりにくくなることがある。
     *
     * そこで exec 前に明示的に flush して、
     *
     *   "Files in ...:\n"
     *
     * を確実に画面へ出している。
     *
     * つまりこの fflush は、
     *   「exec 前の最後の自前出力を確定させる」
     * ための処理である。
     */
    fflush(stdout);

    /*
     * execv("/bin/ls", av);
     *
     * ここがこのコードの核心である。
     *
     * execv は exec 系関数の1つであり、
     * 現在プロセスの実行内容を別プログラムへ置き換える。
     *
     * 関数名の意味を分解すると、
     *
     *   exec : プロセス内容を置き換える
     *   v    : 引数を vector（配列）として渡す
     *
     * である。
     *
     * ------------------------------------------------------------------
     * 1. 第1引数 "/bin/ls" の意味
     * ------------------------------------------------------------------
     *
     * これは実行したいプログラム実体のパスである。
     *
     * ここではフルパスを明示しているので、
     * PATH 検索は行わない。
     *
     * つまり
     *
     *   /bin/ls
     *
     * を直接実行対象として指定している。
     *
     * ------------------------------------------------------------------
     * 2. 第2引数 av の意味
     * ------------------------------------------------------------------
     *
     * これは新しいプログラムに渡す引数ベクタ（argv 配列）である。
     *
     * 中身は実際には
     *
     *   av[0] = "ls"
     *   av[1] = "-l"
     *   av[2] = buf
     *   av[3] = NULL
     *
     * である。
     *
     * たとえば buf = "/tmp" なら、
     * 概念的には新しいプログラムは次のように起動される。
     *
     *   /bin/ls
     *     argv[0] = "ls"
     *     argv[1] = "-l"
     *     argv[2] = "/tmp"
     *     argv[3] = NULL
     *
     * つまり実質的には
     *
     *   /bin/ls -l /tmp
     *
     * が実行される。
     *
     * ------------------------------------------------------------------
     * 3. execl との違い
     * ------------------------------------------------------------------
     *
     * 前の execl 版では、引数を1個ずつ並べていた。
     *
     *   execl("/bin/ls", "ls", "-l", buf, NULL);
     *
     * それに対して execv 版では、
     * 同じ引数列を配列にして渡している。
     *
     *   char *av[] = { "ls", "-l", buf, NULL };
     *   execv("/bin/ls", av);
     *
     * 図にするとこうである。
     *
     *   execl:
     *     引数をその場で列挙
     *
     *   execv:
     *     引数配列を先に作ってから渡す
     *
     * どちらも最終的に新しいプログラムが受け取る argv は同じである。
     *
     * ------------------------------------------------------------------
     * 4. 成功時は戻らない
     * ------------------------------------------------------------------
     *
     * execv に成功すると、現在のプロセスイメージは /bin/ls に完全に置換される。
     *
     * そのため、
     *
     *   perror("execv");
     *   exit(1);
     *
     * へ進むのは execv が失敗した場合だけである。
     *
     * 普通の関数呼び出しなら戻ってくるが、
     * exec 系関数は成功時には呼び出し元へ戻らない。
     *
     * これが最も重要な性質の1つである。
     *
     * ------------------------------------------------------------------
     * 5. プロセス数は増えない
     * ------------------------------------------------------------------
     *
     * ここも重要である。
     *
     * execv は新しい子プロセスを作るのではない。
     * 今のこのプロセス自身が /bin/ls になる。
     *
     * つまり、
     *   「自分が消えて ls が新しく増える」
     * のでもなく、
     *   「自分の中身が ls に差し替わる」
     * と理解するのが正しい。
     */
    execv("/bin/ls", av);

    /*
     * perror("execv");
     *
     * ここに到達するのは execv が失敗した場合だけである。
     *
     * もし execv に成功していれば、
     * その時点でこのプロセスはすでに /bin/ls へ置換されているため、
     * 元のコードへ戻ってこない。
     *
     * したがって、この perror は
     *   「execv 失敗時専用のエラー表示」
     * になっている。
     *
     * 失敗理由の例:
     *   - /bin/ls が存在しない
     *   - 実行権限がない
     *   - メモリ不足
     *   - システム資源不足
     *
     * perror("execv") により、errno に対応した人間向けメッセージを表示する。
     *
     * 例:
     *   execv: No such file or directory
     *   execv: Permission denied
     *
     * など。
     *
     * exec 系関数の直後に perror を置くのは典型的な書き方である。
     * 理由は、成功時には戻らないので、
     * 直後のコードをそのまま失敗処理専用にできるからである。
     */
    perror("execv");

    /*
     * execv に失敗したので異常終了する。
     *
     * exit(1) により、
     *   「このプログラムは正常には完了できなかった」
     * ことを呼び出し元へ伝える。
     *
     * 成功時にはここへ来ないので、
     * この exit(1) は失敗経路専用の終了処理である。
     */
    exit(1);
}
