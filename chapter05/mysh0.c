/* proc/mysh0.c */
#include <stdio.h>      /* perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型, fork/wait に関連する型定義 */
#include <sys/wait.h>   /* wait を使うためのヘッダ */
#include <unistd.h>     /* fork, execlp を使うためのヘッダ */
#include "mysub.h"      /* getstr を使うための自作ヘッダ */

/*
 * このコードの主題は「簡易シェル」と「wait() による子プロセスの回収」である。
 *
 * このプログラムはとても小さいが、UNIX / Linux のシェルが行う
 * 基本動作の骨格を持っている。
 *
 * 具体的には、1回のループで次のことを行う。
 *
 *   1. ユーザにコマンド名を入力させる
 *   2. fork() で子プロセスを作る
 *   3. 子プロセス側では execlp() でそのコマンドを実行する
 *   4. 親プロセス側では wait() で子プロセスの終了を待つ
 *   5. 子が終わったら、次のコマンド入力へ戻る
 *
 * つまり、このコードは
 *
 *   「1コマンドずつ前面で実行する最小のシェル」
 *
 * と見ることができる。
 *
 * 特にこのコードで重要なのは wait() である。
 *
 * なぜ wait() が必要か:
 *
 *   - fork() すると親と子の2プロセスが存在する
 *   - 子プロセスは execlp() で実コマンドへ変身して実行される
 *   - 親プロセスが wait() しないと、
 *       子が終わっても終了状態を回収できず、
 *       ゾンビプロセスになる可能性がある
 *   - また、wait() によって「親は子が終わるまで次のプロンプトを出さない」
 *     という前面実行型のシェル動作になる
 *
 * したがって wait() は、このコードでは
 *
 *   1. 子プロセスの終了を待つ
 *   2. 子プロセスの終了状態を回収する
 *   3. 親子の実行順を整え、シェルらしい動作にする
 *
 * という3つの役割を担っている。
 */

int main(void) {
    /*
     * cmd:
     *   ユーザが入力したコマンド名を格納する文字列バッファである。
     *
     * ここでは非常に単純なシェルなので、
     *   "ls"
     *   "pwd"
     *   "date"
 * のような「コマンド名1個だけ」を入力する想定である。
     *
     * 重要:
     *   このコードは引数付きコマンド
     *
     *     ls -l
     *
     * のような複雑な入力分解は行っていない。
     *
     *   getstr() で読んだ文字列全体を、そのまま「コマンド名」として
     *   execlp(cmd, cmd, (char *)0) に渡している。
     *
     *   したがって "ls -l" と入力すると、
     *   "ls -l" という名前の実行ファイルを探しにいってしまう。
     *
     *   あくまで最小シェルのサンプルであり、
     *   コマンドライン解析はまだ扱っていない。
     */
    char cmd[1024];

    /*
     * status:
     *   wait() が回収した子プロセスの終了状態を受け取るための整数変数である。
     *
     * ここが wait() を理解するうえで大事である。
     *
     * wait(&status) は単に
     *   「子が終わるまで待つ」
     * だけではない。
     *
     * 子が終了したとき、その終了情報を status に書き込む。
     *
     * この status には、
     *   - 通常終了したか
     *   - シグナルで終わったか
     *   - 終了コードはいくつか
     * などの情報がビット形式で入る。
     *
     * このコードではその中身を解析していないが、
     * 本来は次のようなマクロで見ることができる。
     *
     *   WIFEXITED(status)
     *   WEXITSTATUS(status)
     *   WIFSIGNALED(status)
     *   WTERMSIG(status)
     *
     * つまり status は、
     *   「wait が子の終了を回収したときの結果格納場所」
     * である。
     */
    int status;

    /*
     * for (;;)
     *
     * 無限ループであり、簡易シェルとして
     * 何度でもコマンド入力 -> 実行 -> 待機
     * を繰り返す。
     *
     * 1回のループが、シェルから見た「1コマンド実行」に対応する。
     */
    for (;;) {
        /*
         * getstr("@ ", cmd, sizeof(cmd));
         *
         * プロンプト "@ " を表示し、ユーザにコマンド名を入力させる。
         *
         * たとえば
         *   ls
         * と入力すれば
         *   cmd = "ls"
         * になる。
         *
         * この時点ではまだ何も実行していない。
         * 単に「次に実行したいプログラム名」を文字列として受け取っただけである。
         *
         * 実行の流れとしては
         *
         *   入力 -> fork -> 子で exec -> 親で wait
         *
         * の最初の「入力」段階に相当する。
         */
        getstr("@ ", cmd, sizeof(cmd));

        /*
         * if (fork() == 0)
         *
         * ここが親子分岐の中心である。
         *
         * fork() を呼ぶと、現在のプロセスが複製され、
         * 以後は
         *
         *   - 親プロセス
         *   - 子プロセス
         *
         * の2つが存在するようになる。
         *
         * fork() の戻り値:
         *
         *   -1 : 失敗
         *    0 : 子プロセス側
         *   >0 : 親プロセス側（その値は子PID）
         *
         * このコードでは、戻り値が 0 なら子プロセス側に入り、
         * そうでなければ親プロセス側へ進む。
         *
         * 注意:
         *   ここでは fork() 失敗時の -1 チェックが省略されている。
         *   教材としては簡潔だが、実務コードでは本来
         *
         *     pid_t pid;
         *     if ((pid = fork()) == -1) { ... }
         *
         *   のように明示的に処理したい。
         */
        if (fork() == 0) {
            /* 子プロセス */

            /*
             * ここへ来るのは子プロセス側である。
             *
             * 子プロセスの役割は、
             * 親から受け継いだ cmd を使って、
             * 実際のコマンドプログラムへ変身することだ。
             *
             * つまりこの子プロセスは、
             *   「シェルがユーザの代わりに起動する実行担当」
             * である。
             */

            /*
             * execlp(cmd, cmd, (char *)0);
             *
             * execlp は exec 系関数の1つであり、
             * 現在プロセスの実行内容を別プログラムへ置き換える。
             *
             * ここでの各引数の意味:
             *
             * 第1引数:
             *   cmd
             *   実行したいプログラム名
             *
             *   execlp の "p" は PATH 検索を意味するので、
             *   たとえば cmd = "ls" なら PATH をたどって
             *   /bin/ls などを探して実行する。
             *
             * 第2引数:
             *   cmd
             *   新しいプログラムから見た argv[0]
             *
             * 第3引数:
             *   (char *)0
             *   引数列終端の NULL
             *
             * つまりこの呼び出しは概念的には
             *
             *   ls
             *
             * や
             *
             *   pwd
             *
             * のような「引数なしコマンド」を1つ起動するのと同じである。
             *
             * 重要:
             *   exec に成功したら、この子プロセスはその時点で別プログラムに
             *   完全に置き換わる。
             *   したがって、成功時にはこの関数呼び出しから戻ってこない。
             *
             * つまり、この行の次にある perror(cmd); exit(1); は
             * execlp 失敗時専用の処理である。
             */
            execlp(cmd, cmd, (char *)0);

            /*
             * ここへ来るのは execlp が失敗した場合だけである。
             *
             * 典型的な失敗理由:
             *   - コマンドが見つからない
             *   - 実行権限がない
             *   - システム資源不足
             *
             * perror(cmd) によって、
             * たとえば
             *
             *   nosuchcmd: No such file or directory
             *
             * のように、コマンド名をラベルとしてエラー理由を表示できる。
             *
             * このように exec 系関数の直後に perror を置くのは典型的である。
             * 理由は、成功時には戻らないので、
             * 直後のコードをそのまま失敗処理専用にできるからである。
             */
            perror(cmd);

            /*
             * execlp に失敗したので、子プロセスは異常終了する。
             *
             * exit(1) の 1 は失敗を表す終了コードである。
             * これにより親プロセスは wait() 後に、
             * 必要なら status を見て「失敗した」と判断できる。
             *
             * このコードでは status を解析していないが、
             * wait() はこの終了情報をきちんと回収する。
             */
            exit(1);
        }

        /* 親プロセス */

        /*
         * ここへ来るのは親プロセス側である。
         *
         * 親プロセスの役割は、
         *   - 子プロセスを起動すること
         *   - その子プロセスが終わるまで待つこと
         *   - 終わったら次のコマンド入力へ戻ること
         *
         * つまりこの簡易シェルでは、
         * 親プロセス自身はコマンドを実行しない。
         * 実行役はあくまで子であり、
         * 親はシェル本体として残り続ける。
         */

        /*
         * if (wait(&status) == (pid_t)-1)
         *
         * ここがこのコードの最大の主題である wait() である。
         *
         * wait() は、
         *   「終了した子プロセスを1つ回収する」
         * 関数である。
         *
         * もう少し正確に言うと、wait() は次の2つを行う。
         *
         *   1. 子プロセスがまだ動いていれば、その終了まで親をブロックする
         *   2. 子プロセスが終わったら、その終了状態を回収してゾンビを消す
         *
         * つまり wait() は「待つ」だけでなく「回収する」ことも本質である。
         *
         * ------------------------------------------------------------------
         * 1. なぜ待つ必要があるのか
         * ------------------------------------------------------------------
         *
         * このプログラムは前面実行型の簡易シェルである。
         *
         * ユーザが
         *   ls
         * を実行したら、その ls が終わるまで次のプロンプト "@ " を出してほしくない。
         *
         * もし wait() をしないと、
         * 親プロセスは fork 直後にすぐ次のループへ進み、
         * 子がまだ動いているのに次のプロンプトを出してしまう。
         *
         * すると見た目がシェルらしくなくなる。
         *
         * したがって wait() は、
         *   「子コマンドが終わるまで親シェルは待機する」
         * という前面実行の振る舞いを実現している。
         *
         * ------------------------------------------------------------------
         * 2. なぜ回収が必要なのか
         * ------------------------------------------------------------------
         *
         * 子プロセスは exit() すると完全に消えるわけではなく、
         * 親がその終了状態を受け取るまで、
         * カーネル内部には終了情報が残る。
         *
         * この状態の子プロセスをゾンビプロセスという。
         *
         * 親が wait() を呼ぶことで、
         * カーネルはその終了情報を親へ渡し、
         * 子プロセスの表項目を完全に掃除できる。
         *
         * つまり wait() をしないと、
         * 子が終わるたびにゾンビが残る可能性がある。
         *
         * ------------------------------------------------------------------
         * 3. status の意味
         * ------------------------------------------------------------------
         *
         * wait(&status) の第1引数には、
         * 子プロセスの終了状態を書き込むための変数のアドレスを渡す。
         *
         * 子が通常終了したのか、
         * シグナルで終わったのか、
         * 終了コードはいくつか、
         * といった情報が status に入る。
         *
         * このコードではその詳細を解析していないが、
         * たとえば次のように使える。
         *
         *   if (WIFEXITED(status)) {
         *       int code = WEXITSTATUS(status);
         *   }
         *
         * つまり status は、
         *   「待ち終えた子の結果報告書」
         * のようなものである。
         *
         * ------------------------------------------------------------------
         * 4. 戻り値の意味
         * ------------------------------------------------------------------
         *
         * wait() の戻り値は、
         * 正常時には「回収した子プロセスの PID」を返す。
         *
         * エラー時には -1 を返す。
         *
         * ここでは
         *
         *   == (pid_t)-1
         *
         * で失敗を検出している。
         *
         * 失敗理由の例:
         *   - 待つべき子が存在しない
         *   - シグナル割り込みなど
         *
         * このプログラムでは毎回 1 個だけ子を作ってすぐ待っているので、
         * 正常ならその子の PID が返る。
         *
         * ------------------------------------------------------------------
         * 5. このコードでの wait() の意味を一言で言うと
         * ------------------------------------------------------------------
         *
         *   「親シェルが、起動した1つの子コマンドの終了を待ち、
         *    その終了状態を回収してから次の入力へ進む」
         *
         * である。
         */
        if (wait(&status) == (pid_t)-1) {
            /*
             * wait() が失敗した場合のエラー処理。
             *
             * perror("wait") により、errno に対応したエラーメッセージを表示する。
             *
             * 例:
             *   wait: No child processes
             *
             * など。
             *
             * wait が失敗すると、
             *   - 子の終了管理ができない
             *   - シェルの実行順制御が崩れる
             * ため、ここでは異常終了している。
             */
            perror("wait");

            /*
             * 親シェル自身を異常終了させる。
             *
             * このコードは簡易サンプルなので、
             * wait 失敗時に再試行や復旧は行わず、そのまま終了する設計である。
             */
            exit(1);
        }

        /*
         * wait() が成功すると、1つの子プロセスは完全に回収され、
         * 親は再びループ先頭へ戻る。
         *
         * つまり制御の流れはこうなる。
         *
         *   @ ls      <- ユーザ入力
         *   子で ls 実行
         *   親は wait
         *   ls 終了
         *   親が wait から戻る
         *   @          <- 次のプロンプト
         *
         * この繰り返しにより、
         * 「1コマンドずつ実行し、その終了を待ってから次へ進む」
         * 最小シェルになっている。
         */
    }

    /*
     * このコードでは for(;;) が無限ループなので通常ここへは到達しない。
     *
     * ただし構文上の main 終了点として exit(0) を書いている。
     * もし将来 break 条件や "exit" コマンド処理を追加したなら、
     * ここで正常終了する設計に発展させられる。
     */
    exit(0);
}
