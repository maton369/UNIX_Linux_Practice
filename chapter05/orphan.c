/* proc/orphan.c */
#include <stdio.h>      /* printf を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型, fork/getpid/getppid に関連する型定義 */
#include <unistd.h>     /* fork, getpid, getppid, sleep を使うためのヘッダ */

/*
 * このコードの主題は「継親（けいおや）」、つまり
 *   親プロセスが先に終了したあと、子プロセスの親が別のプロセスへ付け替わる
 * という現象である。
 *
 * UNIX / Linux では、通常、あるプロセスは必ず「親プロセス」を持つ。
 * fork() で子を作ると、最初は
 *
 *   親 ----> 子
 *
 * という親子関係になる。
 *
 * しかし、親プロセスが子より先に終了すると、
 * 子を「親なし」のまま放置するわけにはいかない。
 *
 * そこで OS は、その子を別の特別なプロセスの子として引き取る。
 * この「後から親になるプロセス」が、ここでいう継親だと考えると分かりやすい。
 *
 * このコードでは、
 *
 *   1. fork() で子を作る
 *   2. 親はすぐに自分の PID を表示して終了する
 *   3. 子は少し sleep してから getppid() を呼ぶ
 *   4. その時点では元の親はもう死んでいるので、
 *      getppid() は「元の親」ではなく「継親の PID」を返す
 *
 * という流れになっている。
 *
 * つまりこのコードは、
 *
 *   「子プロセスは、親が先に死ぬと別のプロセスに引き取られ、
 *    getppid() の返り値が変わる」
 *
 * ことを観察する最小例である。
 *
 * 重要:
 *   現代の Linux では継親は必ずしも PID 1 に限らない。
 *   実行環境によっては systemd やサブリーパー機構などにより、
 *   別のプロセスが引き取ることもある。
 *
 *   そのため、このコードの本質は
 *     「PID 1 になること」
 *   ではなく、
 *     「元の親ではない別のプロセスが新しい親になること」
 *   である。
 */

int main(void) {
    /*
     * fork() を呼ぶと、現在のプロセスが複製され、
     * 以後は
 *
     *   - 親プロセス
     *   - 子プロセス
     *
     * の2つが存在するようになる。
     *
     * fork() の戻り値:
     *
     *   -1 : 失敗
     *    0 : 子プロセス側
     *   >0 : 親プロセス側（その値は子 PID）
     *
     * このコードでは簡潔さを優先して fork() の失敗チェックを省略し、
     * 戻り値が 0 かどうかだけで親子を分岐している。
     *
     * 実務コードなら本来は
     *
     *   pid_t pid = fork();
     *   if (pid == -1) { ... }
     *   else if (pid == 0) { ... }
     *   else { ... }
     *
     * のように書く方が安全である。
     */
    if (fork() == 0) {
        /* 子プロセス */

        /*
         * ここへ来るのは子プロセス側である。
         *
         * この子プロセスの役割は、
         *   「親が先に終了したあと、自分の親 PID がどう見えるかを観察する」
         * ことである。
         *
         * そのため、子はすぐには getppid() を呼ばず、
         * まず少し待つ。
         */

        /*
         * sleep(3);
         *
         * 子プロセスを 3 秒間停止させる。
         *
         * この待ち時間の意図は非常に重要である。
         *
         * もし子が fork() 直後にすぐ getppid() を呼ぶと、
         * まだ元の親プロセスが生きている可能性が高い。
         * そうすると getppid() は「元の親の PID」を返してしまい、
         * 継親への付け替わりを観察できない。
         *
         * そこで子をわざと 3 秒眠らせる。
         * 一方、親プロセスはこのあとすぐ exit(0) する。
         *
         * その結果、子が眠りから覚める頃には元の親はほぼ確実に終了しており、
         * 子は別のプロセスへ引き取られている可能性が高い。
         *
         * 図にするとこうである。
         *
         *   時刻 t0: fork() 直後
         *       親 -- 子
         *
         *   時刻 t1: 親が exit(0)
         *       親は消える
         *
         *   時刻 t2: 子はまだ sleep 中
         *
         *   時刻 t3: 子が起きて getppid()
         *       その時点では継親が新しい親になっている
         *
         * つまり、この sleep は
         *   「元の親が死ぬ時間を確保する」
         * ための観察用ディレイである。
         */
        sleep(3);

        /*
         * printf("\nmy parent is now pid %d\n", getppid());
         *
         * ここで子プロセスが getppid() を呼んでいる。
         *
         * getppid() は
         *   「現在このプロセスの親として OS が認識しているプロセスの PID」
         * を返す関数である。
         *
         * 重要なのは、
         * getppid() が返すのは「生みの親」ではなく、
         *   今この瞬間の親
         * だという点である。
         *
         * したがって、親が先に死んだあとで呼ぶと、
         * 元の親 PID ではなく、引き取り先の継親 PID が返る。
         *
         * 文字列 "my parent is now pid ..." の "now" は、
         * まさにこの意味を表している。
         *
         * つまりこの行は、
         *
         *   「自分の親は、もはや fork() したときの親ではなく、
         *    今は別のプロセスに変わっている」
         *
         * ことを観察している。
         *
         * 実行例のイメージ:
         *
         *   parent process, pid 12345
         *   my parent is now pid 1
         *
         * あるいは環境によっては
         *
         *   my parent is now pid 999
         *
         * のように、PID 1 以外が出ることもある。
         *
         * ここで重要なのは数値そのものではなく、
         *   「最初の親 PID ではなくなっている」
         * という事実である。
         *
         * 先頭に "\n" を入れているのは、
         * 親側の出力と見た目を区切りやすくするためである。
         */
        printf("\nmy parent is now pid %d\n", getppid());

        /*
         * 子プロセスは観察が終わったので正常終了する。
         *
         * この時点で親はすでにいないので、
         * 子は継親のもとで終了することになる。
         */
        exit(0);
    }

    /* 親プロセス */

    /*
     * ここへ来るのは親プロセス側である。
     *
     * 親の役割は非常に単純で、
     *   - 自分の PID を表示する
     *   - すぐ終了する
     * ことである。
     *
     * つまりこの親は、
     *   「子を残して先に死ぬ親」
     * を意図的に作っている。
     *
     * その結果、子は orphan（孤児プロセス）になり、
     * 後から別の親に引き取られる。
     */

    /*
     * printf("parent process, pid %d\n", getpid());
     *
     * 親自身の PID を表示する。
     *
     * これは観察の基準値として重要である。
     * 後で子が getppid() で表示する値と比較することで、
     *   「元の親 PID と今の親 PID は違う」
     * ことが分かる。
     *
     * たとえばここで
     *
     *   parent process, pid 12345
     *
     * と出たなら、
     * 子があとで
     *
     *   my parent is now pid 1
     *
     * と出したとき、
     *   12345 ではなく 1 になっている
     * ことから、親が付け替わったと理解できる。
     */
    printf("parent process, pid %d\n", getpid());

    /*
     * exit(0);
     *
     * 親プロセスをすぐ正常終了させる。
     *
     * これがこのコードの肝である。
     *
     * もし親が生き続ければ、子は orphan にならず、
     * getppid() を呼んでも元の親 PID のままである可能性が高い。
     *
     * しかしここで親を即終了させることで、
     * 子は
     *
     *   「親を失った子プロセス」
     *
     * つまり orphan になる。
     *
     * OS はその orphan を放置せず、
     * 別の適切なプロセスを親として再設定する。
     * その結果、子の getppid() は新しい親 PID を返すようになる。
     *
     * 図にするとこうである。
     *
     *   fork() 直後:
     *       親(P) ---> 子(C)
     *
     *   親 exit 後:
     *       親(P) 消滅
     *       継親(S) ---> 子(C)
     *
     * つまりこの exit(0) は、
     *   「子を孤児化させるトリガ」
     * の役割を持っている。
     */
    exit(0);
}
