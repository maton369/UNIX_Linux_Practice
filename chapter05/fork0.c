/* proc/fork0.c */
#include <stdio.h>      /* printf, fflush を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型, fork/getpid に関連する型定義 */
#include <unistd.h>     /* fork, getpid, sleep を使うためのヘッダ */

/*
 * このコードの主題は「fork() によるプロセス複製」である。
 *
 * UNIX / Linux では、fork() を呼ぶと
 *
 *   - もともと動いていたプロセス（親プロセス）
 *   - その複製として新しく作られるプロセス（子プロセス）
 *
 * の2つが存在するようになる。
 *
 * 重要なのは、fork() の「呼び出し地点以降」を
 * 親と子の両方がそれぞれ実行し続けることだ。
 *
 * このコードは、その最も基本的な観察用サンプルであり、
 * 特に次の点を見せている。
 *
 * 1. fork() 前は当然1つのプロセスしかいない
 *
 * 2. fork() 後は親と子の2つのプロセスが存在する
 *
 * 3. 親と子は「同じ変数の値」を持って処理を再開する
 *
 * 4. ただし PID（プロセスID）は親子で異なる
 *
 * つまりこのコードは、
 *
 *   「同じメモリ内容を引き継いだ2つの別プロセスが、
 *    異なる PID を持って並行に動き始める」
 *
 * ことを可視化するための最小例である。
 *
 * なお、このサンプルでは fork() の戻り値を使って
 * 親と子を分岐していない。
 * そのため、fork() の後は親も子も同じコードを実行し、
 * それぞれが "after fork: ..." を表示する。
 */

int main(void) {
    /*
     * i:
     *   親プロセスが持っている通常のローカル変数である。
     *
     * 初期値として 1234 を入れている。
     *
     * この変数を置いている理由は、
     *   「fork() 後に、親と子の両方で同じ値が見える」
     * ことを観察するためである。
     *
     * fork() は新しいプロセスを作るとき、
     * 親プロセスのアドレス空間を論理的に引き継ぐ。
     * そのため、fork() 直後には子プロセスから見ても
     *
     *   i == 1234
     *
     * である。
     *
     * ただし重要なのは、
     *   「同じ値を持っている」のであって、
     *   「同じ1個の変数を共有している」わけではない
     * という点である。
     *
     * fork() 後は親と子は別プロセスなので、
     * 片方が i を変更しても、もう片方の i は通常変わらない。
     *
     * このコードでは変更を加えないので、
     * 親子どちらも 1234 のまま表示される。
     */
    int i = 1234;

    /*
     * pid:
     *   現在のプロセスIDを一時的に格納するための変数である。
     *
     * 型は pid_t であり、これは PID を表すための専用型である。
     *
     * このコードでは、
     *   - fork() 前に自分の PID を取得して表示
     *   - fork() 後にもう一度自分の PID を取得して表示
     * する。
     *
     * その結果、
     *   fork 前: 1つの PID だけ出る
     *   fork 後: 親と子で異なる PID がそれぞれ出る
     * ことが分かる。
     */
    pid_t pid;

    /*
     * getpid()
     *
     * 現在実行している「このプロセス自身」の PID を取得する。
     *
     * fork() 前なので、この時点ではまだ親プロセス1つしか存在しない。
     * したがって、ここで得られる PID は「親プロセスの PID」である。
     */
    pid = getpid();

    /*
     * fork 前の状態を表示する。
     *
     * ここではまだプロセスは1個だけなので、
     * この行は1回しか表示されない。
     *
     * 表示内容の意味:
     *   pid = 現在プロセスの PID
     *   i   = 現在プロセスのローカル変数の値
     *
     * 例:
     *   before fork: pid=12345, i=1234
     *
     * この時点では当然「1行だけ」出る。
     */
    printf("before fork: pid=%d, i=%d\n", pid, i);

    /*
     * fflush(stdout);
     *
     * これは非常に重要である。
     *
     * printf() による標準出力 stdout は、環境によってはバッファリングされる。
     * もしこの時点で出力がバッファにたまったまま fork() すると、
     * 親プロセスと子プロセスの両方が「同じバッファ内容」を持った状態になり、
     * 後でそれが二重に表示されることがある。
     *
     * つまり、fflush(stdout) をしないと
     *
     *   before fork: ...
     *
     * が2回出たように見える場合があり、
     * 観察結果が分かりにくくなる。
     *
     * そこで fork() の前に stdout を flush して、
     * それまでの表示内容を確実に出し切っている。
     *
     * この一手により、
     *   - "before fork" は1回だけ表示
     *   - "after fork" は親と子で2回表示
     * という、fork() の本質が見えやすくなる。
     *
     * 図にするとこうである。
     *
     *   fflush 前:
     *     stdout buffer = "before fork: ...\n"
     *
     *   fflush 後:
     *     stdout buffer = 空
     *     画面に表示済み
     *
     *   その後 fork:
     *     親子ともに「空の stdout バッファ」を持って開始
     */
    fflush(stdout);

    /*
     * fork();
     *
     * ここがこのプログラムの中心である。
     *
     * fork() を呼ぶと、現在のプロセスをもとに子プロセスが新しく作られる。
     *
     * fork() 後の世界では、
     *   - 親プロセス
     *   - 子プロセス
     * の2つが存在する。
     *
     * そして重要なのは、
     * 親も子も「fork() の戻り直後」から実行を続けることだ。
     *
     * つまり、この行の次の行から
     *   親プロセスも実行する
     *   子プロセスも実行する
     * ことになる。
     *
     * さらに重要:
     *   fork() の戻り値は本来とても重要である。
     *
     *   - 親プロセスには「子の PID」が返る
     *   - 子プロセスには 0 が返る
     *   - 失敗時には -1 が返る
     *
     * しかしこのサンプルでは、その戻り値を変数に受けていない。
     * そのため
     *
     *   「親か子かで分岐する」
     *
     * ことはせず、両方とも同じ後続コードを実行する。
     *
     * この設計によって、
     *   after fork の表示が2回出る
     * という fork の基本挙動を素直に観察できる。
     */
    fork();

    /*
     * fork() 後にもう一度 getpid() を呼ぶ。
     *
     * ここは親プロセスと子プロセスの両方で実行される。
     *
     * したがって、
     *   - 親は親自身の PID を得る
     *   - 子は子自身の PID を得る
     *
     * ことになる。
     *
     * ここで親子の PID は異なるので、
     * "after fork: ..." の出力には異なる PID が現れる。
     */
    pid = getpid();

    /*
     * fork 後の状態を表示する。
     *
     * この行は、
     *   親プロセスが1回
     *   子プロセスが1回
     * 実行する。
     *
     * したがって、通常は2行表示される。
     *
     * 例:
     *   after fork: pid=12345, i=1234   <- 親
     *   after fork: pid=12346, i=1234   <- 子
     *
     * ここから読み取れること:
     *
     * 1. PID は親子で違う
     *    -> つまり別プロセスである
     *
     * 2. i はどちらも 1234
     *    -> fork() 時点のメモリ内容が引き継がれている
     *
     * ただし注意:
     *   表示順は一定ではない。
     *
     * 親が先に出ることもあれば、子が先に出ることもある。
     * なぜなら fork() 後はスケジューラが
     * 親と子のどちらを先に実行するかを決めるためである。
     *
     * つまり
     *
     *   after fork: pid=親, i=1234
     *   after fork: pid=子, i=1234
     *
     * の順になるとは限らない。
     *
     * ここは「並行実行の結果、出力順は未定」と理解するのが重要である。
     */
    printf("after fork: pid=%d, i=%d\n", pid, i);

    /*
     * sleep(1);
     *
     * コメントにある通り、この sleep は本質的には重要ではない。
     *
     * ただし観察用としては少し意味がある。
     * 親と子の両方がすぐ終了してしまうと、
     * 実行タイミングや表示の見え方が慌ただしくなることがある。
     *
     * ここで1秒待つことで、
     * 親子がともに少しだけ生き残り、
     * 実験観察をしやすくしている。
     *
     * しかし fork の本質そのもの
     *   「親子が2つ存在する」
     *   「PID が違う」
     *   「変数値は同じで始まる」
     * には直接関係しないので、
     * コメント通り重要ではない補助的な待ち時間である。
     */
    sleep(1);  /* このsleepは重要ではない */

    /*
     * exit(0);
     *
     * 親プロセスも子プロセスも、ここまで来たら正常終了する。
     *
     * fork() の後はプロセスが2つあるので、
     * この exit(0) も2回実行されることになる。
     *
     * つまり
     *   - 親が正常終了
     *   - 子も正常終了
     * する。
     *
     * このコードでは wait() を使っていないので、
     * 親子終了の管理はここでは扱っていない。
     * あくまで fork() の生成と直後の観察が主題である。
     */
    exit(0);
}
