/* llio/goodnight.c */

#include <sys/types.h>  /* off_t など、lseek/open/write で使う型定義 */
#include <stdio.h>      /* perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/stat.h>   /* open に関連する定義が入ることがある */
#include <fcntl.h>      /* open, O_WRONLY を使うためのヘッダ */
#include <sys/uio.h>    /* write に関連する定義が入ることがある */
#include <unistd.h>     /* lseek, write, close を使うためのヘッダ */

/*
 * このコードの主題は「lseek() によるファイルオフセットの移動」である。
 *
 * 低水準入出力では、open() でファイルを開くと、
 * カーネルはそのファイルに対して「現在どこを読んでいるか / 書いているか」
 * を表す位置情報を管理する。
 *
 * その位置情報を一般に
 *
 *   ファイルオフセット (file offset)
 *
 * と呼ぶ。
 *
 * read() や write() は、このファイルオフセット位置を基準に動く。
 *
 * たとえば通常の write(fd, "abc", 3) は
 *   - 現在のオフセット位置に 3 バイト書く
 *   - 書いた後、オフセットを 3 だけ進める
 * という動作になる。
 *
 * ここで lseek() を使うと、
 *   「次の read / write をファイルのどの位置から始めるか」
 * を自分で変更できる。
 *
 * このコードでは、
 *
 *   1. bye.txt を書き込み専用で開く
 *   2. lseek(fd, 5, SEEK_SET) でオフセットを 5 バイト目へ移動する
 *   3. その位置から "night!\n" を 7 バイト書く
 *   4. close する
 *
 * という流れになっている。
 *
 * つまりこのプログラムは、
 *   「ファイルの先頭から 5 バイト目を起点にして内容を上書きする」
 * サンプルである。
 *
 * 重要:
 *   lseek() は「ファイル内容そのものを変更する関数」ではない。
 *   あくまで「次に read/write する位置」を動かす関数である。
 *
 *   実際に内容が変わるのは、その後の write() を呼んだときである。
 */

int main(void) {
    /*
     * fd:
     *   file descriptor の略であり、
     *   open() が成功したときに返す「開かれたファイルの識別番号」である。
     *
     * 以後の
     *   lseek(fd, ...)
     *   write(fd, ...)
     *   close(fd)
     * はすべて、この fd が表すファイルを対象にする。
     */
    int fd;

    /*
     * open("bye.txt", O_WRONLY)
     *
     * bye.txt を書き込み専用で開く。
     *
     * 第1引数:
     *   "bye.txt"
     *   対象ファイル名
     *
     * 第2引数:
     *   O_WRONLY
     *   書き込み専用で開くことを意味する
     *
     * 重要な注意:
     *   このコードには O_CREAT が無いので、bye.txt が存在しないと失敗する。
     *
     *   また O_TRUNC も無いので、ファイル内容を空にせず、
     *   既存内容の上に一部を書き換える動作になる。
     *
     * 戻り値:
     *   成功時:
     *     0 以上のファイルディスクリプタ
     *
     *   失敗時:
     *     -1
     *     errno に理由が入る
     *
     * ここでは < 0 で失敗判定している。
     * open の失敗値は -1 なので、実質的には == -1 と同じ意味である。
     */
    if ((fd = open("bye.txt", O_WRONLY)) < 0) {
        /*
         * open に失敗したら、その理由を perror で表示して異常終了する。
         *
         * 例:
         *   open: No such file or directory
         *   open: Permission denied
         */
        perror("open");
        exit(1);
    }

    /*
     * lseek(fd, 5, SEEK_SET)
     *
     * これがこのコードの核心である。
     *
     * lseek は、ファイルディスクリプタ fd に対応する
     * 「現在のファイルオフセット」を移動する関数である。
     *
     * 引数の意味:
     *
     * 第1引数 fd:
     *   どの開かれたファイルのオフセットを動かすか
     *
     * 第2引数 5:
     *   何バイト移動するか、またはどの位置へ移動するかを表すオフセット値
     *
     * 第3引数 SEEK_SET:
     *   基準位置をどこに取るかを表す指定
     *
     * SEEK_SET は
     *   「ファイル先頭を基準にする」
     * という意味である。
     *
     * したがって
     *
     *   lseek(fd, 5, SEEK_SET)
     *
     * は
     *
     *   「ファイル先頭から 5 バイト目に移動せよ」
     *
     * という意味になる。
     *
     * ここで「5 バイト目」とは、
     * 先頭を 0 バイト目と数える感覚で、
     *
     *   位置 0,1,2,3,4 を飛ばして
     *   次の write は位置 5 から始まる
     *
     * ということになる。
     *
     * たとえば bye.txt の内容が事前に
     *
     *   See you later!
     *   Bye!
     *
     * のようだったとする。
     *
     * 先頭からバイト位置を概念的に書くと、
     *
     *   0:'S'
     *   1:'e'
     *   2:'e'
     *   3:' '
     *   4:'y'
     *   5:'o'
     *   6:'u'
     *   ...
     *
     * となる。
     *
     * このときオフセット 5 に移動して write すると、
     * 位置 5 から後ろが上書きされる。
     *
     * 戻り値:
     *   成功時:
     *     新しいファイルオフセット位置
     *
     *   失敗時:
     *     -1
     *     errno に理由が入る
     *
     * このコードでは、成功時の戻り値そのものは使わず、
     * -1 かどうかだけで失敗判定している。
     *
     * 重要:
     *   lseek は「位置を変えるだけ」であり、
     *   この時点ではまだファイル内容は変わっていない。
     */
    if (lseek(fd, 5, SEEK_SET) == -1) {
        /*
         * lseek に失敗した場合のエラー表示。
         *
         * 例:
         *   lseek: Illegal seek
         *
         * なお、通常ファイルでは使えるが、
         * パイプや端末など seek できない対象に対しては
         * 失敗する場合がある。
         *
         * ここも lseek の重要な性質である。
         * つまり、すべての fd が seek 可能とは限らない。
         */
        perror("lseek");
        exit(1);
    }

    /*
     * write(fd, "night!\n", 7)
     *
     * ここで初めて、実際のファイル内容を変更する。
     *
     * 直前の lseek によって、
     * 「次に書く位置」はファイル先頭から 5 バイト目になっている。
     * したがって、この write はその位置から 7 バイトを書き込む。
     *
     * "night!\n" はバイト列としては
     *
     *   'n' 'i' 'g' 'h' 't' '!' '\n'
     *
     * の 7 バイトである。
     *
     * C の文字列リテラルには末尾に '\0' が付くが、
     * write は「指定したバイト数だけ」書くので、
     * ここでは終端 '\0' は書かれない。
     *
     * もし元のファイル内容が
     *
     *   See you later!
     *   Bye!
     *
     * だった場合、位置 5 から "night!\n" を上書きすると、
     * 概念的には例えば次のように変化する。
     *
     *   元:
     *     S e e _ y o u _ l a t e r ! ...
     *             ^
     *             ここ(位置5)から書く
     *
     *   書き込み:
     *             n i g h t ! \n
     *
     *   結果:
     *     See ynight!
     *     ...
     *
     * のように、「位置 5 から後ろの一部が置き換わる」ことになる。
     *
     * つまりこのコードは append（末尾追加）ではなく、
     * 途中位置からの overwrite（上書き）である。
     *
     * 戻り値:
     *   成功時:
     *     実際に書けたバイト数
     *
     *   失敗時:
     *     -1
     *
     * このコードでは「7 バイト全部書けるはず」と考えて、
     * 戻り値が 7 と一致するか確認している。
     *
     * 厳密には通常ファイルでも部分書き込みの可能性を一般論としては考えるが、
     * この教材では簡潔化している。
     */
    if (write(fd, "night!\n", 7) != 7) {
        /*
         * write に失敗、または 7 バイト全部書けなかった場合のエラー処理。
         */
        perror("write");
        exit(1);
    }

    /*
     * close(fd)
     *
     * open で得たファイルディスクリプタを閉じる。
     *
     * 低水準 I/O の基本パターンは
     *
     *   open -> lseek/read/write -> close
     *
     * のようになる。
     *
     * close により、
     *   - この fd をもう使わないことをカーネルへ伝える
     *   - 関連資源を解放する
     *
     * 小さなプログラムでは、終了時にカーネルが回収してくれるが、
     * 明示的に close するのが基本である。
     */
    if (close(fd) < 0) {
        /*
         * close に失敗した場合のエラー表示。
         */
        perror("close");
        exit(1);
    }

    /*
     * ここまで来れば
     *   - open 成功
     *   - lseek 成功
     *   - write 成功
     *   - close 成功
     * なので正常終了である。
     */
    return 0;
}
