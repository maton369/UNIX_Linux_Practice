/* llio/hop.c */

#include <sys/types.h>  /* off_t など、lseek/open/write で使う型定義 */
#include <stdio.h>      /* perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/stat.h>   /* open の mode 引数に関連する定義 */
#include <fcntl.h>      /* open, O_WRONLY, O_CREAT, O_TRUNC を使うためのヘッダ */
#include <sys/uio.h>    /* write に関連する定義が入ることがある */
#include <unistd.h>     /* write, lseek, close を使うためのヘッダ */

/*
 * このコードの主題は「穴 (hole)」である。
 *
 * ここでいう穴とは、
 *   ファイルの論理的な位置としては存在しているが、
 *   実際には明示的なデータを書き込んでいない領域
 * のことである。
 *
 * UNIX / Linux の通常ファイルでは、lseek() によって
 * 「ファイルの現在位置（ファイルオフセット）」を前へ進めたあと、
 * その先で write() すると、
 * 書いていない中間部分が「穴」になることがある。
 *
 * このようなファイルは一般に「疎ファイル (sparse file)」と呼ばれる。
 *
 * 重要なポイント:
 *   穴は「読み出したときには 0 バイト列のように見える」ことが多いが、
 *   必ずしも物理ディスク上にそのぶんの実データが書かれているとは限らない。
 *
 * つまり、
 *   - 論理サイズ（ファイル長）は大きい
 *   - でも実際にディスク上で消費しているブロック数は少ない
 * ということが起こりうる。
 *
 * このコードでは、
 *   1. "hop" を 3 バイト書く
 *   2. lseek(d, 3, SEEK_CUR) で 3 バイトぶん先へ飛ぶ
 * を 8 回繰り返す。
 *
 * その結果、ファイル hop.out の内容は概念的には
 *
 *   "hop" [穴3バイト] "hop" [穴3バイト] ...
 *
 * のような並びになる。
 *
 * 図にするとイメージはこうである。
 *
 *   hop___hop___hop___hop___hop___hop___hop___hop___
 *
 * ここで "_" は穴（未書き込み領域）を表す。
 *
 * ただし読み出し時には、この穴の部分は通常 0 バイトとして見えるため、
 * テキストとしてそのまま目で見ても分かりにくい。
 * od, hexdump, xxd などで見ると理解しやすい。
 */

int main(void) {
    /*
     * d:
     *   file descriptor（ファイルディスクリプタ）を入れる変数である。
     *
     * open() が成功すると、
     *   hop.out を表す整数番号
     * がここへ入る。
     *
     * 以後の
     *   write(d, ...)
     *   lseek(d, ...)
     *   close(d)
     * はすべて、この d が表すファイルを対象にする。
     *
     * i:
     *   for ループの回数を数えるための変数である。
     *   今回は 8 回繰り返して、
     *   "hop" と穴3バイトの組を 8 セット作る。
     */
    int d, i;

    /*
     * open("hop.out", O_WRONLY|O_CREAT|O_TRUNC, 0666)
     *
     * hop.out を書き込み専用で開く。
     *
     * 第2引数の意味:
     *
     *   O_WRONLY
     *     書き込み専用で開く
     *
     *   O_CREAT
     *     ファイルが存在しなければ新しく作る
     *
     *   O_TRUNC
     *     すでに存在していれば中身を空にする
     *
     * 第3引数 0666:
     *   O_CREAT によって新規作成するときのパーミッション指定である。
     *   実際の最終権限は umask の影響を受ける。
     *
     * この open により、
     *   「空の出力ファイルを最初から作り直して書き始める」
     * 状態になる。
     *
     * 戻り値:
     *   成功時:
     *     0 以上のファイルディスクリプタ
     *
     *   失敗時:
     *     -1
     *     errno に理由が入る
     */
    if ((d = open("hop.out", O_WRONLY|O_CREAT|O_TRUNC, 0666)) == -1) {
        /*
         * open に失敗したら理由を表示して異常終了する。
         */
        perror("open");
        exit(1);
    }

    /*
     * for (i = 0; i < 8; i++)
     *
     * 次の 2 操作を 8 回繰り返す。
     *
     *   1. "hop" を 3 バイト書く
     *   2. 現在位置からさらに 3 バイト先へ飛ぶ
     *
     * これにより、ファイルには
     *
     *   [hop][穴][hop][穴][hop][穴]...
     *
     * というパターンが繰り返し作られる。
     *
     * 1 回の繰り返しでファイルオフセットは合計 6 バイト進む。
     *   - write で 3 バイト進む
     *   - lseek でさらに 3 バイト進む
     *
     * したがって 8 回で論理的には
     *
     *   6 * 8 = 48 バイト
     *
     * の長さに到達する。
     *
     * そのうち実際に明示的に書いているのは
     *
     *   3 * 8 = 24 バイト
     *
     * だけであり、残り 24 バイトは穴になる。
     */
    for (i = 0; i < 8; i++) {

        /*
         * write(d, "hop", 3)
         *
         * 現在のファイルオフセット位置に "hop" の 3 バイトを書き込む。
         *
         * ここで重要なのは、
         * "hop" という文字列リテラル自体は内部的には
         *
         *   'h' 'o' 'p' '\0'
         *
         * という 4 バイト相当のデータを持つが、
         * write は第3引数で指定した 3 バイトしか書かないことだ。
         *
         * つまり終端の '\0' は書き込まれない。
         *
         * 書き込まれるのは純粋に
         *
         *   'h' 'o' 'p'
         *
         * の 3 バイトである。
         *
         * また、write は成功すると「実際に書けたバイト数」を返す。
         * このコードでは -1 だけを失敗として扱っている。
         *
         * 厳密には部分書き込みの可能性も一般論としてはあるが、
         * 通常ファイルへのこの程度の短い書き込みでは、
         * 教材としては簡潔化してよい。
         */
        if (write(d, "hop", 3) == -1) {
            /*
             * write に失敗した場合のエラー処理。
             */
            perror("write");
            exit(1);
        }

        /*
         * lseek(d, 3, SEEK_CUR)
         *
         * ここが「穴」を作る核心である。
         *
         * lseek はファイルオフセットを移動する関数であり、
         * この呼び出しの意味は
         *
         *   「現在位置を基準にして、さらに 3 バイト先へ進め」
         *
         * である。
         *
         * 第3引数 SEEK_CUR は
         *   current（現在位置）を基準にする
         * という意味である。
         *
         * したがって、
         *   直前の write で "hop" を 3 バイト書き終えた位置
         * からさらに 3 バイト進む。
         *
         * この 3 バイトぶんには何も書いていないので、
         * 次に write したとき、その間が「穴」になる。
         *
         * 重要:
         *   lseek 自体はファイル内容に 0 を書き込むわけではない。
         *   単に「次に書く位置」を先へずらしているだけである。
         *
         *   その結果として、
         *   書かなかった中間領域が論理的に存在するようになる。
         *
         * 図にすると 1 回のループではこうなる。
         *
         *   初期位置:      ^
         *   write "hop":   hop^
         *   lseek +3:      hop___^
         *
         * ここで "_" が穴である。
         *
         * つまり、
         *   write が「実データを書く」
         *   lseek が「次の実データ位置を飛ばす」
         * ことで、間に穴ができる。
         */
        if (lseek(d, 3, SEEK_CUR) == -1) {
            /*
             * lseek に失敗した場合のエラー処理。
             *
             * 通常ファイルでは問題なく使えることが多いが、
             * seek 不可能な対象（パイプなど）では失敗しうる。
             */
            perror("lseek");
            exit(1);
        }

        /*
         * ここまでで 1 回のループが完了した。
         *
         * 1 回ごとのファイル内容イメージ:
         *
         *   1回目後: hop___
         *   2回目後: hop___hop___
         *   3回目後: hop___hop___hop___
         *   ...
         *
         * 最後には
         *
         *   hop___hop___hop___hop___hop___hop___hop___hop___
         *
         * のような論理構造になる。
         *
         * ただし実際にファイルをテキスト表示すると、穴は見えにくい。
         * 穴の部分は通常、読み出すと 0 バイト列として見えるためである。
         */
    }

    /*
     * close(d)
     *
     * open で得たファイルディスクリプタを閉じる。
     *
     * 低水準 I/O の基本パターンは
     *
     *   open -> write/lseek -> close
     *
     * のようになる。
     *
     * close を呼ぶことで、
     *   - この fd をもう使わないことをカーネルへ伝える
     *   - 関連資源を解放する
     *
     * このコードでは close の戻り値をチェックしていないが、
     * 厳密には失敗確認を書くこともできる。
     *
     * 今回の主題は穴の生成なので、簡潔化されている。
     */
    close(d);

    /*
     * 正常終了。
     */
    return 0;
}

/*
 * このコードから学ぶべきことのまとめ
 *
 * 1. lseek はファイル内容そのものを変更しない
 *
 *    lseek は「次に read/write する位置」を変えるだけである。
 *
 * 2. 書かずに位置だけ進めると、中間に穴ができる
 *
 *    これが sparse file（疎ファイル）の基本原理である。
 *
 * 3. 穴は論理的には存在するが、実際にはディスク上で省略されうる
 *
 *    そのため、ファイルサイズは大きく見えても
 *    実ディスク使用量は小さいことがある。
 *
 * 4. 読み出すと穴は通常 0 バイト列として見える
 *
 *    ただし「穴」と「本当に 0 を書いた領域」は
 *    読み出し結果だけでは区別しにくいことがある。
 *
 * 5. SEEK_CUR は現在位置基準の相対移動である
 *
 *    今回は毎回
 *      write で 3 バイト進み、
 *      lseek でさらに 3 バイト進む
 *    ため、6 バイト周期のパターンになる。
 *
 *
 * 図でまとめると:
 *
 *   ループ1回:
 *     [hop][hole]
 *
 *   ループ2回:
 *     [hop][hole][hop][hole]
 *
 *   ループ8回:
 *     [hop][hole][hop][hole][hop][hole][hop][hole]
 *     [hop][hole][hop][hole][hop][hole][hop][hole]
 *
 * ここで
 *   hop  = 実際に write した 3 バイト
 *   hole = lseek で飛ばした未書き込みの 3 バイト
 *
 *
 * このコードの本質を一言で言うと、
 *
 *   「write でデータを書いたあと、lseek で未書き込み領域を飛ばしながら進むことで、疎ファイルの穴を作っている」
 *
 * である。
 */
