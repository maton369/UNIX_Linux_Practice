/* llio/readhello.c */

#include <sys/types.h>  /* ssize_t など、read/open で使われる型定義 */
#include <stdio.h>      /* perror, printf を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/stat.h>   /* open に関連する定義が入ることがある */
#include <fcntl.h>      /* open, O_RDONLY を使うためのヘッダ */
#include <sys/uio.h>    /* read に関連する定義が入ることがある */
#include <unistd.h>     /* read, close を使うためのヘッダ */

/*
 * このコードの主題は「低水準入出力 (low-level I/O) によるファイル読み出し」である。
 *
 * 特にここでは、UNIX / Linux の基本的な I/O パターン
 *
 *   open -> read を繰り返す -> close
 *
 * をそのまま体験できるようになっている。
 *
 * 高水準入出力の fopen / fgets / fread ではなく、
 * 低水準入出力の
 *
 *   open
 *   read
 *   close
 *
 * を使っている点が重要である。
 *
 * 低水準入出力では、ファイルは FILE * ではなく
 * 「ファイルディスクリプタ (file descriptor)」という整数で扱う。
 *
 * このコード全体のアルゴリズムは次の通りである。
 *
 *   1. hello.txt を読み取り専用で open する
 *   2. 10 バイトのバッファ buf を用意する
 *   3. read(fd, buf, sizeof(buf)) を繰り返し呼ぶ
 *   4. read が返す「実際に読めたバイト数」を cc で受け取る
 *   5. 0 より大きい間は、まだデータが読めているのでループ継続
 *   6. 0 になったら EOF (ファイル終端) なので読み終わり
 *   7. -1 ならエラー
 *   8. 最後に close(fd) でファイルを閉じる
 *
 * つまりこのプログラムは、
 *   「ファイルを少しずつ固定長バッファへ読み込む」
 * という低水準 I/O の基本形である。
 */

int main(void) {
    /*
     * fd:
     *   file descriptor の略であり、open() が成功したときに返す整数の識別子である。
     *
     * この値を使って、
     *   read(fd, ...)
     *   close(fd)
     * のように「どのファイルを操作するか」を指定する。
     *
     * 多くの環境では、
     *   0 = 標準入力
     *   1 = 標準出力
     *   2 = 標準エラー出力
     * が最初から使われているため、
     * 最初に open した通常ファイルには 3 が割り当てられることが多い。
     *
     * ただしこれは状況に依存するので保証ではない。
     */
    int fd;

    /*
     * cc:
     *   read() の戻り値を受け取る変数である。
     *
     * 型が ssize_t なのは重要である。
     *
     * read() は
     *   成功時: 0以上のバイト数
     *   EOF時 : 0
     *   失敗時: -1
     * を返す。
     *
     * つまり「正の値も 0 も -1 も表せる型」が必要であり、
     * そのため signed なサイズ型である ssize_t を使う。
     *
     * ここで受け取る意味:
     *   cc > 0 なら「その回に実際に読めたバイト数」
     *   cc == 0 なら EOF
     *   cc == -1 なら エラー
     */
    ssize_t cc;

    /*
     * buf:
     *   実際にファイルから読み込んだデータを一時的に置くバッファである。
     *
     * ここでは 10 バイトの固定長配列として用意している。
     *
     * 重要:
     *   read() は「文字列として読む」とは限らない。
     *   単に生のバイト列を buf に入れるだけである。
     *
     * したがって、この buf の中身は
     *   必ずしも '\0' 終端の C 文字列ではない。
     * そのため、このコードでは buf を %s で表示していない。
     *
     * この点は高水準入出力の fgets などと違うところである。
     */
    char buf[10];

    /*
     * open("hello.txt", O_RDONLY)
     *
     * hello.txt を読み取り専用 (O_RDONLY) で開く。
     *
     * 成功時:
     *   0 以上のファイルディスクリプタを返す
     *
     * 失敗時:
     *   -1 を返し、errno に理由が入る
     *
     * ここでは
     *
     *   (fd = open(...)) == -1
     *
     * という形で、
     *   1. open の戻り値を fd に代入
     *   2. その値が -1 かどうか判定
     * を同時に行っている。
     *
     * もし hello.txt が存在しない、権限がない、などの場合は失敗する。
     */
    if ((fd = open("hello.txt", O_RDONLY)) == -1) {

        /*
         * perror("open")
         *
         * 直前のエラー理由を errno に基づいて人間向け文字列で表示する。
         *
         * 例:
         *   open: No such file or directory
         *
         * "open" は、どの処理で失敗したのかを示すラベルである。
         */
        perror("open");

        /*
         * open に失敗した場合、この先 read や close を行う対象が存在しないため、
         * 異常終了する。
         */
        exit(1);
    }

    /*
     * while ((cc = read(fd, buf, sizeof(buf))) > 0)
     *
     * ここがこのコードの中核である。
     *
     * read(fd, buf, sizeof(buf)) は、
     *
     *   - fd が表すファイルから
     *   - buf へ
     *   - 最大 sizeof(buf) バイトだけ
     * 読み込む。
     *
     * 第1引数 fd:
     *   どのファイルから読むか
     *
     * 第2引数 buf:
     *   読んだデータの書き込み先
     *
     * 第3引数 sizeof(buf):
     *   最大何バイト読みたいか
     *
     * ここで sizeof(buf) はコンパイル時に 10 になる。
     * つまり「最大10バイトずつ読む」という意味である。
     *
     * read の戻り値:
     *   > 0 : 実際に読めたバイト数
     *   = 0 : EOF（もう読むデータがない）
     *   = -1: エラー
     *
     * この while 条件では、
     *
     *   cc = read(...)
     *
     * でその回の戻り値を cc に保存しつつ、
     *
     *   cc > 0
     *
     * で「まだ何か読めたか」を判定している。
     *
     * つまりこのループは
     *   「データがある限り、10 バイト単位で読み続ける」
     * という意味になる。
     *
     * 例:
     *   hello.txt の大きさが 23 バイトなら、
     *
     *   1回目: 10 バイト読む -> cc = 10
     *   2回目: 10 バイト読む -> cc = 10
     *   3回目:  3 バイト読む -> cc = 3
     *   4回目: EOF            -> cc = 0
     *
     * となる。
     *
     * この「最後の回だけ要求サイズより少ない」というのが read の普通の挙動である。
     * read は常に要求バイト数ぴったり返すわけではない。
     */
    while ((cc = read(fd, buf, sizeof(buf))) > 0) {

        /*
         * この時点では、
         *   cc にその回に実際に読めたバイト数
         *   buf の先頭 cc バイトに読み込んだデータ
         * が入っている。
         *
         * 重要:
         *   buf 全体 10 バイトが必ず全部埋まっているとは限らない。
         *   有効なのは先頭から cc バイトだけである。
         *
         * したがって、この直後に buf を処理するなら、
         *   0 から cc-1 まで
         * だけを見るべきである。
         *
         * このサンプルでは読み込んだ中身そのものは表示せず、
         * 何バイト読めたかだけを表示している。
         */
        printf("%d bytes read\n", (int)cc);

        /*
         * ここで実際のデータ処理をすることも多い。
         * 例えば:
         *   - 標準出力へ write する
         *   - 解析する
         *   - 別ファイルへ転送する
         *
         * このサンプルでは read の挙動観察が目的なので省略されている。
         */
    }

    /*
     * while ループを抜けた理由は 2 通りある。
     *
     * 1. cc == 0
     *    -> EOF に達したので正常に読み終えた
     *
     * 2. cc == -1
     *    -> read 中にエラーが起きた
     *
     * そのため、ここで cc == -1 かどうかを確認する。
     */
    if (cc == -1) {

        /*
         * read が失敗した場合のエラー表示。
         *
         * 例:
         *   read: Input/output error
         *
         * などが出る可能性がある。
         */
        perror("read");

        /*
         * 読み取りに失敗したので異常終了する。
         */
        exit(1);
    }

    /*
     * ここまで来て cc == -1 ではないなら、
     * ループ終了理由は cc == 0、つまり EOF である。
     *
     * したがって、ファイルは正常に最後まで読み終えたことになる。
     *
     * ただしまだ fd は開いたままなので、最後に close(fd) する必要がある。
     */

    /*
     * close(fd)
     *
     * open で得たファイルディスクリプタを閉じる。
     *
     * 低水準 I/O の基本パターンは
     *
     *   open -> read/write -> close
     *
     * である。
     *
     * close を行うことで、
     *   - プロセスがその fd をもう使わないことをカーネルへ伝える
     *   - 関連する資源を解放する
     * ことができる。
     *
     * 小さなプログラムではプロセス終了時にカーネルが回収してくれるが、
     * 明示的に close するのが基本である。
     */
    if (close(fd) == -1) {

        /*
         * close に失敗した場合のエラー表示。
         *
         * close は普段あまり失敗を意識しないこともあるが、
         * 実際にはエラー確認するのが丁寧である。
         */
        perror("close");

        /*
         * close 失敗も異常として扱う。
         */
        exit(1);
    }

    /*
     * ここまで来れば、
     *   open 成功
     *   read 完了
     *   close 成功
     * なので正常終了である。
     */
    return 0;
}
