/* llio/writebye.c */

#include <sys/types.h>  /* open, write で使われる型定義が入ることがある */
#include <stdio.h>      /* perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/stat.h>   /* open に関連する定義が入ることがある */
#include <fcntl.h>      /* open, O_WRONLY を使うためのヘッダ */
#include <sys/uio.h>    /* write に関連する定義が入ることがある */
#include <unistd.h>     /* write, close を使うためのヘッダ */

/*
 * このコードの主題は「低水準入出力 (low-level I/O) によるファイル書き込み」である。
 *
 * ここで使っているのは C 標準ライブラリの
 *
 *   fopen / fprintf / fclose
 *
 * ではなく、UNIX / Linux の低水準 I/O である
 *
 *   open / write / close
 *
 * である。
 *
 * 低水準 I/O では、ファイルは FILE * ではなく
 * 「ファイルディスクリプタ (file descriptor)」という整数で扱う。
 *
 * このコード全体の流れは次の通りである。
 *
 *   1. bye.txt を書き込み専用で open する
 *   2. write() を 3 回呼び、文字列を順に書き込む
 *   3. 最後に close() でファイルディスクリプタを閉じる
 *
 * 書き込まれる内容は連続してファイルへ出力されるので、
 * 結果として bye.txt には概念的に
 *
 *   See you later!
 *   Bye!
 *
 * が入る。
 *
 * 重要:
 *   write は「文字列を知っている関数」ではなく、
 *   「指定したメモリアドレスから指定したバイト数だけ書く関数」
 * である。
 *
 * そのため、このコードでは
 *
 *   write(fd, "See you", 7)
 *
 * のように、
 *   - どこから書くか
 *   - 何バイト書くか
 * を明示している。
 *
 * ここが printf や puts などとの大きな違いである。
 */

int main(void) {
    /*
     * fd:
     *   file descriptor の略であり、
     *   open() が成功したときに返す「開かれたファイルの識別番号」である。
     *
     * 以後の write や close は、この fd を通じて
     * 「どのファイルへ書くのか」
     * 「どのファイルを閉じるのか」
     * を指定する。
     *
     * 多くの実行環境では、
     *   0 = 標準入力
     *   1 = 標準出力
     *   2 = 標準エラー出力
     * が最初から使われているので、
     * この open が最初の追加オープンなら 3 が返ることが多い。
     *
     * ただしこれは環境依存であり保証ではない。
     */
    int fd;

    /*
     * open("bye.txt", O_WRONLY)
     *
     * bye.txt を「書き込み専用」で開く。
     *
     * 第1引数:
     *   "bye.txt"
     *   対象ファイルのパスである。
     *   このコードではカレントディレクトリにある bye.txt を想定している。
     *
     * 第2引数:
     *   O_WRONLY
     *   write only の略で、
     *   「書き込み専用で開く」ことを意味するフラグである。
     *
     * 重要な注意:
     *   このコードは O_CREAT を付けていない。
     *   したがって bye.txt が存在しない場合は open に失敗する。
     *
     *   また O_TRUNC も付けていないので、
     *   既存ファイルを開いた場合、内容を自動で空にはしない。
     *   単に現在のファイルオフセット位置（通常は先頭）から書き込む。
     *
     *   つまり既存内容の上書きのされ方は、
     *   ファイルの元の長さや書き込み位置によって変わる。
     *
     * 戻り値:
     *   成功時:
     *     0 以上のファイルディスクリプタ
     *
     *   失敗時:
     *     -1
     *     さらに errno に失敗理由が入る
     *
     * ここでは
     *
     *   (fd = open(...)) == -1
     *
     * によって、
     *   1. 戻り値を fd に保存し
     *   2. 失敗かどうかを同時に判定
     * している。
     */
    if ((fd = open("bye.txt", O_WRONLY)) == -1) {
        /*
         * open が失敗した場合、
         * perror("open") により errno に対応するエラーメッセージを表示する。
         *
         * 例:
         *   open: No such file or directory
         *   open: Permission denied
         *
         * など。
         *
         * "open" は、どの処理で失敗したかを示すラベルである。
         */
        perror("open");

        /*
         * 書き込み対象ファイルを開けなかったので、
         * 以後の write や close を続けても意味がない。
         * そのため異常終了する。
         */
        exit(1);
    }

    /*
     * write(fd, "See you", 7)
     *
     * ここでは文字列リテラル "See you" の先頭アドレスから
     * 7 バイトだけ fd が表すファイルへ書き込む。
     *
     * "See you" は文字数としては
     *
     *   S e e _ y o u
     *
     * の 7 文字である。
     *
     * C の文字列リテラルには末尾に '\0' が付くが、
     * write は「文字列全体」を知っているわけではないので、
     * 第3引数で指定した 7 バイトだけを書く。
     *
     * つまり '\0' は書かれない。
     *
     * ここが printf や puts との違いである。
     *
     * write の戻り値:
     *   成功時:
     *     実際に書けたバイト数
     *   失敗時:
     *     -1
     *
     * このコードでは「7 バイト全部書けるはず」という前提で、
     *
     *   write(...) != 7
     *
     * と比較している。
     *
     * つまり、
     *   - -1 の失敗
     *   - 途中までしか書けなかった部分書き込み
     * のどちらも異常として扱っている。
     *
     * 小さな通常ファイルでは 1 回で全部書けることが多いが、
     * 低水準 I/O の一般論としては write は部分書き込みを起こしうる。
     *
     * そのため厳密には、実務では
     * 「全部書き終わるまで write を繰り返す」
     * ループを書くこともある。
     *
     * この教材では簡潔さを優先して、
     * 指定バイト数と一致することをそのまま確認している。
     */
    if (write(fd, "See you", 7) != 7) {
        /*
         * write が -1 を返した場合は perror で理由を表示できる。
         *
         * ただし「部分書き込みで 7 に満たなかったが errno は失敗を示していない」
         * という場合に perror を呼ぶと、期待通りの意味ある理由が出ない可能性はある。
         *
         * それでもこのコードでは、
         * 「7 バイト全部書けなかったら異常」
         * という学習用の単純化として perror を使っている。
         */
        perror("write");
        exit(1);
    }

    /*
     * 2 回目の write:
     *
     *   write(fd, " later!", 7)
     *
     * ここでは " later!" の 7 バイトを書き込む。
     *
     * バイト列としては
     *   ' ' 'l' 'a' 't' 'e' 'r' '!'
     * の 7 バイトである。
     *
     * 1 回目の write の続き位置から書かれるため、
     * ファイル内容はここまでで概念的に
     *
     *   See you later!
     *
     * となる。
     *
     * 低水準 I/O では、通常ファイルに対する write は
     * 現在のファイルオフセット位置に書き込み、
     * 書いた分だけオフセットが進む。
     *
     * そのため、複数回の write を連続で呼ぶと
     * 内容は自然に後ろへ連結されていく。
     */
    if (write(fd, " later!", 7) != 7) {
        perror("write");
        exit(1);
    }

    /*
     * 3 回目の write:
     *
     *   write(fd, "\nBye!\n", 6)
     *
     * この文字列リテラルは
     *
     *   '\n' 'B' 'y' 'e' '!' '\n'
     *
     * の 6 バイトを持つ。
     *
     * 先頭の '\n' は改行であるため、
     * ここまでの内容
     *
     *   See you later!
     *
     * の次の行へ移動してから、
     *
     *   Bye!
     *
     * を書き、その末尾でも改行する。
     *
     * したがって 3 回の write がすべて成功した後、
     * bye.txt の内容は概念的に
     *
     *   See you later!
     *   Bye!
     *
     * になる。
     *
     * ここでも第3引数 6 は、
     * 末尾の '\0' を含まない「実際に書きたいバイト数」である。
     */
    if (write(fd, "\nBye!\n", 6) != 6) {
        perror("write");
        exit(1);
    }

    /*
     * close(fd)
     *
     * open で得たファイルディスクリプタを閉じる。
     *
     * 低水準 I/O の基本パターンは
     *
     *   open -> write/read -> close
     *
     * である。
     *
     * close することで、
     *   - この fd をもう使わないことをカーネルへ伝える
     *   - 関連する資源を解放する
     * ことができる。
     *
     * 小さなプログラムでは、プロセス終了時にカーネルが回収してくれることが多いが、
     * 明示的に close するのが基本である。
     */
    if (close(fd) == -1) {
        /*
         * close に失敗した場合のエラー表示。
         *
         * close 失敗は普段意識しにくいが、
         * 実際にはエラー確認しておくのが丁寧である。
         */
        perror("close");
        exit(1);
    }

    /*
     * ここまで来れば
     *   open 成功
     *   3 回の write 成功
     *   close 成功
     * なので正常終了である。
     */
    return 0;
}
