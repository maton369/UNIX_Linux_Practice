/* llio/showfd.c */

/*
 * このコードの主題は「低水準入出力 (low-level I/O)」である。
 *
 * C / UNIX / Linux の入出力には、大きく分けて次の 2 系統がある。
 *
 * 1. 高水準入出力
 *    例:
 *      fopen, fclose, fread, fwrite, fprintf, fscanf
 *
 *    これは C 標準ライブラリ stdio が提供するものであり、
 *    FILE * を使ってバッファリング付きで扱う。
 *
 * 2. 低水準入出力
 *    例:
 *      open, close, read, write, lseek
 *
 *    これは UNIX / Linux のシステムコールに近い層であり、
 *    「ファイルディスクリプタ (file descriptor)」という整数値を使って扱う。
 *
 * このコードは後者の低水準入出力の入口であり、
 *
 *   - open() でファイルを開く
 *   - 成功したらファイルディスクリプタを得る
 *   - その整数値を表示する
 *
 * という最小例になっている。
 *
 * 重要:
 *   open() が返すのは FILE * ではなく int である。
 *   この int が「ファイルディスクリプタ」であり、
 *   以後 read / write / close などの低水準 API はこの整数を使って対象を識別する。
 */

#include <sys/types.h>  /* open に関連する型定義が入ることがある */
#include <stdio.h>      /* perror, printf を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/stat.h>   /* open に関連する定数や mode 用の定義が入ることがある */
#include <fcntl.h>      /* open, O_RDONLY などのフラグを使うためのヘッダ */

/*
 * main:
 *   input.txt を低水準 I/O の open() で開き、
 *   得られたファイルディスクリプタを表示する。
 *
 * このコード全体の流れ:
 *
 *   1. open("input.txt", O_RDONLY) を呼ぶ
 *   2. 成功ならファイルディスクリプタ fd を得る
 *   3. 失敗なら -1 が返るので perror で理由を表示して終了する
 *   4. 成功時は fd の値を表示して終了する
 *
 * ここで特に重要なのは、
 *   「ファイルを開く」とは、単にパス文字列を知っているだけではなく、
 *   カーネルにそのファイルを使いたいと伝えて、
 *   以後の操作に使う識別子を受け取ること
 * だという点である。
 */
int main(void) {
    /*
     * fd:
     *   file descriptor の略であり、
     *   open() が成功したときに返す「開かれたファイルの識別番号」である。
     *
     * 型が int なのは、低水準 I/O ではファイルを「整数番号」で扱うからである。
     *
     * 典型的には、プロセスには最初から次の 3 つのファイルディスクリプタがある。
     *
     *   0 : 標準入力   (stdin)
     *   1 : 標準出力   (stdout)
     *   2 : 標準エラー (stderr)
     *
     * そのため、このプログラムで最初に open() したファイルは、
     * 多くの環境では 3 が返ることが多い。
     *
     * ただし、これは状況により変わるので保証ではない。
     */
    int fd;

    /*
     * open("input.txt", O_RDONLY)
     *
     * open は、指定したパスのファイルを開くための低水準 API である。
     *
     * 第1引数:
     *   "input.txt"
     *   開きたいファイルのパスである。
     *   このコードではカレントディレクトリにある input.txt を想定している。
     *
     * 第2引数:
     *   O_RDONLY
     *   read only の略で、
     *   「読み取り専用で開く」ことを意味するフラグである。
     *
     * つまりこの呼び出しは、
     *
     *   「input.txt を読み取り専用で開いてほしい」
     *
     * とカーネルへ依頼していることになる。
     *
     * 戻り値:
     *   成功時:
     *     0 以上の整数（ファイルディスクリプタ）
     *
     *   失敗時:
     *     -1
     *     さらに errno に失敗理由が入る
     *
     * ここでは
     *
     *   (fd = open(...)) == -1
     *
     * という形で、
     * 「戻り値を fd に代入しつつ、その結果が -1 かどうかを判定」
     * している。
     *
     * これにより、
     *   成功したときは fd に使えるディスクリプタ番号が入り、
     *   失敗したときはすぐエラー処理へ進む。
     */
    if ((fd = open("input.txt", O_RDONLY)) == -1) {

        /*
         * perror("open")
         *
         * 直前の失敗理由を errno に基づいて人間向けメッセージで表示する。
         *
         * たとえば input.txt が存在しない場合は典型的に
         *
         *   open: No such file or directory
         *
         * のように表示される。
         *
         * ここで "open" は、
         * 「どの処理で失敗したのか」を示すラベルとして付けている。
         *
         * perror は標準エラー出力(stderr)へ出力する。
         */
        perror("open");

        /*
         * exit(1)
         *
         * 異常終了を表す終了コード 1 でプログラムを終了する。
         *
         * open に失敗した場合、この後 fd を使った処理は続けられないため、
         * 即座に終了するのが自然である。
         *
         * UNIX / Linux では慣習的に
         *
         *   0   : 正常終了
         *   0以外: 異常終了
         *
         * とされることが多い。
         */
        exit(1);
    }

    /*
     * ここまで来たら open は成功している。
     *
     * つまり fd には、
     *   input.txt を表すファイルディスクリプタ番号
     * が入っている。
     *
     * printf("fd = %d\n", fd);
     *
     * により、その整数値を表示する。
     *
     * たとえば多くの単純な実行環境では
     *
     *   fd = 3
     *
     * のようになることがある。
     *
     * これは、
     *   0, 1, 2 がすでに標準入出力用に使われていて、
     *   次の空き番号として 3 が割り当てられた
     * という意味である。
     *
     * ただし、すでに別の fd が開かれていれば 4, 5, ... になることもある。
     *
     * 重要:
     *   この整数自体がデータ本体ではなく、
     *   カーネル内の「開かれたファイル表」への参照キーのようなものだと考えるとよい。
     */
    printf("fd = %d\n", fd);

    /*
     * このサンプルでは close(fd) を呼んでいない。
     *
     * 小さな一瞬で終わるプログラムでは、プロセス終了時にカーネルが
     * 開いているファイルディスクリプタを回収するため、
     * 実害が見えにくいことも多い。
     *
     * しかし一般には、open したら対応する close(fd) を呼ぶのが基本である。
     *
     * つまり、より丁寧な形なら本来は例えば次のように書く。
     *
     *   if (close(fd) == -1) {
     *       perror("close");
     *       exit(1);
     *   }
     *
     * このコードの主題は open と fd の確認なので省略されているが、
     * 低水準 I/O では
     *
     *   open -> read/write -> close
     *
     * が基本パターンである。
     */

    /*
     * 正常終了。
     */
    return 0;
}
