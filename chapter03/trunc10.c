/* llio/trunc10.c */

#include <stdio.h>      /* perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <unistd.h>     /* truncate を使うためのヘッダ */
#include <sys/types.h>  /* truncate に関連する型定義が入ることがある */

/*
 * このコードの主題は「truncate() によるファイル長の変更」である。
 *
 * truncate() は、
 *   「指定したパスのファイルの長さを、指定したバイト数に変更する」
 * 関数である。
 *
 * 重要なのは、truncate() が
 *   - ファイルを open して read/write するのではなく
 *   - パス名を直接指定して
 *   - ファイルの論理的な長さを変更する
 * という点である。
 *
 * このコードでは
 *
 *   truncate("hello.txt", 10)
 *
 * を呼んでいるので、
 *   hello.txt のファイル長を 10 バイトにする
 * という意味になる。
 *
 * truncate の動作は、元のファイル長によって 2 通りに分かれる。
 *
 * 1. 元の長さが 10 バイトより大きい場合
 *    -> 10 バイトより後ろを切り捨てる
 *
 * 2. 元の長さが 10 バイトより小さい場合
 *    -> 10 バイトまで拡張する
 *
 * 拡張時には、新しく増えた部分は「0 バイト列のように見える領域」になる。
 * ファイルシステムによっては、ここが穴 (hole) を含む疎ファイルになることもある。
 *
 * つまり truncate は、
 *   「既存内容をそのまま順番に書き換える」のではなく、
 *   「ファイルの長さそのものを調整する」
 * 操作である。
 *
 * 低水準入出力の流れで言えば、
 *   open -> lseek -> write -> close
 * を使わずに、
 *   ファイルのサイズ属性だけを直接変更する
 * 関数だと考えると分かりやすい。
 */

int main(void) {
    /*
     * truncate("hello.txt", 10)
     *
     * 第1引数:
     *   "hello.txt"
     *   長さを変更したい対象ファイルのパスである。
     *
     * 第2引数:
     *   10
     *   変更後のファイルサイズ（バイト単位）である。
     *
     * つまりこの呼び出しは、
     *
     *   「hello.txt の長さを 10 バイトにしなさい」
     *
     * という意味になる。
     *
     * 具体例をいくつか挙げる。
     *
     * 例1: 元の hello.txt が 20 バイトだった場合
     *
     *   変更前:
     *     [0 ... 19] の 20 バイト
     *
     *   truncate 後:
     *     [0 ... 9] の 10 バイトだけ残る
     *
     *   つまり 10 バイト目以降は切り捨てられる。
     *
     * 例2: 元の hello.txt が 5 バイトだった場合
     *
     *   変更前:
     *     [0 ... 4] の 5 バイト
     *
     *   truncate 後:
     *     [0 ... 9] の 10 バイトになる
     *
     *   元の 5 バイトの後ろに 5 バイトぶんの拡張領域が追加される。
     *   その追加部分を読むと通常は 0 として見える。
     *
     * ここで重要なのは、
     * truncate は「ファイル内容の途中に文字列を書く」わけではなく、
     * あくまで「ファイル長」を変更しているだけだという点である。
     *
     * 戻り値:
     *   成功時:
     *     0
     *
     *   失敗時:
     *     -1
     *     errno に失敗理由が入る
     *
     * このコードでは -1 かどうかを見て失敗判定している。
     */
    if (truncate("hello.txt", 10) == -1) {
        /*
         * truncate に失敗した場合、
         * perror("truncate") により errno に対応する人間向けメッセージを表示する。
         *
         * 例:
         *   truncate: No such file or directory
         *   truncate: Permission denied
         *
         * など。
         *
         * よくある失敗理由:
         *   - hello.txt が存在しない
         *   - 書き込み権限がない
         *   - ファイルシステムや対象の種類が truncate を許さない
         *
         * "truncate" は、どの処理で失敗したかを示すラベルである。
         */
        perror("truncate");

        /*
         * ファイル長変更に失敗したので、異常終了する。
         *
         * UNIX / Linux では慣習的に
         *   0   : 正常終了
         *   0以外: 異常終了
         * とすることが多い。
         */
        exit(1);
    }

    /*
     * ここまで来れば truncate は成功している。
     *
     * したがって hello.txt の長さは 10 バイトになっている。
     *
     * このサンプルでは成功時に追加の表示はしていないが、
     * 実際には ls -l や stat コマンドなどでサイズを確認すると分かりやすい。
     *
     * たとえば:
     *
     *   ls -l hello.txt
     *
     * でサイズ欄が 10 になっていることを確認できる。
     */

    /*
     * 正常終了。
     */
    return 0;
}

/*
 * このコードから学ぶべきことのまとめ
 *
 * 1. truncate() はファイル長を直接変更する
 *
 *    read/write のように「データを順に扱う」のではなく、
 *    ファイルサイズそのものを指定値に変える。
 *
 * 2. 短くすることも長くすることもできる
 *
 *    - 長いファイルを短くする -> 後ろを切り捨てる
 *    - 短いファイルを長くする -> 後ろに拡張領域を作る
 *
 * 3. 拡張時の追加部分は通常 0 バイト列のように見える
 *
 *    ただし実装やファイルシステムによっては、
 *    物理的には穴 (hole) として扱われることがある。
 *
 * 4. truncate() はパス名ベースの操作である
 *
 *    open した file descriptor ではなく、ここでは "hello.txt" というパスを直接渡している。
 *
 * 5. 失敗時は -1 を返し、errno に理由が入る
 *
 *    そのため perror と組み合わせると、原因を確認しやすい。
 *
 *
 * 図でまとめると:
 *
 *   例: 元のファイルが 15 バイト
 *
 *     [0.........9][10....14]
 *      残る部分      切り捨て部分
 *
 *   truncate(..., 10) 後:
 *
 *     [0.........9]
 *
 *
 *   例: 元のファイルが 6 バイト
 *
 *     [0.....5]
 *
 *   truncate(..., 10) 後:
 *
 *     [0.....5][6..9]
 *      元の内容   拡張部分
 *
 *
 * このコードの本質を一言で言うと、
 *
 *   「truncate() を使って、hello.txt の論理的なファイル長をちょうど 10 バイトに変更している」
 *
 * である。
 */
