/* llio/writeout.c */

#include <sys/types.h>  /* ssize_t など、read/write/open で使われる型定義 */
#include <stdio.h>      /* perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/stat.h>   /* open の mode 引数に関連する定義 */
#include <fcntl.h>      /* open, O_WRONLY, O_CREAT, O_TRUNC を使うためのヘッダ */
#include <sys/uio.h>    /* read, write に関連する定義が入ることがある */
#include <unistd.h>     /* read, write, close, STDIN_FILENO を使うためのヘッダ */

/*
 * このコードの主題は「標準入力から読み、通常ファイルへ書く」という
 * 低水準入出力の基本パターンである。
 *
 * 特に今回は「標準入出力」のうち、
 *   - 標準入力 (standard input)
 * を低水準 I/O の file descriptor として扱っている点が重要である。
 *
 * UNIX / Linux の世界では、標準入出力も「ファイルの一種」として扱える。
 * そのため、通常ファイルと同じように read / write で操作できる。
 *
 * プロセスには通常、最初から次の 3 つのファイルディスクリプタが開かれている。
 *
 *   0 : 標準入力   (stdin)   = STDIN_FILENO
 *   1 : 標準出力   (stdout)  = STDOUT_FILENO
 *   2 : 標準エラー (stderr)  = STDERR_FILENO
 *
 * このコードでは、そのうち
 *
 *   STDIN_FILENO
 *
 * を read() の入力元として使っている。
 *
 * つまりこのプログラムは、
 *
 *   「キーボード入力やリダイレクトされた入力を標準入力から受け取り、
 *    その内容を output.file にそのままコピーして保存する」
 *
 * という動作をする。
 *
 * 低水準 I/O の観点で全体の流れを書くと次のようになる。
 *
 *   1. output.file を書き込み用に open する
 *   2. 標準入力 (fd = 0) から buf にデータを読む
 *   3. 読めた分だけ output.file へ write する
 *   4. EOF になるまで繰り返す
 *   5. 最後に output.file を close する
 *
 * つまりアルゴリズムとしては、
 *
 *   標準入力 -> バッファ -> 通常ファイル
 *
 * の中継処理である。
 *
 * 高水準入出力の fgets / fputs / fread / fwrite ではなく、
 * read / write を使っているため、
 * 「標準入力も単なる file descriptor 0 である」
 * ことがよく見えるコードになっている。
 */

int main(void) {
    /*
     * ofd:
     *   output file descriptor のつもりの変数であり、
     *   output.file を open した結果得られるファイルディスクリプタを保持する。
     *
     * この値は以後
     *   write(ofd, ...)
     *   close(ofd)
     * のように使われる。
     */
    int ofd;

    /*
     * cc:
     *   read() の戻り値を受け取る変数である。
     *
     * read() は
     *   > 0 : 実際に読めたバイト数
     *   = 0 : EOF（入力終端）
     *   = -1: エラー
     * を返す。
     *
     * そのため、負の値も表せる ssize_t を使っている。
     *
     * この変数はループの継続条件そのものにも使われる。
     */
    ssize_t cc;

    /*
     * buf:
     *   標準入力から読み込んだデータを一時的に保持するバッファである。
     *
     * ここでは 1024 バイトの固定長配列にしている。
     *
     * 重要:
     *   read() は「文字列として読む」関数ではない。
     *   生のバイト列を buf に入れるだけである。
     *
     * したがって、この buf の中身は必ずしも '\0' 終端の文字列ではない。
     *   有効なのは read() が返した cc バイト分だけ
     * である。
     *
     * このコードではその性質を正しく踏まえて、
     *   write(ofd, buf, cc)
     * として「読めたバイト数だけ」書き戻している。
     */
    char buf[1024];

    /*
     * open("output.file", O_WRONLY|O_CREAT|O_TRUNC, 0666)
     *
     * output.file を書き込み用に開く。
     *
     * 第1引数:
     *   "output.file"
     *   出力先ファイル名である。
     *
     * 第2引数:
     *   O_WRONLY | O_CREAT | O_TRUNC
     *
     *   O_WRONLY
     *     書き込み専用で開く
     *
     *   O_CREAT
     *     ファイルが存在しなければ新しく作る
     *
     *   O_TRUNC
     *     すでに存在していれば長さを 0 に切り詰める
     *     つまり中身を空にして最初から書き直す
     *
     * 第3引数:
     *   0666
     *   O_CREAT を付けたとき、新規作成ファイルの許可モードを指定する。
     *
     *   ここでの 0666 は 8 進数で、
     *   所有者・グループ・その他に対して
     *   読み書き許可を与える意図である。
     *
     *   ただし実際の最終権限は umask の影響を受けるため、
     *   そのまま 0666 になるとは限らない。
     *
     * 戻り値:
     *   成功時:
     *     0 以上のファイルディスクリプタ
     *
     *   失敗時:
     *     -1
     *     さらに errno に理由が入る
     *
     * ここでは
     *
     *   (ofd = open(...)) == -1
     *
     * により、戻り値を ofd に保存しつつ失敗判定している。
     */
    if ((ofd = open("output.file", O_WRONLY|O_CREAT|O_TRUNC, 0666)) == -1) {
        /*
         * open に失敗した場合、
         * perror("open") により errno に対応する人間向けメッセージを表示する。
         *
         * 例:
         *   open: Permission denied
         *   open: No such file or directory
         *
         * など。
         */
        perror("open");

        /*
         * 出力ファイルを開けなければ以後の処理は続けられないので、
         * 異常終了する。
         */
        exit(1);
    }

    /*
     * while ((cc = read(STDIN_FILENO, buf, sizeof(buf))) > 0)
     *
     * ここがこのコードの中心である。
     *
     * read(STDIN_FILENO, buf, sizeof(buf)) は、
     *
     *   - 標準入力 (fd = STDIN_FILENO = 通常 0)
     *   - から
     *   - buf へ
     *   - 最大 sizeof(buf) バイト
     * 読み込む。
     *
     * つまり「標準入力から 1024 バイトずつ読む」という意味になる。
     *
     * ここで重要なのは、
     *   標準入力も read() の対象として普通のファイルと同じように扱える
     * という点である。
     *
     * 標準入力の具体例:
     *   - 端末からキーボード入力されたデータ
     *   - シェルのリダイレクトで渡されたファイル内容
     *   - パイプで前段コマンドから流れてきたデータ
     *
     * たとえば
     *
     *   ./a.out < input.txt
     *
     * のように実行すると、STDIN_FILENO から読む内容は input.txt の中身になる。
     *
     * さらに
     *
     *   echo hello | ./a.out
     *
     * のようにすると、標準入力には前段コマンドの出力が入る。
     *
     * これが UNIX の「すべてを file descriptor 経由で扱える」強さである。
     *
     * 戻り値 cc の意味:
     *   cc > 0:
     *     その回に実際に読めたバイト数
     *
     *   cc == 0:
     *     EOF（入力が終わった）
     *
     *   cc == -1:
     *     エラー
     *
     * この while では
     *   cc > 0 の間だけループを続ける。
     *
     * つまり、
     *   「標準入力からまだデータが来る限り、読み続ける」
     * という意味である。
     */
    while ((cc = read(STDIN_FILENO, buf, sizeof(buf))) > 0) {

        /*
         * この時点では、
         *   buf の先頭 cc バイトに
         *   標準入力から読んだデータ
         * が入っている。
         *
         * 重要:
         *   有効なのは buf 全体ではなく先頭 cc バイトだけである。
         *   read() はバッファを '\0' 終端してくれるわけではない。
         *
         * したがって、このデータをそのまま output.file にコピーするには、
         *   write(ofd, buf, cc)
         * として「読めたぶんだけ」書けばよい。
         *
         * write の戻り値は「実際に書けたバイト数」である。
         *
         * このコードでは
         *
         *   if (write(ofd, buf, cc) < cc)
         *
         * として、
         *   - -1 の失敗
         *   - cc バイト未満しか書けなかった部分書き込み
         * の両方を異常として扱っている。
         *
         * ここでのアルゴリズムは、
         *
         *   read で受け取ったチャンクを
         *   そのまま write で出力先へ流す
         *
         * という「コピー」である。
         *
         * つまりこのループは全体として
         *
         *   標準入力の内容を output.file へそのまま転送する
         *
         * 役割を持つ。
         *
         * 厳密には、write は通常ファイルでも部分書き込みを起こしうるため、
         * 実務では「全部書き切るまで write を繰り返す」ループを書くこともある。
         *
         * ただしこの教材では簡潔さを優先し、
         * cc バイト全部書けなければ即エラーとしている。
         */
        if (write(ofd, buf, cc) < cc) {
            /*
             * write が失敗、または期待した長さぶん書けなかった場合のエラー処理。
             *
             * perror("write") は errno に基づく理由を表示する。
             *
             * 注意:
             *   「部分書き込みだが errno は設定されていない」という場合には、
             *   perror の内容が必ずしもその原因を正確に表すとは限らない。
             *
             * それでもこのコードでは、
             *   「読んだぶん全部書けなければ異常」
             * という学習用の単純化として perror を使っている。
             */
            perror("write");
            exit(1);
        }
    }

    /*
     * while ループを抜けた理由は 2 通りある。
     *
     * 1. cc == 0
     *    -> 標準入力が EOF に達した
     *       つまり正常に入力が終わった
     *
     * 2. cc == -1
     *    -> read 中にエラーが起きた
     *
     * そのため、ここで cc == -1 かどうかを判定する。
     */
    if (cc == -1) {
        /*
         * 標準入力からの read に失敗した場合のエラー表示。
         *
         * たとえば端末・パイプ・ファイルのいずれを標準入力にしていても、
         * read は同じインターフェースで扱える。
         * ここが低水準 I/O と標準入出力の重要な接点である。
         */
        perror("read");
        exit(1);
    }

    /*
     * ここまで来て cc == -1 ではないなら、
     * ループ終了理由は cc == 0、つまり EOF である。
     *
     * したがって、標準入力からのコピー処理は正常に終了したことになる。
     *
     * 最後に output.file を表す ofd を close() して資源を解放する。
     */

    /*
     * close(ofd)
     *
     * open で得たファイルディスクリプタを閉じる。
     *
     * 低水準 I/O の基本パターンは
     *
     *   open -> read/write -> close
     *
     * である。
     *
     * close を行うことで、
     *   - この fd をもう使わないことをカーネルへ伝える
     *   - 関連する資源を解放する
     * ことができる。
     */
    if (close(ofd) == -1) {
        /*
         * close に失敗した場合のエラー表示。
         */
        perror("close");
        exit(1);
    }

    /*
     * ここまで来れば
     *   - 出力ファイル open 成功
     *   - 標準入力から EOF まで read 成功
     *   - 出力ファイルへの write 成功
     *   - close 成功
     * なので正常終了である。
     */
    return 0;
}
