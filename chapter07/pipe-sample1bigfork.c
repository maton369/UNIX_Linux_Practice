#include <stdio.h>      /* printf を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型, fork/read/write/wait に関係する型定義が入ることがある */
#include <sys/uio.h>    /* read(), write() の宣言が入ることがある */
#include <sys/wait.h>   /* wait() を使うためのヘッダ */
#include <unistd.h>     /* pipe(), fork(), read(), write(), close() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「pipe を通して大量データを親から子へ送り、
 *    子側では read() が何回に分かれて返ってくるかを観察する」
 *
 * ことである。
 *
 * 前の単純な例では "Hello\n" のような非常に短い文字列を pipe に流していた。
 * その場合は 1 回の read() で全部読めてしまい、
 *
 *   「pipe は内部に有限サイズのバッファを持つ」
 *   「write() したデータは read() で分割されて届くことがある」
 *
 * という点が見えにくかった。
 *
 * このコードでは、あえて 1 MiB (= 1024 * 1024 バイト) という大きなバッファを
 * 親から子へ送り、子側で
 *
 *   while ((n = read(...)) > 0)
 *
 * と繰り返し読むことで、
 *
 *   - 1 回の write() が必ずしも 1 回の read() に対応しないこと
 *   - pipe の中身はストリームであり、境界付きメッセージではないこと
 *   - 書き込み側が close() すると、読み取り側は最終的に EOF を受け取ること
 *
 * を観察できるようにしている。
 *
 * つまりこのプログラムは、
 *
 *   親プロセス:
 *     大きなデータ列を pipe に書き込む
 *
 *   子プロセス:
 *     pipe から読めるだけ読み、各 read() の返したバイト数 n を表示する
 *
 * という「大量データのストリーム転送観察プログラム」である。
 */

enum {
    /*
     * SIZE = 1024 * 1024
     *
     * 1 MiB の大きさである。
     *
     * このサイズは pipe の内部バッファ容量より通常かなり大きい。
     * そのため親が 1 回の write() で大量データを書こうとしても、
     * カーネル内部では少しずつ運ばれ、子の read() でも複数回に分かれて
     * データが返ってくることが多い。
     *
     * つまりこの SIZE は、
     *   「pipe が分割動作する様子を見やすくするための大きなデータ量」
     * として使われている。
     */
    SIZE = 1024 * 1024,
};

int main(void)
{
    /*
     * n:
     *   read() が実際に読んだバイト数を受け取る変数である。
     *
     *   read() の戻り値は
     *     >0 : 読めたバイト数
     *      0 : EOF
     *     -1 : エラー
     *   である。
     *
     * fd[2]:
     *   pipe() が返す 2 本のファイルディスクリプタを入れる配列。
     *
     *   成功時は
     *     fd[0] = 読み取り口
     *     fd[1] = 書き込み口
     *   になる。
     *
     * buf:
     *   子プロセスが pipe から読み取ったデータを一時的に格納する受信バッファ。
     *   サイズは 1 MiB ある。
     *
     * buf2:
     *   親プロセスが pipe に流し込む送信バッファ。
     *
     *   初期値として "Hello\n" を先頭に持つが、
     *   配列全体は SIZE バイトある。
     *
     *   重要:
     *     C では
     *
     *       char buf2[SIZE] = "Hello\n";
     *
     *     と書くと、先頭には
     *
     *       'H' 'e' 'l' 'l' 'o' '\n' '\0'
     *
     *     が入り、その残り全部は 0 で埋められる。
     *
     *     つまり buf2 全体は
     *
     *       "Hello\n" + 大量の '\0'
     *
     *     というデータ列になっている。
     *
     * pid:
     *   fork() の戻り値を受け取る変数。
     *
     *   -1 : fork 失敗
     *    0 : 子プロセス側
     *   >0 : 親プロセス側（その値は子 PID）
     */
    int n, fd[2];
    char buf[SIZE], buf2[SIZE] = "Hello\n";
    pid_t pid;

    /*
     * pipe(fd)
     *
     * 新しいパイプを作る。
     *
     * 成功すると
     *   fd[0] = 読み取り口
     *   fd[1] = 書き込み口
     * になる。
     *
     * 図にするとこうである。
     *
     *   fd[1] ----書く----> [ pipe buffer ] ----読む----> fd[0]
     *
     * このコードでは pipe() のエラー判定を省略しているが、
     * 実務コードなら
     *
     *   if (pipe(fd) == -1) { ... }
     *
     * と確認したい。
     */
    pipe(fd);

    /*
     * fork()
     *
     * 現在のプロセスを複製して親子に分かれる。
     *
     * 重要:
     *   fork() は pipe() の後に呼ばれているので、
     *   親も子も両方とも fd[0] と fd[1] を持った状態で始まる。
     *
     * ここから先で、
     *   - 子は読む役
     *   - 親は書く役
     * と役割分担し、不要な端を閉じる。
     */
    if ((pid = fork()) == 0) {
        /* 子プロセス */

        /*
         * 子プロセスの役割は「受信側」である。
         *
         * 親が pipe に流した大量データを読み取り、
         * 各 read() が何バイト返したかを表示する。
         *
         * したがって子には
         *   fd[0] = 読み取り口
         * だけが必要であり、
         * 書き込み口 fd[1] は不要である。
         */

        /*
         * close(fd[1]);
         *
         * 子側では pipe の書き込み口を閉じる。
         *
         * これにより、
         *   「子はこの pipe に書かない」
         * ことを明確にする。
         *
         * また、不要な書き込み口を閉じることは EOF 判定にも重要である。
         * 読み取り側から見て、書き込み口を誰も持っていなくなったとき、
         * read() は最終的に 0 を返して EOF を知らせる。
         */
        close(fd[1]);

        /*
         * while ((n = read(fd[0], buf, sizeof(buf))) > 0)
         *
         * ここがこのプログラムの核心である。
         *
         * 子プロセスは pipe の読み取り口から、
         * 読めるだけ繰り返しデータを読む。
         *
         * 1 回の read() で最大 sizeof(buf) = 1 MiB 読めるようにしているが、
         * 実際に何バイト返るかは pipe 内部の状態次第である。
         *
         * 親は 1 MiB を 1 回の write() で書こうとしているが、
         * pipe は「メッセージ単位」ではなく「バイトストリーム」なので、
         * 子から見るとたとえば
         *
         *   65536 バイト
         *   65536 バイト
         *   65536 バイト
         *   ...
         *
         * のように複数回に分かれて届くことがある。
         *
         * つまり、この while ループは
         *
         *   「EOF が来るまで pipe を読み続ける」
         *
         * アルゴリズムである。
         *
         * ここで read() の戻り値 n の意味は:
         *
         *   n > 0:
         *     その回に読めた実際のバイト数
         *
         *   n == 0:
         *     EOF
         *     -> もう書き込み側が閉じられていて、これ以上データが来ない
         *
         *   n == -1:
         *     エラー
         *
         * このコードでは n == -1 のエラー処理は省略されている。
         */
        while ((n = read(fd[0], buf, sizeof(buf))) > 0) {
            /*
             * printf("%6sn = %d\n", buf, n);
             *
             * この行は「読んだデータの先頭を少し見せつつ、今回の read() が何バイト返したか」
             * を表示したい意図のコードである。
             *
             * ただし、ここにはかなり重要な注意点がある。
             *
             * 注意 1:
             *   書式 "%6s" は「文字列」として buf を読む。
             *
             *   しかし buf は read() で受け取った生データであり、
             *   read() は末尾に '\0' を付けない。
             *
             *   そのため、一般には %s で表示するのは危険である。
             *
             * ただし今回の buf2 は
             *   "Hello\n" の後ろが大量の '\0'
             * で埋まっているので、
             * 最初の方の read() ではたまたま buf 中に '\0' が早く現れ、
             * 文字列表示が成立しやすい。
             *
             * 注意 2:
             *   "%6s" の 6 は「最大6文字」ではなく
             *   「最小フィールド幅 6」である。
             *
             *   つまり文字列が6文字より短ければ左に空白を足すが、
             *   長ければそのまま全部出そうとする。
             *
             *   多くの人が precision と混同しやすいが、
             *   これは長さ制限ではない。
             *
             *   もし最大6文字だけ表示したいなら、
             *     "%.6s"
             *   や
             *     "%.*s"
             *   のような形式を使いたい。
             *
             * 注意 3:
             *   buf2 の先頭は "Hello\n" なので、
             *   最初の read() ではたぶん
             *
             *     Hello
             *
             *   のような見た目になるが、
             *   実際にはその後ろに大量の '\0' が続いている。
             *
             * このコードの本質的な観察対象は n の方であり、
             *   「何バイトずつ read() が返ってくるか」
             * を見るのが主目的である。
             */
            printf("%6sn = %d\n", buf, n);
        }

        /*
         * while ループを抜けるのは、
         *   n == 0 （EOF）
         * または
         *   n == -1（エラー）
         * のときである。
         *
         * このプログラムではエラー判定をしていないので、
         * ループ終了理由の区別はしていない。
         *
         * 通常は親が fd[1] を close() したあと、
         * すべてのデータを読み終えると n == 0 になってループ終了する。
         */

        /*
         * close(fd[0]);
         *
         * 子プロセス側で読み取り口も使い終わったので閉じる。
         *
         * これで子は pipe に関するファイルディスクリプタを
         * すべて解放したことになる。
         */
        close(fd[0]);

        /*
         * 元コードでは明示的な return や exit は書かれていないが、
         * main の末尾まで来れば終了する。
         *
         * 明示的に return 0; と書くとより分かりやすい。
         */
    } else {
        /* 親プロセス */

        /*
         * 親プロセスの役割は「送信側」である。
         *
         * 親は pipe に大量データを書き込み、
         * 子がそれを読む。
         *
         * したがって親には
         *   fd[1] = 書き込み口
         * だけが必要であり、
         * 読み取り口 fd[0] は不要である。
         */

        /*
         * close(fd[0]);
         *
         * 親側では pipe の読み取り口を閉じる。
         *
         * 親はこの pipe から読まないため、
         * 不要な端は早めに閉じるのが正しい。
         */
        close(fd[0]);

        /*
         * write(fd[1], buf2, sizeof(buf2));
         *
         * 親プロセスが、buf2 全体を pipe の書き込み口へ流し込む。
         *
         * ここで非常に重要なのは、
         * 書いているサイズが
         *
         *   sizeof(buf2) = 1 MiB
         *
         * だという点である。
         *
         * つまり親は "Hello\n" だけを書いているのではなく、
         * その後ろの大量の '\0' を含む 1 MiB 全体を書いている。
         *
         * 図にすると、
         *
         *   buf2:
         *     [ H ][ e ][ l ][ l ][ o ][ \n ][ \0 ][ \0 ][ \0 ] ... （残り全部 \0）
         *
         * をまるごと pipe に送っている。
         *
         * このことにより、
         * 子側では read() が複数回に分かれやすくなり、
         * n の値を観察しやすくなる。
         *
         * 重要:
         *   write() も「1 MiB 全部を必ず一度に書ける」とは限らない。
         *   ただしここでは戻り値をチェックしていない。
         *
         *   実務コードでは部分書き込みの可能性も考慮して、
         *   ループで最後まで書くべきである。
         */
        write(fd[1], buf2, sizeof(buf2));

        /*
         * close(fd[1]);
         *
         * 親はもう書く予定がないので、書き込み口を閉じる。
         *
         * これは非常に重要で、
         * 子側の read() が最終的に EOF (0) を受け取るためには、
         * 書き込み側がすべて閉じられていなければならない。
         *
         * もし親が fd[1] を閉じずに持ち続けると、
         * 子は「まだ将来データが来るかもしれない」とみなされ、
         * read() が EOF にならず待ち続ける可能性がある。
         *
         * したがって pipe 通信では、
         *   「送信が終わったら書き込み口を閉じる」
         * のが重要な終了通知になる。
         */
        close(fd[1]);

        /*
         * wait(NULL);
         *
         * 親プロセスが子プロセスの終了を待つ。
         *
         * これにより、
         *   - 子が終わる前に親が先に終了しない
         *   - 子の終了状態を回収できる
         *   - ゾンビプロセスを防げる
         *
         * という効果がある。
         *
         * このコードでは子の終了コードそのものには興味がないので、
         * 引数は NULL にしている。
         *
         * 親のアルゴリズムとしては
         *
         *   1. 読み取り口を閉じる
         *   2. 大量データを書く
         *   3. 書き込み口を閉じる
         *   4. 子が全部読み終えて終わるのを待つ
         *
         * という流れになる。
         */
        wait(NULL);
    }
}
