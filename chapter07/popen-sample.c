#include <stdio.h>      /* fgets, pclose, perror, popen, printf を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */

/*
 * このプログラムの主題は、
 *
 *   「シェルコマンドを子プロセスとして実行し、その標準出力を
 *    C プログラム側から FILE* ストリームとして読む」
 *
 * ことである。
 *
 * ここで中心になるのは
 *
 *   popen()
 *
 * である。
 *
 * popen() は、
 *   「コマンドを実行する子プロセスを起動し、
 *    その入出力の片側をパイプでつないで、
 *    C の FILE* ストリームとして扱えるようにする」
 * 関数である。
 *
 * このコードでは
 *
 *   popen("ls -l *.c | wc", "r")
 *
 * としているので、
 *
 *   1. シェル経由で "ls -l *.c | wc" を実行する
 *   2. そのコマンド列の標準出力をパイプで受ける
 *   3. その読み取り口を FILE* として返してもらう
 *
 * という流れになる。
 *
 * つまりこのプログラムは、
 *
 *   「C プログラムの中からシェルパイプラインを実行し、
 *    その結果1行を fgets() で読み取って表示する」
 *
 * 最小例である。
 *
 * ここで実行しているコマンド
 *
 *   ls -l *.c | wc
 *
 * は概念的には、
 *
 *   - カレントディレクトリにある *.c を long format で列挙する
 *   - その出力全体を wc に渡す
 *   - wc が行数・単語数・バイト数を数えて1行出力する
 *
 * という処理である。
 *
 * したがって、この C プログラムは最終的に
 *   wc の出力した1行
 * を buf に読み込み、それを printf で表示している。
 */

int main(void)
{
    /*
     * fp:
     *   popen() が返す FILE* ストリームである。
     *
     * 普通の fopen() が「通常ファイル」を FILE* として開くのに対し、
     * popen() は「子プロセスの標準入出力につながったパイプ」を FILE* として返す。
     *
     * 今回は "r" モードなので、
     *   子プロセスの標準出力をこちらが読むためのストリーム
     * になる。
     *
     * buf:
     *   fgets() で 1 行読み込むためのバッファ。
     *
     * 今回のコマンドは wc の結果1行を返す想定なので、
     * 1024 バイトあれば十分大きい。
     */
    FILE *fp;
    char buf[1024];

    /*
     * popen("ls -l *.c | wc", "r")
     *
     * ここがこのプログラムの核心である。
     *
     * 第1引数:
     *   "ls -l *.c | wc"
     *   -> 実行したいシェルコマンド文字列
     *
     * 第2引数:
     *   "r"
     *   -> このプログラム側が「読む」モードで接続したいことを表す
     *
     * "r" の意味:
     *   - 子プロセスは標準出力へ書く
     *   - 親であるこのプログラムは、その出力を読む
     *
     * つまりデータフローは概念的に
     *
     *   子プロセス:
     *     ls -l *.c | wc
     *          |
     *          v
     *      標準出力
     *          |
     *          v
     *      pipe
     *          |
     *          v
     *   このプログラム:
     *      FILE *fp から読む
     *
     * となる。
     *
     * popen() の内部では概念的に、
     *   1. pipe() を作る
     *   2. fork() する
     *   3. 子で shell を起動してコマンド文字列を実行する
     *   4. 親ではその pipe の片側を FILE* に変換して返す
     * というような処理が行われる。
     *
     * そのため、ユーザ側は pipe/fork/dup2/exec を直接書かなくても、
     * 「コマンドの出力を読む」ことが簡単にできる。
     *
     * 戻り値:
     *   成功時:
     *     有効な FILE* を返す
     *
     *   失敗時:
     *     NULL を返す
     *
     * 失敗理由の例:
     *   - 子プロセス作成失敗
     *   - pipe 作成失敗
     *   - システム資源不足
     */
    if ((fp = popen("ls -l *.c | wc", "r")) == NULL) {
        /*
         * popen() が失敗した場合のエラー処理。
         *
         * perror("popen") により errno に応じた理由を表示する。
         *
         * 例:
         *   popen: Too many open files
         *
         * など。
         */
        perror("popen");
        exit(1);
    }

    /*
     * fgets(buf, sizeof(buf), fp);
     *
     * popen() が返したストリーム fp から 1 行読む。
     *
     * ここで fp は「子プロセスの標準出力につながった読み取りストリーム」
 * なので、
     * 実際には
     *
     *   ls -l *.c | wc
     *
     * の出力を読んでいることになる。
     *
     * wc は通常、結果を 1 行で出力する。
     * たとえば
     *
     *      12      84     560
     *
     * のような行数・単語数・バイト数の1行である。
     *
     * fgets() はその1行を buf に読み込み、
     * 改行 '\n' も入るなら一緒に保持する。
     *
     * このコードでは戻り値の NULL チェックをしていないが、
     * 実務コードなら
     *
     *   if (fgets(...) == NULL) { ... }
     *
     * のように EOF やエラーを確認したい。
     *
     * ただしこのサンプルでは、
     * 「popen でコマンド出力を読み込める」ことを最短で示すため、
     * 単純化されている。
     */
    fgets(buf, sizeof(buf), fp);

    /*
     * printf("%s", buf);
     *
     * fgets() で読み込んだ1行をそのまま標準出力へ表示する。
     *
     * buf には通常すでに改行 '\n' が含まれているので、
     * ここでは printf に追加の "\n" を付けず、そのまま出している。
     *
     * つまりこの1行で、
     *
     *   「シェルコマンドの出力を、C プログラムが受け取って再表示する」
     *
     * ことが達成される。
     *
     * このプログラムの最終成果物は、この1行表示である。
     */
    printf("%s", buf);

    /*
     * pclose(fp);
     *
     * popen() で開いたストリームは fclose() ではなく pclose() で閉じる。
     *
     * 理由:
     *   popen() は単なる FILE* を開いているだけではなく、
     *   裏で子プロセスも起動しているからである。
     *
     * pclose() は概念的に
     *   - パイプを閉じる
     *   - 子プロセスの終了を待つ
     * という後始末を行う。
     *
     * つまり pclose() は
     *   popen() に対応する専用のクローズ関数
     * である。
     *
     * ここでも戻り値チェックは省略されているが、
     * 実務では子プロセスの終了ステータス確認に使うこともある。
     *
     * たとえば
     *   コマンド自体は起動できたが内部で失敗した
     * 場合には、その終了コードが pclose() 側に反映されることがある。
     */
    pclose(fp);

    /*
     * ここまで来たということは、
     *   - popen() でコマンド起動に成功した
     *   - その出力を1行読めた
     *   - 後始末として pclose() を呼んだ
     *
     * ということである。
     *
     * return 0 により正常終了を表す。
     */
}
