#include <stdio.h>      /* fprintf() を使うためのヘッダ */
#include <sys/types.h>  /* pid_t 型, fork() に関係する型定義が入ることがある */
#include <unistd.h>     /* pipe(), fork(), dup2(), close(), execlp() を使うためのヘッダ */

/*
 * このプログラムの主題は、
 *
 *   「2つのコマンドを pipe でつないで実行する」
 *
 * ことである。
 *
 * シェルで書くと、概念的には次のような形に相当する。
 *
 *   command1 | command2
 *
 * つまり、
 *
 *   command1 の標準出力
 *        ↓
 *      pipe
 *        ↓
 *   command2 の標準入力
 *
 * というデータの流れを作りたい。
 *
 * このコードはその仕組みを、
 *
 *   1. pipe() で通信路を作る
 *   2. fork() で親子に分かれる
 *   3. 子プロセス側で標準出力を pipe の書き込み口に付け替える
 *   4. 親プロセス側で標準入力を pipe の読み取り口に付け替える
 *   5. それぞれ exec で別コマンドへ置き換わる
 *
 * という形で実現している。
 *
 * 非常に重要なのは、
 *   「pipe を作っただけでは command1 | command2 にはならない」
 * という点である。
 *
 * pipe は単に
 *
 *   fd[0] = 読み取り口
 *   fd[1] = 書き込み口
 *
 * を作るだけである。
 *
 * そこからさらに dup2() によって
 *
 *   command1 の stdout (fd=1) -> fd[1]
 *   command2 の stdin  (fd=0) -> fd[0]
 *
 * と結び直してはじめて、
 * シェルのパイプラインと同じ意味になる。
 *
 * つまりこのコードの本質は、
 *
 *   「親子2プロセスの標準入出力を pipe の両端に再配線してから exec する」
 *
 * ことにある。
 */

int main(int argc, char *argv[])
{
    /*
     * fd[2]:
     *   pipe() が返す 2 本のファイルディスクリプタを入れる配列。
     *
     * 成功時は
     *
     *   fd[0] = パイプの読み取り口
     *   fd[1] = パイプの書き込み口
     *
     * になる。
     *
     * 図にするとこうである。
     *
     *   fd[1] ----write----> [ pipe buffer ] ----read----> fd[0]
     *
     * pid:
     *   fork() の戻り値を受け取る変数。
     *
     *   -1 : fork 失敗
     *    0 : 子プロセス側
     *   >0 : 親プロセス側（その値は子 PID）
     */
    int fd[2];
    pid_t pid;

    /*
     * 引数個数チェック。
     *
     * このプログラムは少なくとも
     *
     *   argv[0] = プログラム名
     *   argv[1] = command1
     *   argv[2] = command2
     *
     * の 3 つが必要である。
     *
     * つまり使い方は概念的に
     *
     *   program command1 command2
     *
     * であり、作られる処理は
     *
     *   command1 | command2
     *
     * となる。
     *
     * ただしこの元コードには注意点がある。
     *
     *   if (argc < 3) {
     *       fprintf(...);
     *   }
     *
     * となっていて、usage を表示したあと exit していない。
     *
     * そのため引数不足でも処理が続いてしまい、
     * argv[1] や argv[2] を不正に参照する危険がある。
     *
     * 実務コードなら本来ここで
     *
     *   exit(1);
     *
     * したい。
     *
     * ただしここでは元コードの構造を保ったまま、
     * 主にパイプライン構築の本体を説明する。
     */
    if (argc < 3) {
        /*
         * 使い方の表示。
         *
         * ここでの message は
         *
         *   command1 command2
         *
         * しか受け取れないことを示している。
         *
         * 注意:
         *   このプログラムは execlp() を使っており、
         *   command1 / command2 に追加引数は渡していない。
         *
         *   つまり
         *     ls | wc
         * のような「単独コマンド名 2 つ」は扱えるが、
         *     ls -l | wc -l
         * のような引数付きはそのままでは扱えない。
         */
        fprintf(stderr, "%s command1 command2\n", argv[0]);
    }

    /*
     * pipe(fd)
     *
     * 親子プロセス間で使うためのパイプを作る。
     *
     * ここでできるのは「通信路」だけであり、
     * まだ command1 / command2 には結びついていない。
     *
     * アルゴリズムの観点では、ここは
     *
     *   「将来 command1 の出力と command2 の入力をつなぐための通路を準備する」
     *
     * フェーズである。
     *
     * 元コードではエラー判定が省略されているが、
     * 実務コードなら
     *
     *   if (pipe(fd) == -1) { ... }
     *
     * と確認したい。
     */
    pipe(fd);

    /*
     * fork()
     *
     * 現在のプロセスを複製して、
     * 親プロセスと子プロセスの 2 つに分ける。
     *
     * 重要:
     *   pipe() を fork() より前に呼んでいるため、
     *   親も子もどちらも fd[0], fd[1] を持った状態で始まる。
     *
     * ここから先で、
     *
     *   子プロセス  -> command1 を担当
     *   親プロセス  -> command2 を担当
     *
     * と役割分担し、それぞれ不要な端を閉じ、
     * 必要な端を標準入出力に付け替える。
     *
     * これが pipe + fork + exec によるパイプライン構築の基本形である。
     */
    if ((pid = fork()) == 0) {
        /* 子プロセス */

        /*
         * 子プロセスは command1 を実行する側である。
         *
         * パイプライン
         *
         *   command1 | command2
         *
         * において command1 は「左側のコマンド」であり、
         * 自分の標準出力へ書いた内容が pipe に流れてほしい。
         *
         * したがって子プロセスでは、
         *
         *   stdout (fd=1) -> fd[1] (pipe の書き込み口)
         *
         * という再配線を行う必要がある。
         */

        /*
         * dup2(fd[1], 1);
         *
         * ここが子プロセス側の核心である。
         *
         * dup2(oldfd, newfd) は
         *   newfd を oldfd と同じ対象へ向ける
         * 関数である。
         *
         * 今回は
         *
         *   oldfd = fd[1]   -> pipe の書き込み口
         *   newfd = 1       -> 標準出力
         *
         * なので、結果として
         *
         *   標準出力が pipe の書き込み口を向く
         *
         * ことになる。
         *
         * 図にすると
         *
         *   dup2 前:
         *     fd 1  -> 端末
         *     fd[1] -> pipe 書き込み口
         *
         *   dup2 後:
         *     fd 1  -> pipe 書き込み口
         *     fd[1] -> pipe 書き込み口
         *
         * となる。
         *
         * これにより、あとで exec される command1 は
         *   「普通に標準出力へ出しているだけ」
         * のつもりでも、その出力は pipe に流れる。
         *
         * つまり子プロセスでは
         *
         *   command1 の stdout を pipe に接続
         *
         * している。
         */
        dup2(fd[1], 1);

        /*
         * close(fd[0]); close(fd[1]);
         *
         * dup2() が終わったあとは、元の pipe 用ディスクリプタそのものは不要になる。
         *
         * 子プロセスにとって必要なのは
         *   「標準出力 fd=1 が pipe の書き込み口を向いていること」
         * であって、
         *   元の fd[1] という別名
         * を持ち続ける必要はない。
         *
         * また、子は pipe の読み取り側を使わないので fd[0] も不要である。
         *
         * したがって両方閉じる。
         *
         * これにより、
         *   - 不要な fd を減らせる
         *   - EOF 判定の邪魔をしない
         *
         * という効果がある。
         *
         * 特に pipe では「使わない端を閉じる」のが非常に重要な作法である。
         */
        close(fd[0]);
        close(fd[1]);

        /*
         * execlp(argv[1], argv[1], (char *)NULL);
         *
         * ここで子プロセス自身を command1 に置き換える。
         *
         * execlp の意味:
         *
         *   exec  : 現在プロセスを別プログラムへ置き換える
         *   l     : 引数を list 形式で渡す
         *   p     : PATH を検索して実行ファイルを探す
         *
         * ここでは
         *
         *   command1 = argv[1]
         *
         * を起動し、
         * そのコマンドから見た argv[0] も argv[1] にしている。
         *
         * つまり概念的には
         *
         *   argv[1]
         *
         * というコマンドを「引数なし」で起動している。
         *
         * 重要:
         *   exec に成功すると、この元コードには戻ってこない。
         *
         *   しかも、exec 後もファイルディスクリプタ表は引き継がれるので、
         *   さきほど作った
         *
         *     stdout -> pipe 書き込み口
         *
         *   という接続もそのまま新しい command1 に渡る。
         *
         * その結果、command1 の標準出力は pipe に流れる。
         *
         * 注意:
         *   元コードでは execlp 失敗時の perror + exit が無い。
         *   実務コードなら戻ってきた場合にエラー処理を書くべきである。
         */
        execlp(argv[1], argv[1], (char *)NULL);
    }

    /*
     * ここから下は親プロセス側である。
     *
     * 親プロセスは command2 を実行する側になる。
     *
     * パイプライン
     *
     *   command1 | command2
     *
     * において command2 は「右側のコマンド」であり、
     * 標準入力から読むデータが pipe から来てほしい。
     *
     * したがって親プロセスでは、
     *
     *   stdin (fd=0) -> fd[0] (pipe の読み取り口)
     *
     * という再配線を行う必要がある。
     */

    /*
     * dup2(fd[0], 0);
     *
     * ここが親プロセス側の核心である。
     *
     * 今回は
     *
     *   oldfd = fd[0]   -> pipe の読み取り口
     *   newfd = 0       -> 標準入力
     *
     * なので、結果として
     *
     *   標準入力が pipe の読み取り口を向く
     *
     * ことになる。
     *
     * 図にすると
     *
     *   dup2 前:
     *     fd 0  -> キーボード / 端末入力
     *     fd[0] -> pipe 読み取り口
     *
     *   dup2 後:
     *     fd 0  -> pipe 読み取り口
     *     fd[0] -> pipe 読み取り口
     *
     * これにより、あとで exec される command2 は
     *   「普通に標準入力から読んでいるだけ」
     * のつもりでも、その入力は command1 から pipe 経由で届く。
         *
     * つまり親プロセスでは
     *
     *   command2 の stdin を pipe に接続
     *
     * している。
     */
    dup2(fd[0], 0);

    /*
     * close(fd[0]); close(fd[1]);
     *
     * 親プロセスでも、dup2() が終わったあとは元の pipe ディスクリプタは不要である。
     *
     * 必要なのは
     *   「標準入力 fd=0 が pipe の読み取り口を向いていること」
     * だけである。
     *
     * 元の fd[0] は別名なので不要であり、
     * 書き込み口 fd[1] は親では使わないのでこれも不要である。
     *
     * したがって両方閉じる。
     *
     * これも EOF の正しい伝達や資源管理にとって重要である。
     */
    close(fd[0]);
    close(fd[1]);

    /*
     * execlp(argv[2], argv[2], (char *)NULL);
     *
     * 親プロセス自身を command2 に置き換える。
     *
     * ここでも command2 は引数なしで起動している。
     *
     * 重要:
     *   exec に成功すると、このコードには戻らない。
     *
     *   そして exec 後もファイルディスクリプタ表は引き継がれるので、
     *   さきほど作った
     *
     *     stdin -> pipe 読み取り口
     *
     *   という接続もそのまま新しい command2 に渡る。
     *
     * その結果、command2 は自分の標準入力から読んでいるつもりで、
     * 実際には command1 の出力を受け取ることになる。
     *
     * これで全体として
     *
     *   command1 | command2
     *
     * が成立する。
     *
     * データフロー図にするとこうである。
     *
     *   command1 (子)
     *       stdout
     *         |
     *         v
     *      pipe 書き込み口
     *         |
     *         v
     *      [ pipe ]
     *         |
     *         v
     *      pipe 読み取り口
     *         |
     *         v
     *   command2 (親)
     *       stdin
     *
     * 注意:
     *   ここも exec 失敗時のエラー処理が無い。
     *   本来なら perror(argv[2]); exit(1); が欲しい。
     */
    execlp(argv[2], argv[2], (char *)NULL);
}
