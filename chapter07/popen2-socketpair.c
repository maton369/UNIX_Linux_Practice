#include <sys/types.h>  /* fork(), read(), write() に関係する型定義が入ることがある */
#include <sys/socket.h> /* socketpair(), shutdown(), AF_UNIX, SOCK_STREAM, SHUT_WR を使うためのヘッダ */
#include <sys/uio.h>    /* read(), write() の宣言が入ることがある */
#include <unistd.h>     /* close(), dup2(), execlp(), fork(), read(), write() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「socketpair() を使って、子プロセスと双方向に通信できる popen2 的な仕組みを作る」
 *
 * ことである。
 *
 * 通常の pipe は一方向通信である。
 * つまり、
 *
 *   書く側 ----> 読む側
 *
 * という流れしか作れない。
 *
 * 一方で socketpair(AF_UNIX, SOCK_STREAM, ...) は、
 * 同一マシン内で使う「双方向ストリーム通信路」を 2 本のファイルディスクリプタとして返す。
 *
 * つまり、
 *
 *   fd[0] <=====> fd[1]
 *
 * のように、両側が「読むことも書くこともできる」。
 *
 * この性質を使うと、
 *
 *   親プロセス:
 *     子へデータを送る
 *     子から結果を受け取る
 *
 *   子プロセス:
 *     標準入力からデータを受け取る
 *     標準出力へ結果を書く
 *
 * という「双方向の popen」を比較的自然に作れる。
 *
 * このコードでは具体的に、
 *
 *   1. popen2_socketpair("sort") を呼ぶ
 *   2. 内部で socketpair() と fork() を使って、
 *      親と子を1本の双方向通信路でつなぐ
 *   3. 子プロセスでは stdin と stdout の両方をその通信路へ dup2() する
 *   4. 子プロセスは /bin/sh -c "sort" を実行する
 *   5. 親プロセスは返ってきた fd を使って
 *      "orange\n", "apple\n", "banana\n" を sort に送る
 *   6. shutdown(fd, SHUT_WR) で「もう送るものは無い」と通知する
 *   7. sort がソート済み結果を返す
 *   8. 親はそれを read() で受け取り、標準出力へ流す
 *
 * という流れになっている。
 *
 * つまりこのプログラムは、
 *
 *   「子プロセスの stdin/stdout を 1 本の双方向ストリームへ結び付け、
 *    親が対話的にコマンドへ入出力する」
 *
 * 最小例である。
 *
 * 非常に重要なのは、
 * ここで使っているのが pipe ではなく socketpair だという点である。
 *
 * pipe だけだと双方向通信には通常 2 本必要になるが、
 * socketpair なら 1 組で双方向のやりとりができる。
 */

/*
 * popen2_socketpair(command)
 *
 * 指定した command を子プロセスで実行し、
 * 親プロセスがその子と双方向に通信するためのファイルディスクリプタを返す関数。
 *
 * 名前の意味としては、
 *
 *   popen  : コマンドを開く
 *   2      : 入力も出力も扱える双方向版
 *   socketpair : 実装に socketpair を使っている
 *
 * と考えると分かりやすい。
 *
 * 戻り値:
 *   成功時:
 *     親プロセス側で子と通信するための fd を返す
 *
 *   失敗時:
 *     -1
 *
 * 親から見ると、この戻り値 fd に対して
 *
 *   write(fd, ...)
 *
 * すると子の標準入力へ送られ、
 *
 *   read(fd, ...)
 *
 * すると子の標準出力から返ってきた結果を読める、
 * というイメージになる。
 */
int popen2_socketpair(const char *command)
{
    /*
     * fd[2]:
     *   socketpair() が返す 2 本のソケットファイルディスクリプタを受け取る配列。
     *
     * 成功すると、
     *
     *   fd[0] <=====> fd[1]
     *
     * という双方向接続ができる。
     *
     * pipe のように
     *   fd[0] は読むだけ
     *   fd[1] は書くだけ
     * ではない点が重要で、
     * どちらも read/write できる。
     */
    int fd[2];

    /*
     * socketpair(AF_UNIX, SOCK_STREAM, 0, fd)
     *
     * ここでローカルな双方向ストリーム通信路を作る。
     *
     * 第1引数 AF_UNIX:
     *   同一マシン内のローカル通信を表すアドレスファミリ。
     *
     * 第2引数 SOCK_STREAM:
     *   バイトストリーム型の通信を表す。
     *   TCP 的な「順序付きストリーム」に近い性質を持つ。
     *
     * 第3引数 0:
     *   通常はデフォルトプロトコルを使う。
     *
     * 第4引数 fd:
     *   結果の 2 本の fd を受け取る配列。
     *
     * 成功すると、概念的には
     *
     *   fd[0] <=====> fd[1]
     *
     * という接続ができる。
     *
     * 失敗時は -1 を返すので、
     * この関数全体も -1 を返して失敗を呼び出し元へ伝える。
     *
     * アルゴリズム的には、ここが
     *
     *   「親子間の双方向通信路の作成」
     *
     * に相当する。
     */
    if (socketpair(AF_UNIX, SOCK_STREAM, 0, fd) == -1)
        return -1;

    /*
     * fork()
     *
     * 現在のプロセスを複製して親子に分ける。
     *
     * ここでは socketpair() を先に作っているので、
     * 親も子もどちらも fd[0], fd[1] を引き継いだ状態で始まる。
     *
     * ここから先で、
     *
     *   子: fd[0] を使って stdin/stdout をつなぐ
     *   親: fd[1] を使って子と通信する
     *
     * という役割分担を作る。
     *
     * 元コードでは fork() 失敗時のチェックがない。
     * 実務コードなら pid == -1 を確認したい。
     */
    if (fork() == 0) {
        /* 子プロセス */

        /*
         * 子プロセス側では、この通信路の片側 fd[0] を
         *
         *   標準入力  (fd=0)
         *   標準出力  (fd=1)
         *
         * の両方に接続する。
         *
         * これにより、あとで exec されるコマンドは
         *
         *   - stdin から読んだデータを親から受け取る
         *   - stdout に書いたデータを親へ返す
         *
         * という動きをするようになる。
         */

        /*
         * dup2(fd[0], 0);
         *
         * 子プロセスの標準入力 (fd=0) を fd[0] と同じ対象へ向ける。
         *
         * 結果として、
         *
         *   子の stdin <- socketpair の fd[0]
         *
         * となる。
         *
         * つまり親が fd[1] に write() したデータは、
         * 子から見ると stdin から読める。
         */
        dup2(fd[0], 0);

        /*
         * dup2(fd[0], 1);
         *
         * 子プロセスの標準出力 (fd=1) も同じく fd[0] と同じ対象へ向ける。
         *
         * 結果として、
         *
         *   子の stdout -> socketpair の fd[0]
         *
         * となる。
         *
         * つまり子が stdout へ書いたデータは、
         * 親から見ると fd[1] から read() できる。
         *
         * これにより、子の stdin と stdout の両方が
         * 「同じ双方向通信路」につながる。
         *
         * 図にすると概念的にはこうである。
         *
         *   親 fd[1] <=====> 子 fd[0]
         *                       ↑   ↑
         *                       |   |
         *                    stdin stdout
         *
         * つまり子は、1本のストリームで
         * 入力も受け、出力も返す。
         */
        dup2(fd[0], 1);

        /*
         * close(fd[0]); close(fd[1]);
         *
         * dup2() が終わったあとは、
         * 元の fd[0], fd[1] そのものは不要になる。
         *
         * 子に必要なのは
         *
         *   stdin  -> fd[0] 側の接続
         *   stdout -> fd[0] 側の接続
         *
         * という「0番と1番への複製結果」であって、
         * 元の fd[0] という別名は要らない。
         *
         * また fd[1] は親側が使う端なので、子では不要である。
         *
         * 不要な fd を閉じる理由:
         *   - 資源節約
         *   - 通信終了判定を明確にする
         *   - 意図しないデータ経路を残さない
         */
        close(fd[0]);
        close(fd[1]);

        /*
         * execlp("sh", "/bin/sh", "-c", command, (char *)NULL);
         *
         * 子プロセス自身をシェル実行に置き換える。
         *
         * ここでは直接 command を実行するのではなく、
         *
         *   /bin/sh -c command
         *
         * の形でシェルに解釈させている。
         *
         * つまり command 文字列には
         * 単純なコマンド名だけでなく、
         * シェル構文を含むものも書ける。
         *
         * 今回の main() では "sort" を渡しているので、
         * 実質的には sort コマンドが起動する。
         *
         * 重要:
         *   exec に成功すると、この元コードには戻ってこない。
         *
         *   そして exec 後もファイルディスクリプタ表は引き継がれるので、
         *   さきほど作った
         *
         *     stdin  <- socketpair
         *     stdout -> socketpair
         *
         *   の接続もそのまま子コマンドへ渡る。
         *
         * その結果、sort は
         *   標準入力からデータを受け取り、
         *   標準出力へ結果を書く
         * だけなのに、
         * 実際には親プログラムと対話しているように見える。
         */
        execlp("sh", "/bin/sh", "-c", command, (char *)NULL);

        /*
         * 元コードでは exec 失敗時の perror + exit がない。
         * 本来はここにエラー処理が欲しい。
         */
    }

    /*
     * ここから下は親プロセス側である。
     *
     * 親は fd[1] を使って子と通信する。
     * 子側は fd[0] を stdin/stdout に結びつけているので、
     * 親に必要なのは fd[1] だけである。
     */

    /*
     * close(fd[0]);
     *
     * 親では fd[0] 側は使わないので閉じる。
     *
     * これにより、親は
     *
     *   fd[1] だけを持って子と通信する
     *
     * 形になる。
     *
     * 図にすると、
     *
     *   親: fd[1]
     *   子: stdin/stdout <- fd[0]
     *
     * という役割分担が完成する。
     */
    close(fd[0]);

    /*
     * return fd[1];
     *
     * 親プロセスは、自分が子と通信するための fd を呼び出し元へ返す。
     *
     * この戻り値 fd に対して
     *
     *   write(fd, ...)
     *
     * すると子の stdin へ送られ、
     *
     *   read(fd, ...)
     *
     * すると子の stdout から返ってきたデータを受け取れる。
     *
     * つまりこの fd は、
     *   「子コマンドと双方向会話するための窓口」
     * である。
     */
    return fd[1];
}

int main(void)
{
    /*
     * buf:
     *   子プロセス（ここでは sort）が返してくるデータを受け取るためのバッファ。
     *
     * n:
     *   read() が実際に読んだバイト数を受け取る変数。
     *
     * fd:
     *   popen2_socketpair() が返す、子と通信するための fd。
     */
    char buf[1024];
    int n, fd;

    /*
     * fd = popen2_socketpair("sort");
     *
     * sort コマンドを子プロセスとして起動し、
     * その stdin/stdout と双方向にやりとりできる fd を受け取る。
     *
     * この時点で概念的には、
     *
     *   親 fd <=====> 子 sort
     *                   stdin/stdout
     *
     * という接続が完成している。
     *
     * つまり親はこの fd に対して
     *   - write() で sort へ入力を送る
     *   - read() で sort の結果を受け取る
     * ことができる。
     *
     * 元コードでは fd == -1 の失敗チェックをしていない。
     * 実務コードなら確認したい。
     */
    fd = popen2_socketpair("sort");

    /*
     * write(fd, "orange\n", 7);
     * write(fd, "apple\n",  6);
     * write(fd, "banana\n", 7);
     *
     * 親から sort の標準入力へ 3 行の文字列を送る。
     *
     * ここで送っているデータは
     *
     *   orange
     *   apple
     *   banana
     *
     * の 3 行である。
     *
     * sort は標準入力から行を読み取り、
     * 行単位でソートして標準出力へ返すコマンドなので、
     * これら 3 行を全部送ったあと、
     * 子側では辞書順で並べ替えが行われる。
     *
     * 期待される出力順は通常
     *
     *   apple
     *   banana
     *   orange
     *
     * になる。
     *
     * 重要:
     *   ここでは複数回 write() しているが、
     *   socketpair はストリームなので、
     *   子側から見ると「バイト列の連続」として届く。
     *
     *   ただし sort は改行区切りで入力を解釈するので、
     *   行として正しく処理できる。
     *
     * 元コードでは write() の戻り値チェックを省略している。
     */
    write(fd, "orange\n", 7);
    write(fd, "apple\n",  6);
    write(fd, "banana\n", 7);

    /*
     * shutdown(fd, SHUT_WR);
     *
     * ここが非常に重要である。
     *
     * 親は「もうこれ以上 sort に入力を送らない」ことを
     * 子へ通知したい。
     *
     * しかしこの fd は双方向ソケットなので、
     * 完全に close(fd) してしまうと
     *
     *   - 書くこともできなくなる
     *   - 読むこともできなくなる
     *
     * つまり子から返ってくる結果を読む前に接続そのものを失ってしまう。
     *
     * そこで shutdown(fd, SHUT_WR) を使う。
     *
     * SHUT_WR の意味:
     *   「このソケットでは、こちら側からの書き込みだけを終了する」
     *
     * すると、
     *   - 親 -> 子 方向の送信は終了
     *   - でも 子 -> 親 方向の受信はまだ可能
     *
     * となる。
     *
     * 図にするとこうである。
     *
     *   shutdown 前:
     *     親 <=====> 子
     *
     *   shutdown(fd, SHUT_WR) 後:
     *     親 -X-> 子   （送信終了）
     *     親 <-  子   （受信は継続）
     *
     * これが sort のようなフィルタ型コマンドには非常に大事である。
     *
     * sort は EOF が来るまで
     *   「まだ次の行が来るかもしれない」
     * と考えて入力を待つ。
     *
     * つまり親が「入力終わり」を伝えないと、
     * sort は処理完了せず結果を返さない可能性がある。
     *
     * したがってこの shutdown は、
     *
     *   「入力の終端を通知しつつ、出力を受け取るための接続は残す」
     *
     * 操作である。
     */
    shutdown(fd, SHUT_WR);

    /*
     * while (1) { ... }
     *
     * ここからは子プロセス（sort）が返してくる出力を
     * EOF まで読み続けるループである。
     *
     * 親はすでに入力送信を終えたので、
     * あとは sort が処理結果を stdout に書くのを受け取るだけでよい。
     */
    while (1) {
        /*
         * n = read(fd, buf, sizeof(buf));
         *
         * 子プロセスの stdout 側から返ってきたデータを読む。
         *
         * read() の戻り値の意味:
         *   n > 0  : 実際に読めたバイト数
         *   n == 0 : EOF（相手側も出力を終えて接続が閉じられた）
         *   n < 0  : エラー
         *
         * ここでは EOF かエラーならループを抜ける。
         */
        n = read(fd, buf, sizeof(buf));

        /*
         * n <= 0 なら終了。
         *
         * n == 0:
         *   sort がすべての出力を書き終え、
         *   こちらがそれを読み切ったことを意味する。
         *
         * n < 0:
         *   エラーである。
         *
         * 元コードではエラーと EOF を区別せず break している。
         * 実務コードなら perror などで区別したい。
         */
        if (n <= 0) break;

        /*
         * write(1, buf, n);
         *
         * 読んだ結果をそのまま標準出力へ流す。
         *
         * 1 は STDOUT_FILENO と同じで、通常は端末を意味する。
         *
         * したがって最終的に画面には、
         * sort が返したソート済み結果が表示される。
         *
         * 期待される見た目は通常
         *
         *   apple
         *   banana
         *   orange
         *
         * となる。
         *
         * ここでは文字列関数ではなく write() を使って、
         * 読んだバイト数 n だけを正確に出力している。
         */
        write(1, buf, n);
    }

    /*
     * close(fd);
     *
     * 最後に通信 fd を閉じる。
     *
     * ここまで来た時点では
     *   - こちらの送信は shutdown で終わっている
     *   - 相手からの出力も読み終わっている
     * ので、もうこの通信路は不要である。
     *
     * したがって close(fd) して資源を解放する。
     */
    close(fd);

    /*
     * 元コードでは明示的な return 0; は書いていないが、
     * main の末尾まで来れば終了する。
     *
     * 明示すると読みやすい。
     */
}
