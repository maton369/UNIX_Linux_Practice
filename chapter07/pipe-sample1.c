#include <stdio.h>      /* perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */
#include <sys/types.h>  /* read, write に関係する型定義が入ることがある */
#include <sys/uio.h>    /* read, write の宣言が入ることがある */
#include <unistd.h>     /* pipe, read, write, close, STDOUT_FILENO を使うためのヘッダ */

/*
 * このプログラムの主題は「パイプ (pipe) によるプロセス内データ受け渡しの基本」である。
 *
 * UNIX / Linux の pipe は、
 *
 *   「書き込み口」と「読み取り口」が対になった一方向通信路
 *
 * である。
 *
 * イメージとしては、
 *
 *   書き込み側  ---->  パイプの中  ---->  読み取り側
 *
 * のような流れになっている。
 *
 * 本来 pipe は親子プロセス間通信などで非常によく使われるが、
 * このコードではまず最も単純な形として
 *
 *   「同じプロセスが pipe に書いて、自分で pipe から読む」
 *
 * という使い方をしている。
 *
 * つまりこのプログラムは、
 *
 *   1. pipe() で通信路を作る
 *   2. その書き込み口へ "Hello\n" を書く
 *   3. その読み取り口からデータを読む
 *   4. 読んだ内容を標準出力へ出す
 *
 * という流れを示す最小例である。
 *
 * 重要なのは、
 *   pipe はメモリバッファのように見えても、
 *   使い方としては「ファイルディスクリプタを介したストリーム」
 * だという点である。
 *
 * つまり read()/write() で扱える。
 *
 * このコードは、
 *
 *   「pipe は read/write で扱える一方向のデータ通路である」
 *
 * ことを示している。
 */

int main(void)
{
    /*
     * fd[2]:
     *   pipe() が返す 2 本のファイルディスクリプタを格納する配列である。
     *
     * pipe(fd) に成功すると、
     *
     *   fd[0] = 読み取り口
     *   fd[1] = 書き込み口
     *
     * になる。
     *
     * つまりこの 2 本で 1 本のパイプを表す。
     *
     * 図にするとこうである。
     *
     *   fd[1] --書く--> [ pipe buffer ] --読む--> fd[0]
     *
     * nbytes:
     *   read() が実際に読んだバイト数を受け取る変数。
     *
     *   read() は「何バイト読めたか」を戻り値で返すので、
     *   それを保存してあとで write() に渡す。
     *
     * buf:
     *   pipe から読んだデータを一時的に格納するバッファ。
     *
     *   ここでは 1024 バイト確保しているが、
     *   実際にこの例で流すのは "Hello\n" の 6 バイトだけである。
     */
    int fd[2], nbytes;
    char buf[1024];

    /*
     * pipe(fd)
     *
     * 新しいパイプを作る。
     *
     * 成功すると、
     *
     *   fd[0] = 読み取り用ファイルディスクリプタ
     *   fd[1] = 書き込み用ファイルディスクリプタ
     *
     * がセットされる。
     *
     * 失敗時は -1 を返す。
     *
     * 失敗理由の例:
     *   - プロセスが持てるファイルディスクリプタ数の上限に達した
     *   - システム全体の資源不足
     *
     * アルゴリズムの観点では、ここが
     *
     *   「通信路の生成」
     *
     * に相当する。
     */
    if (pipe(fd) == -1) {
        /*
         * pipe() 失敗時のエラー処理。
         *
         * perror("pipe") により errno に応じた理由を表示する。
         *
         * 例:
         *   pipe: Too many open files
         *
         * など。
         */
        perror("pipe");
        exit(1);
    }

    /*
     * write(fd[1], "Hello\n", 6);
     *
     * パイプの書き込み口 fd[1] に 6 バイト書く。
     *
     * ここで書いている文字列は
     *
     *   "Hello\n"
     *
     * であり、文字数は
     *
     *   H e l l o \n
     *
     * の 6 バイトである。
     *
     * 重要:
     *   ここでは文字列終端 '\0' は書いていない。
     *
     *   つまり流しているのは C 文字列全体ではなく、
     *   単なる 6 バイトのデータ列である。
     *
     * 図にするとこうである。
     *
     *   write(fd[1], "Hello\n", 6)
     *          |
     *          v
     *   パイプ内部バッファ:
     *     [ H ][ e ][ l ][ l ][ o ][ \n ]
     *
     * この段階では、まだ標準出力には何も表示されていない。
     * 単にパイプの中にデータが入っただけである。
     *
     * 注意:
     *   元コードでは write() の戻り値チェックをしていない。
     *   実務コードなら
     *
     *     if (write(...) != 6) { ... }
     *
     * のように確認したい。
     */
    write(fd[1], "Hello\n", 6);

    /*
     * nbytes = read(fd[0], buf, sizeof(buf));
     *
     * パイプの読み取り口 fd[0] からデータを読む。
     *
     * 第1引数:
     *   fd[0]
     *   -> パイプの読み取り口
     *
     * 第2引数:
     *   buf
     *   -> 読み出したデータを入れる先
     *
     * 第3引数:
     *   sizeof(buf)
     *   -> 最大 1024 バイトまで読める
     *
     * しかし実際には、いまパイプに入っているのは 6 バイトだけなので、
     * この read() は通常 6 を返す。
     *
     * つまり read 後の状態は概念的に
     *
     *   nbytes = 6
     *   buf[0..5] = "Hello\n"
     *
     * となる。
     *
     * 図にするとこうである。
     *
     *   パイプ内部:
     *     [ H ][ e ][ l ][ l ][ o ][ \n ]
     *
     *          |
     *          v
     *
     *   read(fd[0], buf, 1024)
     *
     *          |
     *          v
     *
     *   buf:
     *     [ H ][ e ][ l ][ l ][ o ][ \n ] ...
     *
     * 重要:
     *   read() は C 文字列を読む関数ではないので、
     *   buf の末尾に '\0' を自動では付けない。
     *
     *   したがって文字列関数で扱うなら自分で終端を付ける必要がある。
     *
     * このコードでは後で write() を使って「読んだバイト数だけ」そのまま出すので、
     * '\0' は不要である。
     *
     * また、read() の戻り値の意味は次の通り。
     *
     *   >0 : 読んだバイト数
     *    0 : EOF
     *   -1 : エラー
     *
     * この例では通常 6 が返る。
     *
     * 注意:
     *   元コードでは read() のエラー判定をしていない。
     *   実務コードなら nbytes == -1 を確認したい。
     */
    nbytes = read(fd[0], buf, sizeof(buf));

    /*
     * write(STDOUT_FILENO, buf, nbytes);
     *
     * さきほど pipe から読んだデータを、
     * 今度は標準出力へそのまま書く。
     *
     * STDOUT_FILENO は通常 1 であり、
     * 標準出力（普通は端末画面）を表す。
     *
     * つまりここでは
     *
     *   「pipe から取り出したデータ列を、そのまま画面へ流す」
     *
     * ことになる。
     *
     * nbytes をそのまま使っているのが重要で、
     * これにより
     *
     *   実際に read() が何バイト読んだか
     *
     * だけを正確に出力できる。
     *
     * もしここで文字列関数前提にしてしまうと、
     * '\0' 終端が無いデータで問題が起こりうる。
     *
     * しかしこのコードは
     *
     *   read() で得た「生バイト列」を
     *   write() でそのまま流す
     *
     * という対称的な構造なので安全である。
     *
     * 図にするとこうである。
     *
     *   pipe -> buf -> stdout
     *
     * つまりプログラム全体のデータフローは
     *
     *   "Hello\n"
     *      |
     *      v
     *   pipe(fd[1])
     *      |
     *      v
     *   pipe buffer
     *      |
     *      v
     *   read(fd[0], buf, ...)
     *      |
     *      v
     *   write(STDOUT_FILENO, buf, nbytes)
     *
     * となる。
     *
     * 注意:
     *   ここも戻り値チェックは省略されている。
     */
    write(STDOUT_FILENO, buf, nbytes);

    /*
     * close(fd[0]); close(fd[1]);
     *
     * パイプの読み取り口と書き込み口を両方閉じる。
     *
     * pipe() は 2 本のファイルディスクリプタを新しく消費するので、
     * 使い終わったら close() して資源を返す必要がある。
     *
     * ここでは
     *
     *   fd[0] = 読み取り口
     *   fd[1] = 書き込み口
     *
     * の両方を閉じている。
     *
     * 特に親子プロセス間通信では、
     * 不要な端を早めに閉じることが EOF 判定やデッドロック回避に重要になる。
     *
     * このサンプルでは同一プロセス内で完結しているため、
     * 最後にまとめて閉じている。
     *
     * 図:
     *
     *   利用前:
     *     fd[0] 読み取り口 有効
     *     fd[1] 書き込み口 有効
     *
     *   close 後:
     *     両方とも無効
     *
     * これでパイプに関する資源は解放される。
     */
    close(fd[0]);
    close(fd[1]);

    /*
     * 元コードでは return 文が書かれていないが、
     * main の末尾まで来れば C99 以降では 0 を返したのと同様に扱われることが多い。
     *
     * ただし明示的に
     *
     *   return 0;
     *
     * と書く方が読みやすい。
     */
}
