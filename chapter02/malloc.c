#include <stdio.h>   /* perror, printf を使うためのヘッダ */
#include <stdlib.h>  /* malloc, free, exit を使うためのヘッダ */

/*
 * このコードの主題は、
 *   - malloc による動的メモリ確保
 *   - NULL チェック
 *   - ポインタ経由での値の読み書き
 *   - free による解放
 * である。
 *
 * 特に重要なのは、
 * 「int 型の変数を通常のローカル変数として置く」のではなく、
 * 「実行時に必要になった分だけヒープ領域に int 1個分のメモリを確保し、
 *  その領域をポインタで扱う」
 * という流れである。
 *
 * C 言語では malloc は
 *   「必要なバイト数だけヒープからメモリを確保し、
 *    その先頭アドレスを返す」
 * 関数である。
 *
 * したがって malloc 自体が int を返すわけではなく、
 * 「int を置けるだけの場所の先頭アドレス」を返す。
 *
 * このコード全体の流れは次の通りである。
 *
 *   1. int 1個分のメモリをヒープに確保する
 *   2. 確保に失敗していないか確認する
 *   3. 確保した領域に 99 を書き込む
 *   4. その値を読み出して表示する
 *   5. 最後に解放する
 *
 * 低レイヤー的には、
 *   「アドレスを受け取り、その先のメモリを int として使う」
 * というポインタ操作の基本例でもある。
 */

int main(void)
{
    /*
     * malloc(sizeof(int))
     *
     * sizeof(int) は、int 型1個を格納するのに必要なバイト数を表す。
     * 多くの環境では 4 バイトであることが多いが、C 言語規格上は実装依存である。
     *
     * malloc(sizeof(int)) は、その int 1個分のサイズだけヒープ領域を確保し、
     * その先頭アドレスを返す。
     *
     * ここで返ってくる値は「場所」であって「中身」ではないため、
     * それを受ける変数 p の型は int *（int へのポインタ）になる。
     *
     * つまり p は、
     *   「int 型の値が置かれている場所を指す変数」
     * である。
     *
     * イメージ:
     *
     *   p -----> [ int 1個分の未初期化領域 ]
     *
     * 注意:
     *   malloc が返すメモリの中身は初期化されていない。
     *   したがって、代入する前に *p を読んではいけない。
     */
    int *p = malloc(sizeof(int));

    /*
     * malloc はメモリ確保に失敗すると NULL を返す。
     *
     * NULL は「どこも指していない特別なポインタ値」である。
     * したがって p == NULL なら、
     *   「int を置くための領域を確保できなかった」
     * ことを意味する。
     *
     * このチェックをせずに *p = 99; のような処理へ進むと、
     * NULL を間接参照することになり、クラッシュや未定義動作の原因になる。
     *
     * そのため、malloc の直後に NULL チェックを書くのは非常に重要である。
     */
    if (p == NULL) {
        /*
         * perror("malloc")
         *
         * 直近のエラー内容を errno に基づいて人間向け文字列で表示する。
         * ここでは「malloc に失敗した」という文脈でエラーを出したいので、
         * ラベルとして "malloc" を付けている。
         *
         * 典型的には次のような出力になることがある。
         *
         *   malloc: Cannot allocate memory
         *
         * これは標準エラー出力(stderr)へ出力される。
         */
        perror("malloc");

        /*
         * exit(1)
         *
         * 異常終了を表す終了コード 1 でプログラムを終了する。
         * ここで続行しても安全に処理できないため、即座に終了するのが妥当である。
         *
         * UNIX/Linux では一般に
         *   0   : 正常終了
         *   0以外: 異常終了
         * という慣習がある。
         */
        exit(1);
    }

    /*
     * *p = 99;
     *
     * p は「int 型の値が置かれている場所」を指している。
     * *p は、その場所にある「実際の int の中身」を表す。
     *
     * したがって
     *
     *   *p = 99;
     *
     * は
     *
     *   「p が指しているヒープ上の int 領域に 99 を書き込む」
     *
     * という意味である。
     *
     * イメージ:
     *
     *   p -----> [ 99 ]
     *
     * ここで初めて、その未初期化領域に意味のある値が入る。
     */
    *p = 99;

    /*
     * printf("%d\n", *p);
     *
     * ここでは、p が指している先の int 値を読み出して表示している。
     *
     * 先ほど *p = 99; と書き込んだので、
     * 今 *p を読むと 99 が得られる。
     *
     * したがって出力は典型的には
     *
     *   99
     *
     * となる。
     *
     * 重要なのは、
     *   p 自体を表示しているのではなく、
     *   p が指している先の値を表示している
     * という点である。
     *
     * - p   : アドレス
     * - *p  : そのアドレス先にある int 値
     *
     * という違いを意識する必要がある。
     */
    printf("%d\n", *p);

    /*
     * free(p)
     *
     * p が指しているヒープ領域を解放する。
     *
     * ここで解放されるのは
     *   p というポインタ変数そのもの
     * ではなく、
     *   p が指している先の動的メモリ
     * である。
     *
     * free を呼んだ後、その領域はもうこのプログラムが使ってよい領域ではない。
     * したがって、その後に
     *
     *   *p を読む
     *   *p に書く
     *   再度 free(p) する
     *
     * といったことは危険である。
     *
     * free の後の p は古いアドレス値を保持したままになるため、
     * ぶらぶらポインタ (dangling pointer) になる。
     *
     * より安全にするなら、free 後に
     *
     *   p = NULL;
     *
     * としておくと誤用を減らせる。
     */
    free(p);

    /*
     * 教育的・実務的には次の1行を追加することも多い。
     *
     *   p = NULL;
     *
     * ただしこの小さなサンプルでは、この後 p を使っていないので、
     * 現状でも実害はない。
     */

    /*
     * 正常終了。
     */
    return 0;
}
