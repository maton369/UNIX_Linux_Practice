#include <stdio.h>  /* printf を使うためのヘッダ */

/*
 * このコードの主題は「関数ポインタ」である。
 *
 * 関数ポインタとは、
 *   「関数そのもの」ではなく、
 *   「関数の場所（アドレス）」を保持できるポインタ
 * である。
 *
 * 普通のポインタが
 *   int の場所
 *   char の場所
 * などを指すのに対して、
 * 関数ポインタは
 *   実行可能コードとして配置された関数
 * を指す。
 *
 * そのため、関数ポインタを使うと
 *   - 実行時にどの関数を呼ぶか切り替える
 *   - コールバックを渡す
 *   - 処理の種類ごとに関数を差し替える
 * といったことができる。
 *
 * このコードでは、
 *   1. add という加算関数
 *   2. mul という乗算関数
 * を用意し、
 *
 *   int (*func)(int, int);
 *
 * という関数ポインタに
 *   add を入れたり
 *   mul を入れたり
 * して、
 * 「同じ func(...) という呼び出し方で、実際の処理だけを切り替える」
 * ことを示している。
 */

/*
 * add:
 *   2つの int を受け取り、その和を返す通常の関数である。
 *
 * 引数:
 *   x, y
 *
 * 戻り値:
 *   x + y
 *
 * この関数の型は概念的には
 *
 *   int (int, int)
 *
 * である。
 *
 * つまり
 *   - int を返す
 *   - 引数として int を2個取る
 * 関数である。
 *
 * 関数ポインタ func には、この型に一致する関数しか代入できない。
 */
int add(int x, int y)
{
    return x + y;
}

/*
 * mul:
 *   2つの int を受け取り、その積を返す通常の関数である。
 *
 * add と同様に、
 *   - int を返す
 *   - 引数は int, int
 * なので、関数ポインタ func に代入できる。
 *
 * つまり add と mul は
 * 「処理内容は違うが、関数としての型は同じ」
 * という関係にある。
 *
 * これが関数ポインタで差し替えられる条件である。
 */
int mul(int x, int y)
{
    return x * y;
}

int main(void)
{
    /*
     * int (*func)(int, int);
     *
     * これは関数ポインタの宣言である。
     *
     * 見た目が少し難しいが、内側から順に読むと理解しやすい。
     *
     *   func
     *     という名前の
     *
     *   *func
     *     ポインタであり
     *
     *   (*func)(int, int)
     *     int, int を引数に取る関数を指し
     *
     *   int (*func)(int, int)
     *     その関数は int を返す
     *
     * という意味になる。
     *
     * つまり func は
     *
     *   「int を返し、引数として int を2個受け取る関数」
     *
     * を指せるポインタである。
     *
     * 普通の変数でたとえると、
     *
     *   int x;
     *
     * は「int 型の値を入れる箱」だが、
     *
     *   int (*func)(int, int);
     *
     * は「その型の関数のアドレスを入れる箱」である。
     *
     * イメージ:
     *
     *   func -----> add
     *
     * または
     *
     *   func -----> mul
     *
     * のように使う。
     */
    int (*func)(int, int);

    /*
     * func = add;
     *
     * add 関数のアドレスを関数ポインタ func に代入している。
     *
     * C では関数名 add は、この文脈では
     * 「その関数の先頭アドレス」のように扱われる。
     *
     * 厳密には &add と書いてもよいが、
     * 関数ポインタへの代入では通常 add だけで十分である。
     *
     * この代入の後、func は add を指す。
     *
     * イメージ:
     *
     *   func -----> add(x, y) { return x + y; }
     */
    func = add;

    /*
     * printf("%d\n", func(10, 20));
     *
     * ここでは関数ポインタ func を通じて関数を呼び出している。
     *
     * 今 func は add を指しているので、
     *
     *   func(10, 20)
     *
     * は実質的に
     *
     *   add(10, 20)
     *
     * と同じ意味になる。
     *
     * したがって結果は
     *
     *   10 + 20 = 30
     *
     * であり、30 が表示される。
     *
     * 関数ポインタの呼び出しは
     *
     *   (*func)(10, 20)
     *
     * と明示的に書くこともできる。
     * しかし C では
     *
     *   func(10, 20)
     *
     * と書いても同じ意味で解釈される。
     *
     * つまり、この行は
     *   「func が現在指している関数を 10,20 で呼び出す」
     * という意味である。
     */
    printf("%d\n", func(10, 20));

    /*
     * func = mul;
     *
     * 今度は func が指す先を add から mul へ切り替えている。
     *
     * ここで重要なのは、
     * func 自体の型は変わっていないことだが、
     * 「どの関数を指すか」だけが実行時に変更されている点である。
     *
     * つまり、
     *
     *   さっきまで func -----> add
     *
     * だったのが、
     *
     *   今は func -----> mul
     *
     * になる。
     *
     * これが関数ポインタの大きな利点であり、
     * 同じ呼び出し構文で処理内容を差し替えられる。
     */
    func = mul;

    /*
     * printf("%d\n", func(10, 20));
     *
     * 今 func は mul を指しているので、
     *
     *   func(10, 20)
     *
     * は実質的に
     *
     *   mul(10, 20)
     *
     * を意味する。
     *
     * したがって結果は
     *
     *   10 * 20 = 200
     *
     * であり、200 が表示される。
     *
     * このように、呼び出し側は
     *
     *   func(10, 20)
     *
     * としか書いていないのに、
     * 実際に呼ばれる関数は事前の代入によって変わる。
     *
     * これが
     *   「関数を値のように扱う」
     * 感覚に近い部分である。
     */
    printf("%d\n", func(10, 20));

    /*
     * 正常終了。
     */
    return 0;
}

/*
 * このコードから学ぶべきことのまとめ
 *
 * 1. 関数ポインタは「関数のアドレス」を保持する
 *
 *    普通のポインタがデータを指すのに対し、
 *    関数ポインタは関数を指す。
 *
 * 2. 関数ポインタには「型」がある
 *
 *    このコードの func は
 *
 *      int を返し
 *      int, int を引数に取る関数
 *
 *    だけを指せる。
 *
 * 3. 同じ型の関数なら差し替えられる
 *
 *    add と mul は処理内容は違うが、関数型が同じなので
 *    同じ func に代入できる。
 *
 * 4. 呼び出し構文は普通の関数呼び出しに近い
 *
 *      func(10, 20)
 *
 *    と書くだけで、func が指す関数を呼べる。
 *
 * 5. 実行時に処理を切り替えられる
 *
 *    これがコールバックや戦略切り替えの基本になる。
 *
 *
 * 実務での典型例:
 *
 * - qsort の比較関数
 * - シグナルハンドラ
 * - GUI やイベント処理のコールバック
 * - OS / ドライバでの関数テーブル
 * - 仮想関数テーブル風の設計
 *
 *
 * 図でまとめると次のようになる。
 *
 *   初期状態:
 *     func は未設定
 *
 *   func = add;
 *     func -----> add
 *
 *   func(10, 20)
 *     ↓
 *     add(10, 20)
 *     ↓
 *     30
 *
 *   func = mul;
 *     func -----> mul
 *
 *   func(10, 20)
 *     ↓
 *     mul(10, 20)
 *     ↓
 *     200
 *
 *
 * このコードの本質を一言で言うと、
 *
 *   「同じ型の複数の関数を、1つの関数ポインタ変数を通じて切り替えて呼び出している」
 *
 * である。
 */
