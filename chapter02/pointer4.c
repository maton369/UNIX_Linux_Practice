#include <stdio.h>   /* printf, snprintf を使うためのヘッダ */
#include <stdlib.h>  /* malloc, free を使うためのヘッダ */

/*
 * このコードの主題は、
 *   - 動的メモリ確保
 *   - 関数から文字列を返す方法
 *   - free 後のポインタの扱い
 *   - ぶらぶらポインタ (dangling pointer)
 * である。
 *
 * 特に重要なのは「ぶらぶらポインタ」である。
 *
 * ぶらぶらポインタとは:
 *   かつては有効なメモリ領域を指していたが、
 *   その領域が free などによって解放されたあとも、
 *   まだ古いアドレス値を保持したままになっているポインタ
 * のことである。
 *
 * 例えば、
 *   char *p = malloc(...);
 *   free(p);
 *
 * とした直後の p は、自動的に NULL にはならない。
 * 依然として「昔その領域があったアドレス値」を持っている。
 * しかし、その領域はもう自分のものではないので、
 *   *p を読む
 *   p[0] に書く
 *   printf("%s", p) する
 * などは未定義動作につながる。
 *
 * これが「ぶらぶらポインタ」である。
 *
 * このコードは、free のあとに p を使っていないので、
 * その点では安全である。
 * ただし、free 後に p を NULL にしないと、
 * 将来コードを追加したときに誤用の余地が残る。
 *
 * そのため学習上は、
 *
 *   free(p);
 *   p = NULL;
 *
 * まで書く習慣を付けると分かりやすい。
 */

/*
 * itoa:
 *   整数 i を文字列へ変換し、その文字列を格納した
 *   動的確保メモリへのポインタを返す関数である。
 *
 * 注意:
 *   これは標準Cの itoa ではなく、このコード内で独自に定義した関数である。
 *
 * 返り値:
 *   malloc で確保した文字列バッファへのポインタ
 *
 * 呼び出し側の責任:
 *   この関数が返したポインタはヒープ上の動的メモリを指すので、
 *   使い終わったら呼び出し側が free しなければならない。
 *
 * ここが重要である。
 * もしローカル配列
 *
 *   char buf[100];
 *   return buf;
 *
 * のようにして返すと、その配列は関数終了とともに寿命が尽きるため、
 * 返されたポインタは不正になる。
 * それも一種の危険なポインタだが、
 * 今回のコードでは malloc を使っているため、その問題は避けている。
 */
char *itoa(int i)
{
    /*
     * snprintf(NULL, 0, "%d", i)
     *
     * snprintf は通常「文字列バッファへ整形出力する」関数だが、
     * 第1引数を NULL、第2引数を 0 にすると、
     * 「実際には書き込まず、何文字必要かだけを計算する」
     * という使い方ができる。
     *
     * "%d" は整数を10進文字列として出力する書式である。
     *
     * 例えば i = 1234 なら、
     *   "1234"
     * という4文字が必要なので、n には 4 が入る。
     *
     * ここでの戻り値 n は「終端の '\0' を除いた文字数」である。
     */
    int n = snprintf(NULL, 0, "%d", i);

    /*
     * malloc(n + 1)
     *
     * 文字列には最後に '\0' (ヌル終端) が必要なので、
     * 必要文字数 n に加えて 1 バイト余分に確保する。
     *
     * たとえば "1234" は見た目は4文字だが、
     * C文字列としては
     *
     *   '1' '2' '3' '4' '\0'
     *
     * の 5 バイト必要になる。
     *
     * malloc はヒープ領域から必要バイト数分のメモリを確保し、
     * その先頭アドレスを返す。
     *
     * 返ってくるポインタは有効な動的メモリを指しているが、
     * まだ中身は未初期化である。
     *
     * 注意:
     *   malloc が失敗すると NULL を返す。
     *   実務では NULL チェックを書くべきである。
     */
    char *buf = malloc(n + 1);

    /*
     * 本来はここで malloc 失敗を確認するのが安全である。
     *
     * 例:
     *   if (buf == NULL) return NULL;
     *
     * 今回の元コードは最小例なので省略しているが、
     * 実務では省略しない方がよい。
     */

    /*
     * snprintf(buf, n + 1, "%d", i)
     *
     * 今度は実際に buf へ文字列を書き込む。
     *
     * 第2引数に n + 1 を渡しているので、
     * 末尾の '\0' まで含めて安全に書き込める。
     *
     * これにより buf の中身は、
     *   i を10進表現したC文字列
     * になる。
     *
     * i = 1234 の場合:
     *
     *   buf[0] = '1'
     *   buf[1] = '2'
     *   buf[2] = '3'
     *   buf[3] = '4'
     *   buf[4] = '\0'
     */
    snprintf(buf, n + 1, "%d", i);

    /*
     * buf を返す。
     *
     * ここで返しているのはローカル配列ではなく、
     * malloc で確保したヒープメモリへのポインタである。
     *
     * そのため、関数を抜けてもこのメモリは有効であり、
     * 呼び出し元 main で安全に使える。
     *
     * ただし、その寿命管理責任は呼び出し元に移る。
     * つまり、呼び出し元は使い終わったら free しなければならない。
     */
    return buf;
}

int main(void)
{
    /*
     * itoa(1234) を呼び出し、その戻り値を p に受け取る。
     *
     * この時点で p は、
     *   "1234"
     * という文字列が入ったヒープメモリを指している。
     *
     * イメージ:
     *
     *   p -----> +-----+-----+-----+-----+------+
     *            | '1' | '2' | '3' | '4' | '\0' |
     *            +-----+-----+-----+-----+------+
     *
     * p 自体は main のローカル変数だが、
     * p が指している先の実体は malloc で確保されたヒープ領域である。
     */
    char *p = itoa(1234);

    /*
     * 実務では、itoa が NULL を返す可能性も考えてチェックした方が安全である。
     *
     * 例:
     *   if (p == NULL) {
     *       fprintf(stderr, "memory allocation failed\n");
     *       return 1;
     *   }
     */

    /*
     * p が指している C文字列を表示する。
     *
     * %s は、char * が指すヌル終端文字列を表示する。
     * したがって出力は典型的には
     *
     *   p=1234
     *
     * となる。
     */
    printf("p=%s\n", p);

    /*
     * free(p)
     *
     * p が指しているヒープメモリを解放する。
     *
     * ここで重要なのは、
     *   解放されるのは「p という変数」ではなく、
     *   「p が指している先の動的メモリ」
     * だという点である。
     *
     * free の後、その領域はもはやこのプログラムが使ってよい領域ではない。
     * OS やメモリアロケータの管理下に戻る。
     *
     * しかし p 自体の中身は自動では変わらない。
     * つまり free(p); の直後の p は、
     *   もう有効ではない領域の古いアドレス
     * を保持している。
     *
     * この状態の p が「ぶらぶらポインタ (dangling pointer)」である。
     */
    free(p);

    /*
     * ここで p はぶらぶらポインタになっている。
     *
     * ダメな例:
     *
     *   printf("%s\n", p);   // 解放済み領域を読むことになるかもしれない
     *   p[0] = 'X';          // 解放済み領域への書き込み
     *   free(p);             // 二重freeの危険
     *
     * これらは未定義動作やクラッシュの原因になる。
     *
     * そのため、free 直後に
     *
     *   p = NULL;
     *
     * としておくと、
     *   「もう指していない」
     * ことが明示でき、誤用防止になる。
     *
     * 特に大きいコードでは有効である。
     */

    /*
     * 学習用・実務用には次のように書くとさらに安全である。
     *
     *   free(p);
     *   p = NULL;
     *
     * NULL にしておけば、
     *   printf("%s", p)
     * のような誤用は依然として問題だが、
     *   free(p)
     * を再度呼んだ場合は free(NULL) が安全に無視されるため、
     * 二重 free の被害を減らせる。
     */
    p = NULL;

    /*
     * 正常終了。
     */
    return 0;
}
