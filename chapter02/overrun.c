#include <stdlib.h>  /* malloc を使うためのヘッダ */
#include <string.h>  /* strlen, strcpy を使うためのヘッダ */

/*
 * このコードの主題は「バッファオーバーラン (buffer overrun / buffer overflow)」である。
 *
 * バッファオーバーランとは、
 *   確保したメモリ領域（バッファ）の大きさを超えて
 *   読み書きしてしまうこと
 * を指す。
 *
 * 特に C 言語では、配列や動的確保領域に対して
 * 「ここまでが安全な範囲」という検査を自動ではしてくれないことが多いため、
 * プログラマ自身が
 *   - 何バイト必要か
 *   - 実際に何バイト確保したか
 *   - コピーするデータは何バイトあるか
 * を正しく一致させる必要がある。
 *
 * このコードでは、
 *
 *   char *s2 = "hello";
 *   char *s1 = malloc(strlen(s2));
 *   strcpy(s1, s2);
 *
 * という流れになっている。
 *
 * 一見すると、
 *   "hello" は 5 文字
 *   strlen("hello") も 5
 *   だから 5 バイト確保すればよさそう
 * と見えるが、ここに落とし穴がある。
 *
 * C の文字列は、見た目の文字列本体だけでなく、
 * 末尾に '\0'（ヌル文字、終端文字）を持つ必要がある。
 *
 * つまり "hello" はメモリ上では典型的に
 *
 *   'h' 'e' 'l' 'l' 'o' '\0'
 *
 * の 6 バイト必要である。
 *
 * しかしこのコードでは
 *
 *   malloc(strlen(s2))
 *
 * なので 5 バイトしか確保していない。
 *
 * その状態で strcpy(s1, s2) を行うと、
 * "hello" に加えて終端の '\0' までコピーしようとするため、
 * 6 バイト書こうとして 1 バイトはみ出す。
 *
 * これがバッファオーバーランである。
 *
 * この「1バイトだけなら大丈夫そう」という感覚は危険である。
 * たった 1 バイトのはみ出しでも未定義動作であり、
 * クラッシュ・メモリ破壊・セキュリティ脆弱性の原因になりうる。
 */

int main(void)
{
    /*
     * s2 は文字列リテラル "hello" を指すポインタである。
     *
     * 重要:
     *   "hello" は単なる 5 文字ではなく、
     *   C 文字列としては終端文字 '\0' を含む。
     *
     * したがって実際のメモリ上の並びは概念的には
     *
     *   +-----+-----+-----+-----+-----+------+
     *   | 'h' | 'e' | 'l' | 'l' | 'o' | '\0' |
     *   +-----+-----+-----+-----+-----+------+
     *
     * である。
     *
     * s2 はこの先頭を指している。
     *
     * なお、文字列リテラルは書き換えてはいけないので、
     * 本来は
     *
     *   const char *s2 = "hello";
     *
     * と書く方がより適切である。
     */
    char *s2 = "hello";

    /*
     * strlen(s2)
     *
     * strlen は C 文字列の長さを返すが、
     * 返す値には終端の '\0' は含まれない。
     *
     * つまり s2 = "hello" の場合、
     *
     *   strlen(s2) == 5
     *
     * になる。
     *
     * ここで注意すべきなのは、
     * 「文字列として必要な総バイト数」と
     * 「strlen が返す文字数」は違う
     * ということである。
     *
     * 必要な総バイト数は
     *
     *   strlen(s2) + 1
     *
     * である。
     *
     * なぜなら最後に '\0' を置く 1 バイトが必要だからである。
     *
     * しかしこのコードでは
     *
     *   malloc(strlen(s2))
     *
     * としているため、5 バイトしか確保していない。
     *
     * その結果、s1 が指す先の領域は概念的には
     *
     *   +-----+-----+-----+-----+-----+
     *   |  ?  |  ?  |  ?  |  ?  |  ?  |
     *   +-----+-----+-----+-----+-----+
     *
     * の 5 バイト分しかない。
     *
     * つまり "hello\0" を丸ごと入れるには 1 バイト足りない。
     */
    char *s1 = malloc(strlen(s2));

    /*
     * 元コードでは malloc の失敗チェックがない。
     *
     * 実務では、
     *
     *   if (s1 == NULL) { ... }
     *
     * のように確認する必要がある。
     *
     * ただし今回の主題は主にバッファオーバーランなので、
     * 一番重要なのは「サイズが 1 足りない」点である。
     */

    /*
     * strcpy(s1, s2)
     *
     * strcpy は、s2 が指す C 文字列を
     * 終端の '\0' も含めて s1 側へコピーする。
     *
     * つまり "hello" の場合、実際には次の 6 バイトを書こうとする。
     *
     *   'h' 'e' 'l' 'l' 'o' '\0'
     *
     * しかし s1 側に確保したのは 5 バイトだけである。
     *
     * そのため、書き込みは概念的にこうなる。
     *
     *   s1 の確保領域:
     *   +-----+-----+-----+-----+-----+
     *   |  ?  |  ?  |  ?  |  ?  |  ?  |
     *   +-----+-----+-----+-----+-----+
     *
     *   strcpy 後に書きたい内容:
     *   +-----+-----+-----+-----+-----+------+
     *   | 'h' | 'e' | 'l' | 'l' | 'o' | '\0' |
     *   +-----+-----+-----+-----+-----+------+
     *
     *                                          ^^^^^^
     *                                          この '\0' がはみ出す
     *
     * つまり最後の '\0' を書くときに、
     * 確保領域の外側へ 1 バイト書き込んでしまう。
     *
     * これがバッファオーバーランである。
     *
     * 重要:
     *   C 言語では、このはみ出しをランタイムが自動的に防いでくれるとは限らない。
     *   そのため、
     *     - たまたま動いているように見える
     *     - すぐクラッシュする
     *     - 別のデータを壊す
     *     - 後から別の場所でおかしくなる
     *   など、結果は不定である。
     *
     * これは未定義動作であり、極めて危険である。
     */
    strcpy(s1, s2);

    /*
     * 元コードでは free(s1) が無いので、
     * このまま終了すると動的確保領域は未解放のままになる。
     *
     * ただし今回の最重要問題はメモリリークではなく、
     * strcpy 時点ですでにバッファオーバーランを起こしている点である。
     *
     * つまり、このコードは「解放し忘れ」の前に
     * 「そもそも安全にコピーできていない」ことが問題である。
     */

    return 0;
}

/*
 * このコードの問題点まとめ
 *
 * 1. malloc(strlen(s2)) では '\0' の分が足りない
 *
 *    "hello" は 5 文字だが、
 *    C 文字列として格納するには
 *
 *      'h' 'e' 'l' 'l' 'o' '\0'
 *
 *    の 6 バイト必要である。
 *
 * 2. strcpy は '\0' も含めてコピーする
 *
 *    そのため、コピー先は strlen(s2) + 1 バイト必要である。
 *
 * 3. 結果として 1 バイトはみ出して書き込む
 *
 *    これはバッファオーバーランであり、未定義動作である。
 *
 * 4. free(s1) も無い
 *
 *    したがって、もしオーバーランを別にしても、未解放領域が残る。
 *
 *
 * 安全な書き方の例:
 *
 *   const char *s2 = "hello";
 *   char *s1 = malloc(strlen(s2) + 1);
 *   if (s1 == NULL) {
 *       return 1;
 *   }
 *   strcpy(s1, s2);
 *   free(s1);
 *
 *
 * より意識すべきポイント:
 *
 * - strlen は '\0' を含まない
 * - strcpy は '\0' までコピーする
 * - 文字列用バッファは「見た目の文字数 + 1」が基本
 *
 *
 * このコードの本質を一言で言うと、
 *
 *   「終端文字の 1 バイトを見落として、コピー先バッファを 1 バイト不足のまま使っている」
 *
 * である。
 */
