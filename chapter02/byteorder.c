#include <stdio.h>   /* printf を使うためのヘッダ */

/*
 * このコードの主題は「バイトオーダ (byte order)」である。
 *
 * バイトオーダとは、
 *   複数バイトで表現される整数値をメモリ上に並べるとき、
 *   「どのバイトを先頭アドレス側に置くか」
 * という規則のことである。
 *
 * 代表的には次の 2 つがある。
 *
 * 1. リトルエンディアン (little endian)
 *    - 下位バイトを低アドレス側に置く
 *    - 例えば 0x12345678 はメモリ上で
 *
 *        78 56 34 12
 *
 *      の順に並ぶ
 *
 * 2. ビッグエンディアン (big endian)
 *    - 上位バイトを低アドレス側に置く
 *    - 例えば 0x12345678 はメモリ上で
 *
 *        12 34 56 78
 *
 *      の順に並ぶ
 *
 * このコードは、
 *   int 型の値 0x12345678 をメモリ上に置き、
 *   その先頭アドレスを unsigned char * として見て、
 *   1バイトずつ観察する
 * ことで、実行環境のバイトオーダを確認するサンプルである。
 *
 * 重要な考え方:
 *   int i = 0x12345678;
 * は「数としては 0x12345678」という 1 つの値である。
 * しかしメモリ上では 4 バイト（典型例）に分けて格納される。
 *
 * その並び順は CPU / ABI に依存する。
 *
 * このコードは、その「メモリ上での実際の並び」を可視化している。
 */

int main(void)
{
    /*
     * int 型変数 i に 0x12345678 を代入する。
     *
     * ここでの 0x12345678 は 16進数表記であり、
     * バイト単位で分けると概念的には
     *
     *   0x12
     *   0x34
     *   0x56
     *   0x78
     *
     * という 4 つの 1 バイト値から構成される。
     *
     * ただし「数としての意味」と「メモリ上の並び」は別である。
     *
     * 数としては常に
     *
     *   0x12345678
     *
     * だが、メモリ上で
 *
     *   12 34 56 78
     *
     * と並ぶか
     *
     *   78 56 34 12
     *
     * と並ぶかは環境依存である。
     *
     * 多くの x86 / x86_64 Linux 環境ではリトルエンディアンなので、
     * メモリ上では典型的に
     *
     *   78 56 34 12
     *
     * の順になる。
     */
    int i = 0x12345678;

    /*
     * unsigned char *c = (unsigned char *)&i;
     *
     * ここがこのコードの核心である。
     *
     * まず
     *
     *   &i
     *
     * は変数 i のアドレスを意味する。
     *
     * つまり、
     *   「i がメモリ上のどこから始まっているか」
     * を表す。
     *
     * 本来 &i の型は int * である。
     * しかしここでは、それを
     *
     *   (unsigned char *)
     *
     * にキャストしている。
     *
     * なぜ unsigned char * にするのか:
     *
     * - char / unsigned char は 1 バイト単位でメモリを観察するのに適している
     * - c[0], c[1], c[2], c[3] のように書くことで、
     *   i を構成する各バイトを順に見られる
     *
     * つまりこの1行は、
     *
     *   「int として持っている値を、今度はバイト列として覗く」
     *
     * ための準備である。
     *
     * unsigned char を使う理由:
     *   もし単なる char だと、環境によっては符号付きとして解釈され、
     *   0x80 以上の値が負数っぽく見えることがある。
     *   unsigned char にしておけば、
     *   1 バイト値を 0〜255 の範囲の生の値として扱いやすい。
     *
     * イメージ:
     *
     *   i という int オブジェクト
     *   +----+----+----+----+
     *   | ?? | ?? | ?? | ?? |
     *   +----+----+----+----+
     *    ^
     *    |
     *   &i
     *
     *   c はその先頭を指す unsigned char * なので、
     *
     *   c[0] = 先頭バイト
     *   c[1] = 次のバイト
     *   c[2] = 次のバイト
     *   c[3] = 次のバイト
     *
     * を意味する。
     */
    unsigned char *c = (unsigned char *)&i;

    /*
     * printf("%x:%x:%x:%x\n", c[0], c[1], c[2], c[3]);
     *
     * ここでは i のメモリ内容を、1 バイトずつ 16 進数で表示している。
     *
     * c[0] は i の先頭アドレスに置かれた 1 バイト
     * c[1] はその次の 1 バイト
     * c[2] はその次の 1 バイト
     * c[3] はその次の 1 バイト
     *
     * を表す。
     *
     * したがって、この出力を見ることで
     * 「この環境では int の各バイトがどの順番で並んでいるか」
     * を直接確認できる。
     *
     * 典型例:
     *
     * 1. リトルエンディアン環境
     *
     *    数値: 0x12345678
     *    メモリ:
     *
     *      c[0] = 0x78
     *      c[1] = 0x56
     *      c[2] = 0x34
     *      c[3] = 0x12
     *
     *    出力例:
     *
     *      78:56:34:12
     *
     * 2. ビッグエンディアン環境
     *
     *    数値: 0x12345678
     *    メモリ:
     *
     *      c[0] = 0x12
     *      c[1] = 0x34
     *      c[2] = 0x56
     *      c[3] = 0x78
     *
     *    出力例:
     *
     *      12:34:56:78
     *
     * したがってこのプログラムは、
     * 実行結果を見るだけでバイトオーダを判断できる。
     *
     * 注意1:
     *   このコードは int が少なくとも 4 バイトであることを暗に想定している。
     *   多くの現在の環境ではそうだが、C 規格上は int のサイズは実装依存である。
     *
     * 注意2:
     *   %x で unsigned char の値を出しているが、
     *   可変長引数では整数拡張が起こるため、実際には int として渡される。
     *   このコードは実用上よく動く。
     *
     * 注意3:
     *   表示桁数を固定したければ %02x を使うと見やすい。
     *   たとえば 0x08 などでも 08 と2桁で表示できる。
     */
    printf("%x:%x:%x:%x\n", c[0], c[1], c[2], c[3]);

    /*
     * まとめ:
     *
     * このコードから学べることは次の通りである。
     *
     * 1. 複数バイトの整数値は、メモリ上では複数の 1 バイトに分かれて置かれる
     *
     * 2. その並び順は環境に依存し、代表的には
     *    - リトルエンディアン
     *    - ビッグエンディアン
     *    がある
     *
     * 3. int * を unsigned char * に変換すると、
     *    オブジェクトを 1 バイト単位で観察できる
     *
     * 4. 数値としての意味と、メモリ上の並びは区別して考える必要がある
     *
     * 特に低レイヤーでは、
     *   - ネットワーク通信
     *   - ファイルフォーマット
     *   - バイナリ解析
     *   - デバイスレジスタアクセス
     * などでバイトオーダが非常に重要になる。
     *
     * 例えばネットワークでは、一般にビッグエンディアン
     * （ネットワークバイトオーダ）が使われることが多い。
     * そのため、ホスト側がリトルエンディアンなら変換が必要になる。
     */

    return 0;
}
