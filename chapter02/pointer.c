#include <stdio.h>   /* printf を使うためのヘッダ */

/*
 * このコードの主題は「ポインタ」である。
 *
 * C言語におけるポインタとは、
 *   「ある値そのもの」ではなく、
 *   「その値がメモリ上のどこに置かれているか（アドレス）」を保持する変数
 * である。
 *
 * つまり、
 *   char c;
 * は「1文字を入れる箱」であり、
 *
 *   char *p;
 * は「char 型の値が置かれている場所を指す箱」
 * である。
 *
 * よくある直感的なたとえ:
 *
 *   c  = 実際の部屋
 *   p  = その部屋の住所メモ
 *
 * である。
 *
 * このプログラムでは、
 *   1. char 型変数 c を用意する
 *   2. そのアドレスをポインタ p に入れる
 *   3. p を通して c に 'A' を書き込む
 *   4. p を通して c の値を読む
 *
 * という流れで、
 * 「ポインタは間接的に変数へアクセスするための仕組み」
 * であることを示している。
 */

int main(void)
{
    /*
     * ここでは 2 つの変数を同時に宣言している。
     *
     *   char c;
     *     - 1文字を格納するための変数である。
     *     - まだこの時点では初期化されていないので、
     *       読み出してはいけない。
     *
     *   char *p = &c;
     *     - p は「char 型を指すポインタ」である。
     *     - * が付いているので「p 自体の型は char ではなくポインタ型」である。
     *     - &c は「c のアドレス」を意味する。
     *
     * ここで重要なのは、
     *
     *   c   は値そのものを入れる変数
     *   p   は c の場所（アドレス）を入れる変数
     *
     * という違いである。
     *
     * つまりこの1行の後、
     *   p は c を指している
     * と言う。
     *
     * イメージ:
     *
     *   c  : [ ? ]          ← char の箱
     *   p  : [ &c ]         ← c の住所が入っている
     *
     * また、
     *   &c
     * の & は「アドレス演算子」であり、
     * 「変数 c のメモリ上の番地を取り出す」働きを持つ。
     */
    char c, *p = &c;

    /*
     * *p = 'A';
     *
     * ここがポインタの最重要ポイントである。
     *
     * まず p は c のアドレスを持っている。
     * したがって *p は、
     *   「p が指している先にある char 型の実体」
     * を意味する。
     *
     * これを「間接参照」あるいは「デリファレンス」と呼ぶ。
     *
     * つまり、
     *
     *   *p = 'A';
     *
     * は
     *
     *   p が指している先の変数に 'A' を代入する
     *
     * という意味である。
     *
     * 今 p は c を指しているので、実際には
     *
     *   c = 'A';
     *
     * とほぼ同じ効果になる。
     *
     * ただし書き方としては、
     *   変数名 c を直接使わず、
     *   その変数を指すポインタ p 経由で書いている
     * という点が重要である。
     *
     * イメージ:
     *
     *   p -----> c
     *
     *   *p = 'A'
     *
     * を実行すると、
     *
     *   c の中身が 'A' になる。
     */
    *p = 'A';

    /*
     * printf("%c\n", *p);
     *
     * ここでは、p が指している先の値を文字として表示している。
     *
     * 再び *p が登場しているが、今回は左辺ではなく右辺にあるため、
     * 「代入先」ではなく「読み出す値」として使われている。
     *
     * つまり、
     *
     *   *p
     *
     * は
     *
     *   p が指している先に入っている char 型の値
     *
     * を表す。
     *
     * 先ほど *p = 'A'; を実行したので、
     * 今 *p を読むと 'A' が得られる。
     *
     * そのため出力は典型的には次のようになる。
     *
     *   A
     *
     * %c は「1文字として表示する」ための書式指定子である。
     *
     * なお、ここで
     *
     *   printf("%c\n", c);
     *
     * と書いても同じ結果になる。
     * なぜなら今は
     *
     *   c == *p
     *
     * だからである。
     *
     * しかしこのコードではあえて *p を使うことで、
     * 「ポインタ経由で書いた値を、ポインタ経由で読める」
     * ことを示している。
     */
    printf("%c\n", *p);

    /*
     * 正常終了を表す。
     */
    return 0;
}
