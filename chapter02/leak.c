#include <stdlib.h>  /* malloc を使うためのヘッダ */

/*
 * このコードの主題は「メモリリーク」である。
 *
 * メモリリーク (memory leak) とは、
 *   malloc などで確保した動的メモリへの参照を失い、
 *   そのメモリを free できなくなってしまうこと
 * を指す。
 *
 * 重要なのは、
 *   「メモリを確保した」こと自体が問題なのではなく、
 *   「その確保したメモリを後で解放するための手がかり
 *    （ポインタ）を失ってしまう」こと
 * が問題だという点である。
 *
 * このコードではまさにそれが起きている。
 *
 * 流れを先に言うと、
 *
 *   1. 1個目の int 用メモリを malloc で確保する
 *   2. その領域に 10 を書く
 *   3. free しないまま、同じ変数 p に 2個目の malloc の結果を代入する
 *   4. その瞬間、1個目のメモリを指すポインタを失う
 *   5. 1個目のメモリはもう free できず、メモリリークになる
 *
 * つまりこのコードの本質は、
 *   「ポインタ変数 p 自体を再利用したことで、
 *    以前に確保した領域への参照を上書きして失ってしまった」
 * という点にある。
 */

int main(void)
{
    /*
     * malloc(sizeof(int))
     *
     * int 型1個分の大きさだけヒープ領域を確保し、
     * その先頭アドレスを返す。
     *
     * p は int * 型なので、
     * 「int を置くための領域を指すポインタ」として使える。
     *
     * この時点のイメージ:
     *
     *   p -----> [ 未初期化の int 領域 ]
     *
     * 注意:
     *   元コードでは NULL チェックが省略されているが、
     *   実務では malloc が失敗して NULL を返す可能性があるため、
     *   本来は確認すべきである。
     */
    int *p = malloc(sizeof(int));

    /*
     * *p = 10;
     *
     * p が指している先の int 領域に 10 を書き込む。
     *
     * この時点のイメージ:
     *
     *   p -----> [ 10 ]
     *
     * ここではまだ問題はない。
     * p は有効な動的メモリを指しており、その中に 10 が入っている。
     */
    *p = 10;

    /*
     * ここがメモリリークの原因である。
     *
     * 再び malloc(sizeof(int)) を呼び、
     * 新しく確保した別のヒープ領域のアドレスを p に代入している。
     *
     * つまり、もともと p が指していた
     *
     *   [ 10 ]
     *
     * のアドレスを上書きしてしまう。
     *
     * 代入前:
     *
     *   p -----> [ 10 ]   ← 1個目の確保領域
     *
     * 代入後:
     *
     *   p -----> [ 未初期化の int 領域 ]   ← 2個目の確保領域
     *
     * このとき、1個目の領域を指していたアドレス値は
     * どこにも残っていない。
     *
     * つまり、
     *   1個目の領域を free するための手がかりを失った
     * ことになる。
     *
     * この状態がメモリリークである。
     *
     * 重要:
     *   リークしたメモリは「まだ確保されたまま」だが、
     *   もうプログラムから到達できない。
     *   したがって、そのメモリは事実上回収不能になる。
     *
     * このコードではさらに、2個目の領域も free していない。
     * そのため、終了時点では
     *
     *   - 1個目の領域: 参照喪失によるリーク
     *   - 2個目の領域: 未解放のまま終了
     *
     * となる。
     *
     * ただし、学習上特に注目すべきは
     * 「1個目の領域への参照を上書きで失った」
     * という点である。
     */
    p = malloc(sizeof(int));

    /*
     * このサンプルではここで終了する。
     *
     * もしこの後に free(p); を1回だけ書いても、
     * 解放されるのは 2個目の領域だけである。
     * 1個目の領域はすでにアドレスを失っているため、
     * 依然としてリークしたままになる。
     *
     * つまり、
     *
     *   int *p = malloc(sizeof(int));
     *   *p = 10;
     *   p = malloc(sizeof(int));
     *   free(p);
     *
     * としても、1個目は回収できない。
     *
     * これが「ポインタの上書きによるメモリリーク」の典型例である。
     */

    return 0;
}

/*
 * このコードの問題点まとめ
 *
 * 1. 最初の malloc の結果を free する前に上書きしている
 *    → 1個目の確保領域への参照を失う
 *
 * 2. malloc の戻り値 NULL チェックがない
 *    → 失敗時に *p = 10; で危険
 *
 * 3. 2回目の確保領域も free していない
 *    → これも未解放
 *
 *
 * 安全な書き方の例 1:
 *   使い終わってから再利用する
 *
 *   int *p = malloc(sizeof(int));
 *   if (p == NULL) return 1;
 *   *p = 10;
 *   free(p);
 *   p = malloc(sizeof(int));
 *   if (p == NULL) return 1;
 *   free(p);
 *
 *
 * 安全な書き方の例 2:
 *   別のポインタ変数を使う
 *
 *   int *p1 = malloc(sizeof(int));
 *   int *p2 = malloc(sizeof(int));
 *   ...
 *   free(p1);
 *   free(p2);
 *
 *
 * このコードから学ぶべきこと
 *
 * - malloc したら、必ず free まで責任を持つ
 * - ポインタ変数に新しいアドレスを代入するときは、
 *   以前の領域をもう解放したか確認する
 * - 「メモリを持っていること」より、
 *   「そのメモリへの参照を保っていること」が重要
 *
 * 低レイヤーでは、
 *   ポインタの値を上書きする
 * という一見単純な操作が、
 *   回収不能なヒープ領域を生む
 * ことがあるので注意が必要である。
 */
