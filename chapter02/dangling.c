#include <stdlib.h>  /* malloc, free を使うためのヘッダ */

/*
 * このコードの主題は「ぶらぶらポインタ (dangling pointer)」である。
 *
 * ぶらぶらポインタとは、
 *   以前は有効なメモリ領域を指していたが、
 *   その領域が free などで解放されたあとも、
 *   ポインタ変数の中に古いアドレス値だけが残っている状態
 * を指す。
 *
 * 重要なのは、
 *   free(p);
 * を実行しても、
 *   p という変数そのものが自動で消えたり NULL になったりはしない
 * という点である。
 *
 * つまり free の後でも p には「昔その領域を指していたアドレス」が残る。
 * しかし、その領域はもうこのプログラムが使ってよい領域ではない。
 *
 * この状態で
 *   *p を読む
 *   *p に書く
 *   p を再び free する
 * といったことをすると、未定義動作になる。
 *
 * このコードはその中でも特に危険な
 *   free 後に *p = 99; で解放済み領域へ書き込む
 * 例である。
 *
 * これは一般に use-after-free（解放後使用）と呼ばれる問題でもある。
 * ぶらぶらポインタを使ってしまった結果として起こる典型例である。
 */

int main(void)
{
    /*
     * malloc(sizeof(int))
     *
     * int 型1個分の大きさだけヒープ領域を確保し、
     * その先頭アドレスを返す。
     *
     * p は int * 型なので、
     * 「int を置くための領域を指すポインタ」として使える。
     *
     * この時点のイメージ:
     *
     *   p -----> [ 未初期化の int 領域 ]
     *
     * 注意:
     *   malloc が失敗すると NULL を返す。
     *   元コードでは NULL チェックが省略されているが、
     *   実務では確認するべきである。
     *
     * なお、この時点では領域は確保されただけであり、
     * 中身は未初期化なので、まだ *p を読んではいけない。
     */
    int *p = malloc(sizeof(int));

    /*
     * free(p)
     *
     * p が指しているヒープ領域を解放する。
     *
     * ここで解放されるのは
     *   p という変数
     * ではなく、
     *   p が指している先の動的メモリ
     * である。
     *
     * free の後、その領域はメモリアロケータへ返されるため、
     * もはやこのプログラムが自由に読み書きしてよい場所ではない。
     *
     * しかし p の中身そのものは自動では変わらない。
     * したがってこの直後の状態は概念的に次のようになる。
     *
     *   p -----> [ 以前は自分のものだったが、今は使ってはいけない領域 ]
     *
     * これが「ぶらぶらポインタ」である。
     *
     * つまり p は
     *   何かを指しているように見える
     * が、
     *   その先はもう有効なオブジェクトではない
     * 状態になっている。
     */
    free(p);

    /*
     * *p = 99;
     *
     * ここが致命的な問題である。
     *
     * *p は「p が指している先の int 実体」を意味する。
     * しかし free(p) の後では、その先にもう有効な int オブジェクトは存在しない。
     *
     * それにもかかわらず、
     *
     *   *p = 99;
     *
     * によって解放済み領域へ書き込もうとしている。
     *
     * これは
     *   use-after-free
     * の一種であり、未定義動作である。
     *
     * 未定義動作とは、
     *   - たまたま動いてしまうかもしれない
     *   - 何も起きないように見えるかもしれない
     *   - クラッシュするかもしれない
     *   - 別のデータを壊すかもしれない
     *   - セキュリティ脆弱性につながるかもしれない
     * という、結果が保証されない危険な状態である。
     *
     * 特にメモリアロケータは、free 済み領域を将来別用途に再利用する可能性がある。
     * そのため、この書き込みは自分がもう所有していない領域を壊すことになる。
     *
     * たとえば次のようなことが起こりうる。
     *
     *   1. free(p) した
     *   2. その領域が内部管理情報や別の確保領域として再利用された
     *   3. *p = 99; でそこを書き換えた
     *   4. ヒープ破壊や不正動作が起こる
     *
     * このため、free 後のポインタを使ってはいけない。
     */
    *p = 99;

    /*
     * このコードはここで終了するが、
     * すでに未定義動作を起こしているため、
     * 正常終了する保証はない。
     */

    return 0;
}

/*
 * このコードから学ぶべきこと
 *
 * 1. free 後のポインタは自動では NULL にならない
 *
 *    free(p);
 *    の直後でも p には古いアドレス値が残る。
 *
 * 2. その古いアドレス値を使うと危険
 *
 *    - *p を読む
 *    - *p に書く
 *    - free(p) をもう一度呼ぶ
 *
 *    はいずれも問題になりうる。
 *
 * 3. 「ポインタ変数が残っている」ことと
 *    「その先のオブジェクトが有効である」ことは別である
 *
 *    これは低レイヤーで非常に重要な区別である。
 *
 * 4. free 後はすぐ NULL を代入すると事故を減らせる
 *
 *    たとえば次のようにする。
 *
 *      free(p);
 *      p = NULL;
 *
 *    こうしておけば、
 *      *p = 99;
 *    のような誤用は依然としてバグだが、
 *    「古い有効そうなアドレス」が残るよりは問題に気づきやすい。
 *
 *    また、
 *      free(p);
 *      p = NULL;
 *      free(p);
 *    のように再度 free しても、free(NULL) は安全に無視される。
 *
 *
 * 安全な書き方の例:
 *
 *   int *p = malloc(sizeof(int));
 *   if (p == NULL) {
 *       return 1;
 *   }
 *
 *   *p = 99;
 *
 *   free(p);
 *   p = NULL;
 *
 *   以後は p を使わない
 *
 *
 * このコードの問題を一言で言うと、
 *
 *   「解放済みメモリを、ぶらぶらポインタ経由で再び書き込んでいる」
 *
 * である。
 */
