/* stdio/acctadd.c */
#include <stdio.h>      /* fopen, fclose, fgets, fputs, fprintf, putc,
                           snprintf, sscanf, stderr, stdin などを使うためのヘッダ */
#include <string.h>     /* strchr を使うためのヘッダ */
#include <time.h>       /* time, localtime_r, time_t, struct tm を使うためのヘッダ */

/*
 * 家計簿データを書き込む先のファイル名。
 *
 * このプログラムは、入力された
 *   - 項目名 (item)
 *   - 金額 (value)
 * を、日付付きでこのファイルへ追記していく。
 *
 * "a" モードで fopen しているため、
 * 既存内容を消さずに末尾へ追加していく動作になる。
 */
#define ACCTBOOK "acctbook.txt"

/*
 * 各種固定長バッファサイズの定義。
 *
 * MAXLINELEN:
 *   1 項目（item や value、最終出力行）の想定最大長。
 *
 * BUFLEN:
 *   実際の文字配列サイズ。
 *   MAXLINELEN 文字に加えて、C文字列終端 '\0' の1文字分を確保する。
 *
 * つまり、
 *   「文字列として扱える最大 80 文字」
 * を想定し、そのために 81 バイトの配列を使う設計である。
 */
enum {
    MAXLINELEN = 80,  /* 各項目について */
    BUFLEN = MAXLINELEN + 1,
};

int main(void) {
    /*
     * fp:
     *   家計簿ファイル acctbook.txt を表す FILE * ストリーム。
     *
     * buf:
     *   最終的にファイルへ書き込む 1 行分の完成済み文字列を入れるバッファ。
     *   例:
     *     "2026/03/15 1200.00 lunch\n"
     *
     * item:
     *   ユーザが入力した「項目名」を入れるバッファ。
     *   例:
     *     "lunch"
     *
     * value:
     *   ユーザが入力した金額文字列を一時的に入れるバッファ。
     *   例:
     *     "1200\n"
     *
     * unixtime:
     *   現在時刻を UNIX 時間（1970-01-01 00:00:00 UTC からの秒数）
     *   として保持する変数。
     *
     * now:
     *   localtime_r() によって、現在時刻を年月日などに分解した結果を入れる構造体。
     *
     * year, month, day:
     *   実際に出力行へ埋め込むための年月日。
     *
     * val:
     *   ユーザ入力した金額文字列を sscanf() で数値化した結果を入れる。
     *
     * p:
     *   strchr() で改行 '\n' の位置を探した結果を受けるポインタ。
     *   item 入力末尾の改行除去に使う。
     */
    FILE *fp;
    char buf[BUFLEN];
    char item[BUFLEN], value[BUFLEN];
    time_t unixtime;
    struct tm now;
    int year, month, day;
    double val;
    char *p;

    /*
     * time(NULL)
     *
     * 現在時刻を UNIX 時間として取得する。
     * 戻り値は time_t 型であり、「現在時刻を秒単位で表した値」である。
     *
     * ここでは
     *   unixtime = time(NULL);
     * として、その値を unixtime に保存している。
     *
     * time(NULL) の NULL は、
     * 「戻り値だけ使うので、time_t を別ポインタ経由では受け取らない」
     * という意味である。
     */
    unixtime = time(NULL);

    /*
     * localtime_r(&unixtime, &now)
     *
     * UNIX 時間 unixtime を、ローカルタイムの年月日・時刻に分解し、
     * struct tm 型の now に格納する。
     *
     * localtime() ではなく localtime_r() を使っている点が重要である。
     *
     * localtime_r() は reentrant / thread-safe な版であり、
     * 呼び出し側が結果格納先 now を用意する。
     *
     * これにより、グローバルな静的領域を内部で共有する localtime() よりも、
     * 安全に時刻分解結果を扱いやすい。
     */
    localtime_r(&unixtime, &now);

    /*
     * struct tm の各フィールドは、そのまま人間の年月表示になっていない点に注意する。
     *
     * now.tm_year:
     *   1900 年からの経過年数
     *   たとえば 2026 年なら 126 が入る
     *   そのため 1900 を足して実年へ直す
     *
     * now.tm_mon:
     *   0 〜 11 の範囲で月を表す
     *   つまり 1 月 = 0, 12 月 = 11
     *   そのため人間向けの月へ直すには 1 を足す
     *
     * now.tm_mday:
     *   1 〜 31 の日付がそのまま入る
     */
    year = now.tm_year + 1900;
    month = now.tm_mon + 1; /* tm_mon = 0..11 */
    day = now.tm_mday;

    /*
     * fopen(ACCTBOOK, "a")
     *
     * 家計簿ファイル acctbook.txt を追記モード ("a") で開く。
     *
     * "a" モードの意味:
     *   - ファイルが存在しなければ新規作成する
     *   - ファイルが存在すれば内容を保持したまま開く
     *   - 書き込みは常にファイル末尾へ行われる
     *
     * つまりこのプログラムは、
     *   既存の家計簿を消さずに、1エントリずつ後ろへ足していく
     * 設計である。
     *
     * 注意:
     *   元コードでは fopen の失敗チェックが省略されている。
     *   実務コードでは
     *
     *     if ((fp = fopen(ACCTBOOK, "a")) == NULL) { ... }
     *
     * のように NULL チェックすべきである。
     *
     * ただしここでは元コードの構造を保ったまま、意図を説明している。
     */
    fp = fopen(ACCTBOOK, "a");

    /*
     * for (;;)
     *
     * 無限ループであり、ユーザから
     *   項目名
     *   金額
     * を何件でも入力させる。
     *
     * 終了条件はこのループヘッダには書かれておらず、
     * 中で stdin の EOF に達したら break する設計になっている。
     *
     * つまり、
     *   「入力が続く限り家計簿エントリを追加し続ける」
     * という対話型ループである。
     */
    for (;;) {
        /*
         * fputs("item? ", stderr)
         *
         * ユーザに「項目名を入力してほしい」というプロンプトを表示する。
         *
         * stderr を使っている理由:
         *   対話型ツールでは、プロンプトや警告を stderr に出しておくと、
         *   stdout を別用途に使う場合でも表示系メッセージを分離しやすい。
         *
         * このプログラムでは実際の出力本体はファイル fp へ行くため、
         * 画面へ出す案内は stderr でよい、という設計である。
         */
        fputs("item? ", stderr);

        /*
         * fgets(item, sizeof(item), stdin)
         *
         * 標準入力 stdin から 1 行を読み取り、item に入れる。
         *
         * fgets の特徴:
         *   - 最大 sizeof(item)-1 文字まで読む
         *   - 改行 '\n' があれば、それも含めて読む
         *   - 最後に '\0' を付ける
         *
         * したがって item は C文字列として扱える。
         *
         * 注意:
         *   元コードでは fgets の戻り値 NULL を直接チェックしていないが、
         *   直後の feof(stdin) 判定で EOF を見ている。
         *   より厳密には戻り値自体も確認したい。
         */
        fgets(item, sizeof(item), stdin);

        /*
         * if (feof(stdin)) break;
         *
         * stdin に EOF フラグが立っているなら、
         * それはユーザが入力終了を指示した（たとえば端末で Ctrl-D）と解釈し、
         * ループを抜ける。
         *
         * ここがこのプログラムの終了条件である。
         *
         * 重要:
         *   fgets() の終了原因は EOF だけでなくエラーもありうるが、
         *   元コードでは ferror(stdin) は見ていない。
         *   ここでは教材として主に EOF 終了の構造を見せている。
         */
        if (feof(stdin)) break;

        /*
         * strchr(item, '\n')
         *
         * item の中に改行文字 '\n' があるか探す。
         *
         * fgets() は、行がバッファ内に収まれば末尾改行も含めて読み込むので、
         * 普通の短い入力なら item の末尾には '\n' が入っている。
         *
         * その改行は、ファイルへ保存する「項目名」には不要なので、
         * 見つかったら '\0' に置き換えて切り落とす。
         *
         * 例:
         *   入力: "lunch\n"
         *   処理後: "lunch\0"
         *
         * こうして、item を「改行無しの普通の文字列」として扱えるようにする。
         */
        if ((p = strchr(item, '\n')) != NULL) *p = '\0'; /* \n を切り落とす */

        /*
         * 金額入力のプロンプトを表示する。
         */
        fputs("value? ", stderr);

        /*
         * 金額文字列を1行受け取る。
         *
         * ここでも fgets() を使って、まずは文字列として受け取る。
         * その後 sscanf() で double に変換する設計である。
         *
         * つまりこのプログラムは
         *   いきなり数値入力する
         * のではなく、
         *   一旦文字列として1行受け取り、それを解析する
         * 方式を採っている。
         */
        fgets(value, sizeof(value), stdin);

        /*
         * sscanf(value, "%lf", &val)
         *
         * value 文字列から double 値を1個読み取る。
         *
         * "%lf":
         *   double への変換指定
         *
         * 戻り値:
         *   成功時:
         *     読み取れた項目数（ここでは 1 を期待）
         *
         *   失敗時:
         *     0 や EOF など
         *
         * ここでは
         *
         *   != 1
         *
         * を失敗とみなしている。
         *
         * つまり、
         *   「value の入力が数値として解釈できなければ、そのエントリは無視する」
         * という設計である。
         *
         * 例:
         *   "1200\n"  -> 成功, val = 1200.0
         *   "12.5\n"  -> 成功, val = 12.5
         *   "abc\n"   -> 失敗
         */
        if (sscanf(value, "%lf", &val) != 1) {
            /*
             * 数値として解釈できなかった場合の警告を stderr に出す。
             *
             * "%s" で value を出しているので、
             * 元入力の改行がそのまま含まれている可能性がある点に注意。
             * ただしこのコードではそれも含めて簡易的に警告を出している。
             *
             * "ignored" とある通り、この入力は家計簿へ書き込まず、
             * ループ先頭へ戻って次の項目入力へ進む。
             */
            fprintf(stderr, "value \"%s\" invalid; ignored\n", value);
            continue;
        }

        /*
         * snprintf(buf, sizeof(buf), "%04d/%02d/%02d %.2f %s\n", ...)
         *
         * ここで最終的にファイルへ書く1行を組み立てる。
         *
         * 生成される形式は概念的に次の通り:
         *
         *   YYYY/MM/DD 金額 項目名\n
         *
         * たとえば
         *   year = 2026, month = 3, day = 15
         *   val = 1200
         *   item = "lunch"
         * の場合、
         *
         *   "2026/03/15 1200.00 lunch\n"
         *
         * のような文字列になる。
         *
         * 書式指定の意味:
         *
         *   %04d
         *     4桁、足りなければ先頭を 0 埋めした整数
         *     年用
         *
         *   %02d
         *     2桁、足りなければ 0 埋めした整数
         *     月や日用
         *
         *   %.2f
         *     小数点以下 2 桁固定の実数
         *     金額用
         *
         *   %s
         *     項目名文字列
         *
         * snprintf を使う利点:
         *   - 出力先バッファサイズを指定できる
         *   - 何文字必要だったかを戻り値で分かる
         *   - バッファオーバーランを防ぎやすい
         *
         * 戻り値:
         *   生成に必要だった文字数（終端 '\0' は含まない）
         *
         * したがって、その戻り値が sizeof(buf) 以上なら
         *   「バッファに収まらず切り詰めが起きた」
         * と判断できる。
         */
        if (snprintf(buf, sizeof(buf), "%04d/%02d/%02d %.2f %s\n",
                 year, month, day, val, item) >= sizeof(buf)) {
             /*
              * ここへ来るのは、完成行が BUFLEN に収まりきらなかったときである。
              *
              * snprintf はバッファ終端 '\0' を保証するが、
              * 切り詰められた結果、末尾改行 '\n' が消えてしまうことがある。
              *
              * そこで
              *   buf[sizeof(buf)-2] = '\n';
              * として、最後から2文字目を改行にしている。
              *
              * 最後の1文字分は通常 '\0' のために確保される。
              *
              * つまり
              *   [ ... ][ '\n' ][ '\0' ]
              * の形に整えようとしている。
              *
              * これにより、切り詰めが起きても
              * 「1レコード1行」というファイル構造が崩れにくい。
              */
             buf[sizeof(buf)-2] = '\n';

             /*
              * 「長すぎるので切り詰めた」ことを stderr へ警告する。
              * さらに、実際にどのような行として保存されるかを表示する。
              *
              * これにより、ユーザは
              *   - どの入力が長すぎたのか
              *   - どう切り詰められたのか
              * を確認できる。
              */
             fprintf(stderr, "Warning: entry too long; truncated to:\n%s", buf);
        }

        /*
         * fputs(buf, fp)
         *
         * ここで完成済みの1行を家計簿ファイルへ追記する。
         *
         * fp は "a" モードで開かれているので、
         * 既存内容の末尾へ順次追加される。
         *
         * つまりこのループを1回通るごとに、
         * 家計簿ファイルへ新しい1エントリが1行増える。
         *
         * 注意:
         *   元コードでは fputs の戻り値をチェックしていない。
         *   実務では書き込み失敗を確認したい。
         */
        fputs(buf, fp);
    }

    /*
     * ループ終了後、家計簿ファイルを閉じる。
     *
     * fclose(fp) によって
     *   - 出力バッファが必要に応じて flush される
     *   - ファイル資源が解放される
     *
     * 出力ストリームでは fclose 時に初めて失敗が見える場合もあるので、
     * 厳密には戻り値チェックが望ましい。
     */
    fclose(fp);

    /*
     * putc('\n', stderr)
     *
     * 終了時に stderr へ改行を1文字出している。
     *
     * なぜ必要か:
     *   最後のプロンプト "item? " は改行なしで表示される。
     *   ユーザが EOF で入力終了したとき、そのままだと端末表示が
     *   行途中で止まったように見えることがある。
     *
     * そこで最後に改行を出して、表示を整えている。
     *
     * putc は「1文字だけストリームへ書く」関数であり、
     * ここでは stderr へ '\n' を出している。
     */
    putc('\n', stderr);

    /*
     * 正常終了。
     */
    return 0;
}
