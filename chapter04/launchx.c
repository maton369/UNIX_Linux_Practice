/* stdio/launchx.c */
#include <stdio.h>      /* printf を使うためのヘッダ */
#include <unistd.h>     /* sleep を使うためのヘッダ */

/*
 * このコードの主題は「行バッファリング (line buffering)」である。
 *
 * 一見すると、このプログラムは
 *
 *   5
 *   4
 *   3
 *   2
 *   1
 *   go!
 *
 * を 1 秒おきに少しずつ表示しそうに見える。
 *
 * しかし実際には、実行環境によっては
 *   5 4 3 2 1
 * を1文字ずつすぐに表示せず、
 * 最後の
 *
 *   printf(" go!\n");
 *
 * の時点まで画面に出てこないことがある。
 *
 * その理由が「標準出力 stdout のバッファリング」であり、
 * 特に端末へ向けた stdout ではよく
 * 「行バッファリング」
 * が使われるためである。
 *
 * 行バッファリングとは:
 *   出力を1文字書くたびに即座に画面へ送るのではなく、
 *   内部バッファにためておき、
 *   主に改行 '\n' が出たタイミングでまとめて出す方式
 * である。
 *
 * つまりこのプログラムでは、
 *
 *   printf("%d", i);
 *
 * は改行を含まないため、数字が stdout のバッファにたまるだけで、
 * 端末へまだ出ないことがある。
 *
 * そして最後の
 *
 *   printf(" go!\n");
 *
 * で改行 '\n' が出た瞬間、
 * それまでたまっていた
 *
 *   54321 go!\n
 *
 * がまとめて表示されることがある。
 *
 * 重要:
 *   これは「printf が動いていない」のではない。
 *   printf は毎回呼ばれているが、
 *   出力先まで即座に flush（押し出し）されていない
 * だけである。
 *
 * つまりこのコードは、
 *   sleep(1) で時間を空けても、
 *   改行が無ければ見た目上は逐次表示されないことがある
 * という、stdio のバッファリング挙動を観察するサンプルである。
 *
 * さらに重要な補足:
 *   stdout のバッファリング方式は、出力先によって変わることがある。
 *
 *   - 端末に出しているとき:
 *       行バッファリングになりやすい
 *
 *   - ファイルやパイプへリダイレクトしているとき:
 *       完全バッファリングになりやすい
 *
 * そのため、
 *
 *   ./a.out
 *
 * と
 *
 *   ./a.out > out.txt
 *
 * では見え方・タイミングが異なることがある。
 */

int main(void) {
    /*
     * i:
     *   カウントダウンのためのループ変数である。
     *
     * このプログラムでは
     *   5, 4, 3, 2, 1
     * の順に表示したいので、
     * i を 5 から 1 まで減らしていく。
     */
    int i;

    /*
     * for (i = 5; i > 0; i--)
     *
     * 5 から 1 まで 1 ずつ減らしながらループする。
     *
     * 各反復でやることは 2 つ:
     *
     *   1. 現在の i を printf("%d", i) で出力する
     *   2. sleep(1) で 1 秒待つ
     *
     * 直感的には「1 秒ごとに数字が出る」ように見えるが、
     * 実際には stdout のバッファリング方式次第で、
     * 画面にはすぐ出ない場合がある。
     *
     * これがこのサンプルの観察ポイントである。
     */
    for (i = 5; i > 0; i--) {

        /*
         * printf("%d", i);
         *
         * 現在の整数 i を 10 進数で出力する。
         *
         * ここで重要なのは、
         * この書式文字列には改行 '\n' が含まれていないことだ。
         *
         * たとえば i = 5 のとき、
         *
         *   "5"
         *
         * という1文字が stdout に書き込まれる。
         *
         * しかし stdout が行バッファリングされている場合、
         * この "5" は即座に端末へ表示されるのではなく、
         * いったん stdout の内部バッファにたまることがある。
         *
         * つまり概念的にはこうなる。
         *
         *   1回目後: バッファ = "5"
         *   2回目後: バッファ = "54"
         *   3回目後: バッファ = "543"
         *   4回目後: バッファ = "5432"
         *   5回目後: バッファ = "54321"
         *
         * そして改行がまだ無いので、
         * 端末に見えていないことがある。
         *
         * ここが「行バッファリング」の核心である。
         *
         * もし本当に1秒ごとに画面へ見せたいなら、
         * 代表的には次のどちらかを行う。
         *
         *   1. 改行を毎回入れる
         *      printf("%d\n", i);
         *
         *   2. 明示的に fflush(stdout) する
         *      printf("%d", i);
         *      fflush(stdout);
         *
         * このサンプルではあえてそれをしていない。
         * そのため、「改行が無いと表示が遅れることがある」現象を観察できる。
         */
        printf("%d", i);

        /*
         * sleep(1);
         *
         * 1 秒間プロセスを停止させる。
         *
         * この呼び出しにより、
         * ループの各反復の間に 1 秒ずつ空白時間が入る。
         *
         * 直感的には
         *   「1秒待っているのだから、その間に数字が見えるはず」
         * と考えがちだが、
         * バッファリングがあると話は別である。
         *
         * つまり sleep は
         *   「時間を空ける」
 * だけであり、
         *   「stdout の中身を表示装置へ押し出す」
 * わけではない。
         *
         * したがって、
         *   printf("%d", i);
         *   sleep(1);
         *
         * の順でも、数字が毎秒見える保証はない。
         *
         * これが
         *   計算した / printf を呼んだ
         * ことと
         *   実際にユーザの画面に見える
         * ことが別である、という重要な点である。
         */
        sleep(1);
    }

    /*
     * printf(" go!\n");
     *
     * ループが終わった後、
     *   " go!\n"
     * を出力する。
     *
     * ここで先頭に半角スペースがあるので、
     * それまでにたまっていた "54321" の後に
     *
     *   " go!"
     *
     * が続き、さらに最後に改行 '\n' が付く。
     *
     * 行バッファリング環境では、
     * この改行 '\n' がきっかけになって stdout のバッファが flush され、
     * それまでたまっていた内容が一気に表示されることがある。
     *
     * たとえばバッファ内部では概念的に
     *
     *   "54321"
     *
     * がたまっていて、ここで
     *
     *   " go!\n"
     *
     * が追加されると、
     *
     *   "54321 go!\n"
     *
     * という1行がまとめて端末へ送られる。
     *
     * そのため実際の見た目としては、
     * 5秒待ったあとに突然
     *
     *   54321 go!
     *
     * と一気に出たように見える場合がある。
     *
     * この挙動こそが、
     * 「行バッファリングでは改行が flush の契機になる」
     * ということを示している。
     */
    printf(" go!\n");

    /*
     * 正常終了。
     *
     * なお、プログラム終了時には通常 stdout は flush されるため、
     * 改行が無くても終了時にまとめて見えることがある。
     *
     * ただしこのコードでは最後に改行付き出力をしているため、
     * その時点で表示されることが多い。
     */
    return 0;
}

/*
 * このコードから学ぶべきことのまとめ
 *
 * 1. printf を呼んでも、すぐ画面に見えるとは限らない
 *
 *    printf は stdout のバッファへ書くだけで、
 *    即座に表示装置まで届くとは限らない。
 *
 * 2. 端末向け stdout は行バッファリングされることが多い
 *
 *    その場合、改行 '\n' が出るまで出力がたまることがある。
 *
 * 3. sleep() は時間を空けるだけで、flush はしない
 *
 *    したがって
 *      printf("%d", i);
 *      sleep(1);
 *    としても、毎秒表示されるとは限らない。
 *
 * 4. 改行や fflush(stdout) が出力可視化の契機になる
 *
 *    毎回すぐ見せたいなら、例えば
 *
 *      printf("%d", i);
 *      fflush(stdout);
 *
 *    のようにする。
 *
 * 5. 出力先によってバッファリング方式は変わる
 *
 *    - 端末: 行バッファリングになりやすい
 *    - ファイル / パイプ: 完全バッファリングになりやすい
 *
 *
 * 図でまとめると:
 *
 *   ループ1回目:
 *     printf("5")
 *     stdoutバッファ = "5"
 *     まだ改行なし
 *
 *   ループ2回目:
 *     printf("4")
 *     stdoutバッファ = "54"
 *
 *   ...
 *
 *   ループ5回目:
 *     stdoutバッファ = "54321"
 *
 *   最後:
 *     printf(" go!\n")
 *     stdoutバッファ = "54321 go!\n"
 *     改行が来たので flush
 *     画面にまとめて表示
 *
 *
 * このコードの本質を一言で言うと、
 *
 *   「改行なしの printf は行バッファリングされた stdout にため込まれ、最後の改行でまとめて表示されることがある」
 *
 * である。
 */
