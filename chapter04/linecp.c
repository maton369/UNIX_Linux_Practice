/* stdio/linecp.c */
#include <stdio.h>      /* fopen, fclose, fgets, fputs, perror, stderr を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */

/*
 * enum を使って1行分のバッファ長を定義している。
 *
 * ここでは
 *   LINELEN = 1024
 * なので、最大 1023 文字 + 終端文字 '\0'
 * を入れられる配列として buf を使うことになる。
 *
 * #define で書いてもよいが、このように enum を使うと
 * 「整数定数である」ことが明示しやすい。
 */
enum {
    LINELEN = 1024,
};

/*
 * このコードの主題は「stdio を使った一行ずつの読み書き」である。
 *
 * ここでは低水準入出力の read/write ではなく、
 * C 標準ライブラリ stdio の
 *
 *   fgets()
 *   fputs()
 *
 * を使っている。
 *
 * これらは
 *   - fgets() : 入力ストリームから1行ずつ読む
 *   - fputs() : 文字列を出力ストリームへ書く
 * ための高水準関数である。
 *
 * このプログラム全体の役割は、
 *
 *   1. 入力ファイルを開く
 *   2. 出力ファイルを開く
 *   3. fgets() で入力ファイルを1行ずつ読む
 *   4. 読んだ1行を fputs() で出力ファイルへ書く
 *   5. EOF に達するまで繰り返す
 *   6. 最後に両方のファイルを閉じる
 *
 * という「行単位コピー」である。
 *
 * つまり、アルゴリズムとしては
 *
 *   入力ストリーム ----(1行読む)----> buf ----(1行書く)----> 出力ストリーム
 *
 * を繰り返している。
 *
 * 低水準入出力版との違い:
 *
 *   - read/write は「何バイト読むか」「何バイト書くか」を自分で意識する
 *   - fgets/fputs は「行」や「文字列」を意識して扱える
 *
 * そのため、このコードは「テキストファイルを一行ずつ処理する」例としてとても典型的である。
 */

int main(int argc, char *argv[]) {
    /*
     * ifp:
     *   input FILE pointer のつもりの変数であり、
     *   入力ファイルを表す FILE * を入れる。
     *
     * ofp:
     *   output FILE pointer のつもりの変数であり、
     *   出力ファイルを表す FILE * を入れる。
     *
     * stdio ではファイルを int fd ではなく FILE * で扱う。
     * FILE * には stdio ライブラリが内部で管理する
     *   - バッファ
     *   - 現在位置
     *   - EOF 状態
     *   - エラー状態
     * などの情報が結び付いている。
     */
    FILE *ifp, *ofp;

    /*
     * buf:
     *   fgets() で「1行分」を受け取るためのバッファである。
     *
     * LINELEN = 1024 なので、
     *   最大 1023 文字 + '\0'
     * を保持できる。
     *
     * 重要:
     *   fgets() は、改行 '\n' があればそれも含めて buf に入れる。
     *   そして最後に '\0' を付ける。
     *
     * つまり buf は「C文字列」として使える。
     *
     * ただし、入力行が 1023 文字を超えると、
     * 1 回の fgets() では1行全体を取り切れず、
     * 行の途中までしか読めないことがある。
     *
     * このコードは基本的な行コピーの教材なので、
     * そこまでは深く扱わず、標準的なテキスト行を対象としている。
     */
    char buf[LINELEN];

    /*
     * argc != 3 の判定
     *
     * このプログラムはコマンドライン引数として
     *
     *   argv[1] : 入力ファイル名
     *   argv[2] : 出力ファイル名
     *
     * の2つを期待している。
     *
     * argc は引数個数であり、
     * プログラム名自身も含むため、
     *
     *   linecp infile outfile
     *
     * の形なら
     *
     *   argc == 3
     *
     * になる。
     *
     * したがって argc != 3 なら、
     * 使い方が間違っていると判断して usage を表示し、終了する。
     */
    if (argc != 3) {
        /*
         * fputs("usage: ...", stderr)
         *
         * 使い方メッセージを標準エラー出力 stderr へ表示する。
         *
         * ここで stderr を使っているのは、
         * usage やエラーメッセージは通常の出力結果ではなく
         * 診断メッセージだからである。
         *
         * stderr は通常、標準出力 stdout とは別に扱われる。
         */
        fputs("usage: linecp <infile> <outfile>\n", stderr);

        /*
         * 引数不正なので異常終了する。
         */
        exit(1);
    }

    /*
     * fopen(argv[1], "r")
     *
     * argv[1] で指定された入力ファイルを読み込みモードで開く。
     *
     * "r" は read mode を意味し、
     * 既存ファイルを読み込むためのモードである。
     *
     * 戻り値:
     *   成功時:
     *     有効な FILE *
     *
     *   失敗時:
     *     NULL
     *
     * 本来は NULL チェックをするべきである。
     * しかし元コードでは省略されている。
     *
     * そのため、このままだと fopen に失敗した場合、
     * 以後の fgets(ifp, ...) で不正動作の危険がある。
     *
     * 学習用としては
     *   「入力ストリームを開く」
     * ことに集中しているが、
     * 実務コードとしては必ず失敗確認を書くべきである。
     */
    ifp = fopen(argv[1], "r");

    /*
     * fopen(argv[2], "w")
     *
     * argv[2] で指定された出力ファイルを
     * 書き込みモードで開く。
     *
     * "w" は write mode を意味し、
     *   - 存在しなければ新規作成
     *   - 存在すれば中身を空にして最初から書き直す
     * モードである。
     *
     * したがって、このプログラムは出力先を「上書きコピー」する。
     *
     * 戻り値:
     *   成功時:
     *     有効な FILE *
     *
     *   失敗時:
     *     NULL
     *
     * これも本来は NULL チェックをすべきである。
     */
    ofp = fopen(argv[2], "w");

    /*
     * while (fgets(buf, sizeof(buf), ifp) != NULL)
     *
     * ここがこのコードの中心である。
     *
     * fgets(buf, sizeof(buf), ifp) は、
     * 入力ストリーム ifp から最大 sizeof(buf)-1 文字を読み取り、
     * buf に C文字列として格納する。
     *
     * fgets の挙動:
     *   - 改行 '\n' に出会ったら、その改行も含めて読み込む
     *   - あるいはバッファ上限まで読む
     *   - 最後に '\0' を付ける
     *
     * 戻り値:
     *   成功時:
     *     buf を返す（つまり NULL ではない）
     *
     *   EOF またはエラー時:
     *     NULL
     *
     * この while は
     *
     *   「1行（またはバッファに入る分）を読む」
     *   「成功した限り、その行を処理する」
     *
     * という形になっている。
     *
     * 重要:
     *   このコードは「1行ずつ処理する」つもりで書かれているが、
     *   非常に長い行は1回の fgets で分割される可能性がある。
     *
     * ただし通常のテキストファイルでは、
     * 1行がこのサイズ内に収まることが多く、
     * 学習用としては「一行ずつ読む」理解で問題ない。
     */
    while (fgets(buf, sizeof(buf), ifp) != NULL) {

        /*
         * fputs(buf, ofp)
         *
         * buf に入っている C文字列を、そのまま出力ストリーム ofp へ書く。
         *
         * fgets() で読み込んだ行には、
         * 元の入力に改行 '\n' が含まれていればそれも buf 内に保持されている。
         * そのため、fputs() でそのまま書けば、
         * 行区切りも含めて入力内容を再現できる。
         *
         * つまり、
         *   fgets() が「1行を文字列として受け取る」
         *   fputs() が「その文字列をそのまま書く」
         * という対応になっている。
         *
         * データの流れは次のようになる。
         *
         *   入力ファイル
         *      |
         *      v
         *   fgets(...)
         *      |
         *      v
         *      buf   （1行分の文字列）
         *      |
         *      v
         *   fputs(buf, ofp)
         *      |
         *      v
         *   出力ファイル
         *
         * このため、このループ全体は
         *   「入力ファイルを一行ずつ出力ファイルへコピーする」
         * アルゴリズムになっている。
         *
         * 戻り値:
         *   成功時:
         *     非負値
         *
         *   失敗時:
         *     EOF
         *
         * 元コードでは EOF のとき perror("fputs") を呼んでいるが、
         * 厳密には stdio の関数失敗は errno が必ず意味ある値を持つとは限らない。
         *
         * それでも学習用としては、
         *   「書き込みに失敗したら診断する」
         * 意図は分かりやすい。
         */
        if (fputs(buf, ofp) == EOF)
            perror("fputs");
    }

    /*
     * ここで while を抜けた理由は 2 通りある。
     *
     * 1. 正常に EOF に達した
     * 2. fgets がエラーで NULL を返した
     *
     * 元コードでは feof(ifp) や ferror(ifp) による区別はしていない。
     * より厳密には区別してエラー処理を書くこともできる。
     *
     * このサンプルでは「基本的な一行コピー」に主題を絞っている。
     */

    /*
     * fclose(ofp)
     *
     * 出力ストリームを閉じる。
     *
     * 特に出力側の fclose は重要である。
     * stdio ではバッファリングがあるため、
     * まだバッファに残っているデータが fclose 時に flush されることがある。
     *
     * つまり、fputs で見た目上は全部書けていても、
     * fclose に失敗すると最終的な保存に失敗する可能性がある。
     *
     * 元コードでは戻り値チェックが省略されているが、
     * 実務では確認するのが望ましい。
     */
    fclose(ofp);

    /*
     * fclose(ifp)
     *
     * 入力ストリームを閉じる。
     *
     * 読み込み専用ストリームなので ofp ほど flush の意味は強くないが、
     * 資源解放のために閉じるのが基本である。
     */
    fclose(ifp);

    /*
     * 正常終了。
     */
    return 0;
}
