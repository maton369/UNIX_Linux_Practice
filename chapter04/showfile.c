/* stdio/showfile.c */
#include <stdio.h>      /* fopen, fclose, getc, putchar, perror を使うためのヘッダ */
#include <stdlib.h>     /* exit を使うためのヘッダ */

/*
 * 読み込み対象のファイル名をマクロで定義している。
 *
 * こうしておくと、ソースコード中に "hello.txt" を何度も直書きせずに済む。
 * もし対象ファイル名を変えたくなった場合も、この定義を1か所直せばよい。
 */
#define INFILE          "hello.txt"

/*
 * このコードの主題は「stdio を使った1文字ずつの読み書き」である。
 *
 * 低水準入出力では open/read/write/close を使って
 * 「何バイト読むか」「何バイト書くか」を自分で指定したが、
 * このコードでは stdio（標準入出力ライブラリ）を使って、
 *
 *   - fopen()   : ファイルを開く
 *   - getc()    : ファイルから1文字ずつ読む
 *   - putchar() : 標準出力へ1文字ずつ書く
 *   - fclose()  : ファイルを閉じる
 *
 * という、より高水準な関数で処理している。
 *
 * 特に重要なのは、
 *   getc() と putchar() が「1文字ずつ処理する関数」だという点である。
 *
 * このプログラム全体のアルゴリズムは次の通りである。
 *
 *   1. hello.txt を読み込み用で開く
 *   2. getc(fp) で1文字ずつ取り出す
 *   3. EOF に達するまで、その文字を putchar(c) で標準出力へ出す
 *   4. 最後に fclose(fp) でファイルを閉じる
 *
 * つまりこのコードは、
 *
 *   「ファイルの内容を1文字ずつ読んで、そのまま画面へ1文字ずつ表示する」
 *
 * という、最も基本的なファイル表示プログラムである。
 *
 * 概念的には、簡易な cat コマンドの stdio 版のようなものだと考えてよい。
 */

int main(void) {
    /*
     * fp:
     *   FILE * 型のポインタである。
     *
     * 低水準入出力ではファイルを int 型のファイルディスクリプタで扱ったが、
     * stdio では FILE * を使う。
     *
     * FILE * は、単なるファイル識別子ではなく、
     * stdio ライブラリが内部で管理する
     *   - バッファ
     *   - 現在位置
     *   - エラー状態
     *   - EOF 状態
     * などの情報を含んだ「ストリーム」のハンドルである。
     *
     * したがって fopen() に成功すると、
     * 以後 getc(fp) や fclose(fp) のように、
     * FILE * を通してファイルを操作する。
     */
    FILE *fp;

    /*
     * c:
     *   getc() で読み取った1文字を保持する変数である。
     *
     * ここで型が char ではなく int なのは非常に重要である。
     *
     * getc() は、通常の文字を返すだけでなく、
     * ファイル終端を表す特別な値 EOF も返す。
     *
     * EOF は通常 -1 で表現されることが多く、
     * これは char 型では安全に区別できない可能性がある。
     *
     * そのため、
     *   「文字」も
     *   「EOF」も
     * 区別できるように int 型で受けるのが正しい。
     *
     * これは stdio の1文字入力関数を使うときの基本的な約束である。
     */
    int c;

    /*
     * fopen(INFILE, "r")
     *
     * INFILE（ここでは "hello.txt"）を読み込みモードで開く。
     *
     * 第1引数:
     *   INFILE
     *   開きたいファイル名
     *
     * 第2引数:
     *   "r"
     *   読み込み専用モード (read mode)
     *
     * 戻り値:
     *   成功時:
     *     有効な FILE * を返す
     *
     *   失敗時:
     *     NULL を返す
     *
     * 低水準入出力の open() が int fd を返していたのに対し、
     * fopen() は FILE * を返すところが大きな違いである。
     *
     * ここでは
     *
     *   (fp = fopen(...)) == NULL
     *
     * という形で、
     *   1. 戻り値を fp に代入
     *   2. それが NULL かどうかを判定
     * している。
     */
    if ((fp = fopen(INFILE, "r")) == NULL) {
        /*
         * fopen に失敗した場合、
         * perror("fopen") により errno に対応するエラーメッセージを表示する。
         *
         * 例:
         *   fopen: No such file or directory
         *   fopen: Permission denied
         *
         * など。
         *
         * "fopen" は、どの処理で失敗したかを示すラベルである。
         */
        perror("fopen");

        /*
         * 対象ファイルを開けなかったので、以後の処理は続けられない。
         * そのため異常終了する。
         */
        exit(1);
    }

    /*
     * while ((c = getc(fp)) != EOF)
     *
     * ここがこのコードの中心である。
     *
     * getc(fp) は、FILE * fp が表すストリームから
     * 次の1文字を読み取る関数である。
     *
     * 戻り値:
     *   成功時:
     *     読み取った1文字を unsigned char から int に拡張した値
     *
     *   ファイル終端に達したとき:
     *     EOF
     *
     *   エラー時:
     *     EOF（ただし EOF かエラーかは feof/ferror で区別する）
     *
     * このコードでは、
     *
     *   c = getc(fp)
     *
     * で1文字読んで c に保存し、
     *
     *   c != EOF
     *
     * で「まだ読み取る文字があるかどうか」を判定している。
     *
     * つまりこのループは、
     *
     *   「1文字読む → EOF でなければ処理する → また次の1文字を読む」
     *
     * を繰り返す構造である。
     *
     * この「1回のループで1文字だけ処理する」というのが、
     * getc()/putchar() 型の最も基本的な文字単位処理である。
     */
    while ((c = getc(fp)) != EOF)

        /*
         * putchar(c);
         *
         * 読み取った1文字 c を標準出力 stdout へ書く。
         *
         * putchar() は、
         *   「1文字だけ標準出力へ出す」
         * 関数である。
         *
         * したがって、この1行により
         *
         *   ファイルから1文字読んだら、
         *   その同じ文字を画面へ1文字出す
         *
         * ことになる。
         *
         * このコードは結果的に、
         * hello.txt の内容をそのまま標準出力へ複写している。
         *
         * つまりデータの流れは次のようになる。
         *
         *   ファイル hello.txt
         *         |
         *         v
         *      getc(fp)
         *         |
         *         v
         *         c
         *         |
         *         v
         *     putchar(c)
         *         |
         *         v
         *      標準出力
         *
         * ここで改行文字 '\n' がファイル中に含まれていれば、
         * それも普通の1文字として読み取られ、そのまま putchar で出力される。
         * そのため、ファイル中の改行構造も保たれる。
         */
        putchar(c);

    /*
     * ループを抜けた理由は、
     * getc(fp) が EOF を返したからである。
     *
     * 多くの場合は
     *   「正常にファイルの最後まで読んだ」
     * ことを意味する。
     *
     * ただし厳密には、
     *   EOF に達した場合
     * と
     *   読み取りエラーが起きた場合
     * の両方で EOF が返ることがある。
     *
     * この簡潔なサンプルではそこまで区別していないが、
     * 厳密にやるなら feof(fp) や ferror(fp) を使って判定することもある。
     */

    /*
     * fclose(fp)
     *
     * fopen() で開いたストリームを閉じる。
     *
     * stdio の基本パターンは
     *
     *   fopen -> getc/putc/fread/fwrite/... -> fclose
     *
     * である。
     *
     * fclose() を呼ぶことで、
     *   - stdio の内部バッファを必要に応じて flush する
     *   - 関連資源を解放する
     *   - FILE * ストリームを終了する
     *
     * 戻り値:
     *   成功時:
     *     0
     *
     *   失敗時:
     *     EOF
     *
     * ここでは EOF かどうかを見て失敗判定している。
     */
    if (fclose(fp) == EOF) {
        /*
         * fclose に失敗した場合のエラー表示。
         *
         * 特に書き込みストリームでは flush 失敗が隠れていることがあるため、
         * fclose の戻り値確認は重要である。
         *
         * 今回は読み込み専用なので失敗の場面は少ないが、
         * それでもチェックしておくのは丁寧である。
         */
        perror("fclose");
        exit(1);
    }

    /*
     * ここまで来れば、
     *   - fopen 成功
     *   - getc/putchar による表示完了
     *   - fclose 成功
     * なので正常終了である。
     */
    return 0;
}
