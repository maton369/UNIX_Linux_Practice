#include <stdio.h>      /* getchar(), putchar() を使うためのヘッダ */
#include <termios.h>    /* struct termios, tcgetattr(), tcsetattr(),
                           ECHO, TCSANOW などを使うためのヘッダ */
#include <unistd.h>     /* STDIN_FILENO を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「termios を使って端末の入力設定を変更し、
 *    文字を入力しても自動では画面に表示されないようにする」
 *
 * ことである。
 *
 * 具体的には、
 *
 *   ECHO
 *
 * というローカルフラグを無効にしている。
 *
 * 通常、端末でキーボードから文字を入力すると、
 * その文字はプログラムへ渡されるだけでなく、
 * 端末自身が画面にも表示する。
 * これを「エコー」と呼ぶ。
 *
 * たとえば普通の端末では、
 *
 *   ユーザが a を押す
 *     -> プログラムは 'a' を受け取る
 *     -> 端末も画面に a を表示する
 *
 * という挙動になる。
 *
 * しかしこのコードでは ECHO をオフにするので、
 *
 *   ユーザが a を押す
 *     -> プログラムは 'a' を受け取る
 *     -> 端末は自動では a を表示しない
 *
 * という状態になる。
 *
 * その一方で、このプログラム自身が
 *
 *   putchar(c)
 *
 * で受け取った文字を出力しているので、
 * 結果として「端末の自動エコーではなく、プログラム自身が表示している」
 * 状態を作っている。
 *
 * このコードはとても短いが、
 *
 *   - termios で端末属性を取得する
 *   - 必要なビットだけ変更する
 *   - 変更後の属性を端末へ反映する
 *   - 最後に元の設定へ戻す
 *
 * という、端末制御の基本パターンが入っている。
 *
 * 特に重要なのは、
 *
 *   「端末設定を変えるときは、いきなり好き勝手な設定を書き込むのではなく、
 *    いまの設定を取得して、必要な部分だけ変更し、最後に復元する」
 *
 * という点である。
 *
 * これが termios を安全に使う基本姿勢である。
 */

int main(void)
{
    /*
     * c:
     *   getchar() で読み取った 1 文字を保持する変数である。
     *
     * getchar() の戻り値は int である。
     * 理由は、通常の文字だけでなく EOF も表現する必要があるからである。
     *
     * そのため char ではなく int で受けるのが正しい。
     */
    int c;

    /*
     * struct termios new, old;
     *
     * termios 構造体は、端末の各種設定をまとめて保持するための構造体である。
     *
     * この構造体の中には、
     *
     *   - 入力に関するフラグ
     *   - 出力に関するフラグ
     *   - 制御に関するフラグ
     *   - ローカルモードに関するフラグ
     *   - 特殊文字設定
     *
     * などが入っている。
     *
     * 今回はその中の
     *
     *   c_lflag
     *
     * を主に触る。
     *
     * c_lflag は local flags のことで、
     * 端末のローカルな挙動、たとえば
     *
     *   - ECHO   : 入力文字を自動表示するか
     *   - ICANON : 行単位入力（カノニカルモード）にするか
     *   - ISIG   : Ctrl+C などをシグナルとして解釈するか
     *
     * などを表すビット集合である。
     *
     * old:
     *   現在の端末設定を保存するために使う。
     *   最後に元へ戻すために必要である。
     *
     * new:
     *   old をコピーしてから、一部だけ変更した設定を入れる。
     *
     * つまり
     *
     *   old = 現在設定の退避
     *   new = old を元に編集した新設定
     *
     * という役割分担になっている。
     */
    struct termios new, old;

    /*
     * tcgetattr(STDIN_FILENO, &old);
     *
     * ここで現在の端末属性を取得する。
     *
     * tcgetattr(fd, termios_p) の意味:
     *
     *   「ファイル記述子 fd に対応する端末の現在設定を、
     *    termios 構造体へ読み込む」
     *
     * である。
     *
     * 今回の fd は
     *
     *   STDIN_FILENO
     *
     * なので、標準入力に接続されている端末の属性を読むことになる。
     *
     * 通常、対話端末上で動かしていれば、
     * これはキーボード入力に対応する端末設定になる。
     *
     * ----------------------------------------
     * なぜ最初に現在設定を読むのか
     * ----------------------------------------
     *
     * termios は設定項目が多い。
     * したがって、最初から全部を自前で組み立てるのは危険である。
     *
     * 一般には
     *
     *   1. 現在設定を読む
     *   2. 必要な部分だけ変更する
     *   3. 書き戻す
     *
     * という流れにする。
     *
     * これにより、今回の関心事である ECHO 以外の設定
     * （たとえば ICANON や特殊文字設定など）を壊しにくくなる。
     *
     * 注意:
     *   本来は戻り値を確認してエラーハンドリングしたい。
     *   このサンプルでは簡潔さを優先して省略している。
     */
    tcgetattr(STDIN_FILENO, &old);

    /*
     * new = old;
     *
     * 現在の端末設定 old を new に丸ごとコピーする。
     *
     * これにより new は、
     *
     *   「いまの端末設定と同じだが、これから必要な部分だけ変えるための作業用」
     *
     * になる。
     *
     * ここがとても大事で、
     * いきなり new を適当に埋めるのではなく、
     *
     *   現在設定をベースに差分だけ変更する
     *
     * という考え方を取っている。
     *
     * termios ではこのやり方が基本である。
     */
    new = old;

    /*
     * new.c_lflag &= ~ECHO;
     *
     * ここがこのコードの核心である。
     *
     * c_lflag はローカルモードフラグのビット集合であり、
     * その中の ECHO ビットを落としている。
     *
     * 式を分解するとこうなる。
     *
     *   ECHO
     *     -> 「入力文字を端末が自動表示する」機能を表すビット
     *
     *   ~ECHO
     *     -> ECHO ビットだけ 0、それ以外は 1 のマスク
     *
     *   x &= ~ECHO
     *     -> x の中の ECHO ビットだけを 0 にし、
     *        それ以外のビットはそのまま残す
     *
     * つまりこの 1 行の意味は、
     *
     *   「ローカルフラグのうち、ECHO だけを無効にする」
     *
     * である。
     *
     * ----------------------------------------
     * なぜビット演算を使うのか
     * ----------------------------------------
     *
     * c_lflag は単一の真偽値ではなく、
     * いくつもの設定が 1 つの整数にビットとして詰め込まれている。
     *
     * たとえば概念的には、
     *
     *   c_lflag = ECHO | ICANON | ISIG | ...
     *
     * のような形で複数機能が同時に有効になっていることがある。
     *
     * ここで単純に
     *
     *   new.c_lflag = 0;
     *
     * のようなことをすると、
     * ECHO 以外の設定まで全部消してしまう。
     *
     * だから
     *
     *   必要なビットだけを落とす
     *
     * という書き方をしている。
     *
     * ----------------------------------------
     * このコードで実際に何が変わるか
     * ----------------------------------------
     *
     * この 1 行の後、端末は
     *
     *   「入力文字を自動表示しない」
     *
     * 状態になる。
     *
     * ただし ICANON はそのままなので、
     *
     *   行単位入力（Enter までまとめる）
     *
     * という挙動は変わらない。
     *
     * つまりこのコードは
     *
     *   「ECHO だけ切る」
     *
     * 例であり、
     *
     *   「非カノニカルモードへ切り替える」
     *
     * 例ではない。
     */
    new.c_lflag &= ~ECHO;

    /*
     * tcsetattr(STDIN_FILENO, TCSANOW, &new);
     *
     * ここで変更後の端末設定 new を実際に端末へ反映する。
     *
     * tcsetattr(fd, optional_actions, termios_p) の意味:
     *
     *   「fd に対応する端末へ、termios_p で指定した新しい属性を適用する」
     *
     * である。
     *
     * 第2引数 TCSANOW は、
     *
     *   「今すぐ反映する」
     *
     * ことを意味する。
     *
     * つまりこの行以降、
     * 標準入力に対応する端末では ECHO が無効になる。
     *
     * ----------------------------------------
     * TCSANOW の意味
     * ----------------------------------------
     *
     * termios には反映タイミングとしていくつか選択肢がある。
     *
     *   TCSANOW
     *     -> 直ちに適用
     *
     *   TCSADRAIN
     *     -> 出力が吐き終わってから適用
     *
     *   TCSAFLUSH
     *     -> 未処理入力を捨てるなどして適用
     *
     * このコードは単純にすぐエコーを切りたいので TCSANOW を使っている。
     *
     * つまりこの行で、
     *
     *   「これ以降の入力は端末が勝手には表示しない」
     *
     * 状態へ移る。
     */
    tcsetattr(STDIN_FILENO, TCSANOW, &new);

    /*
     * while ((c = getchar()) != EOF)
     *
     * ここから実際の入出力ループである。
     *
     * getchar() は標準入力から 1 文字読む関数である。
     * ただし今の端末は
     *
     *   ECHO = OFF
     *
     * なので、ユーザがタイプしても端末は自動表示しない。
     *
     * しかし ICANON はまだ ON のままなので、
     * getchar() は通常
     *
     *   Enter が押されるまで入力行が確定しない
     *
     * 挙動を続ける。
     *
     * つまりこのコードは
     *
     *   「1文字入力のたびに即反応する」
     *
     * ような raw モードではない。
     *
     * あくまで
     *
     *   「行入力のままだが、自動エコーだけ消した状態」
     *
     * で動く。
     *
     * そのため、ユーザ体感としては
     *
     *   - キーを打っても画面にすぐは見えない
     *   - Enter を押すと getchar() 側に文字が渡る
     *   - その後 putchar() でプログラムが表示する
     *
     * という流れになる。
     *
     * EOF になるまで、つまり通常は Ctrl+D などで入力終了になるまで
     * このループを続ける。
     */
    while ((c = getchar()) != EOF) {
        /*
         * putchar(c);
         *
         * getchar() で受け取った文字を標準出力へ 1 文字書き戻す。
         *
         * ここで重要なのは、
         *
         *   端末の自動エコーではなく、
         *   プログラム自身が明示的に表示している
         *
         * 点である。
         *
         * つまりこのコードは
         *
         *   「ECHO を切った結果、端末は勝手に表示しないが、
         *    必要ならプログラム自身が表示できる」
         *
         * ことを示している。
         *
         * これにより、
         *
         *   自動エコーとプログラム側表示は別物
         *
         * だと分かる。
         *
         * たとえばパスワード入力では、
         *
         *   - 端末の ECHO は切る
         *   - 入力文字そのものは表示しない
         *
         * という設計がよく使われる。
         *
         * このコードはその第一歩として、
         *
         *   「ECHO を切った上で、自前で表示する」
         *
         * 例になっている。
         */
        putchar(c);
    }

    /*
     * tcsetattr(STDIN_FILENO, TCSANOW, &old);
     *
     * ここで保存しておいた元の端末設定を復元する。
     *
     * これは非常に重要である。
     *
     * termios で端末設定を変更したまま終了すると、
     * その端末セッションが
     *
     *   「入力しても文字が見えない」
     *
     * などの不自然な状態のまま残ってしまうことがある。
     *
     * したがって、端末設定をいじったら最後に戻す、というのは基本である。
     *
     * このコードでは old に最初の状態を保存していたので、
     *
     *   完全に元へ戻せる
     *
     * わけである。
     *
     * ----------------------------------------
     * なぜ最後の復元が大事か
     * ----------------------------------------
     *
     * もしこの復元が無いと、
     * このプログラム終了後も端末で ECHO が無効のまま残り、
     *
     *   シェルに戻っても打鍵が見えない
     *
     * といった困った状態になることがある。
     *
     * だから termios を触るプログラムでは、
     *
     *   - 最初に現状を保存
     *   - 最後に必ず復元
     *
     * が基本パターンになる。
     *
     * 本来は、
     *
     *   - エラーで途中終了しても復元する
     *   - シグナルで落ちても復元する
     *
     * といった配慮もあるとさらに良い。
     * このサンプルでは基本形だけを示している。
     */
    tcsetattr(STDIN_FILENO, TCSANOW, &old);

    /*
     * main() の終わり。
     *
     * このプログラム全体を一言でまとめると、
     *
     *   「termios で ECHO ビットだけを落として端末の自動エコーを無効にし、
     *    入力文字をプログラム自身で表示し、最後に端末設定を元へ戻す」
     *
     * 例である。
     *
     * 主に学ぶべき点は、
     *
     *   - termios は端末設定の集合である
     *   - tcgetattr() で現在設定を読む
     *   - 必要なビットだけ変更する
     *   - tcsetattr() で反映する
     *   - 最後に必ず元へ戻す
     *
     * という基本パターンである。
     */
}
