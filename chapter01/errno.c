#include <stdio.h>   /* perror を使うためのヘッダ */
#include <errno.h>   /* errno および ENOENT を使うためのヘッダ */

int main(void)
{
    /*
     * errno は「直近で発生したエラー内容」を表すために使われる整数変数である。
     *
     * 通常は、
     *   - open()
     *   - read()
     *   - write()
     *   - fork()
     *   - execve()
     * などのシステムコールや、
     * 一部のライブラリ関数が失敗したときに自動的に設定される。
     *
     * ここでは実際にシステムコールを失敗させる代わりに、
     * 手動で errno にエラー番号を代入して、
     * perror() がどのようにエラーメッセージを表示するかを確認している。
     */
    errno = ENOENT;

    /*
     * ENOENT は error number の一種で、
     * "No such file or directory"
     * つまり「そのようなファイルやディレクトリは存在しない」
     * という意味を表す定数である。
     *
     * UNIX/Linux では、たとえば open() で存在しないファイルを開こうとした場合に
     * errno に ENOENT が設定されることがある。
     */

    /*
     * perror("test") は、
     *
     *   "test: " + errno に対応するエラーメッセージ + 改行
     *
     * を標準エラー出力(stderr)に出力する関数である。
     *
     * この時点では errno = ENOENT なので、
     * 実行結果は典型的には次のようになる。
     *
     *   test: No such file or directory
     *
     * ここでの "test" は、呼び出し側が付ける見出しのような文字列であり、
     * どの処理でエラーが起きたのかを分かりやすくするために使う。
     *
     * 注意:
     * perror() 自体は errno を設定する関数ではなく、
     * 「現在の errno の内容を人間向けの文字列にして表示する」ための関数である。
     */
    perror("test");

    /*
     * main の戻り値を省略すると、C の新しい規格では望ましくないため、
     * 明示的に 0 を返して正常終了にしておくとより丁寧である。
     *
     * ただし、この元コードは簡潔な動作確認用なので、
     * 処理内容そのものには問題はない。
     */
    return 0;
}
