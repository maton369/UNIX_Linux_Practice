#include <stdio.h>   /* printf を使うためのヘッダ */

/*
 * 共用体 (union) foo の定義
 *
 * 共用体は、構造体 (struct) と似た文法で複数のメンバを定義できるが、
 * 最も重要な違いは「各メンバが別々の領域を持つのではなく、
 * 同じメモリ領域を共有する」点である。
 *
 * ここでは
 *
 *   int    i;
 *   double d;
 *
 * という 2 つのメンバを定義している。
 *
 * struct の場合:
 *   - i 用の領域
 *   - d 用の領域
 * を別々に確保する。
 *
 * union の場合:
 *   - i と d が同じ先頭アドレスから始まる
 *   - 1つの実体メモリを、見る型を変えて使う
 *
 * そのため、あるメンバへ書き込むと、そのメモリ内容は他のメンバからも見える。
 *
 * 典型的なイメージ:
 *
 *   union foo x;
 *
 *   +----------------------------------+
 *   |      共有される1つの領域         |
 *   +----------------------------------+
 *    ^                                ^
 *    |                                |
 *   x.i として見る                 x.d として見る
 *
 * サイズについて:
 *   共用体のサイズは、通常は「最も大きいメンバを格納できる大きさ」
 *   以上になる。必要ならアラインメントの都合で余白が付くこともある。
 *
 * この例では多くの環境で
 *
 *   sizeof(int)    = 4
 *   sizeof(double) = 8
 *
 * なので、sizeof(union foo) は典型的には 8 になる。
 *
 * 注意:
 *   実際のサイズや配置は環境 (CPU, ABI, コンパイラ) に依存する。
 */
union foo {
    int i;       /* int 型としてこの共有領域を見るためのメンバ */
    double d;    /* double 型としてこの共有領域を見るためのメンバ */
};

int main(void)
{
    /*
     * union foo 型の変数 x を定義する。
     *
     * x は「int としても見られるし、double としても見られる」
     * 1つの共有領域を持つ。
     *
     * ここで重要なのは、
     * x.i 用の領域と x.d 用の領域が別々にあるのではなく、
     * 同じメモリを 2 通りの型で解釈しているだけ、という点である。
     */
    union foo x;

    /*
     * x.d に 12.34 を代入する。
     *
     * これは「共有領域に double 型の表現で 12.34 を書き込む」
     * という意味である。
     *
     * つまりこの代入の直後、共有領域の中身は
     * 「double としての 12.34 のビット列」になっている。
     *
     * イメージ:
     *
     *   x の共有領域
     *   +----------------------------------+
     *   |  12.34 を表す double のビット列  |
     *   +----------------------------------+
     *
     * この時点で x.d を読むと、
     * 書き込んだ通りの 12.34 が得られることを期待できる。
     */
    x.d = 12.34;

    /*
     * x.d を %f で表示する。
     *
     * これは「double として書いたものを、double として読む」ので、
     * 直前の代入内容がそのまま表示される。
     *
     * 典型的には次のような出力になる。
     *
     *   12.340000
     *
     * これは正常であり、共用体の自然な使い方である。
     */
    printf("%f\n", x.d);

    /*
     * x.i を %d で表示する。
     *
     * ここが共用体の学習上の重要ポイントである。
     *
     * 先ほど x.d = 12.34; によって、共有領域には
     * 「double としての 12.34 のビット列」が入っている。
     *
     * しかしここでは、その同じメモリ領域を
     * int 型の値として読み取ろうとしている。
     *
     * つまり、起きていることは概念的には
     *
     *   「double のビット列を int として見ている」
     *
     * ということである。
     *
     * そのため、ここで表示される整数値は
     * 「12」や「13」のような意味のある数になるとは限らない。
     * 実際には、double 12.34 のメモリ表現の一部を
     * int として解釈した値が出るだけである。
     *
     * 典型的には「よく分からない整数値」が出力される。
     *
     * なぜそうなるか:
     *   double と int は内部表現が異なるからである。
     *
     * - int    は通常、2の補数形式の整数ビット列
     * - double は通常、IEEE 754 形式の浮動小数点ビット列
     *
     * で表現される。
     *
     * したがって、double として意味のあるビット列を
     * int として見ても、普通は「元の数値の意味」を保たない。
     *
     * 重要な注意:
     *   C言語では、最後に書き込んだメンバとは異なるメンバを読む行為は、
     *   実装依存・未規定の絡む微妙な領域であり、移植性のあるコードとしては危険である。
     *
     *   学習目的として
     *   「同じメモリを別の型で見るとどう見えるか」
     *   を観察する題材としては有用だが、
     *   実務で安易に使うべきではない。
     *
     * つまりこの printf は、
     *   共用体によってメモリ共有が起きていること
     *   そして「型が変わると同じビット列の意味も変わる」こと
     * を示すための観察コードである。
     */
    printf("%d\n", x.i);

    /*
     * まとめ:
     *
     * このコードから学べることは主に次の通りである。
     *
     * 1. 共用体は複数メンバが同じメモリ領域を共有する
     *
     *    struct のように別々に並ぶのではなく、
     *    union では同じ領域を別の型として使う。
     *
     * 2. あるメンバに書いた内容は、他のメンバからも見える
     *
     *    ただしそれは「同じ意味で見える」とは限らない。
     *
     * 3. 型が異なれば、同じビット列でも解釈が変わる
     *
     *    double 12.34 の表現を int として読んでも、
     *    普通は意味のある整数にはならない。
     *
     * 4. 共用体は低レイヤーでは有用だが、扱いには注意が必要
     *
     *    典型的な用途:
     *    - ハードウェアレジスタの多面的な見方
     *    - バイナリ解析
     *    - メモリ節約
     *    - タグ付き共用体の実装
     *
     *    ただし型の読み替えは移植性や規格上の注意点が多い。
     *
     * 実務で安全性を重視するなら:
     *   - ビット列の再解釈には memcpy を使う
     *   - どのメンバが現在有効かを別のタグで管理する
     *   - union を「メモリ節約」や「排他的状態の表現」に使う
     * といった設計がよく行われる。
     */

    return 0;
}
