#include <stdio.h>   /* printf を使うためのヘッダ */

int main(int argc, char *argv[])
{
    /*
     * main 関数の引数について:
     *
     *   argc
     *     = argument count の略であり、
     *       コマンドライン引数の個数を表す。
     *
     *   argv
     *     = argument vector の略であり、
     *       各コマンドライン引数へのポインタを並べた配列である。
     *
     * UNIX/Linux では、シェルからプログラムを実行すると、
     * 入力したコマンドラインはカーネルと C ランタイムによって分解され、
     * main の argc / argv としてプログラムに渡される。
     *
     * 例えば次のように実行したとする。
     *
     *   ./a.out hello world
     *
     * このとき典型的には以下のようになる。
     *
     *   argc = 3
     *   argv[0] = "./a.out"
     *   argv[1] = "hello"
     *   argv[2] = "world"
     *
     * つまり argv[0] には通常「プログラム名」またはその実行時表記が入り、
     * argv[1] 以降にユーザが与えた引数が順に入る。
     *
     * なお、argv[argc] には慣習的に NULL が入るが、
     * このコードでは argc を使って範囲を管理しているため、
     * argv[argc] そのものは参照していない。
     */

    int i;

    /*
     * i を 0 から argc - 1 まで増やしながらループする。
     *
     * このループの目的は、
     * 「argv 配列の中に入っている全ての引数文字列を順番に表示すること」
     * である。
     *
     * 条件 i < argc としているため、
     * 有効な引数だけをちょうど1回ずつ処理する。
     */
    for (i = 0; i < argc; i++) {

        /*
         * printf で現在の引数番号と、その中身の文字列を表示する。
         *
         * 書式文字列:
         *   "argv [%d] = %s\n"
         *
         *   %d : 整数 i を表示する
         *   %s : argv[i] が指す文字列を表示する
         *   \n : 行末の改行
         *
         * 例えば
         *
         *   ./a.out abc def
         *
         * と実行すると、典型的には次のような出力になる。
         *
         *   argv [0] = ./a.out
         *   argv [1] = abc
         *   argv [2] = def
         *
         * ここで argv[i] は「文字列そのもの」ではなく、
         * 文字列の先頭アドレスを指すポインタである。
         * しかし printf の %s は「char * が指す C 文字列」を表示するため、
         * 人間には普通の文字列として見える。
         */
        printf("argv [%d] = %s\n", i, argv[i]);
    }

    /*
     * すべての引数を表示し終えたので正常終了する。
     *
     * 元のコードでは return が省略されていたが、
     * C では main の最後で 0 を返すと「正常終了」を明示できるため、
     * 学習用には書いておく方が分かりやすい。
     */
    return 0;
}
