#include <stdio.h>   /* printf を使うためのヘッダ */

/*
 * 構造体 foo の定義
 *
 * メンバ:
 *   a : char 型
 *   b : int 型
 *
 * このコードの主題は「構造体のパディング (padding)」である。
 *
 * パディングとは:
 *   コンパイラが、各メンバをCPUにとって扱いやすい境界に揃えるために、
 *   メンバとメンバの間、あるいは構造体の末尾に自動的に挿入する
 *   「見えない余白のバイト」のことである。
 *
 * なぜ必要か:
 *   多くのCPUは、int や long などを「ある程度そろったアドレス境界」に置いた方が
 *   効率よくアクセスできる。場合によっては、ずれた境界にあるデータを読むと
 *   速度低下や例外の原因になることもある。
 *
 * そのためコンパイラは、構造体の各メンバを適切なアラインメント境界
 *   (alignment: 配置境界)
 * に置くように、必要なら隙間を入れる。
 *
 * ここでは
 *   char a;   // 通常 1 バイト
 *   int  b;   // 通常 4 バイト
 * という順番なので、a の直後にそのまま b を置くと、
 * b が「4 バイト境界」に揃わない可能性がある。
 *
 * 典型的な 64bit Linux / x86_64 環境では:
 *
 *   オフセット 0 : a (1バイト)
 *   オフセット 1 : パディング
 *   オフセット 2 : パディング
 *   オフセット 3 : パディング
 *   オフセット 4 : b (4バイト)
 *
 * という配置になりやすい。
 *
 * 図にすると次のようなイメージである。
 *
 *   struct foo
 *
 *   +--------+--------+--------+--------+--------+--------+--------+--------+
 *   |   a    | padding| padding| padding|                b                 |
 *   +--------+--------+--------+--------+--------+--------+--------+--------+
 *   byte 0     byte 1   byte 2   byte 3   byte 4   byte 5   byte 6   byte 7
 *
 * すると sizeof(struct foo) は典型的には 8 になる。
 *
 * 注意:
 *   これは「よくある実装」であり、C言語規格が
 *   必ずこのサイズ・この配置を保証しているわけではない。
 *   実際の値は環境 (CPU, ABI, コンパイラ) に依存する。
 */
struct foo {
    char a;   /* 1バイトの文字型メンバ。先頭に置かれる */
    int  b;   /* 通常4バイト。アラインメントの都合で前にパディングが入ることが多い */
} f;          /* 構造体 foo 型のグローバル変数 f を定義 */

/*
 * グローバル変数 f について:
 *
 * この変数は main の外で定義されているので、静的記憶域期間を持つ。
 * そのため、プログラム開始時にゼロ初期化される。
 *
 * つまり実行開始直後の f は概ね以下のような状態である。
 *
 *   f.a == 0
 *   f.b == 0
 *
 * ただし今回のプログラムでは、値そのものよりも
 * 「サイズ」と「アドレス配置」を観察することが目的である。
 */

int main(void)
{
    /*
     * 1つ目の printf:
     *
     *   sizeof(char)
     *   sizeof(int)
     *   sizeof(struct foo)
     *
     * を表示する。
     *
     * ここで観察したいポイント:
     *
     * 1. char の大きさ
     *    C言語では sizeof(char) は必ず 1 である。
     *    ただし「1バイト」の実ビット数は通常 8 ビットだが、規格上は CHAR_BIT に依存する。
     *
     * 2. int の大きさ
     *    多くの環境では 4 バイトだが、規格上は実装依存である。
     *
     * 3. struct foo の大きさ
     *    単純に char(1) + int(4) = 5 とは限らない。
     *    実際にはパディングが入るため、典型的には 8 になる。
     *
     * つまりこの出力により、
     *
     *   「構造体サイズは、単なるメンバサイズの合計ではない」
     *
     * ことを確認できる。
     *
     * 典型例:
     *
     *   sizeof(char)       = 1
     *   sizeof(int)        = 4
     *   sizeof(struct foo) = 8
     *
     * もし 8 が出たなら、
     *   a の後ろに 3 バイトのパディングが入っている
     * と考えるとつじつまが合う。
     */
    printf("%d, %d, %d\n",
           (int)sizeof(char),
           (int)sizeof(int),
           (int)sizeof(struct foo));

    /*
     * 2つ目の printf:
     *
     *   &f
     *   &f.a
     *   &f.b
     *
     * を表示する。
     *
     * ここで観察したいポイント:
     *
     * - &f   は構造体全体の先頭アドレス
     * - &f.a は最初のメンバ a のアドレス
     * - &f.b は int 型メンバ b のアドレス
     *
     * 通常、先頭メンバ a は構造体の先頭に置かれるので、
     *
     *   &f == &f.a
     *
     * になることが多い。
     *
     * 一方、b は a の直後ではなく、
     * パディングを挟んだ位置に置かれることがある。
     *
     * 典型的な例:
     *
     *   &f   = 0x1000
     *   &f.a = 0x1000
     *   &f.b = 0x1004
     *
     * このとき、
     *
     *   0x1001
     *   0x1002
     *   0x1003
     *
     * に相当する3バイト分が、a と b の間のパディングであると解釈できる。
     *
     * つまり、この printf は
     *
     *   「メモリ上で a と b が本当に離れて配置されている」
     *
     * ことを、アドレス差として観察するためのものである。
     *
     * 注意:
     *   %p でポインタを表示する場合、C言語では引数を (void *) にキャストするのが正式な書き方である。
     *   そのため、ここでは (void *) を付けている。
     */
    printf("%p, %p, %p\n",
           (void *)&f,
           (void *)&f.a,
           (void *)&f.b);

    /*
     * このプログラムから学べることの整理
     *
     * 1. 構造体のサイズは単純な合計とは限らない
     *
     *    char a; int b;
     *
     *    なので見た目は 1 + 4 = 5 バイトだが、
     *    実際にはアラインメントのためにパディングが入り、
     *    sizeof(struct foo) が 8 になることがある。
     *
     * 2. パディングはメモリ上のアドレス差として観察できる
     *
     *    &f.a と &f.b の差が 4 なら、
     *    a の1バイトの後に3バイトの余白があると考えられる。
     *
     * 3. CPU効率やABI上の都合で、コンパイラは配置を最適化している
     *
     *    Cの構造体は「書いた順に隙間なく並ぶ」とは限らない。
     *    低レイヤーではこの点が非常に重要である。
     *
     * 4. バイナリ互換性・ファイルフォーマット・ネットワーク通信では要注意
     *
     *    構造体をそのまま fwrite / send したり、
     *    別言語や別環境とメモリレイアウトを共有したりすると、
     *    パディングの違いがバグの原因になる。
     *
     *    そのため実務では、
     *    - 各フィールドを個別にシリアライズする
     *    - 固定長整数型を使う
     *    - packed 指定を慎重に使う
     *    - ABI を明示的に合わせる
     *    といった対策を取る。
     */

    return 0;
}
