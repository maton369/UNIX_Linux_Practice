#include <stdio.h>    /* perror を使うためのヘッダ */
#include <stdlib.h>   /* exit, system を使うためのヘッダ */
#include <unistd.h>   /* rmdir を使うためのヘッダ */
#include <errno.h>    /* errno と ENOTEMPTY を使うためのヘッダ */

int main(void)
{
    /*
     * rmdir("foo") は、現在の作業ディレクトリにある
     * ディレクトリ "foo" を削除しようとするシステムコールのラッパである。
     *
     * 重要な点:
     * - rmdir は「空のディレクトリ」しか削除できない。
     * - "foo" の中にファイルやサブディレクトリが残っていると失敗する。
     * - 成功時は 0、失敗時は -1 を返し、
     *   失敗理由は errno に設定される。
     *
     * ここでは、戻り値が -1 かどうかで失敗を判定している。
     */
    if (rmdir("foo") == -1) {

        /*
         * errno は、失敗した関数の直後にすぐ保存しておくのが重要である。
         *
         * なぜなら、その後に別の関数を呼ぶと errno の値が変わる可能性があるからである。
         * 特に perror() や system() のような関数呼び出しを挟むと、
         * デバッグ時に「本当に rmdir が返した errno が何だったのか」が
         * 分かりにくくなることがある。
         *
         * そのため、ここではまず old_errno に保存して、
         * 「rmdir が失敗した直後の errno」を確実に保持している。
         */
        int old_errno = errno;

        /*
         * perror("rmdir") は、現在の errno に対応するエラーメッセージを
         * 標準エラー出力(stderr)へ表示する。
         *
         * 形式は次のようになる。
         *
         *   rmdir: <errno に対応する文字列>
         *
         * 例えば "foo" が空でない場合、環境によっては次のように表示される。
         *
         *   rmdir: Directory not empty
         *
         * ここでの "rmdir" は、どの処理でエラーが起きたのかを示すラベルである。
         */
        perror("rmdir");

        /*
         * old_errno が ENOTEMPTY かどうかを確認している。
         *
         * ENOTEMPTY は
         * 「ディレクトリが空ではないため削除できない」
         * ことを表すエラー番号である。
         *
         * つまり、この if 文は
         * 「削除に失敗した理由が『中身が残っているから』である場合だけ」
         * 特別な追加処理を行う、という意味になる。
         *
         * ここで old_errno を使っているのは正しい書き方である。
         * perror() の後に errno を直接見に行くと、
         * 別の値に変化している可能性が理論上あるためである。
         */
        if (old_errno == ENOTEMPTY) {

            /*
             * system("ls foo") は、シェルを介して
             *
             *   ls foo
             *
             * というコマンドを実行する。
             *
             * ここでの意図は、
             * 「foo が空でないために削除できなかったなら、
             *  実際に foo の中に何が入っているのか一覧表示して確認しよう」
             * というデバッグ・観察用の処理である。
             *
             * つまり全体の流れは、
             *
             *   rmdir("foo") に失敗
             *       ↓
             *   失敗理由を perror で表示
             *       ↓
             *   もし理由が ENOTEMPTY なら
             *   ls foo で中身を見せる
             *
             * となっている。
             *
             * 注意点:
             * - system() は内部でシェルを起動するため、簡単だが重い。
             * - セキュリティ上、ユーザ入力を直接 system に渡すのは危険である。
             * - 今回は固定文字列 "ls foo" なので学習用としては分かりやすい。
             *
             * 実務では、必要なら opendir/readdir などを使って
             * C のコードとして安全に列挙する方法もよく使われる。
             */
            system("ls foo");
        }

        /*
         * exit(1) でプログラムを異常終了させる。
         *
         * ここで 1 は「エラーが起きたため正常終了ではない」ことを表す終了ステータスである。
         * UNIX/Linux では慣習的に、
         *
         * - 0   : 正常終了
         * - 0以外: 異常終了
         *
         * として扱うことが多い。
         *
         * このコードでは rmdir に失敗した時点で、
         * その後の正常処理は続けられないという前提で exit(1) している。
         */
        exit(1);
    }

    /*
     * rmdir("foo") が成功した場合は if 文に入らず、ここまで到達する。
     *
     * この場合、"foo" は正しく削除されたことになる。
     * main の最後で 0 を返して正常終了する。
     */
    return 0;
}
