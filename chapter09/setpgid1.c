#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <sys/types.h>  /* pid_t, fork(), getpid() に関係する型定義 */
#include <unistd.h>     /* fork(), getpid(), setpgid(), sleep() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「fork() で親子プロセスを作ったあと、
 *    子プロセスだけを別のプロセスグループへ移し、
 *    それでも端末のフォアグラウンドプロセスグループは自動では変わらない」
 *
 * ことである。
 *
 * これは単に
 *
 *   - 親が '@' を出す
 *   - 子が '.' を出す
 *
 * という出力の違いを観察するコードではない。
 *
 * 本質は、
 *
 *   1. fork() 直後は親子は通常同じプロセスグループにいる
 *   2. しかし子が setpgid(getpid(), getpid()) を呼ぶことで、
 *      子だけが新しいプロセスグループのリーダーになる
 *   3. それでも「端末のフォアグラウンドプロセスグループ」は
 *      自動では切り替わらない
 *
 * という点にある。
 *
 * ----------------------------------------
 * まず用語整理
 * ----------------------------------------
 *
 * UNIX 系 OS では、プロセスは単独で存在するだけでなく、
 *
 *   - プロセスグループ
 *   - セッション
 *
 * といった単位にも属している。
 *
 * このうちプロセスグループは、
 *
 *   「関連する複数プロセスをまとめて 1 つの単位として扱う仕組み」
 *
 * である。
 *
 * 特に端末（ターミナル）では、
 *
 *   フォアグラウンドプロセスグループ
 *
 * という概念が重要である。
 *
 * 端末から Ctrl+C を押すと、
 *
 *   その端末のフォアグラウンドプロセスグループ全体
 *
 * に SIGINT が送られる。
 *
 * Ctrl+Z なら SIGTSTP が、
 * Ctrl+\ なら SIGQUIT が、
 * 同じくフォアグラウンドプロセスグループ全体へ送られる。
 *
 * 重要なのは、
 *
 *   「どのプロセスグループがフォアグラウンドか」
 *
 * は端末側が持っている状態であって、
 *
 *   setpgid() でプロセスグループを変えただけでは、
 *   自動的にフォアグラウンドグループまで切り替わるわけではない
 *
 * という点である。
 *
 * このコードはまさにそこを観察するためのサンプルである。
 *
 * ----------------------------------------
 * このコードで起きること
 * ----------------------------------------
 *
 *   1. fork() で親子を作る
 *   2. 親は '@'、子は '.' を出すようにする
 *   3. 子だけが setpgid(getpid(), getpid()) を呼ぶ
 *      -> 子は自分自身の PID を PGID にした新しいプロセスグループへ移る
 *   4. 親は元のプロセスグループのまま
 *   5. 端末のフォアグラウンドプロセスグループは通常は親側のまま
 *   6. そのため Ctrl+C などは通常、親側のグループへ届く
 *   7. 子は別グループに移っているので、端末からの Ctrl+C を
 *      親と同じようには受けないことがある
 *
 * つまりこのコードは、
 *
 *   「同じ端末にぶら下がっていても、プロセスグループを分けると
 *    端末シグナルの届き方が変わる」
 *
 * ことを示す例である。
 *
 * ただしさらに厳密に言うと、
 *
 *   フォアグラウンドプロセスグループを切り替える操作は
 *   tcsetpgrp() など別の仕組みで行う
 *
 * 必要がある。
 *
 * このコードでは tcsetpgrp() は呼んでいないので、
 *
 *   「子は新しいプロセスグループにはなったが、
 *    フォアグラウンドプロセスグループになったわけではない」
 *
 * という状態になる。
 */

int main(void)
{
    /*
     * pid = fork();
     *
     * 現在のプロセスを親子 2 つのプロセスへ分岐させる。
     *
     * 戻り値の意味:
     *
     *   親プロセス:
     *     子プロセスの PID が返る（正の値）
     *
     *   子プロセス:
     *     0 が返る
     *
     *   失敗時:
     *     -1 が返る
     *
     * このコードではエラーチェックを省略しているが、
     * 実務では pid == -1 を必ず確認したい。
     *
     * ----------------------------------------
     * fork() 直後のプロセスグループ
     * ----------------------------------------
     *
     * fork() しただけでは、
     *
     *   子は通常、親と同じプロセスグループに属したまま生まれる
     *
     * ことが重要である。
     *
     * たとえば fork() 前に
     *
     *   親:
     *     PID  = P1
     *     PGID = G
     *
     * だったとすると、fork() 直後の典型状態は
     *
     *   親:
     *     PID  = P1
     *     PGID = G
     *
     *   子:
     *     PID  = P2
     *     PGID = G
     *
     * となる。
     *
     * つまり
     *
     *   PID は別になる
     *   PGID は同じまま
     *
     * である。
     *
     * この「最初は同じ PGID」という前提が、
     * 後で setpgid() によってどう変わるかを観察するのがこのコードの狙いである。
     */
    pid_t pid = fork();

    /*
     * c:
     *   親子を見分けるための文字である。
     *
     *   親 -> '@'
     *   子 -> '.'
     *
     * としている。
     *
     * これにより実行中の出力が
     *
     *   どちらのプロセスによるものか
     *
     * を目で追いやすくなる。
     *
     * ここで
     *
     *   pid == 0 なら子
     *   pid != 0 なら親
     *
     * なので、
     *
     *   子プロセス  -> '.'
     *   親プロセス  -> '@'
     *
     * になる。
     */
    int c = (pid == 0 ? '.' : '@');

    /*
     * if (pid == 0)
     *
     * ここから子プロセスだけの特別処理に入る。
     *
     * 親はこの if の中には入らない。
     */
    if (pid == 0) {
        /*
         * setpgid(getpid(), getpid());
         *
         * ここがこのコードの核心である。
         *
         * setpgid(pid, pgid) は、
         *
         *   「PID = pid のプロセスを、PGID = pgid のプロセスグループへ移す」
         *
         * システムコールである。
         *
         * 今回は
         *
         *   pid  = getpid()
         *   pgid = getpid()
         *
         * なので、
         *
         *   「自分自身を、自分自身の PID をグループ ID とする
         *    新しいプロセスグループへ移す」
         *
         * という意味になる。
         *
         * これは結果として
         *
         *   子プロセスが新しいプロセスグループのリーダーになる
         *
         * ことを意味する。
         *
         * ----------------------------------------
         * 実行前後のイメージ
         * ----------------------------------------
         *
         * fork() 直後:
         *
         *   親:
         *     PID  = P1
         *     PGID = G
         *
         *   子:
         *     PID  = P2
         *     PGID = G
         *
         * setpgid(getpid(), getpid()) 後:
         *
         *   親:
         *     PID  = P1
         *     PGID = G
         *
         *   子:
         *     PID  = P2
         *     PGID = P2
         *
         * つまり子だけが、
         *
         *   元の親のグループ G から抜けて、
         *   自分をリーダーとする新しいグループへ移る
         *
         * わけである。
         *
         * ----------------------------------------
         * ここで重要な誤解しやすい点
         * ----------------------------------------
         *
         * setpgid() はプロセスグループを変えるだけであって、
         *
         *   フォアグラウンドプロセスグループを変えるわけではない
         *
         * という点が重要である。
         *
         * 端末がどの PGID をフォアグラウンドとみなすかは、
         * 通常は tcsetpgrp() など別の仕組みで制御する。
         *
         * このコードでは tcsetpgrp() を使っていないので、
         *
         *   子は「新しい別グループ」にはなったが、
         *   その端末のフォアグラウンドグループになったわけではない
         *
         * ことになる。
         *
         * そのため Ctrl+C を押したとき、
         *
         *   親のいる元の前景グループにはシグナルが届くが、
         *   子の新しい別グループには同じようには届かないことがある
         *
         * という違いが観察できる。
         *
         * つまりこの行は、
         *
         *   「プロセスグループの所属を変える」
         *
         * が、
         *
         *   「フォアグラウンド化する」
         *
         * のではない。
         *
         * ここがこのコードで学ぶべき最大のポイントである。
         */
        setpgid(getpid(), getpid());
    }

    /*
     * while (1)
     *
     * ここから親子ともに無限ループへ入る。
     *
     * ただし、ここへ来る時点で
     *
     *   親:
     *     元のプロセスグループのまま
     *
     *   子:
     *     新しいプロセスグループへ移動済み
     *
     * である。
     *
     * つまり、
     *
     *   2つのプロセスは同じ端末へ出力しているが、
     *   もはや同じプロセスグループではない
     *
     * という状態で並行動作する。
     *
     * ここが前の「親子が同じ PGID のままだった例」との違いである。
     */
    while (1) {
        /*
         * fprintf(stderr, "%c", c);
         *
         * 親なら '@'、子なら '.' を 1 文字だけ標準エラー出力へ出す。
         *
         * stderr を使っている理由:
         *   stdout よりもバッファリングの影響を受けにくく、
         *   リアルタイムに見えやすいからである。
         *
         * これにより
         *
         *   親はまだ動いているか
         *   子はまだ動いているか
         *
         * を簡単に観察できる。
         *
         * ----------------------------------------
         * フォアグラウンドプロセスグループとの関係
         * ----------------------------------------
         *
         * この出力自体は、親子が同じ端末を共有していることを示している。
         * しかし
         *
         *   同じ端末に出力している
         *
         * ことと
         *
         *   同じフォアグラウンドプロセスグループにいる
         *
         * ことは別である。
         *
         * このコードでは setpgid() により子は別グループへ移っているので、
         * 以後は
         *
         *   「同じ端末に出ているが、端末制御シグナルの扱いは同じではない」
         *
         * 状態が起きうる。
         */
        fprintf(stderr, "%c", c);

        /*
         * sleep(1);
         *
         * 1 秒待つ。
         *
         * これにより親子の出力がゆっくりになり、
         * 端末操作の影響を観察しやすくしている。
         *
         * もし sleep() が無ければ、文字が高速に出すぎて
         * 何が起きているか追いにくくなる。
         *
         * ----------------------------------------
         * Ctrl+C を押したときに何が起きるか
         * ----------------------------------------
         *
         * このコードで最も観察したいのはここである。
         *
         * Ctrl+C は端末の
         *
         *   フォアグラウンドプロセスグループ
         *
         * に対して SIGINT を送る。
         *
         * このプログラムでは、子は setpgid() によって別グループへ移動している。
         * しかし端末のフォアグラウンドグループは自動では切り替わらないので、
         * 通常は親の属する元のグループがフォアグラウンドのままである。
         *
         * そのため典型的には、
         *
         *   親には SIGINT が届く
         *   子には同じようには届かない
         *
         * という状況が起こりうる。
         *
         * その結果、
         *
         *   '@' は止まるが '.' は残る
         *
         * といった挙動を観察できる場合がある。
         *
         * これが
         *
         *   「同じ端末にいること」と
         *   「同じフォアグラウンドプロセスグループにいること」
         *
         * は別だ、という実例である。
         *
         * つまりこのコードは、
         *
         *   setpgid() だけではフォアグラウンドプロセスグループは変わらない
         *
         * ことを体感するための実験でもある。
         */
        sleep(1);
    }

    /*
     * 通常ここには到達しない。
     *
     * 理由:
     *   while(1) が無限ループだからである。
     *
     * ただし端末実験としては、
     *
     *   - Ctrl+C で親側だけ止まる挙動
     *   - 子が別グループにいるため親と違う扱いになること
     *
     * などを観察するのが目的である。
     *
     * なお、もし本当に子のグループをフォアグラウンドへ切り替えたいなら、
     *
     *   tcsetpgrp()
     *
     * が必要になる。
     *
     * このコードはそこまではしていない。
     * あくまで
     *
     *   「プロセスグループを分けただけでは、
     *    フォアグラウンドプロセスグループにはならない」
     *
     * ことを示す最小例である。
     */
}
