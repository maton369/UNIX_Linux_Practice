#include <signal.h>     /* signal(), SIGINT を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <unistd.h>     /* pause() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「シグナルハンドラを登録し、pause() でシグナル待ちをし、
 *    シグナルを1回受け取ったあとに処理を再開する」
 *
 * ことである。
 *
 * これまでの
 *
 *   while(1) で動き続ける例
 *
 * とは違い、このプログラムは自分から何かを繰り返し実行しない。
 * 代わりに
 *
 *   pause()
 *
 * を呼んで、
 *
 *   「何らかのシグナルが届くまで完全に待機する」
 *
 * という動きをする。
 *
 * したがって、このプログラム全体の流れは次のようになる。
 *
 *   1. SIGINT を受けたら handler() を呼ぶように設定する
 *   2. pause() で停止し、シグナル待ち状態になる
 *   3. ユーザが Ctrl+C を押す
 *   4. プロセスに SIGINT が届く
 *   5. handler() が実行されて "handler\n" を出す
 *   6. pause() はそこで解除されて戻る
 *   7. 続いて "hello, world\n" を出す
 *   8. main() が終わり、プログラム終了
 *
 * つまりこのコードは、
 *
 *   「シグナルを1回のイベントとして待ち受け、
 *    そのイベントを契機に停止中の本体処理を再開する」
 *
 * 最小例である。
 */

/*
 * handler(int sig)
 *
 * これはシグナルハンドラである。
 *
 * シグナルハンドラとは、
 *
 *   「特定のシグナルが届いたときに OS から呼ばれる関数」
 *
 * である。
 *
 * 今回は main() で
 *
 *   signal(SIGINT, handler);
 *
 * と設定しているため、
 * SIGINT が届くとこの handler() が実行される。
 *
 * SIGINT は典型的には、
 *
 *   端末で Ctrl+C を押したとき
 *
 * に前景プロセスへ送られるシグナルである。
 *
 * 何も設定しなければ、SIGINT のデフォルト動作は通常
 *
 *   「プロセス終了」
 *
 * である。
 *
 * しかしこのコードでは handler() を登録しているので、
 * Ctrl+C が来ても即終了するのではなく、
 * まずこの関数が呼ばれる。
 */
void handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値である。
     *
     * 今回は SIGINT 用として登録しているので、
     * 通常は sig == SIGINT になる。
     *
     * ただしこのコードでは sig の値そのものは使っていない。
     * したがって未使用であることを明示しておく。
     */
    (void)sig;

    /*
     * fprintf(stderr, "handler\n");
     *
     * シグナルを受け取ったことを可視化するために、
     * 標準エラー出力へ "handler" と改行を出している。
     *
     * これにより実行時には、
     *
     *   Ctrl+C を押した瞬間に handler が呼ばれた
     *
     * ことが分かる。
     *
     * 注意:
     *   厳密な POSIX の観点では、シグナルハンドラ内で安全に使える関数は
     *   限られている。
     *
     *   fprintf() は一般に async-signal-safe としては推奨されない。
     *
     *   教材としては動作を見やすくするために使われることが多いが、
     *   実務では
     *
     *     write(2, "handler\n", 8);
     *
     *   のような、より安全な低水準出力が好まれることが多い。
     *
     * ここでは
     *
     *   「ハンドラが本当に呼ばれた」
     *
     * ことを学習しやすくするための出力と考えるとよい。
     */
    fprintf(stderr, "handler\n");
}

int main(void)
{
    /*
     * signal(SIGINT, handler);
     *
     * ここで
     *
     *   「SIGINT が届いたときの処理を handler にする」
     *
     * という設定を行う。
     *
     * これにより、通常なら Ctrl+C で終了するプログラムが、
     * Ctrl+C を受けたときに handler() を実行するようになる。
     *
     * 図にするとこうである。
     *
     *   設定前:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     デフォルト動作 = 終了
     *
     *   設定後:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     handler() を呼ぶ
     *
     * つまりこの1行が、
     * このプログラムを「シグナル待ちの教材」にしている。
     */
    signal(SIGINT, handler);

    /*
     * pause();
     *
     * ここがこのコードの核心である。
     *
     * pause() は、
     *
     *   「何らかのシグナルが届くまで、このプロセスを停止させる」
     *
     * 関数である。
     *
     * つまりこの行に到達すると、
     * プログラムは自分から先へ進まず、
     * 外部からのシグナルを待つ状態になる。
     *
     * このコードでは SIGINT 用ハンドラを登録済みなので、
     * 実行時には典型的に
     *
     *   Ctrl+C を押すまでここで止まる
     *
     * ことになる。
     *
     * ----------------------------------------
     * pause() の挙動
     * ----------------------------------------
     *
     * pause() は「シグナルが来るまで待つ」ため、
     * 通常の意味では自発的には戻らない。
     *
     * しかしシグナルが届いて、そのシグナルが
     *
     *   - ハンドラで処理される
     *   - またはシグナルによって制御が変わる
     *
     * と、pause() は中断されて戻る。
     *
     * 今回の流れは概念的にこうなる。
     *
     *   main()
     *     |
     *     v
     *   pause() で停止
     *     |
     *     | ここで Ctrl+C
     *     v
     *   SIGINT 到着
     *     |
     *     v
     *   handler() 実行
     *     |
     *     v
     *   pause() が解除されて戻る
     *
     * つまり pause() は、
     *
     *   「シグナル到着そのものをイベントトリガとして本体を再開する」
     *
     * 関数である。
     *
     * ----------------------------------------
     * 戻り値について
     * ----------------------------------------
     *
     * 実際には pause() はシグナルで中断されると -1 を返し、
     * errno は EINTR になることが多い。
     *
     * ただしこのコードでは戻り値を見ていない。
     *
     * 教材としては、
     *
     *   「pause はシグナルで目覚める」
     *
     * という挙動だけを見せる目的なので十分である。
     */
    pause();

    /*
     * fprintf(stderr, "hello, world\n");
     *
     * これは
     *
     *   「pause() のあとに続く本体処理」
     *
     * を表している。
     *
     * 重要なのは、
     * この行はプログラム開始直後には実行されないことである。
     *
     * 実際には
     *
     *   1. pause() で止まる
     *   2. シグナルが届く
     *   3. handler() が走る
     *   4. その後にはじめてここに来る
     *
     * という順序になる。
     *
     * つまり実行時の見え方は普通、
     *
     *   （しばらく何も起きない）
     *   Ctrl+C
     *   handler
     *   hello, world
     *
     * のようになる。
     *
     * この1行により、
     *
     *   「シグナルを受けると pause が解除され、本体処理が再開する」
     *
     * ことがはっきり分かる。
     */
    fprintf(stderr, "hello, world\n");

    /*
     * ここまで来たら main() は終わる。
     *
     * つまりこのプログラムは、
     *
     *   「1回シグナルを待って、その後に1行出して終了する」
     *
     * という構造になっている。
     *
     * while ループがないので、
     * 前のシグナル例のように繰り返し反応し続けるわけではない点に注意する。
     *
     * 今回はあくまで
     *
     *   「pause() が1回のシグナルで解除される」
     *
     * ことを示す単発の例である。
     */
}
