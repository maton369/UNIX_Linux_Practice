#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <sys/types.h>  /* pid_t などの型定義。環境によっては tcsetpgrp() 関連でも参照される */
#include <unistd.h>     /* fork(), setpgid(), sleep(), tcsetpgrp() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「子プロセスを別プロセスグループに分けたうえで、
 *    tcsetpgrp() によってその子のプロセスグループを
 *    端末のフォアグラウンドプロセスグループに切り替える」
 *
 * ことである。
 *
 * 以前の例では、
 *
 *   - fork() しただけでは親子は同じプロセスグループにいる
 *   - setpgid() で子を別プロセスグループにしても、
 *     それだけでは端末のフォアグラウンドプロセスグループは変わらない
 *
 * という点が重要だった。
 *
 * このコードではさらに一歩進んで、
 *
 *   tcsetpgrp()
 *
 * を使い、
 *
 *   「端末が前景として扱うプロセスグループそのものを切り替える」
 *
 * ことをしている。
 *
 * つまりこのプログラムは、
 *
 *   1. fork() で親子を作る
 *   2. 親が子を新しいプロセスグループのリーダーにする
 *   3. 親がその子のプロセスグループを端末のフォアグラウンドへ設定する
 *   4. 以後、Ctrl+C などの端末シグナルは子のグループへ向かう
 *
 * という流れを学ぶための教材である。
 *
 * ----------------------------------------
 * まず重要な概念整理
 * ----------------------------------------
 *
 * UNIX のジョブ制御では、次の 3 つを区別する必要がある。
 *
 * 1. プロセス
 *    - PID を持つ実行主体
 *
 * 2. プロセスグループ
 *    - 関連する複数プロセスをまとめる単位
 *    - PGID（プロセスグループ ID）で識別される
 *
 * 3. フォアグラウンドプロセスグループ
 *    - その端末が「今ユーザ入力を与える相手」として扱っているグループ
 *    - Ctrl+C, Ctrl+Z などの端末生成シグナルはここへ送られる
 *
 * このコードで特に大切なのは、
 *
 *   setpgid() は「どのプロセスグループに属するか」を変えるだけ
 *   tcsetpgrp() は「端末の前景グループはどれか」を変える
 *
 * という違いである。
 *
 * この 2 つは似ているようで全く別の操作であり、
 * このコードはその両方を並べて見せている。
 */

int main(void)
{
    /*
     * pid = fork();
     *
     * 現在のプロセスを親子 2 つのプロセスへ分岐させる。
     *
     * 戻り値:
     *
     *   親プロセス:
     *     子プロセスの PID が返る（正の値）
     *
     *   子プロセス:
     *     0 が返る
     *
     *   失敗時:
     *     -1 が返る
     *
     * このサンプルではエラーチェックを省略しているが、
     * 実務では fork() の失敗確認は重要である。
     *
     * ----------------------------------------
     * fork() 直後の典型状態
     * ----------------------------------------
     *
     * fork() しただけでは、
     *
     *   親:
     *     PID  = P1
     *     PGID = G
     *
     *   子:
     *     PID  = P2
     *     PGID = G
     *
     * のように、
     *
     *   PID は別
     *   PGID は同じ
     *
     * であることが多い。
     *
     * つまり fork() は新しい「プロセス」は作るが、
     * 新しい「プロセスグループ」までは自動で作らない。
     *
     * このあと親が setpgid() を呼ぶことで、
     * 子だけを別プロセスグループへ分けるのがこのコードの次の段階である。
     */
    pid_t pid = fork();

    /*
     * c:
     *   親子を見分けるための表示文字である。
     *
     *   親 -> '@'
     *   子 -> '.'
     *
     * にしている。
     *
     * これにより実行中の端末出力を見れば、
     *
     *   どちらが親由来か
     *   どちらが子由来か
     *
     * を簡単に区別できる。
     *
     * 特にこのコードでは、
     * tcsetpgrp() によって前景グループを切り替えた結果、
     * Ctrl+C などの端末シグナルの影響が親子で変わる可能性がある。
     *
     * その違いを目で観察するためのマーカーとして c が使われている。
     */
    int c = (pid == 0 ? '.' : '@');

    /*
     * if (pid != 0)
     *
     * ここは親プロセスだけが入るブロックである。
     *
     * 親がやっていることは重要で、順番に言うと
     *
     *   1. 子を新しいプロセスグループへ入れる
     *   2. そのグループを端末のフォアグラウンドにする
     *
     * である。
     *
     * つまりこの if ブロックの目的は、
     *
     *   「子のジョブを前景ジョブにする」
     *
     * ことにある。
     *
     * ここがシェルのジョブ制御の縮小版のような動きになっている。
     */
    if (pid != 0) {
        /*
         * setpgid(pid, pid);
         *
         * ここで子プロセスのプロセスグループ ID を変更する。
         *
         * 引数の意味:
         *
         *   第1引数 pid:
         *     変更対象のプロセス。
         *     ここでは「子プロセス」である。
         *
         *   第2引数 pid:
         *     移動先の PGID。
         *     ここでも子の PID を使っている。
         *
         * したがって意味としては、
         *
         *   「子プロセスを、子の PID をグループ ID とする
         *    新しいプロセスグループへ移す」
         *
         * である。
         *
         * その結果、
         *
         *   親:
         *     PID  = P1
         *     PGID = G
         *
         *   子:
         *     PID  = P2
         *     PGID = P2
         *
         * のように、子は自分自身がリーダーの新しいプロセスグループに入る。
         *
         * ----------------------------------------
         * ここでの本質
         * ----------------------------------------
         *
         * この時点ではまだ、
         *
         *   「子は別プロセスグループになった」
         *
         * だけであり、
         *
         *   「そのグループが端末の前景になった」
         *
         * わけではない。
         *
         * つまり setpgid() は
         *
         *   プロセスグループの所属変更
         *
         * を行うだけで、
         *
         *   端末のフォアグラウンド設定
         *
         * はしない。
         *
         * だから次の tcsetpgrp() が必要になる。
         */
        setpgid(pid, pid);

        /*
         * tcsetpgrp(1, pid);
         *
         * ここがこのコードの最大の核心である。
         *
         * tcsetpgrp(fd, pgrp) は、
         *
         *   「端末 fd に対して、プロセスグループ pgrp を
         *    フォアグラウンドプロセスグループとして設定する」
         *
         * 関数である。
         *
         * 引数の意味:
         *
         *   第1引数 1:
         *     ここではファイル記述子 1、つまり標準出力を渡している。
         *
         *     これは「この fd が接続されている端末」に対して
         *     前景グループ設定を行う、という意味である。
         *
         *     実務では 0（標準入力）や /dev/tty を使うことも多い。
         *     教材としては「この端末に対して」という意図で 1 を使っていると理解するとよい。
         *
         *   第2引数 pid:
         *     前景にしたいプロセスグループ ID。
         *
         *     直前の setpgid(pid, pid) によって、
         *     子の PGID は pid になっている。
         *
         *     したがって、
         *
         *       「子プロセスの属する新しいプロセスグループを、
         *        この端末のフォアグラウンドプロセスグループにする」
         *
         *     ことになる。
         *
         * ----------------------------------------
         * tcsetpgrp() が重要な理由
         * ----------------------------------------
         *
         * 端末の Ctrl+C, Ctrl+Z などは、
         *
         *   端末のフォアグラウンドプロセスグループ
         *
         * に向けて送られる。
         *
         * したがってこの呼び出しの後は、
         *
         *   子のグループが前景
         *
         * になる。
         *
         * その結果、
         *
         *   Ctrl+C を押すと子グループへ SIGINT が送られる
         *
         * のが基本挙動になる。
         *
         * つまり、
         *
         *   親は元のグループにいる
         *   子は別グループにいる
         *   しかも端末の前景は子グループに切り替わった
         *
         * ので、
         *
         *   端末操作の影響は子側が受けやすい
         *
         * ことになる。
         *
         * ----------------------------------------
         * setpgid() との違いを再確認
         * ----------------------------------------
         *
         *   setpgid():
         *     「どのグループに属するか」を変える
         *
         *   tcsetpgrp():
         *     「どのグループが端末の前景か」を変える
         *
         * である。
         *
         * このコードはその両方を続けて行っているため、
         *
         *   「子を別グループにし、その別グループを前景にする」
         *
         * ことが完成する。
         *
         * これはシェルがジョブ制御を行うときに本質的にやっていることと近い。
         *
         * ----------------------------------------
         * 端末シグナルとの関係
         * ----------------------------------------
         *
         * この呼び出し後の典型状態:
         *
         *   親:
         *     PID  = P1
         *     PGID = G
         *
         *   子:
         *     PID  = P2
         *     PGID = P2
         *
         *   端末の foreground PGID:
         *     P2
         *
         * したがって、
         *
         *   Ctrl+C -> 子グループへ SIGINT
         *   Ctrl+Z -> 子グループへ SIGTSTP
         *
         * となるのが基本になる。
         *
         * つまり前のコードでは
         *   「子を別グループにしただけ」
         * だったのに対し、
         * 今回は
         *   「子グループを本当に前景へ持ってきた」
         *
         * ところが違う。
         */
        tcsetpgrp(1, pid);
    }

    /*
     * while (1)
     *
     * 親子ともに無限ループで動き続ける。
     *
     * ただしこの時点で、
     *
     *   親:
     *     元のプロセスグループにいる
     *
     *   子:
     *     新しいプロセスグループにいて、
     *     しかもそのグループが端末のフォアグラウンドになっている
     *
     * ので、
     *
     *   親子は同じ端末に出力していても、
     *   端末制御上の立場は同じではない
     *
     * 状態になっている。
     *
     * ここがこのプログラムの観察ポイントである。
     */
    while (1) {
        /*
         * fprintf(stderr, "%c", c);
         *
         * このプロセスに割り当てられた文字を 1 文字だけ出力する。
         *
         *   親 -> '@'
         *   子 -> '.'
         *
         * である。
         *
         * stderr を使うことで、出力が比較的すぐ見えやすくなっている。
         *
         * この出力を観察することで、
         *
         *   どちらのプロセスがまだ動いているか
         *
         * が分かる。
         *
         * 特にこのコードでは、
         * tcsetpgrp() によって前景グループが子へ移っているため、
         * Ctrl+C などを押したときに
         *
         *   子側の '.' に影響が出やすい
         *
         * のを観察しやすい。
         */
        fprintf(stderr, "%c", c);

        /*
         * sleep(1);
         *
         * 1 秒待つ。
         *
         * これにより文字出力がゆっくりになり、
         * 親子の動きや、端末シグナルの影響を観察しやすくしている。
         *
         * ----------------------------------------
         * フォアグラウンドプロセスグループとの関係
         * ----------------------------------------
         *
         * この sleep() 中に Ctrl+C を押すと、
         * 端末は現在のフォアグラウンドプロセスグループへ SIGINT を送る。
         *
         * このコードでは tcsetpgrp(1, pid) により、
         * 子のプロセスグループがフォアグラウンドに設定されている。
         *
         * したがって典型的には、
         *
         *   子側には SIGINT が届く
         *   親側は同じようには影響を受けない
         *
         * という違いが起こりうる。
         *
         * つまり観察上は、
         *
         *   '.' の方が止まり、'@' の方が残る
         *
         * のような挙動を経験できる場合がある。
         *
         * これが
         *
         *   「端末シグナルは PID 単位でも、端末に見えている全プロセス単位でもなく、
         *    フォアグラウンドプロセスグループ単位で配送される」
         *
         * という事実の可視化である。
         */
        sleep(1);
    }

    /*
     * 通常ここには到達しない。
     *
     * 理由:
     *   while(1) が無限ループだからである。
     *
     * ただしこのコードの本当の価値は、
     * 終了することではなく、
     *
     *   setpgid() だけでは不十分で、
     *   tcsetpgrp() によって初めて
     *   「そのグループを端末の前景にする」ことができる
     *
     * と理解する点にある。
     *
     * つまりこのコードは、
     *
     *   プロセスグループの分離
     *   +
     *   端末前景グループの切り替え
     *
     * の両方を含む、ジョブ制御の核心部分を示した最小例である。
     */
}
