#include <signal.h>     /* signal(), SIGCHLD を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), perror(), printf(), stderr を使うためのヘッダ */
#include <stdlib.h>     /* exit() を使うためのヘッダ */
#include <sys/types.h>  /* fork(), waitpid() に関係する型定義 */
#include <sys/wait.h>   /* waitpid(), WNOHANG を使うためのヘッダ */
#include <unistd.h>     /* execlp(), fork(), sleep() を使うためのヘッダ */
#include <errno.h>      /* errno, ECHILD, EINTR を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「子プロセスが終了したときに届く SIGCHLD をハンドラで受け取り、
 *    waitpid(..., WNOHANG) を使って終了済み子プロセスを回収する」
 *
 * ことである。
 *
 * これは単なる fork() の例ではなく、
 *
 *   - 子が終了すると親へ SIGCHLD が届くこと
 *   - 親が wait()/waitpid() しないと子がゾンビになること
 *   - シグナルハンドラの中で waitpid(WNOHANG) を回すことで、
 *     終了した子を非ブロッキングに回収できること
 *
 * を学ぶためのコードである。
 *
 * ----------------------------------------
 * 全体の流れ
 * ----------------------------------------
 *
 * 1. fork() で親子に分岐する
 * 2. 親は SIGCHLD 用ハンドラを登録する
 * 3. 子は execlp("cat", "cat", (char *)0) で cat に変身する
 * 4. 親は while(1) で "." を出し続ける
 * 5. 子の cat が終了すると、親に SIGCHLD が届く
 * 6. 親プロセスの handler() が割り込んで実行される
 * 7. handler() は waitpid(-1, NULL, WNOHANG) を繰り返して、
 *    終了済みの子を全部回収する
 * 8. 回収した PID を表示する
 * 9. handler() 終了後、親はまた "." を出す通常ループへ戻る
 *
 * つまりこのプログラムは、
 *
 *   「親が通常動作を続けながら、子の終了イベントだけを
 *    SIGCHLD ハンドラで非同期に処理する」
 *
 * 例になっている。
 */

/*
 * handler(int sig)
 *
 * これは SIGCHLD 用のシグナルハンドラである。
 *
 * SIGCHLD は、
 *
 *   「子プロセスの状態が変化したとき」
 *
 * に親へ送られるシグナルである。
 *
 * このコードでは特に、
 *
 *   「子が終了したとき」
 *
 * に届くものとして扱っている。
 *
 * 重要なのは、
 * このハンドラの目的が単に通知を受けることではなく、
 *
 *   「waitpid() で終了済みの子を回収すること」
 *
 * にある点である。
 *
 * なぜ回収が必要か:
 *   子プロセスは終了しても、親が wait()/waitpid() で終了状態を受け取るまで
 *   カーネルに「ゾンビプロセス」として残るからである。
 *
 * つまり SIGCHLD ハンドラは、
 *
 *   「子が死んだことを知る」
 *
 * だけではなく、
 *
 *   「死んだ子の後始末をする」
 *
 * 役割を持っている。
 */
void handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値。
     *
     * 今回は signal(SIGCHLD, handler) で登録しているため、
     * 通常は sig == SIGCHLD である。
     *
     * このコードでは sig の値自体は使っていない。
     */
    (void)sig;

    /*
     * pid:
     *   waitpid() が返す PID を受ける変数。
     *
     * waitpid() の戻り値は、
     *   > 0  : 回収できた子の PID
     *   == 0 : WNOHANG 指定時、今すぐ回収できる子はいない
     *   == -1: エラー
     *
     * である。
     */
    pid_t pid;

    /*
     * while ((pid = waitpid(-1, NULL, WNOHANG)) != 0)
     *
     * ここがこのコードの核心である。
     *
     * waitpid(-1, NULL, WNOHANG) の意味:
     *
     *   第1引数 -1:
     *     「任意の子プロセス」を対象にする。
     *
     *   第2引数 NULL:
     *     終了ステータスは今回は不要なので捨てる。
     *
     *   第3引数 WNOHANG:
     *     回収可能な子がいなければブロックせず、すぐ戻る。
     *
     * この指定により、
     *
     *   「いま終わっている子がいれば回収する。
     *    いなければ待たずに戻る」
     *
     * という動作になる。
     *
     * なぜ while で回すのか:
     *
     *   1つの SIGCHLD で、複数の子がすでに終了している可能性があるからである。
     *
     * したがってハンドラでは、
     *
     *   回収できる子がある限り、全部回収する
     *
     * という方針にしている。
     *
     * 図にするとこうである。
     *
     *   SIGCHLD 到着
     *      |
     *      v
     *   waitpid(..., WNOHANG)
     *      |
     *      +--> 子を1つ回収できた -> もう一度回す
     *      |
     *      +--> もう回収できる子がいない -> ループ終了
     *
     * つまりこの while は、
     *
     *   「たまっている終了済み子プロセスを全部掃除する」
     *
     * ためのループである。
     */
    while ((pid = waitpid(-1, NULL, WNOHANG)) != 0) {
        /*
         * pid == -1 の場合は waitpid() エラーである。
         *
         * ただしエラーにも意味の違いがあるので、
         * errno を見て場合分けしている。
         */
        if (pid == -1) {
            /*
             * errno == ECHILD:
             *
             * 「もう回収すべき子プロセスがいない」
             *
             * ことを意味する。
             *
             * これは実質的には正常終了条件として扱ってよい。
             * つまり「掃除が終わった」ということなので break する。
             */
            if (errno == ECHILD) break;

            /*
             * errno == EINTR:
             *
             * waitpid() 自体が別のシグナルで中断されたことを意味する。
             *
             * この場合は「失敗して終わり」ではなく、
             * もう一度 waitpid() を試せばよい。
             *
             * そのため continue して while の先頭へ戻る。
             */
            if (errno == EINTR) continue;

            /*
             * それ以外のエラーは想定外の異常として扱う。
             *
             * perror("waitpid") で理由を表示し、
             * exit(1) で終了する。
             *
             * 注意:
             *   厳密にはシグナルハンドラ内で perror()/exit() を使うのは
             *   async-signal-safe の観点で注意が必要である。
             *
             *   教材としては分かりやすいが、実務では write() や _exit() などの
             *   より安全な手段を選ぶことが多い。
             */
            perror("waitpid");
            exit(1);
        }

        /*
         * ここへ来るのは pid > 0 の場合、
         * つまり実際に1つの子プロセスを回収できた場合である。
         *
         * その子の PID を表示する。
         *
         * これにより、
         *
         *   「SIGCHLD が来て、どの子を回収したか」
         *
         * が観察できる。
         *
         * たとえば
         *
         *   handler: pid = 12345
         *
         * のように見えれば、
         * PID 12345 の子プロセスが終了して回収されたことを意味する。
         *
         * 注意:
         *   これも handler 内 fprintf/printf 系なので、
         *   実務では async-signal-safe の観点で慎重さが必要である。
         *   ここでは教材としての見やすさを優先している。
         */
        printf("handler: pid = %d\n", pid);
    }

    /*
     * ループを抜けた時点で、
     *
     *   - もう回収できる子がいない
     *   - あるいは ECHILD になった
     *
     * ので、この SIGCHLD に対する掃除は完了である。
     *
     * ハンドラから戻ると、
     * 親プロセスは元の while(1) の "." を出す処理へ戻る。
     */
}

int main(void)
{
    /*
     * pid = fork();
     *
     * fork() によって現在プロセスを親子2つに分岐させる。
     *
     * 戻り値:
     *   親では:
     *     子の PID が返る
     *
     *   子では:
     *     0 が返る
     *
     *   失敗時:
     *     -1 が返る
     *
     * このコードではエラーチェックを省略しているが、
     * 実務では অবশ্যず確認したい。
     *
     * この時点で、
     *
     *   親: pid > 0
     *   子: pid == 0
     *
     * という分岐条件が手に入る。
     */
    pid_t pid = fork();

    /*
     * signal(SIGCHLD, handler);
     *
     * ここで SIGCHLD が届いたときの処理を handler に設定する。
     *
     * SIGCHLD は、
     * 子プロセスが終了したり停止したり継続したりしたときに
     * 親へ送られるシグナルである。
     *
     * 今回は主に「子の終了通知」として使っている。
     *
     * 重要:
     *   この呼び出しは fork() の後にあるので、
     *   親も子もこの行を実行する。
     *
     * ただし意味があるのは主に親側である。
     * なぜなら子にはさらに子プロセスを作る処理がなく、
     * 子自身が SIGCHLD を受け取る場面は通常ないからである。
     *
     * 親にとっては、
     *
     *   「自分の子が死んだら handler() を呼ぶ」
     *
     * という設定になる。
     *
     * 注意:
     *   signal() は簡潔だが、細かい制御や厳密さでは sigaction() の方が望ましい。
     *   このコードは概念説明重視の簡潔版だと考えるとよい。
     */
    signal(SIGCHLD, handler);

    /*
     * if (pid == 0)
     *
     * ここから子プロセス側の処理に入る。
     *
     * 子は fork() した直後の同じプログラムの続きではなく、
     * execlp() によって別プログラムへ変身する。
     */
    if (pid == 0) {
        /*
         * execlp("cat", "cat", (char *)0);
         *
         * 子プロセス自身を cat コマンドへ置き換える。
         *
         * execlp() は成功すると元のコードへ戻ってこない。
         *
         * ここで起動する cat は標準入力から読み、標準出力へ書くプログラムである。
         * ターミナルから見ると、子は cat として動作する。
         *
         * cat がどう終了するか:
         *   たとえば EOF を与える、あるいは別途終了させると子プロセスは終わる。
         *
         * その終了時に親へ SIGCHLD が送られる。
         *
         * つまりこの行の役割は、
         *
         *   「終了イベントを起こしうる別プログラムを子として動かす」
         *
         * ことである。
         *
         * 注意:
         *   execlp() 失敗時のエラーチェックは省略されている。
         *   実務なら perror("execlp"); exit(1); などを書きたい。
         */
        execlp("cat", "cat", (char *)0);
    }

    /*
     * ここからは親プロセス側の通常動作である。
     *
     * 親は while(1) で "." を出し続ける。
     * その途中で子が終了すると SIGCHLD が届き、
     * handler() が割り込んで実行される。
     *
     * つまり親の見た目の流れは概念的に、
     *
     *   .....
     *   handler: pid = 12345
     *   ....
     *
     * のようになる。
     *
     * ここで
     *
     *   "." = 親の通常ループ
     *   handler: pid = ... = 子終了イベント処理
     *
     * を表している。
     */
    while (1) {
        /*
         * fprintf(stderr, ".");
         *
         * 親が通常動作中であることを可視化するために、
         * 標準エラー出力へ "." を1文字出す。
         *
         * stderr を使うことで比較的その場で見えやすい。
         */
        fprintf(stderr, ".");

        /*
         * sleep(1);
         *
         * 1秒待つ。
         *
         * これにより "." の出力が 1 秒ごとになり、
         * 子終了時の SIGCHLD 割り込みが観察しやすくなる。
         *
         * 子がこの sleep() の最中に終了した場合でも、
         * SIGCHLD によって sleep() が中断され、
         * handler() が実行されることがある。
         *
         * つまりこのコードでは、
         *
         *   「通常動作は sleep を含んでいても、
         *    子終了シグナルは非同期に割り込む」
         *
         * ことが分かる。
         */
        sleep(1);
    }

    /*
     * このコードは通常ここへ到達しない。
     *
     * 理由:
     *   親は while(1) で無限ループしているからである。
     *
     * ただし SIGCHLD が来ても handler() で子回収をして、
     * その後またループへ戻るので、
     * 親は子終了後も動き続ける。
     *
     * つまりこのプログラムは、
     *
     *   「親は生き続けながら、子の死だけを非同期に処理する」
     *
     * 例になっている。
     */
}
