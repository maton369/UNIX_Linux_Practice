#include <signal.h>     /* sigaction(), sigemptyset(), struct sigaction, SIGINT を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <unistd.h>     /* sleep() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「signal() ではなく sigaction() を使って SIGINT のハンドラを登録し、
 *    1秒ごとに動作し続けるループの途中で Ctrl+C に反応させる」
 *
 * ことである。
 *
 * 以前の signal(SIGINT, handler) の例と比べると、
 * このコードでは
 *
 *   struct sigaction
 *
 * を自分で組み立てて
 *
 *   sigaction(SIGINT, &act, NULL);
 *
 * で登録している点が重要である。
 *
 * つまりこれは、
 *
 *   「より正式で詳細なシグナル設定 API である sigaction() の基本形」
 *
 * を学ぶためのサンプルである。
 *
 * このプログラム全体の流れは次のようになる。
 *
 *   1. SIGINT 用の設定 struct sigaction act を作る
 *   2. act.sa_handler = handler により、SIGINT 到着時の処理関数を指定する
 *   3. act.sa_flags = 0 により、特別な追加動作なしで使う
 *   4. sigemptyset(&act.sa_mask) により、ハンドラ実行中の追加ブロック集合を空にする
 *   5. sigaction(SIGINT, &act, NULL) でその設定を登録する
 *   6. while(1) で 1秒ごとに "." を出し続ける
 *   7. ユーザが Ctrl+C を押すと SIGINT が届く
 *   8. デフォルト終了ではなく handler() が呼ばれ、"@" を出す
 *   9. その後また while ループへ戻って "." の出力が続く
 *
 * 実行時の見た目としては概念的に、
 *
 *   ....@..@....
 *
 * のようになることがある。
 *
 * ここで
 *
 *   "." = 通常のループ処理
 *   "@" = SIGINT を受けたときのハンドラ処理
 *
 * を表している。
 */

/*
 * handler(int sig)
 *
 * これは SIGINT が届いたときに呼ばれるシグナルハンドラである。
 *
 * シグナルハンドラとは、
 *
 *   「特定のシグナルが届いたときに OS から呼ばれる関数」
 *
 * である。
 *
 * 普通の関数呼び出しと違って、
 * main() の while ループの流れとは独立に、
 * 非同期に割り込む形で実行される。
 *
 * たとえば main() が
 *
 *   - sleep(1) の最中
 *   - fprintf(stderr, ".") の前後
 *   - while の繰り返し途中
 *
 * のどこにいようと、
 * Ctrl+C による SIGINT が届いた瞬間にこの関数が呼ばれる可能性がある。
 *
 * この意味でシグナルハンドラは、
 *
 *   「通常処理に外から割り込むイベント処理」
 *
 * と考えると分かりやすい。
 */
void handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値である。
     *
     * 今回は sigaction(SIGINT, &act, NULL) で登録しているため、
     * 通常は sig == SIGINT である。
     *
     * このコードでは sig の値自体は使っていないが、
     * たとえば複数シグナルを1つのハンドラで処理する場合には
     *
     *   if (sig == SIGINT) ...
     *
     * のように分岐に使うことができる。
     */
    (void)sig;  /* 今回は未使用なので明示しておく */

    /*
     * fprintf(stderr, "@");
     *
     * SIGINT が届いたことを可視化するために、
     * 標準エラー出力へ "@" を1文字出している。
     *
     * これにより、
     * 普段は "." が1秒ごとに出続ける中で、
     * Ctrl+C を押したタイミングだけ "@" が割り込んで見える。
     *
     * なぜ stderr を使うのか:
     *   stderr は通常 stdout よりもその場で見えやすく、
     *   観察用出力として都合が良いからである。
     *
     * 注意:
     *   厳密な POSIX の観点では、シグナルハンドラ内で安全に使える関数は限られる。
     *   fprintf() は一般に async-signal-safe とは言いにくい。
     *
     *   実務では
     *
     *     write(2, "@", 1);
     *
     *   のような低水準出力の方が安全寄りである。
     *
     * ただしこのコードは教材なので、
     *   「ハンドラが呼ばれたことを見やすくする」
     * ことを優先している。
     */
    fprintf(stderr, "@");
}

int main(void)
{
    /*
     * act:
     *   sigaction() に渡す設定構造体である。
     *
     * struct sigaction は、
     *
     *   - どのハンドラを使うか
     *   - ハンドラ実行中にどのシグナルを追加でブロックするか
     *   - どのようなフラグ付き動作にするか
     *
     * などをまとめて表現する。
     *
     * signal() が簡易な登録関数だとすると、
     * sigaction() はより正式で詳細な設定方法である。
     */
    struct sigaction act;

    /*
     * act.sa_handler = handler;
     *
     * ここで「シグナルが届いたときに呼ぶ関数」を設定する。
     *
     * sa_handler は、
     *
     *   void (*)(int)
     *
     * 型の、1引数ハンドラ用メンバである。
     *
     * つまり今回は
     *
     *   handler(int sig)
     *
     * 形式の通常ハンドラを使う、という意味になる。
     *
     * なお struct sigaction には、
     *
     *   sa_handler
     *   sa_sigaction
     *
     * の2系統がある。
     *
     *   sa_handler:
     *     単純な 1 引数ハンドラ
     *
     *   sa_sigaction:
     *     siginfo_t などを受け取る 3 引数ハンドラ
     *
     * 今回は詳細情報付きではなく、基本形としての 1 引数ハンドラを使っている。
     */
    act.sa_handler = handler;

    /*
     * act.sa_flags = 0;
     *
     * ここで sigaction() の追加動作フラグを設定する。
     *
     * 今回は 0 なので、
     *
     *   「特別なフラグは何も使わない」
     *
     * という意味になる。
     *
     * たとえば sigaction では
     *
     *   SA_SIGINFO
     *     -> 詳細情報付き 3 引数ハンドラを使う
     *
     *   SA_RESTART
     *     -> 一部の中断されたシステムコールを自動再開させる
     *
     * などのフラグがあるが、
     * このサンプルではまず基本形だけを使っている。
     *
     * つまりこの1行は、
     *
     *   「sigaction の最も単純な使い方」
     *
     * を表している。
     */
    act.sa_flags = 0;

    /*
     * sigemptyset(&act.sa_mask);
     *
     * sa_mask は、
     *
     *   「このハンドラの実行中に、追加でブロックしておきたいシグナル集合」
     *
     * を表す。
     *
     * sigemptyset() はそれを空集合に初期化する。
     *
     * つまり今回は
     *
     *   「ハンドラ実行中に特別に追加でブロックしたいシグナルはない」
     *
     * という意味になる。
     *
     * ここで大切なのは、
     * sa_mask を未初期化のままにせず、
     * 明示的に空にしていることだ。
     *
     * シグナル集合を扱う API では、
     * まず sigemptyset() または sigfillset() で初期化してから使うのが基本である。
     */
    sigemptyset(&act.sa_mask);

    /*
     * sigaction(SIGINT, &act, NULL);
     *
     * ここで実際に SIGINT に対して act の設定を登録する。
     *
     * 各引数の意味:
     *
     *   第1引数 SIGINT:
     *     設定対象のシグナル番号。
     *
     *     SIGINT は、端末で Ctrl+C を押したときに送られる
     *     割り込みシグナルとしてよく知られている。
     *
     *   第2引数 &act:
     *     新しく設定したい struct sigaction。
     *
     *   第3引数 NULL:
     *     古い設定を受け取る先。
     *
     *     今回は以前の設定を保存する必要がないので NULL を渡している。
     *
     * この1行により、
     *
     *   「Ctrl+C で SIGINT が来たら handler() を呼ぶ」
     *
     * という設定が有効になる。
     *
     * 図にするとこうである。
     *
     *   設定前:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     デフォルト動作 = 終了
     *
     *   設定後:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     handler() 実行
     *
     * つまりこの行によって、
     * 「Ctrl+C で止まるプログラム」から
     * 「Ctrl+C に反応しても継続するプログラム」へ振る舞いが変わる。
     *
     * 注意:
     *   実務コードなら戻り値を確認して、
     *   失敗時の perror()/exit() を書くことが多い。
     *   このサンプルでは簡潔さを優先して省略している。
     */
    sigaction(SIGINT, &act, NULL);

    /*
     * while (1)
     *
     * 無限ループである。
     *
     * このループには終了条件がないので、
     * 通常の制御フローだけを見るとプログラムは自力では止まらない。
     *
     * しかし sigaction() で SIGINT の処理を handler に変えているので、
     * Ctrl+C が届くと
     *
     *   - プログラムは終了しない
     *   - 代わりに handler() が割り込んで呼ばれる
     *
     * ことになる。
     *
     * この意味で、
     *
     *   main の通常処理 = "." を出し続ける
     *   外部イベント処理 = "@" を出す
     *
     * という二層構造になっている。
     */
    while (1) {
        /*
         * sleep(1);
         *
         * 1 秒間停止する。
         *
         * これにより "." の出力が 1 秒ごとになり、
         * シグナルによる割り込みが見やすくなる。
         *
         * もし sleep() がなければ "." が非常に高速に出続けて、
         * "@" の割り込みが観察しにくい。
         *
         * なお、sleep() はシグナルによって中断されることがある。
         * つまり Ctrl+C が来ると、
         *
         *   sleep 中でも handler() が割り込んで呼ばれる
         *
         * 可能性がある。
         *
         * このコードでは sleep() の戻り値を見ていないので、
         * 中断された残り時間は扱っていない。
         */
        sleep(1);

        /*
         * fprintf(stderr, ".");
         *
         * 通常動作の可視化として "." を1文字出す。
         *
         * これにより、
         *
         *   「プログラムが1秒ごとに生きて動いている」
         *
         * ことを観察できる。
         *
         * stderr を使っているので、
         * バッファリングの影響を比較的受けにくく、
         * その場で見えやすい。
         *
         * 実行中の見た目としてはたとえば
         *
         *   ....@..@...
         *
         * のようになりうる。
         *
         * ここで
         *
         *   "." = main ループの通常処理
         *   "@" = SIGINT ハンドラによる割り込み
         *
         * を表している。
         */
        fprintf(stderr, ".");
    }

    /*
     * このコードは通常ここへ到達しない。
     *
     * 理由:
     *   while(1) が無限ループだからである。
     *
     * さらに、SIGINT を受けてもデフォルト終了ではなく
     * handler() を呼ぶ設定にしているため、
     * Ctrl+C を押しても基本的にはプログラムは継続する。
     *
     * つまりこのプログラムは、
     *
     *   「sigaction() でハンドラ登録したシグナルに反応しながら、
     *    通常処理を継続する例」
     *
     * として動作する。
     */
}
