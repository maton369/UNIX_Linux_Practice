#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <sys/types.h>  /* pid_t, fork() に関係する型定義 */
#include <unistd.h>     /* fork(), sleep(), getpid(), getppid(), getpgrp() などのヘッダ */

/*
 * このコードの主題は、
 *
 *   「fork() によって親子 2 プロセスができたとき、
 *    それらは通常同じプロセスグループに属したまま動く」
 *
 * ことである。
 *
 * 一見するとこのプログラムは単に
 *
 *   - 親が '@' を出す
 *   - 子が '.' を出す
 *
 * だけのコードに見える。
 *
 * しかし本質は、
 *
 *   「親子が同じ端末の前景プロセスグループに属したまま動き続けるため、
 *    端末からの Ctrl+C や Ctrl+Z などのジョブ制御シグナルを
 *    まとめて受ける」
 *
 * という UNIX のプロセスグループの性質を観察しやすい点にある。
 *
 * ----------------------------------------
 * プロセスグループとは何か
 * ----------------------------------------
 *
 * UNIX 系 OS では、各プロセスは単独で存在するだけでなく、
 * 「プロセスグループ」という単位にも属している。
 *
 * プロセスグループは簡単に言うと、
 *
 *   「関連する複数プロセスを 1 まとまりとして扱う単位」
 *
 * である。
 *
 * 特に端末（ターミナル）では、
 *
 *   - 前景プロセスグループ（foreground process group）
 *   - 背景プロセスグループ（background process group）
 *
 * という概念が重要である。
 *
 * ユーザが端末で Ctrl+C を押すと、
 * その端末の「前景プロセスグループ全体」に対して SIGINT が送られる。
 *
 * つまり Ctrl+C は
 *
 *   「今動いている 1 プロセスだけ」
 *
 * ではなく、
 *
 *   「前景ジョブを構成する同じプロセスグループの全プロセス」
 *
 * に届く。
 *
 * このコードでは fork() しても setpgid() などを呼んでいないため、
 * 親子は通常同じプロセスグループに属する。
 *
 * したがってこのプログラムを端末で走らせると、
 *
 *   親も子も同じ前景プロセスグループにいる
 *
 * ことが多く、
 * Ctrl+C を押すと親子両方にまとめて SIGINT が届く。
 *
 * これがこのコードで観察したい「プロセスグループ」の核心である。
 *
 * ----------------------------------------
 * このプログラムの全体アルゴリズム
 * ----------------------------------------
 *
 *   1. fork() で親子 2 プロセスに分岐する
 *   2. 親は '@' を担当する
 *   3. 子は '.' を担当する
 *   4. どちらも無限ループで 1 秒ごとに文字を出し続ける
 *
 * 見た目としては概念的に
 *
 *   @.@.@@..@...
 *
 * のように、親子 2 つのプロセスの出力が混ざって見える。
 *
 * そしてその状態で Ctrl+C を押すと、
 *
 *   「同じプロセスグループにいる親子がまとめて止まる」
 *
 * ことを確認できる。
 *
 * つまりこのコードは、
 *
 *   「fork() 後の親子が通常同じプロセスグループに属する」
 *
 * ことを可視化する最小サンプルである。
 */

int main(void)
{
    /*
     * pid = fork();
     *
     * fork() により、現在のプロセスをほぼ複製して
     * 親プロセスと子プロセスの 2 つに分岐する。
     *
     * 戻り値の意味は次の通りである。
     *
     *   親プロセス:
     *     子プロセスの PID が返る（正の値）
     *
     *   子プロセス:
     *     0 が返る
     *
     *   失敗時:
     *     -1 が返る
     *
     * このコードではエラーチェックを省略しているが、
     * 本来は pid == -1 の場合を確認して perror()/exit() などを入れたい。
     *
     * ----------------------------------------
     * プロセスグループの観点でここが重要
     * ----------------------------------------
     *
     * fork() は「新しいプロセス」を作るが、
     * 特に何も設定しなければ、
 *
     *   子プロセスは親と同じプロセスグループに属したまま
 *
     * 生まれる。
     *
     * つまりこの直後の典型的な状態は、
     *
     *   親: PID = P1, PGID = G
     *   子: PID = P2, PGID = G
     *
     * のようになる。
     *
     * ここで
     *   PID  = プロセス ID
     *   PGID = プロセスグループ ID
     *
     * である。
     *
     * つまり PID は別々でも、
     * PGID は同じ、というのがポイントである。
     *
     * 図にするとこうである。
     *
     *   fork() 前:
     *       [ 親プロセス ]
     *         PID = P1
     *         PGID = G
     *
     *   fork() 後:
     *       [ 親プロセス ]   [ 子プロセス ]
     *         PID = P1         PID = P2
     *         PGID = G         PGID = G
     *
     * つまり「別プロセスにはなるが、同じグループには残る」。
     *
     * このコードは setpgid() や setsid() を呼んでいないので、
     * まさにこの典型例になっている。
     */
    pid_t pid = fork();

    /*
     * c:
     *   どちらのプロセスかを出力文字で区別するための変数。
     *
     * 条件演算子
     *
     *   (pid == 0 ? '.' : '@')
     *
     * により、
     *
     *   子プロセス (pid == 0) -> '.'
     *   親プロセス (pid != 0) -> '@'
     *
     * となる。
     *
     * つまりこの変数は
 *
     *   「今動いているのが親か子か」
     *
     * を見分けるための可視化マーカーである。
     *
     * この1文字の違いにより、
     * 実行中の出力が親由来なのか子由来なのかを目で追いやすくなる。
     */
    int c = (pid == 0 ? '.' : '@');

    /*
     * while (1)
     *
     * 無限ループである。
     *
     * ここから先は親も子も同じコード構造を回す。
     * ただし c の値だけが違うため、
     *
     *   親は '@'
     *   子は '.'
     *
     * を出し続ける。
     *
     * つまりこの while は、
     *
     *   「親子が同時に生き続けていること」
     *
     * を観察するための本体処理である。
     *
     * ここで大切なのは、
     * 親と子が別プロセスなので、
     *
     *   - それぞれ独立にスケジューリングされる
     *   - それぞれ独立に sleep() する
     *   - それぞれ独立に fprintf() する
     *
     * ことである。
     *
     * その結果、出力は厳密に交互になる保証はない。
     * たとえば
     *
     *   @.@.@.
     *
     * のように見えることもあれば、
     *
     *   @@..@...
     *
     * のように偏って見えることもある。
     *
     * これは「2プロセスが同時に端末へ書いている」ためである。
     */
    while (1) {
        /*
         * fprintf(stderr, "%c", c);
         *
         * このプロセスに割り当てられた文字を 1 文字だけ標準エラー出力へ出す。
         *
         * stderr を使っている理由:
         *   標準出力 stdout よりもバッファリングの影響を受けにくく、
         *   文字がその場で見えやすいからである。
         *
         * これにより、
         *
         *   親が '@'
         *   子が '.'
         *
         * をリアルタイムに出し続けていることを観察しやすい。
         *
         * ----------------------------------------
         * プロセスグループとの関係
         * ----------------------------------------
         *
         * この出力が親子両方から見えていること自体が、
         *
         *   「同じ端末にぶら下がっている前景ジョブの一部」
         *
         * であることの観察材料になる。
         *
         * つまりこの出力は単なる飾りではなく、
         *
         *   「親子が同じジョブの一員として端末に接続されている」
         *
         * ことを示している。
         */
        fprintf(stderr, "%c", c);

        /*
         * sleep(1);
         *
         * 1 秒間スリープする。
         *
         * これにより文字出力がゆっくりになり、
         * 親子が並行して動いている様子や、
         * Ctrl+C を押したときにまとめて止まる様子を観察しやすくしている。
         *
         * もし sleep() が無ければ、
         * 親子両方が高速で文字を出し続け、
         * 観察しにくくなる。
         *
         * ----------------------------------------
         * Ctrl+C とプロセスグループ
         * ----------------------------------------
         *
         * この sleep() 中に Ctrl+C を押すと、
         * 端末は前景プロセスグループ全体へ SIGINT を送る。
         *
         * このコードでは親子が通常同じ前景プロセスグループにいるため、
         *
         *   親も子も両方 SIGINT を受ける
         *
         * ことになる。
         *
         * つまり、
         *
         *   「@ も . も同時に止まる」
         *
         * のが典型的な挙動である。
         *
         * ここで学ぶべきことは、
         *
         *   Ctrl+C は “親だけ” や “子だけ” を止めるのではなく、
         *   前景プロセスグループ全体に届く
         *
         * という点である。
         *
         * これがジョブ制御におけるプロセスグループの実用的な意味である。
         */
        sleep(1);
    }

    /*
     * 通常ここには到達しない。
     *
     * 理由:
     *   while(1) が無限ループだからである。
     *
     * ただし実際の端末実験では、
     *
     *   - Ctrl+C で SIGINT を受けて終了する
     *   - Ctrl+Z で SIGTSTP を受けて停止する
     *
     * などのジョブ制御操作により、
     * 親子まとめて状態が変わるのを観察できる。
     *
     * その「まとめて変わる」理由が、
     *
     *   同じプロセスグループに属しているから
     *
     * である。
     */
}
