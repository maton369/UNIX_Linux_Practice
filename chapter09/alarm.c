#include <stdio.h>      /* printf(), fflush() を使うためのヘッダ */
#include <unistd.h>     /* alarm() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「alarm() によって、一定時間後にプロセスへシグナルが届くことを観察する」
 *
 * ことである。
 *
 * このプログラムは一見すると、
 *   - 少し時間のかかる空回しをして
 *   - "." を表示し続ける
 * だけの単純なコードに見える。
 *
 * しかし本質は、
 *
 *   alarm(3)
 *
 * を呼んでいる点にある。
 *
 * alarm(3) は、
 *   「3 秒後にこのプロセスへ SIGALRM というシグナルを送る予約」
 * を行うシステムコールである。
 *
 * このプログラムでは SIGALRM に対する独自ハンドラを設定していないため、
 * デフォルト動作が使われる。
 *
 * SIGALRM のデフォルト動作は通常
 *
 *   「プロセス終了」
 *
 * である。
 *
 * したがってこのプログラムは、
 *
 *   1. alarm(3) で 3 秒後のアラームを予約する
 *   2. それまで while(1) でループを続ける
 *   3. "." を何個か表示する
 *   4. 3 秒経過した時点で SIGALRM が届く
 *   5. その結果、プロセスは途中で終了する
 *
 * という流れになる。
 *
 * つまりこのコードは、
 *
 *   「無限ループしているプログラムでも、alarm() によって
 *    時限的に止められる」
 *
 * ことを示す最小例である。
 */

/*
 * loop()
 *
 * 時間稼ぎのための関数である。
 *
 * ここでは意味のある計算はしておらず、
 * 単に for ループを大量に回して CPU 時間を消費している。
 *
 * 目的は、
 *   "." がものすごく高速に出すぎないようにし、
 *   alarm(3) が来るまでの間に
 *   「少しずつ進んでいる感じ」
 * を見やすくすることである。
 *
 * つまりこの関数は、
 *   「わざと少し遅くするためのダミー処理」
 * である。
 */
void loop(void)
{
    /*
     * i:
     *   ループ回数を数えるための変数。
     */
    int i;

    /*
     * for (i = 0; i < 0xFFFFFF; i++)
     *     ;
     *
     * 0xFFFFFF は 16 進数表記であり、
     * 10 進数では 16777215 に相当する。
     *
     * つまりこの for 文は、
     * 約 1600 万回の空ループを回す。
     *
     * ループ本体が
     *
     *     ;
     *
     * だけになっているので、
     * 実際には何もしていない。
     *
     * ただし「何もしていない」のではなく、
     * CPU は
     *   - ループ条件判定
     *   - インクリメント
     * を繰り返しているため、
     * 時間だけは消費する。
     *
     * これは busy loop（ビジーループ）や busy wait（ビジーウェイト）に近い形である。
     *
     * 注意:
     *   コンパイラ最適化が強いと、このような空ループは
     *   「意味がない」と判断されて消される可能性がある。
     *
     *   このコードが教材としてそのまま意図通り動きやすいのは、
     *   最適化なし（たとえば -O0）での実験を暗に想定しているからである。
     *
     * つまりこの関数は、
     *   「一定時間 CPU を忙しくさせる」
     * ことが目的であり、値を計算するためのものではない。
     */
    for (i = 0; i < 0xFFFFFF; i++)
        ;
}

int main(void)
{
    /*
     * alarm(3)
     *
     * 3 秒後にこのプロセスへ SIGALRM を送る予約を行う。
     *
     * alarm() は秒単位で動く単純なタイマであり、
     * 引数に指定した秒数が経つと、
     * カーネルがそのプロセスへ SIGALRM を配送する。
     *
     * ここで重要なのは、
     *   alarm() 自体が 3 秒待つのではない
     * という点である。
     *
     * alarm(3) を呼んだ瞬間に関数はすぐ戻り、
     * 「3 秒後にシグナルを送る予約だけ」がセットされる。
     *
     * つまりこの後の while ループは普通に進行する。
     * そして 3 秒たった時点で、非同期に SIGALRM が届く。
     *
     * このコードでは signal() や sigaction() で
     * SIGALRM のハンドラを登録していないため、
     * デフォルト動作が使われる。
     *
     * デフォルト動作は通常
     *
     *   プロセス終了
     *
     * なので、3 秒後にはこのプログラムは途中で終了する。
     *
     * 一言でいうと、
     *   「3 秒の時限爆弾を仕掛ける」
     * ような行である。
     */
    alarm(3);

    /*
     * while (1)
     *
     * 無限ループである。
     *
     * このコードだけを見ると、通常ならこのプログラムは永遠に止まらない。
     * しかし実際には alarm(3) で SIGALRM が届くため、
     * 3 秒後に外から止められる。
     *
     * つまりこの while(1) は、
     *   「自力では終わらない処理」
     * を表している。
     */
    while (1) {
        /*
         * loop();
         *
         * 時間稼ぎを行う。
         *
         * これにより "." が少し間をあけて表示されるようになり、
         * alarm の効果を見やすくしている。
         *
         * もしこれが無ければ、環境によっては "." が非常に高速に大量表示される。
         */
        loop();

        /*
         * printf(".");
         *
         * 標準出力へピリオド 1 文字を出力する。
         *
         * ただし printf() の出力は通常バッファリングされるため、
         * この時点ではまだ画面に見えていない可能性がある。
         *
         * 特に "." のように改行を含まない短い出力は、
         * 行バッファリング環境ではすぐには表示されないことがある。
         */
        printf(".");

        /*
         * fflush(stdout);
         *
         * ここが非常に重要である。
         *
         * stdout のバッファ内容を強制的に実際の出力先へ流す。
         *
         * この行が無いと、
         * "." はバッファに溜まったままになり、
         * alarm でプログラムが終了するまで画面に見えないことがある。
         *
         * つまりこの fflush は、
         *   「今出した "." をその場で見えるようにする」
         * ための処理である。
         *
         * このコードでは alarm によって非同期に終了させるので、
         * バッファを明示的に flush しておかないと、
         * 観察したい出力が見えないまま終わる可能性がある。
         *
         * したがって、
         *   printf("."); fflush(stdout);
         *
         * は
         *   「進捗表示を確実に見せる」
         * セットだと考えるとよい。
         */
        fflush(stdout);
    }

    /*
     * このコードは通常ここへ到達しない。
     *
     * 理由:
     *   while(1) が無限ループだからである。
     *
     * しかし実際には 3 秒後に SIGALRM が届いてプロセスが終了するため、
     * main の末尾に到達する前にプログラム全体が止まる。
     *
     * つまりこのプログラムの終了経路は return ではなく、
     * シグナルによる非同期終了である。
     */
}
