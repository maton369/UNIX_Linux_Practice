#include <signal.h>     /* sigaction(), sigemptyset(), struct sigaction, siginfo_t,
                           SA_SIGINFO, SIGSEGV, SIGBUS を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), printf(), stderr を使うためのヘッダ */
#include <stdlib.h>     /* exit() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「sigaction() を使ってシグナルハンドラを詳細情報付きで登録し、
 *    メモリアクセス違反（SIGSEGV / SIGBUS）が起きたときに
 *    faulting address（障害アドレス）を取り出す」
 *
 * ことである。
 *
 * これは単なる
 *
 *   「シグナルを捕まえる」
 *
 * 例ではなく、
 *
 *   - signal() より高機能な sigaction() の使い方
 *   - SA_SIGINFO を使った 3 引数ハンドラ
 *   - siginfo_t から障害情報を読む方法
 *   - SIGSEGV と SIGBUS の違いを意識した登録
 *
 * を学ぶためのコードである。
 *
 * このプログラムの大きな流れは次の通りである。
 *
 *   1. struct sigaction を準備する
 *   2. SA_SIGINFO を指定し、3引数ハンドラ handler() を登録する
 *   3. SIGSEGV と SIGBUS の両方に対してその設定を適用する
 *   4. わざと不正アドレス 0x123 を int* にキャストする
 *   5. *p を読むことで不正メモリアクセスを発生させる
 *   6. OS が SIGSEGV または SIGBUS をこのプロセスへ送る
 *   7. 登録済み handler() が呼ばれる
 *   8. handler() が info->si_addr を使って障害アドレスを表示する
 *   9. exit(1) で終了する
 *
 * つまりこのコードは、
 *
 *   「例外的なメモリアクセス違反を、sigaction() による詳細付きハンドラで観察する」
 *
 * 最小例である。
 */

/*
 * handler(int sig, siginfo_t *info, void *q)
 *
 * これは SA_SIGINFO 形式のシグナルハンドラである。
 *
 * 通常の signal() でよく見る単純ハンドラは
 *
 *   void handler(int sig)
 *
 * という 1 引数形式だが、
 * sigaction() では SA_SIGINFO を指定することで
 *
 *   void handler(int sig, siginfo_t *info, void *ucontext)
 *
 * という 3 引数形式を使える。
 *
 * この 3 引数形式の利点は、
 * 単に「どのシグナルが来たか」だけでなく、
 * そのシグナルに付随する詳細情報も受け取れることにある。
 *
 * たとえば SIGSEGV / SIGBUS の場合、
 *
 *   info->si_addr
 *
 * に「どのアドレスへのアクセスで障害が起きたか」が入ることがある。
 *
 * これがこのコードの最大の学習ポイントである。
 */
void handler(int sig, siginfo_t *info, void *q)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値。
     *
     * 今回は
     *   - SIGSEGV
     *   - SIGBUS
     * のどちらかが来る想定である。
     *
     * このサンプルでは sig の値そのものを分岐には使っていないが、
     * 実際には
     *
     *   if (sig == SIGSEGV) ...
     *   if (sig == SIGBUS)  ...
     *
     * のように分けて扱うことも多い。
     */
    (void)sig;

    /*
     * info:
     *   シグナルに関する追加情報を保持する siginfo_t 構造体へのポインタ。
     *
     * SA_SIGINFO を指定した場合にだけ、
     * こうした詳細情報をハンドラで受け取れる。
     *
     * 今回は主に
     *
     *   info->si_addr
     *
     * を使っている。
     *
     * si_addr:
     *   メモリアクセス違反系シグナル（SIGSEGV / SIGBUS など）で、
     *   問題の原因になったアドレスを示すことがある。
     *
     * このコードでは、
     *
     *   int *p = (int*)0x123;
     *   printf("%d\n", *p);
     *
     * によって不正アドレス 0x123 を読もうとしているので、
     * 多くの環境では si_addr に 0x123 が入ることが期待される。
     *
     * つまりこの1行は、
     *
     *   「どのアドレスで落ちたのか」
     *
     * を観察するための出力である。
     *
     * 注意:
     *   厳密な POSIX 的には、シグナルハンドラ内で安全に使える関数は限られており、
     *   fprintf() や exit() は async-signal-safe の観点で注意が必要である。
     *
     *   実務的には write() や _exit() を使う設計の方が堅牢なことが多い。
     *
     * ただしこのコードは教育用であり、
     *   「si_addr が見える」
     * ことを分かりやすく示すのが主目的である。
     */
    fprintf(stderr, "faulting address = %p\n", info->si_addr);

    /*
     * q:
     *   3 番目の引数は、一般には ucontext_t へのポインタとして解釈されることが多い。
     *
     * ここには、シグナル発生時点のレジスタ状態や実行文脈に関する情報が入っている場合がある。
     * ただしこのサンプルでは使っていない。
     *
     * 将来的には、
     *   - どの命令で落ちたか
     *   - レジスタ状態
     *   - スタック情報
     * などを見る高度なデバッグ用途に発展させられる。
     */
    (void)q;

    /*
     * exit(1);
     *
     * 障害情報を表示したあと、プロセスを終了する。
     *
     * ここで終了しないと、もともとの不正メモリアクセス命令へ戻って
     * 再び同じ障害を起こす可能性が高い。
     *
     * つまりこのコードでは、
     *
     *   「落ちた理由を表示したら、そこで観察を打ち切って終了する」
     *
     * という設計になっている。
     *
     * 注意:
     *   シグナルハンドラ内では exit() より _exit() の方が安全寄りである場合もある。
     *   ただし教材としては exit(1) の方が分かりやすい。
     */
    exit(1);
}

int main(void)
{
    /*
     * act:
     *   sigaction() に渡す設定構造体である。
     *
     * struct sigaction は、
     *   - どの関数をハンドラにするか
     *   - ハンドラ実行中にどのシグナルをブロックするか
     *   - どんなフラグ付き動作をするか
     * などをまとめて表す。
     *
     * signal() が「簡易ハンドラ登録 API」だとすると、
     * sigaction() は「詳細設定付きの正式 API」と考えると分かりやすい。
     */
    struct sigaction act;

    /*
     * act.sa_sigaction = handler;
     *
     * ここが sigaction() の重要ポイントの1つである。
     *
     * struct sigaction には通常、
     *
     *   sa_handler
     *   sa_sigaction
     *
     * の 2 系統のハンドラ指定がある。
     *
     *   sa_handler:
     *     1 引数形式
     *       void (*)(int)
     *
     *   sa_sigaction:
     *     3 引数形式
     *       void (*)(int, siginfo_t *, void *)
     *
     * 今回は詳細情報 siginfo_t を使いたいので、
     *
     *   sa_sigaction
     *
     * 側へ handler を入れている。
     *
     * ただし、これだけでは 3 引数形式としては扱われない。
     * 次の sa_flags = SA_SIGINFO; が必要である。
     */
    act.sa_sigaction = handler;

    /*
     * act.sa_flags = SA_SIGINFO;
     *
     * これがこのコードの核心である。
     *
     * SA_SIGINFO を立てることで、
     * ハンドラは
     *
     *   sa_handler ではなく sa_sigaction の 3 引数形式
     *
     * として呼ばれる。
     *
     * つまりこのフラグによって初めて、
     *
     *   handler(int sig, siginfo_t *info, void *q)
     *
     * の形が有効になる。
     *
     * もし SA_SIGINFO を付けなければ、
     * info->si_addr のような詳細情報は受け取れない。
     *
     * 図にするとこうである。
     *
     *   SA_SIGINFO なし:
     *     シグナル到着 -> 1引数ハンドラ
     *
     *   SA_SIGINFO あり:
     *     シグナル到着 -> 3引数ハンドラ
     *                      + siginfo_t 付き
     *
     * したがってこの1行は、
     *
     *   「詳細付きシグナルハンドリングモードへ切り替える」
     *
     * 設定だと考えるとよい。
     */
    act.sa_flags = SA_SIGINFO;

    /*
     * sigemptyset(&act.sa_mask);
     *
     * sa_mask は、
     *   「このハンドラ実行中に追加でブロックしておきたいシグナル集合」
     * を表す。
     *
     * sigemptyset() はそれを空集合に初期化する。
     *
     * つまり今回は
     *
     *   「特別に追加でブロックするシグナルは無い」
     *
     * という意味になる。
     *
     * 注意:
     *   通常、処理中のシグナル自身は自動的にブロックされることが多いが、
     *   sa_mask はそれに加えて何をブロックするかを表す。
     *
     * 学習上は、
     *
     *   sa_mask = ハンドラ実行中の追加ブロック集合
     *
     * と理解するとよい。
     */
    sigemptyset(&act.sa_mask);

    /*
     * sigaction(SIGSEGV, &act, NULL);
     *
     * ここで SIGSEGV に対して act の設定を登録する。
     *
     * sigaction() の引数:
     *
     *   第1引数:
     *     対象シグナル番号
     *
     *   第2引数:
     *     新しい設定
     *
     *   第3引数:
     *     古い設定の取得先（今回は不要なので NULL）
     *
     * SIGSEGV:
     *   segmentation fault を表すシグナルで、
     *   典型的には不正なメモリアクセスで発生する。
     *
     * たとえば
     *   - 存在しないアドレス参照
     *   - 許可されていない領域へのアクセス
     * などで発生しうる。
     */
    sigaction(SIGSEGV, &act, NULL);

    /*
     * sigaction(SIGBUS, &act, NULL);
     *
     * こちらは SIGBUS に対して同じ設定を登録している。
     *
     * SIGBUS:
     *   bus error を表すシグナルで、
     *   アーキテクチャや OS によっては
     *   - アラインメント違反
     *   - 物理的に不正なメモリアクセス
     *   - マップされたファイルへの異常アクセス
     * などで起こることがある。
     *
     * なぜ SIGSEGV と SIGBUS の両方を登録するのか:
     *
     *   この種の不正アクセスは環境によって
     *   SIGSEGV になることもあれば SIGBUS になることもある
     *
     * からである。
     *
     * つまりこのコードは、
     *
     *   「どちらで来ても同じ handler で観察する」
     *
     * ために両方へ同じ act を設定している。
     */
    sigaction(SIGBUS,  &act, NULL);

    /*
     * ここから先は、わざとクラッシュを起こす部分である。
     *
     * 中括弧で囲っているのは単に変数 p のスコープを局所化しているだけで、
     * アルゴリズム上の本質ではない。
     */
    {
        /*
         * int *p = (int*)0x123;
         *
         * 数値 0x123 を int* へ無理やりキャストしている。
         *
         * 0x123 は通常、正当な int オブジェクトを指す有効ポインタではない。
         * つまりこれは意図的に「怪しいポインタ」を作っている。
         *
         * 16進数 0x123 は 10進数で 291 であり、
         * 多くの環境ではこの低いアドレスはアクセス不能である。
         *
         * また int の読み出しとしてはアラインメント的にも不自然なことがある。
         *
         * つまりこの行は、
         *
         *   「クラッシュを起こすための不正ポインタを準備する」
         *
         * 処理である。
         */
        int *p = (int*)0x123;

        /*
         * printf("%d\n", *p);
         *
         * ここで不正ポインタ p を実際に逆参照する。
         *
         * つまり
         *
         *   「アドレス 0x123 にある int を読もうとする」
         *
         * ことになる。
         *
         * これは多くの環境で不正メモリアクセスとなるため、
         * OS はこのプロセスへ SIGSEGV または SIGBUS を送る。
         *
         * その結果、
         *
         *   1. 通常の printf 処理は完了しない
         *   2. 代わりにシグナルハンドラ handler() が呼ばれる
         *   3. handler() が faulting address を表示して終了する
         *
         * という流れになる。
         *
         * したがって、この行は
         *
         *   「sigaction() の効果を実験するために、わざと障害を発生させる」
         *
         * トリガである。
         */
        printf("%d\n", *p);
    }

    /*
     * 通常、ここには到達しない。
     *
     * 理由:
     *   printf("%d\n", *p) のところでシグナルが発生し、
     *   handler() が exit(1) を呼ぶからである。
     *
     * つまりこのプログラムは、
     *
     *   「正常終了するコード」ではなく、
     *   「障害を起こしてその情報を捕捉するコード」
     *
     * である。
     */
}
