#include <signal.h>     /* signal(), SIGINT を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <unistd.h>     /* sleep() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「SIGINT（たとえば Ctrl+C）を受け取ったときの動作を、
 *    デフォルト終了ではなく、自分で用意した関数へ差し替える」
 *
 * ことである。
 *
 * 通常、端末で動いているプログラムに対して Ctrl+C を押すと、
 * そのプロセスには SIGINT というシグナルが送られる。
 *
 * 何も設定しなければ、SIGINT のデフォルト動作は通常
 *
 *   「プロセス終了」
 *
 * である。
 *
 * しかしこのコードでは
 *
 *   signal(SIGINT, handler);
 *
 * によって、
 * 「SIGINT が来たら handler() を呼ぶ」
 * ように設定している。
 *
 * そのため、このプログラムは Ctrl+C を押してもすぐ終了せず、
 * 代わりに handler() が実行されて '@' を出力する。
 *
 * 一方、本体側は
 *
 *   while (1) {
 *       sleep(1);
 *       fprintf(stderr, ".");
 *   }
 *
 * によって 1 秒ごとに '.' を出し続ける。
 *
 * つまり実行中の見た目としては概念的に、
 *
 *   . . . . .   （1秒ごと）
 *
 * のように進みつつ、
 * 途中で Ctrl+C を押すと
 *
 *   @
 *
 * が割り込んで出る。
 *
 * したがってこのコードは、
 *
 *   「シグナルを捕まえて、デフォルト動作を上書きする最小例」
 *
 * である。
 */

/*
 * handler(int sig)
 *
 * シグナルを受け取ったときに呼ばれる関数である。
 *
 * ここでは SIGINT 用ハンドラとして登録される。
 *
 * 引数 sig:
 *   どのシグナルで呼ばれたかを表す整数値。
 *
 *   今回は signal(SIGINT, handler) で登録しているため、
 *   通常は SIGINT が渡される。
 *
 *   ただし、この実装では sig の値そのものは使っていない。
 *
 * 重要:
 *   シグナルハンドラは「通常の関数呼び出し」とは少し違い、
 *   プログラム本体の流れとは非同期に割り込む形で実行される。
 *
 * つまり main() の while ループが動いている途中でも、
 * SIGINT が届いた瞬間に handler() が呼ばれる可能性がある。
 *
 * この意味で handler() は、
 *   「外から飛んでくるイベントに対する割り込み処理」
 * に近い。
 */
void handler(int sig)
{
    /*
     * sig:
     *   受け取ったシグナル番号。
     *
     * 今回は使っていないが、
     * たとえば複数種類のシグナルを同じハンドラで受ける場合は
     *   if (sig == SIGINT) ...
     * のように中で分岐することもできる。
     */
    (void)sig;  /* 未使用引数であることを明示 */

    /*
     * fprintf(stderr, "@");
     *
     * シグナルを受け取ったことを分かりやすく観察するために、
     * 標準エラー出力へ '@' を 1 文字出している。
     *
     * なぜ stderr を使っているのか:
     *   stderr は通常、stdout よりもバッファリングの影響を受けにくく、
     *   その場で出力が見えやすいからである。
     *
     * これにより、
     *   普段は '.' が1秒ごとに出る
     *   Ctrl+C が来た瞬間だけ '@' が出る
     * という観察がしやすい。
     *
     * 注意:
     *   厳密な POSIX の観点では、シグナルハンドラ内で使って安全と保証される関数は
     *   限られている。
     *
     *   fprintf() は async-signal-safe としては一般に推奨されない。
     *
     *   教材としては「ハンドラが呼ばれたことを見せる」ために分かりやすいが、
     *   実務では write(2, "@", 1) のようなより安全な方法を使うことが多い。
     *
     * つまりこの行は、
     *   「シグナルが届いた瞬間に何か起きたことを可視化する」
     * ための学習用出力である。
     */
    fprintf(stderr, "@");
}

int main(void)
{
    /*
     * signal(SIGINT, handler);
     *
     * ここがこのプログラムの核心である。
     *
     * signal() は、
     *   「あるシグナルを受け取ったとき、どの動作をするか」
     * を設定する関数である。
     *
     * 第1引数 SIGINT:
     *   端末で Ctrl+C を押したときなどに送られる割り込みシグナル。
     *
     * 第2引数 handler:
     *   SIGINT を受け取ったときに呼び出す関数。
     *
     * これにより、以後このプロセスでは SIGINT のデフォルト動作
     * （通常は終了）ではなく、
     * handler() が実行されるようになる。
     *
     * 図にするとこうである。
     *
     *   設定前:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     デフォルト動作 = 終了
     *
     *   設定後:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     handler() を呼ぶ
     *
     * つまりこの1行で、
     *   「Ctrl+C されたときの挙動を自分で差し替える」
     * ことができる。
     *
     * 注意:
     *   signal() の細かい挙動は歴史的に環境差異がありうるため、
     *   実務では sigaction() を使う方がより堅牢である。
     *
     * ただし学習用としては signal() の方が簡潔で、
     *   「シグナルを捕まえる」
     * という概念が見えやすい。
     */
    signal(SIGINT, handler);

    /*
     * while (1)
     *
     * 無限ループである。
     *
     * このプログラム本体は自力では終了しない。
     * したがって普通に考えると永遠に '.' を出し続ける。
     *
     * しかし実際には、
     *   Ctrl+C によって SIGINT が届くと handler() が呼ばれる
     * ので、
     * 外部イベントが本体の実行に割り込むことを観察できる。
     */
    while (1) {
        /*
         * sleep(1);
         *
         * 1 秒間スリープする。
         *
         * これにより '.' の出力が 1 秒ごとになり、
         * プログラムの進行と SIGINT の割り込みを見やすくしている。
         *
         * sleep() の最中にシグナルが届くと、
         * そのシグナルによって sleep() が中断されることがある。
         *
         * このコードでは戻り値を見ていないため、
         * 「何秒残っていたか」は扱っていない。
         *
         * 学習上のポイントとしては、
         *   シグナルは計算中だけでなく sleep 中にも割り込める
         * ということがある。
         */
        sleep(1);

        /*
         * fprintf(stderr, ".");
         *
         * 1 秒ごとに '.' を標準エラー出力へ表示する。
         *
         * これにより、
         *   「プログラムが生きていて、ループを回り続けている」
         * ことを観察できる。
         *
         * stderr を使っているので、
         * stdout より即時に見えやすい。
         *
         * 実行イメージとしては、
         *
         *   ....
         *
         * のように時間とともに '.' が増えていく。
         *
         * その途中で Ctrl+C を押すと、
         *
         *   ..@...
         *
         * のように '@' が割り込んで見える可能性がある。
         *
         * つまり '.' は通常動作、
         * '@' はシグナル割り込み発生、
         * を可視化している。
         */
        fprintf(stderr, ".");
    }

    /*
     * このコードは通常ここへ到達しない。
     *
     * 理由:
     *   while(1) が無限ループだからである。
     *
     * しかも SIGINT を受けても終了ではなく handler() を呼ぶ設定にしているため、
     * Ctrl+C だけではループを抜けない。
     *
     * つまりこのプログラムは、
     *   「シグナルを捕まえても終了せず、動き続ける例」
     * として振る舞う。
     */
}
