#include <signal.h>     /* signal(), SIGINT を使うためのヘッダ */
#include <stdio.h>      /* printf() を使うためのヘッダ */

/*
 * カウントの上限値。
 *
 * このプログラムでは
 *
 *   for (i = 0; i < MAX; i++)
 *       counter++;
 *
 * というループで counter を増やしていく。
 *
 * したがって MAX は
 *   「最大で何回 counter++ するか」
 * を表す。
 *
 * ここでは 10^9 回なので、かなり長い時間ループし続ける可能性がある。
 * そのため、実行中に Ctrl+C を押して途中経過を見たくなる。
 *
 * この「実行途中で SIGINT を送って handler() から counter を読む」
 * という状況が、このコードの主題である。
 */
#define MAX 1000000000

/*
 * counter:
 *   ループの進行状況を表す大域変数である。
 *
 * static:
 *   この翻訳単位（この .c ファイル）内だけで見える変数にする。
 *   つまり外部ファイルからは直接参照されない内部リンケージになる。
 *
 * volatile:
 *   ここがこのコードの最重要点である。
 *
 *   volatile は、
 *
 *     「この変数の値は、コンパイラから見ると
 *      通常の逐次実行だけでは説明できない形で変わりうる」
 *
 *   ことをコンパイラへ伝える修飾子である。
 *
 * このコードでは main() が counter を更新し、
 * さらに SIGINT が届くと handler() が非同期に割り込んで counter を読む。
 *
 * つまりコンパイラの立場から見ると、
 *
 *   - main() の for ループ中に counter を更新している最中でも
 *   - 外部イベント（シグナル）で handler() が突然動き、
 *   - その時点の counter を読み取る
 *
 * 可能性がある。
 *
 * もし volatile が無いと、コンパイラは最適化の一環として
 *
 *   - counter の読み書きをレジスタに保持したままにする
 *   - 毎回メモリへ書き戻さない
 *   - 「このループ中に外から見られない」と仮定して変換する
 *
 * 可能性がある。
 *
 * その場合、handler() が読む値が
 *
 *   「本当の途中経過」
 *
 * と一致しないことがありうる。
 *
 * たとえば概念的には、
 *
 *   main():
 *     counter をレジスタ R にだけ積んで増やし続ける
 *     最後にまとめてメモリへ書く
 *
 *   handler():
 *     メモリ上の counter を読む
 *
 * という最適化が起きると、
 * Ctrl+C で途中割り込みした handler() は
 * 古い counter を見てしまう可能性がある。
 *
 * volatile を付けることでコンパイラは
 *
 *   「この変数は毎回ちゃんとメモリ上の値として扱う必要がある」
 *
 * と考えるようになる。
 *
 * したがってこのコードでは volatile により、
 *
 *   「シグナルハンドラから観測される counter が、
 *    最適化で見えなくならないようにする」
 *
 * ことを狙っている。
 *
 * 重要な注意:
 *
 *   volatile は「排他制御」ではない。
 *
 * つまり volatile を付けても
 *
 *   - 複数スレッド間の安全性
 *   - 原子的更新
 *   - データ競合の防止
 *
 * は保証されない。
 *
 * このコードでは主に
 *
 *   「シグナルハンドラからの観測に対して、
 *    コンパイラ最適化で値が隠れないようにする」
 *
 * 目的で使っている。
 *
 * さらに厳密には、シグナルハンドラとやりとりする変数として
 * 標準的に安全寄りなのは sig_atomic_t 型であることが多い。
 * ただしこの教材では long long の進捗値を見せたいため、
 * volatile long long を使っている。
 *
 * つまりこのコードは、
 *
 *   「volatile の役割を学ぶための教材」
 *
 * と理解するのがよい。
 */
static volatile long long counter = 0;

/*
 * handler(int sig)
 *
 * SIGINT 用のシグナルハンドラである。
 *
 * Ctrl+C を押すと、通常は前景プロセスへ SIGINT が送られる。
 * このコードでは signal(SIGINT, handler) によって、
 * その SIGINT を受けたときにこの関数が呼ばれる。
 *
 * main() の for ループが走っている途中でも、
 * シグナルは非同期に割り込む可能性がある。
 *
 * したがってこの関数の役割は、
 *
 *   「いま counter がどこまで進んでいるかを途中観測する」
 *
 * ことにある。
 */
void handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値。
     *
     * 今回は signal(SIGINT, handler) で登録しているため、
     * 通常は sig == SIGINT である。
     *
     * このコードでは sig の値そのものは使っていないので未使用である。
     */
    (void)sig;

    /*
     * printf("%lld\n", counter);
     *
     * 現在の counter の値を表示する。
     *
     * これにより、長いループ処理の途中で Ctrl+C を押すと、
     * その時点までに何回 counter++ されたかの途中経過を見られる。
     *
     * ここで表示される counter が「途中経過らしい値」になることを期待して
     * counter に volatile を付けている。
     *
     * 注意:
     *   厳密な POSIX 的には、シグナルハンドラ内で安全に使える関数は限られる。
     *   printf() は一般に async-signal-safe とは言いにくい。
     *
     *   実務的には write() などを使う方が安全寄りである。
     *
     * ただしこのコードは
     *
     *   「Ctrl+C で途中の counter を見たい」
     *
     * という教材なので、分かりやすさを優先して printf() を使っている。
     */
    printf("%lld\n", counter);
}

int main(void)
{
    /*
     * i:
     *   for ループの添字である。
     *
     * 0 から MAX-1 まで増えていく。
     * 各反復ごとに counter++ が1回行われる。
     */
    long long i;

    /*
     * signal(SIGINT, handler);
     *
     * ここで SIGINT の処理を handler() に差し替える。
     *
     * 通常、Ctrl+C に対応する SIGINT のデフォルト動作は
     * プロセス終了である。
     *
     * しかしこの行により、
     *
     *   Ctrl+C
     *     |
     *     v
     *   SIGINT
     *     |
     *     v
     *   handler() を実行
     *
     * という動きになる。
     *
     * そのため、長いカウントループの実行中に Ctrl+C を押すと
     * 終了する代わりに現在の counter を表示できる。
     *
     * 注意:
     *   実務では signal() より sigaction() を使う方がより堅牢なことが多い。
     *   ここでは簡潔さを優先して signal() を使っている。
     */
    signal(SIGINT, handler);

    /*
     * for (i = 0; i < MAX; i++)
     *     counter++;
     *
     * このループがプログラム本体である。
     *
     * アルゴリズムとしては単純で、
 *
     *   1. i を 0 から MAX-1 まで進める
     *   2. 各反復で counter を 1 増やす
     *
     * だけである。
     *
     * つまり最終的には、理想的には
     *
     *   counter == MAX
     *
     * になる。
     *
     * しかしこのループ中に Ctrl+C が来ると、handler() が割り込んで
     * その時点の counter を表示する。
     *
     * ここで volatile が重要になる。
     *
     * volatile が無い場合、コンパイラは
     *
     *   「counter の値を毎回メモリへ反映しなくてもよい」
     *
     * と最適化する可能性がある。
     *
     * たとえば概念的には、
     *
     *   register tmp = counter;
     *   for (...) tmp++;
     *   counter = tmp;
     *
     * のような変換がありうる。
     *
     * するとループ途中で handler() が走っても、
     * メモリ上の counter は更新前のままで、
 *   期待した途中経過を見られない危険がある。
     *
     * volatile によって、
     *
     *   「counter の読み書きを最適化で勝手に隠さないでほしい」
     *
     * とコンパイラへ伝えている。
     *
     * したがってこのループは、
     *
     *   「volatile 付き変数を main で更新しつつ、
     *    シグナルハンドラで途中観測する」
     *
     * 実験になっている。
     */
    for (i = 0; i < MAX; i++)
        counter++;

    /*
     * printf("counter = %lld\n", counter);
     *
     * ループが最後まで正常に完了した場合の最終結果を表示する。
     *
     * 通常は MAX 回 counter++ しているので、
     *
     *   counter = 1000000000
     *
     * のような表示が期待される。
     *
     * 一方、実行途中に Ctrl+C を押した場合は、
     * handler() から途中経過も表示されうる。
     *
     * したがってこのプログラムは、
     *
     *   - 途中では handler() が counter を表示する
     *   - 最後まで終われば main() が最終値を表示する
     *
     * という二段構えの観測コードになっている。
     */
    printf("counter = %lld\n", counter);

    /*
     * main() はここで終了する。
     *
     * つまりこのプログラムは、
     *
     *   「volatile 変数を長いループで更新し、
     *    シグナルハンドラからその途中値を観測する」
     *
     * 例である。
     *
     * 主に学ぶべき点は、
     *
     *   volatile は
     *     「最適化で値が隠れるのを防ぎ、外部観測可能性を保つ」
     *
     * ための修飾子であって、
     *
     *   排他制御や原子性そのものを保証するものではない
     *
     * ということである。
     */
}
