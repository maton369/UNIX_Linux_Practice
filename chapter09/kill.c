#include <sys/types.h>  /* getpid(), kill() に関係する型定義 */
#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <signal.h>     /* signal(), kill(), raise(), SIGINT を使うためのヘッダ */
#include <unistd.h>     /* getpid() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「シグナルを自分で送る方法としての kill() と raise() の違い」
 *
 * である。
 *
 * シグナルというと、端末からの Ctrl+C や alarm() によって
 * OS から非同期に届くもの、という印象を持ちやすい。
 * しかし実際には、プログラム自身が
 *
 *   kill()
 *   raise()
 *
 * を使って、明示的にシグナルを発生させることもできる。
 *
 * このプログラムでは、
 *
 *   1. SIGINT のハンドラとして handler() を登録する
 *   2. kill(getpid(), SIGINT) を呼ぶ
 *   3. raise(SIGINT) を呼ぶ
 *
 * という順序で動く。
 *
 * そのため概念的には、
 *
 *   - まず kill() で「自分自身」に SIGINT を送る
 *   - 次に raise() で「現在プロセス自身」に SIGINT を送る
 *
 * ことになる。
 *
 * どちらも結果としては handler() が呼ばれるが、
 * 使い方と意味合いには違いがある。
 *
 * このコードの学習ポイントは主に次の 3 つである。
 *
 *   1. signal() によって SIGINT のデフォルト動作を handler に差し替えること
 *   2. kill(pid, sig) は「任意の PID に対してシグナルを送る」一般的な API であること
 *   3. raise(sig) は「現在実行中の自分自身のプロセスへシグナルを送る」簡易 API であること
 *
 * つまりこのコードは、
 *
 *   「プロセス自身が、自分へシグナルを送ってハンドラを発火させる」
 *
 * 例であり、その中で
 *
 *   kill() と raise() の位置づけを比較する
 *
 * 教材である。
 */

/*
 * handler(int sig)
 *
 * これはシグナルハンドラである。
 *
 * シグナルハンドラとは、
 *   「特定のシグナルが届いたときに OS から呼ばれる関数」
 * である。
 *
 * 今回は main() の中で
 *
 *   signal(SIGINT, handler);
 *
 * と設定しているため、
 * SIGINT が届いたときにこの関数が呼ばれる。
 *
 * 通常、SIGINT のデフォルト動作は「プロセス終了」である。
 * しかしこのコードでは handler() を登録しているため、
 * SIGINT を受けても終了する代わりにこの関数が実行される。
 */
void handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値である。
     *
     * 今回は SIGINT 用として登録しているので、
     * 通常は sig == SIGINT である。
     *
     * このコードでは値そのものは使っていない。
     * ただし、複数種類のシグナルを1つのハンドラで受ける場合には
     *
     *   if (sig == SIGINT) ...
     *
     * のように使うことがある。
     */
    (void)sig;

    /*
     * handler が呼ばれたことを見やすくするために、
     * 標準エラー出力へ "handler\n" を出力する。
     *
     * これにより、
     *   kill() や raise() によって本当に SIGINT が届いた
     * ことを観察できる。
     *
     * 注意:
     *   厳密な POSIX 的には、シグナルハンドラ内で安全に使える関数は限られている。
     *   fprintf() は一般に async-signal-safe ではないため、
     *   実務的には write(2, "handler\n", 8) のような書き方がより安全である。
     *
     * ここでは教材として分かりやすさを優先している。
     */
    fprintf(stderr, "handler\n");
}

int main(void)
{
    /*
     * signal(SIGINT, handler);
     *
     * ここで
     *
     *   「SIGINT が届いたときは handler() を呼ぶ」
     *
     * という設定を行う。
     *
     * これを行わない場合、
     * kill(..., SIGINT) や raise(SIGINT) によって SIGINT が届くと、
     * 通常はデフォルト動作によりプロセスが終了してしまう。
     *
     * しかしこのコードでは handler を登録しているため、
     * SIGINT を受けても終了せず、handler() が呼ばれる。
     *
     * 図にするとこうである。
     *
     *   設定前:
     *     SIGINT
     *       |
     *       v
     *     デフォルト動作 = 終了
     *
     *   設定後:
     *     SIGINT
     *       |
     *       v
     *     handler() 実行
     *
     * つまりこの1行があることで、
     * このプログラムは kill() / raise() の挙動を「終了」ではなく
     * 「ハンドラ呼び出し」として観察できる。
     */
    signal(SIGINT, handler);

    /*
     * kill(getpid(), SIGINT);
     *
     * ----------------------------------------
     * kill() とは何か
     * ----------------------------------------
     *
     * kill(pid, sig) は、
     *
     *   「PID = pid のプロセスへ sig というシグナルを送る」
     *
     * システムコールである。
     *
     * 名前が kill なので「殺す関数」に見えるが、
     * 本質は
     *
     *   「プロセスにシグナルを送る一般 API」
     *
     * である。
     *
     * 実際、送るシグナルが SIGKILL なら強制終了に近い意味になるが、
     * 今回のように SIGINT を送る場合は
     *
     *   「SIGINT を届ける」
     *
     * だけであり、必ずしも終了を意味しない。
     *
     * ----------------------------------------
     * getpid() とは何か
     * ----------------------------------------
     *
     * getpid() は現在のプロセス自身の PID を返す関数である。
     *
     * つまり
     *
     *   kill(getpid(), SIGINT)
     *
     * は、
     *
     *   「自分自身のプロセスへ SIGINT を送る」
     *
     * という意味になる。
     *
     * 図にするとこうである。
     *
     *   現在プロセス
     *      PID = getpid()
     *         |
     *         | kill(getpid(), SIGINT)
     *         v
     *   自分自身へ SIGINT を配送
     *
     * ----------------------------------------
     * アルゴリズム上の意味
     * ----------------------------------------
     *
     * この行を実行すると、現在プロセスに SIGINT が送られる。
     * ただし main() が直接 handler() を呼ぶのではない。
     *
     *   1. kill() がカーネルへ「このプロセスへ SIGINT を送りたい」と依頼する
     *   2. カーネルがそのシグナルをこのプロセスへ配送する
     *   3. その結果として handler() が呼ばれる
     *
     * という流れになる。
     *
     * つまり、
     *
     *   「通常の関数呼び出しではなく、シグナル配送を経由して handler が動く」
     *
     * ことが重要である。
     *
     * ----------------------------------------
     * kill() の一般性
     * ----------------------------------------
     *
     * raise() と違って、kill() は自分自身以外にも使える。
     * たとえば
     *
     *   kill(other_pid, SIGTERM);
     *
     * のように、別プロセスへシグナルを送ることもできる
     * （権限上許される場合）。
     *
     * したがって kill() は、
     *
     *   「任意の PID を対象にできる汎用シグナル送信 API」
     *
     * と言える。
     */
    kill(getpid(), SIGINT);

    /*
     * raise(SIGINT);
     *
     * ----------------------------------------
     * raise() とは何か
     * ----------------------------------------
     *
     * raise(sig) は、
     *
     *   「現在実行中の自分自身のプロセスへ sig を送る」
     *
     * 関数である。
     *
     * つまり raise() は、
     * kill() のように送信先 PID を指定するのではなく、
     * 送信先が「現在プロセス自身」に固定された簡易版だと考えると分かりやすい。
     *
     * 概念的には多くの環境で、
     *
     *   raise(SIGINT)
     *
     * は
     *
     *   kill(getpid(), SIGINT)
     *
     * にかなり近い意味を持つ。
     *
     * ----------------------------------------
     * kill() と raise() の違い
     * ----------------------------------------
     *
     * kill():
     *   - 送信先 PID を明示する
     *   - 自分自身にも他プロセスにも送れる
     *   - より一般的・汎用的
     *
     * raise():
     *   - 送信先は常に自分自身
     *   - 書き方が短い
     *   - 「自己シグナル送信」を明示したいときに分かりやすい
     *
     * 図にするとこうである。
     *
     *   kill(getpid(), SIGINT)
     *       = 「自分自身へ送る」一般 API の使い方
     *
     *   raise(SIGINT)
     *       = 「自分自身へ送る」専用の短い書き方
     *
     * ----------------------------------------
     * このコードにおける意味
     * ----------------------------------------
     *
     * すでに直前で kill(getpid(), SIGINT) を呼んでいるため、
     * この行は
     *
     *   「自己シグナル送信をもう1つの API でも試してみる」
     *
     * という比較用の意味を持っている。
     *
     * つまりこのプログラムは、
     *
     *   kill() でも raise() でも、最終的には SIGINT が届いて
     *   handler() が呼ばれる
     *
     * ことを観察する教材である。
     */
    raise(SIGINT);

    /*
     * このあと main() は終わる。
     *
     * signal(SIGINT, handler) によって SIGINT を捕捉しているので、
     * 2回のシグナル送信はいずれも handler() の実行になる。
     *
     * 実行イメージとしては概念的に、
     *
     *   handler
     *   handler
     *
     * のように 2 回表示されてから終了することが期待される。
     *
     * つまり、
     *
     *   1回目: kill(getpid(), SIGINT) に対応する handler
     *   2回目: raise(SIGINT) に対応する handler
     *
     * という対応になる。
     *
     * main() が最後まで実行されれば正常終了する。
     */
}
