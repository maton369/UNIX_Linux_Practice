#include <signal.h>     /* signal(), SIGALRM を使うためのヘッダ */
#include <stdio.h>      /* printf() を使うためのヘッダ */
#include <unistd.h>     /* alarm(), pause() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「signal + alarm + pause を使って sleep のような処理を自作しようとしているが、
 *    そこに競合状態（race condition）が潜んでいる」
 *
 * ことである。
 *
 * 一見するとこのコードは、
 *
 *   1. SIGALRM 用ハンドラを登録する
 *   2. alarm(seconds) で seconds 秒後のアラームを予約する
 *   3. pause() でシグナル待ちをする
 *
 * という流れなので、
 *
 *   「seconds 秒だけ待つ my_sleep()」
 *
 * のように見える。
 *
 * 実際、多くの実行ではそのように動く。
 *
 * しかし本質的には、このコードには
 *
 *   「alarm() で SIGALRM が届くタイミング」と
 *   「pause() に入るタイミング」
 *
 * の間に隙間があり、
 * その隙間でシグナルが先に届いてしまうと、
 * pause() がその後で永久に眠ってしまう可能性がある。
 *
 * これがこのコードで学ぶべき最大のポイントであり、
 *
 *   競合状態（race condition）
 *
 * である。
 *
 * ----------------------------------------
 * まず通常ケース
 * ----------------------------------------
 *
 * このコードが「期待通り」に見える流れはこうである。
 *
 *   main()
 *     |
 *     v
 *   printf("hello(1)\n")
 *     |
 *     v
 *   my_sleep(3)
 *     |
 *     +--> signal(SIGALRM, alarm_handler)
 *     +--> alarm(3)
 *     +--> pause()
 *            |
 *            v
 *         3秒後に SIGALRM 到着
 *            |
 *            v
 *         alarm_handler() 実行
 *            |
 *            v
 *         pause() が解除されて戻る
 *     |
 *     v
 *   printf("hello(2)\n")
 *
 * つまり普通は
 *
 *   hello(1)
 *   （3秒待つ）
 *   hello(2)
 *
 * のように見える。
 *
 * ----------------------------------------
 * しかし本質的な問題
 * ----------------------------------------
 *
 * alarm(seconds) と pause() は別々の関数呼び出しである。
 * したがってその間にはごく短いが確かに「窓」がある。
 *
 * もしその窓の間に SIGALRM が届いてしまうと、
 *
 *   1. alarm() はもう発火した
 *   2. handler() はもう実行された
 *   3. しかしまだ pause() は呼ばれていない
 *
 * という状態になる。
 *
 * そのあと pause() を呼ぶと、
 *
 *   「次のシグナルが来るまで待つ」
 *
 * ことになるが、肝心の SIGALRM はもう消費済みである。
 * その結果、pause() はずっと戻ってこない可能性がある。
 *
 * これが race condition である。
 *
 * 図にするとこうである。
 *
 *   期待する順序:
 *
 *     alarm(seconds) ----> pause() ----> SIGALRM 到着 ----> 戻る
 *
 *   問題の順序:
 *
 *     alarm(seconds) ----> SIGALRM 到着 ----> pause() ----> 永久待機の可能性
 *
 * このコードは、その「危険な順序」が理論上ありうるため、
 * 正しい sleep 実装としては不完全である。
 */

/*
 * alarm_handler(int sig)
 *
 * これは SIGALRM 用のシグナルハンドラである。
 *
 * alarm(seconds) を呼ぶと、
 * seconds 秒後にプロセスへ SIGALRM が送られる。
 *
 * その SIGALRM を受けたときに呼ばれるのがこの関数である。
 *
 * 今回のハンドラは中身が空であり、何もしていない。
 * しかし「何もしない」のではなく、
 *
 *   「SIGALRM を受け取って、pause() を解除させるためだけに存在する」
 *
 * と考えるとよい。
 *
 * つまりこの関数の役割は、
 *
 *   何か処理をすること
 *
 * ではなく、
 *
 *   シグナルをデフォルト動作（通常は終了）で殺さずに受け止めること
 *
 * にある。
 */
void alarm_handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値。
     *
     * 今回は signal(SIGALRM, alarm_handler) で登録しているので、
     * 通常は sig == SIGALRM である。
     *
     * ただしこのサンプルでは値そのものは使っていない。
     */
    (void)sig;

    /*
     * 本体は空である。
     *
     * それでも意味がある理由:
     *
     *   - SIGALRM を捕捉できる
     *   - pause() がこのシグナルで中断されて戻る
     *   - デフォルトの「プロセス終了」を避けられる
     *
     * つまりこの空ハンドラは、
     *
     *   「SIGALRM が来た」というイベントだけを受け止める
     *
     * 最小ハンドラである。
     */
}

/*
 * my_sleep(int seconds)
 *
 * これは sleep(seconds) のような動作を自作しようとしている関数である。
 *
 * 発想としては単純で、
 *
 *   1. seconds 秒後に SIGALRM を予約する
 *   2. そのシグナルが届くまで pause() で眠る
 *
 * というアルゴリズムである。
 *
 * しかしこの実装には race condition があるため、
 * 教材としては重要だが、安全な sleep 実装としては不十分である。
 */
void my_sleep(int seconds)
{
    /*
     * signal(SIGALRM, alarm_handler);
     *
     * ここで SIGALRM が届いたときの処理を alarm_handler に設定する。
     *
     * これをしない場合、
     * alarm(seconds) で SIGALRM が届くと通常はデフォルト動作になってしまう
     * （環境依存だが、終了扱いとして考える教材が多い）。
     *
     * このコードでは
     *
     *   「シグナルで起こしてほしいだけ」
     *
     * なので、空ハンドラを登録して
     *
     *   「SIGALRM が来たらただ戻る」
     *
     * 形を作っている。
     *
     * ただしここでも signal() は簡易 API であり、
     * より厳密には sigaction() の方が望ましい場面が多い。
     */
    signal(SIGALRM, alarm_handler);

    /*
     * alarm(seconds);
     *
     * ここで seconds 秒後に SIGALRM を送る予約をする。
     *
     * 重要なのは、
     *
     *   alarm(seconds) 自体は待たない
     *
     * という点である。
     *
     * この関数はすぐ戻り、
     * カーネル側に
     *
     *   「seconds 秒後に SIGALRM をこのプロセスへ送る」
     *
     * という予約だけを残す。
     *
     * たとえば seconds = 3 なら、
     * 3秒後に SIGALRM が来る予定になる。
     */
    alarm(seconds);

    /*
     * pause();
     *
     * ここで「何らかのシグナルが届くまで待つ」。
     *
     * このコードの期待は、
     *
     *   seconds 秒後に届く SIGALRM で pause() が解除される
     *
     * ことである。
     *
     * つまり理想的にはこの行は
     *
     *   「alarm で予約した SIGALRM が来るまで眠る」
     *
     * 動きになる。
     *
     * ----------------------------------------
     * 競合状態（race condition）の本体
     * ----------------------------------------
     *
     * しかし問題は、
     *
     *   alarm(seconds);
     *   pause();
     *
     * が原子的（atomic）ではないことにある。
     *
     * 2つの呼び出しは別々なので、その間にはごく短いが確実に時間差がある。
     *
     * もしその時間差の間に SIGALRM が届くと、
     *
     *   1. alarm の予約は発火済み
     *   2. alarm_handler は実行済み
     *   3. しかしまだ pause は呼ばれていない
     *
     * という状態になる。
     *
     * その後 pause() を呼ぶと、
     * pause は
     *
     *   「次のシグナルが来るまで待つ」
     *
     * ので、すでに消費してしまった SIGALRM では起きない。
     *
     * その結果、
     *
     *   永久に戻ってこない
     *
     * 危険がある。
     *
     * 図にするとこうである。
     *
     *   正常ケース:
     *
     *     signal設定
     *       |
     *       v
     *     alarm(3)
     *       |
     *       v
     *     pause() で待つ
     *       |
     *       v
     *     3秒後 SIGALRM
     *       |
     *       v
     *     handler
     *       |
     *       v
     *     pause解除
     *
     *   競合ケース:
     *
     *     signal設定
     *       |
     *       v
     *     alarm(3)
     *       |
     *       +----> ここで SIGALRM 到着してしまう
     *       |         |
     *       |         v
     *       |      handler 実行済み
     *       |
     *       v
     *     pause()
     *       |
     *       v
     *     もう SIGALRM は来ない -> ずっと待つ可能性
     *
     * これが race condition である。
     *
     * ----------------------------------------
     * 何が競合しているのか
     * ----------------------------------------
     *
     * 競合している対象は、
     *
     *   - 「SIGALRM の到着タイミング」
     *   - 「pause() に入るタイミング」
     *
     * である。
     *
     * この2つの順序が保証されていないため、
     * 正しく動くこともあれば、まれに壊れることもある。
     *
     * こういう
     *
     *   「イベントの順序次第で正しさが変わるバグ」
     *
     * を競合状態という。
     *
     * ----------------------------------------
     * なぜ pause() が悪いわけではないのか
     * ----------------------------------------
     *
     * 問題は pause() 単体ではなく、
     *
     *   alarm() でシグナルを予約してから pause() に入るまでの間に
     *   シグナルが先に来てしまう
     *
     * という設計である。
     *
     * つまりこれは
     *
     *   「待つ前にシグナルが来る可能性を考慮していない」
     *
     * ことが本質的欠陥である。
     */
    pause();

    /*
     * ここまで来れば、
     * 少なくとも何らかのシグナルによって pause() は解除された。
     *
     * このサンプルでは戻り値や errno は見ていない。
     * 教材としては
     *
     *   「pause がシグナルで解除される」
     *
     * ことと、
     *
     *   「その前後に競合状態がある」
     *
     * ことを理解するのが重要だからである。
     */
}

int main(void)
{
    /*
     * printf("hello(1)\n");
     *
     * my_sleep() に入る前の印として "hello(1)" を表示する。
     *
     * これにより実行時には、
     *
     *   まずここは確実に出る
     *
     * ことが分かる。
     */
    printf("hello(1)\n");

    /*
     * my_sleep(3);
     *
     * 3秒待つつもりで自作 sleep を呼ぶ。
     *
     * 期待する意味:
     *
     *   「3秒後に戻ってくる」
     *
     * しかし実装上は前述の競合状態があるので、
     * 理論的には
     *
     *   - 正常に3秒後に戻ることもある
     *   - まれに SIGALRM を取り逃して永久待ちになることもある
     *
     * という危険がある。
     *
     * この関数呼び出し1つに、
     *
     *   「一見正しそうだが、実は競合状態を含む」
     *
     * という教材的な価値が詰まっている。
     */
    my_sleep(3);

    /*
     * printf("hello(2)\n");
     *
     * my_sleep() が無事に戻ってきたことを示す印である。
     *
     * 正常ケースでは実行結果は概念的に
     *
     *   hello(1)
     *   （3秒待つ）
     *   hello(2)
     *
     * となる。
     *
     * しかし競合ケースでは my_sleep() が戻らず、
     * この行が永遠に実行されない可能性がある。
     *
     * つまり "hello(2)" は、
     *
     *   「競合が起きなかった場合だけ見える出力」
     *
     * だと考えると分かりやすい。
     */
    printf("hello(2)\n");

    /*
     * main はここで終了する。
     *
     * つまりこのプログラムは、
     *
     *   正常なら hello(1) -> 待つ -> hello(2) で終わる
     *
     * が、
     *
     *   競合状態が表面化すると hello(2) まで行けずに止まる可能性がある
     *
     * 例である。
     */
}
