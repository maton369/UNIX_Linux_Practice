#include <signal.h>     /* sigset_t, sigemptyset(), sigaddset(), signal(),
                           sigprocmask(), sigsuspend(), SIGALRM を使うためのヘッダ */
#include <stdio.h>      /* printf() を使うためのヘッダ */
#include <unistd.h>     /* alarm() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「alarm() と pause() を単純に組み合わせた自作 sleep に潜む競合状態を、
 *    シグナルマスクと sigsuspend() を使って解決する」
 *
 * ことである。
 *
 * 前の危険な実装は概念的にはこうであった。
 *
 *   signal(SIGALRM, alarm_handler);
 *   alarm(seconds);
 *   pause();
 *
 * 一見これで
 *
 *   「seconds 秒後の SIGALRM が来るまで待つ」
 *
 * ように見えるが、
 *
 *   alarm() のあと pause() に入るまでの隙間
 *
 * で SIGALRM が先に届くと、
 * そのあと pause() は「次のシグナル」まで待ってしまい、
 * 予定していた SIGALRM はもう消費済みなので永久待ちになる可能性があった。
 *
 * これが race condition（競合状態）である。
 *
 * このコードではその問題を、
 *
 *   1. まず SIGALRM をブロックする
 *   2. そのブロック状態のまま alarm() を設定する
 *   3. sigsuspend() で「一時的に元のマスクへ戻しつつ待つ」
 *
 * という手順で解決している。
 *
 * つまりこのコードは、
 *
 *   「SIGALRM が届く前に待機準備を完全に整え、
 *    そのあと原子的に待機へ入る」
 *
 * 実装である。
 *
 * ----------------------------------------
 * まず結論
 * ----------------------------------------
 *
 * このコードの本質は、
 *
 *   sigprocmask() + sigsuspend()
 *
 * の組み合わせにある。
 *
 * これにより
 *
 *   「シグナルをブロックした状態で準備をして、
 *    待つ瞬間だけ安全にブロック解除する」
 *
 * ことができる。
 *
 * これが競合状態の解決策である。
 */

/*
 * alarm_handler(int sig)
 *
 * SIGALRM 用のシグナルハンドラである。
 *
 * このハンドラは中身が空である。
 * しかし意味がないわけではない。
 *
 * このコードで必要なのは、
 *
 *   「SIGALRM が届いたら、デフォルト動作で終了するのではなく、
 *    ただシグナルを受けて待機を解除すること」
 *
 * である。
 *
 * したがって、空ハンドラでも十分役割を果たす。
 *
 * つまりこの関数の目的は
 *
 *   「何かを処理すること」
 *
 * ではなく、
 *
 *   「SIGALRM をイベントとして受け止めること」
 *
 * にある。
 */
void alarm_handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値。
     *
     * 今回は signal(SIGALRM, alarm_handler) で登録しているため、
     * 通常は sig == SIGALRM である。
     *
     * このコードでは sig の値そのものは使っていない。
     */
    (void)sig;

    /*
     * 本体は空である。
     *
     * それでも意味がある理由:
     *
     *   - SIGALRM が届いたこと自体で sigsuspend() は解除される
     *   - デフォルト動作を避けられる
     *   - 「ただ起こしてくれればよい」用途には十分
     *
     * したがってこの空ハンドラは、
     *
     *   「起床イベントとしての SIGALRM の受け皿」
     *
     * だと考えるとよい。
     */
}

/*
 * my_sleep2(int seconds)
 *
 * これは競合状態を避けて安全に待機するための、
 * 改善版自作 sleep である。
 *
 * アルゴリズムの要点は次の通りである。
 *
 *   1. SIGALRM をブロック対象にしたシグナル集合を作る
 *   2. SIGALRM ハンドラを登録する
 *   3. SIGALRM をブロックする
 *   4. その状態で alarm(seconds) を設定する
 *   5. sigsuspend(&old_sigset) で「元のマスクに一時的に戻して待つ」
 *   6. SIGALRM が届いたら待機解除
 *   7. 最後にシグナルマスクを元へ戻す
 *
 * この順序により、
 *
 *   「alarm() のあと待機に入る前に SIGALRM が来てしまう」
 *
 * という race condition を防いでいる。
 */
void my_sleep2(int seconds)
{
    /*
     * new_sigset:
     *   今回新たに作るシグナル集合である。
     *
     *   この集合には SIGALRM を入れる。
     *   つまり「SIGALRM をブロックしたい」という要求を表す集合になる。
     *
     * old_sigset:
     *   sigprocmask() 呼び出し前の、元のシグナルマスクを保存する変数である。
     *
     *   あとで sigsuspend() や復元処理に使う。
     *
     * この old_sigset が非常に重要で、
     *   「もともとどういうブロック状態だったか」
     * を保存しておくことで、
     *   待機中だけ一時的に適切な状態へ切り替え、
     *   最後に元へ戻せる。
     */
    sigset_t new_sigset, old_sigset;

    /*
     * sigemptyset(&new_sigset);
     *
     * new_sigset を空集合に初期化する。
     *
     * シグナル集合は必ず初期化してから使う。
     * ここでは「まず何も入っていない集合」を作る。
     */
    sigemptyset(&new_sigset);

    /*
     * sigaddset(&new_sigset, SIGALRM);
     *
     * 空集合だった new_sigset に SIGALRM を追加する。
     *
     * これにより new_sigset は
     *
     *   { SIGALRM }
     *
     * を表す集合になる。
     *
     * つまりこの行の意味は、
     *
     *   「SIGALRM をブロック対象にしたい」
     *
     * という指定である。
     */
    sigaddset(&new_sigset, SIGALRM);

    /*
     * signal(SIGALRM, alarm_handler);
     *
     * ここで SIGALRM が届いたときのハンドラを登録する。
     *
     * これにより、alarm(seconds) によって SIGALRM が届いても
     * デフォルト動作ではなく alarm_handler() が実行される。
     *
     * このコードの目的は
     *
     *   「SIGALRM を契機に待機解除する」
     *
     * ことであり、シグナルを受けた瞬間に終了したいわけではない。
     *
     * そのため、このハンドラ登録が必要になる。
     *
     * 注意:
     *   実務では signal() より sigaction() の方が望ましい場合が多いが、
     *   ここでは競合状態解決の本質が
     *
     *     sigprocmask() + sigsuspend()
     *
     * にあるため、signal() で簡潔に書いている。
     */
    signal(SIGALRM, alarm_handler);

    /*
     * sigprocmask(SIG_BLOCK, &new_sigset, &old_sigset);
     *
     * ここが競合状態解決の第一の核心である。
     *
     * sigprocmask() は、現在プロセスのシグナルマスク
     * （どのシグナルを一時的にブロックするか）を操作する関数である。
     *
     * 引数の意味:
     *
     *   第1引数 SIG_BLOCK:
     *     指定集合に含まれるシグナルを「追加でブロックする」
     *
     *   第2引数 &new_sigset:
     *     今回は {SIGALRM}
     *
     *   第3引数 &old_sigset:
     *     変更前のマスクを保存する場所
     *
     * この1行により、
     *
     *   「以後、SIGALRM が届いてもすぐには処理されず、
     *    保留状態（pending）にできる」
     *
     * という状態が作られる。
     *
     * ----------------------------------------
     * なぜ先にブロックするのか
     * ----------------------------------------
     *
     * 競合状態の原因は、
     *
     *   alarm() 設定後、待機に入る前に SIGALRM が届いてしまう
     *
     * ことだった。
     *
     * しかしここで先に SIGALRM をブロックしておけば、
     * たとえ alarm() の直後に SIGALRM 発生条件が満たされても、
     * そのシグナルはすぐには処理されず pending になる。
     *
     * つまり、
     *
     *   「早く届きすぎる」ことが問題にならなくなる
     *
     * のである。
     *
     * 図にするとこうである。
     *
     *   旧実装:
     *     alarm() ----> （隙間） ----> pause()
     *                  ↑
     *              ここで来ると壊れる
     *
     *   新実装:
     *     SIGALRM を先にブロック
     *         |
     *         v
     *     alarm()
     *         |
     *         v
     *     来ても pending になるだけ
     *
     * つまりこの行は、
     *
     *   「SIGALRM を安全に遅延配送させる準備」
     *
     * をしている。
     */
    sigprocmask(SIG_BLOCK, &new_sigset, &old_sigset);

    /*
     * alarm(seconds);
     *
     * ここで seconds 秒後に SIGALRM を送る予約を行う。
     *
     * ただし今はすでに SIGALRM をブロック中である。
     * そのため、もし SIGALRM が来ても即ハンドラ実行にはならず、
     * pending 状態になる。
     *
     * ここが旧実装と大きく違う点である。
     *
     * 旧実装では
     *
     *   alarm() と pause() の間に来た SIGALRM を取り逃した
     *
     * が、
     * 今回は SIGALRM がブロックされているので、
     *
     *   来ても消えずに pending として残る
     *
     * ので、待機準備が完了する前にシグナルが失われることがない。
     */
    alarm(seconds);

    /*
     * sigsuspend(&old_sigset);
     *
     * ここが競合状態解決の第二の核心である。
     *
     * sigsuspend(mask) は、
     *
     *   「シグナルマスクを一時的に mask に差し替えたうえで待機し、
     *    シグナルを受けたら元のマスクへ戻して復帰する」
     *
     * 関数である。
     *
     * 今回は &old_sigset を渡している。
     * つまり
     *
     *   「もともとのシグナルマスクに一時的に戻して待つ」
     *
     * という意味になる。
     *
     * ここが非常に重要である。
     *
     * ----------------------------------------
     * なぜ old_sigset を渡すのか
     * ----------------------------------------
     *
     * 直前まで SIGALRM はブロックされていた。
     * しかし待機するときには、
     *
     *   「SIGALRM が届いたら起きたい」
     *
     * ので、待っている間だけはブロックを外さなければならない。
     *
     * ただし単純に
     *
     *   sigprocmask(SIG_SETMASK, &old_sigset, NULL);
     *   pause();
     *
     * とすると、また
     *
     *   「ブロック解除してから pause するまでの隙間」
     *
     * ができてしまい、race condition が再発する。
     *
     * そこで sigsuspend() を使う。
     *
     * sigsuspend() は
     *
     *   「マスク切り替え」と「待機開始」
     *
     * を実質的に一体の操作として行うため、
     * この隙間が無い。
     *
     * 図にするとこうである。
     *
     *   危険:
     *
     *     ブロック解除 ----> （隙間） ----> pause()
     *
     *   安全:
     *
     *     sigsuspend(old_sigset)
     *       = 「ブロック解除しつつ即待つ」
     *
     * つまり sigsuspend() は、
     *
     *   「待つ直前にシグナルを取り逃さないための原子的待機」
     *
     * を実現する関数である。
     *
     * ----------------------------------------
     * pending な SIGALRM があった場合
     * ----------------------------------------
     *
     * もし alarm() のあとで SIGALRM がすでに来ていて pending になっていたなら、
     * sigsuspend(&old_sigset) によって一時的に SIGALRM のブロックが外れた瞬間に、
     * その pending シグナルが配送される。
     *
     * すると alarm_handler() が呼ばれ、
     * sigsuspend() は解除されて戻る。
     *
     * つまり、
     *
     *   「先に来てしまっていた SIGALRM も、ちゃんと待機解除に使える」
     *
     * ようになる。
     *
     * これが競合状態解決の本質である。
     *
     * ----------------------------------------
     * 戻り値について
     * ----------------------------------------
     *
     * sigsuspend() は通常、シグナルで中断されて -1 を返し、
     * errno は EINTR になる。
     *
     * このサンプルでは戻り値は見ていない。
     * 学習上は
     *
     *   「期待したシグナルで目覚める」
     *
     * ことが重要だからである。
     */
    sigsuspend(&old_sigset);

    /*
     * sigprocmask(SIG_SETMASK, &old_sigset, NULL);
     *
     * sigsuspend() から戻ったあと、シグナルマスクを明示的に元へ戻す。
     *
     * 第1引数 SIG_SETMASK は、
     *
     *   「現在のマスクを指定された集合で置き換える」
     *
     * という意味である。
     *
     * ここで old_sigset を渡すことで、
     *
     *   my_sleep2() 呼び出し前の元のシグナルマスク状態
     *
     * に完全に戻せる。
     *
     * これは非常に大切で、
     * 一時的な待機の都合で変えた SIGALRM ブロック状態を、
     * 関数の外へ漏らさないためである。
     *
     * つまりこの行は、
     *
     *   「関数内部でいじったシグナルマスクを、最後にきちんと元へ戻す」
     *
     * 後始末である。
     *
     * これにより my_sleep2() は、
     *
     *   「外側のシグナルマスク環境を壊さない」
     *
     * ようになっている。
     */
    sigprocmask(SIG_SETMASK, &old_sigset, NULL);

    /*
     * ここまで来れば、安全に待機が完了したことになる。
     *
     * つまりこの関数は、
     *
     *   1. SIGALRM をブロック
     *   2. alarm を予約
     *   3. sigsuspend で原子的に待つ
     *   4. マスクを元へ戻す
     *
     * ことで、旧実装の race condition を解決している。
     */
}

int main(void)
{
    /*
     * printf("hello(1)\n");
     *
     * my_sleep2() に入る前の印として "hello(1)" を表示する。
     *
     * これにより実行結果として、
     *
     *   まずここは確実に出る
     *
     * ことが分かる。
     */
    printf("hello(1)\n");

    /*
     * my_sleep2(3);
     *
     * 改善版の自作 sleep を 3 秒で呼ぶ。
     *
     * 旧実装の my_sleep() と違い、
     * この関数は SIGALRM の到着と待機開始の競合を
     * sigprocmask() + sigsuspend() によって防いでいる。
     *
     * したがって意図としては、
     *
     *   「安全に約3秒待って戻る」
     *
     * 動作になる。
     *
     * ここがこのプログラム全体の検証ポイントであり、
     *
     *   前の危険な実装の修正版
     *
     * になっている。
     */
    my_sleep2(3);

    /*
     * printf("hello(2)\n");
     *
     * my_sleep2() が無事に戻ってきたことを示す印である。
     *
     * 実行結果としては概念的に
     *
     *   hello(1)
     *   （約3秒待つ）
     *   hello(2)
     *
     * のようになる。
     *
     * 旧実装では race condition により、
     * まれにここへ到達できず永久待ちになる可能性があった。
     *
     * この改善版ではその危険を避けることが目的であり、
     * "hello(2)" は
     *
     *   「安全に待機解除できた」
     *
     * ことを示す観察用出力である。
     */
    printf("hello(2)\n");

    /*
     * main はここで終了する。
     *
     * つまりこのプログラムは、
     *
     *   race condition を含む my_sleep() を
     *   シグナルマスクと sigsuspend() によって直した
     *
     * 例である。
     *
     * 本質は、
     *
     *   「ブロック解除」と「待機開始」を分離すると危険なので、
     *    sigsuspend() によって原子的に待機へ入る」
     *
     * ことにある。
     */
}
