#include <signal.h>     /* signal(), SIGINT を使うためのヘッダ */
#include <stdio.h>      /* fprintf(), stderr を使うためのヘッダ */
#include <unistd.h>     /* sleep() を使うためのヘッダ */

/*
 * このコードの主題は、
 *
 *   「SIGINT（典型的には Ctrl+C）を受け取ったときに、
 *    シグナルハンドラが呼ばれること」
 *
 * と、
 *
 *   「そのハンドラの中で signal(SIGINT, handler) をもう一度呼んでいること」
 *
 * である。
 *
 * つまりこれは単なる
 *
 *   "Ctrl+C を押したら @ を出す"
 *
 * という例ではなく、
 *
 *   「シグナルハンドラとは何か」
 *   「ハンドラ登録がどのように働くか」
 *   「なぜハンドラの中で再登録しているのか」
 *
 * を観察するための教材である。
 *
 * ----------------------------------------
 * このプログラム全体の流れ
 * ----------------------------------------
 *
 * 1. main() で signal(SIGINT, handler) を呼ぶ
 *    -> SIGINT が来たら handler() を呼ぶ設定になる
 *
 * 2. while(1) で無限ループに入り、
 *    sleep(1) のあとに "." を出し続ける
 *
 * 3. ユーザが Ctrl+C を押す
 *    -> 端末からこのプロセスへ SIGINT が送られる
 *
 * 4. すると通常の処理の途中に割り込む形で handler() が呼ばれる
 *
 * 5. handler() は "@" を出力する
 *
 * 6. その後、また main() 側の処理に戻り、"." の出力が続く
 *
 * したがって実行中の見た目は概念的には、
 *
 *   ....@..@....
 *
 * のようになることがある。
 *
 * ここで
 *
 *   "." = 通常のループ処理
 *   "@" = SIGINT による割り込み処理
 *
 * を表している。
 */

/*
 * handler(int sig)
 *
 * これはシグナルハンドラである。
 *
 * シグナルハンドラとは、
 *
 *   「特定のシグナルが届いたときに OS から呼ばれる関数」
 *
 * である。
 *
 * 普通の関数呼び出しと違って、
 * プログラム本体の流れ（ここでは while ループ）の都合とは関係なく、
 * 非同期に割り込む形で実行される。
 *
 * つまり main() が
 *
 *   - sleep() 中
 *   - fprintf() の前後
 *   - while ループ中
 *
 * のどこにいようと、SIGINT が届いた瞬間に
 * handler() が呼ばれる可能性がある。
 *
 * この意味でシグナルハンドラは、
 * 「通常の制御フローの外から飛んでくるイベント処理」
 * と考えると分かりやすい。
 */
void handler(int sig)
{
    /*
     * sig:
     *   どのシグナルでこのハンドラが呼ばれたかを表す整数値。
     *
     * 今回は signal(SIGINT, handler) で登録しているので、
     * 通常は sig == SIGINT である。
     *
     * このコードでは sig の値を実際には使っていない。
     * ただし、同じハンドラを複数シグナルに使う設計なら、
     *   if (sig == SIGINT) ...
     * のように中で分岐することもある。
     */
    (void)sig;  /* 今回は未使用なので、未使用警告を避けるため明示している */

    /*
     * signal(SIGINT, handler);
     *
     * ここがこのコードで最も重要な部分の1つである。
     *
     * これは
 *
     *   「SIGINT が来たときの処理を、もう一度 handler に設定する」
     *
     * という意味である。
     *
     * ----------------------------------------
     * なぜハンドラの中で再登録しているのか
     * ----------------------------------------
     *
     * 歴史的に signal() の挙動には処理系差異があり、
     * 一部の古い流儀では
     *
     *   シグナルを1回受けると、そのハンドラ設定が
     *   デフォルト動作に戻ってしまう
     *
     * という実装が存在した。
     *
     * そのような環境では、
     * 最初の Ctrl+C では handler() が呼ばれても、
     * 2回目の Ctrl+C ではもう handler() ではなく
     * デフォルト動作（通常は終了）になってしまう可能性がある。
     *
     * そこで古典的な書き方として、
     *
     *   handler() の中でもう一度 signal(SIGINT, handler) を呼ぶ
     *
     * という再登録パターンが使われることがある。
     *
     * つまりこの1行の意図は、
     *
     *   「次の SIGINT に対しても引き続き handler を使いたい」
     *
     * というものである。
     *
     * ----------------------------------------
     * 現代的な観点
     * ----------------------------------------
     *
     * 現在の POSIX 系環境では、
     * signal() の挙動は昔より安定していることが多いが、
     * それでも厳密で堅牢なシグナル制御には
     *
     *   sigaction()
     *
     * を使うのが一般的である。
     *
     * したがってこの
     *
     *   signal(SIGINT, handler);
     *
     * の再登録は、
     * 「シグナルハンドラの古典的な書き方を学ぶ」
     * という意味合いが強い。
     *
     * ----------------------------------------
     * 注意点
     * ----------------------------------------
     *
     * signal() 自体をハンドラの中で呼ぶ書き方は、
     * 教材としては意味があるが、
     * 実務で最も推奨される形とは限らない。
     *
     * 実務では通常、
     *   - sigaction() を使う
     *   - ハンドラでは最小限の処理しかしない
     * という方針が多い。
     */
    signal(SIGINT, handler);

    /*
     * fprintf(stderr, "@");
     *
     * SIGINT を受け取ったことを分かりやすく観察するために、
     * 標準エラー出力へ "@" を出している。
     *
     * 普段のループは "." を出し続けるので、
     * Ctrl+C を押すと "@" が割り込んで見える。
     *
     * たとえば
     *
     *   ....@..@...
     *
     * のようになれば、
     *
     *   "."  = 通常ループ
     *   "@"  = シグナルハンドラ
     *
     * という区別が非常に分かりやすい。
     *
     * ----------------------------------------
     * なぜ stderr を使うのか
     * ----------------------------------------
     *
     * stderr は通常、stdout よりもバッファリングの影響を受けにくく、
     * その場で表示されやすい。
     *
     * そのため、シグナルが「今まさに来た」ことを観察する用途に向いている。
     *
     * ----------------------------------------
     * 重要な注意: シグナルハンドラ内での関数呼び出し
     * ----------------------------------------
     *
     * シグナルハンドラの中で安全に使える関数は限られている。
     * これを async-signal-safe という。
     *
     * fprintf() は一般に async-signal-safe とはみなされない。
     * したがって、厳密な実務コードでは
     *
     *   write(2, "@", 1);
     *
     * のような、より安全な低水準出力が好まれる。
     *
     * ここでは「ハンドラが呼ばれたことを分かりやすく見せる」
     * ことが目的なので、教材として fprintf() を使っていると理解するとよい。
     */
    fprintf(stderr, "@");
}

int main(void)
{
    /*
     * signal(SIGINT, handler);
     *
     * ここで、
     *
     *   「SIGINT が届いたら handler() を呼ぶ」
     *
     * という設定を行う。
     *
     * SIGINT は、端末上では典型的に Ctrl+C に対応するシグナルである。
     *
     * 何も設定しなければ、SIGINT のデフォルト動作は通常
     *
     *   プロセス終了
     *
     * である。
     *
     * しかしこの行を入れることで、
     *
     *   Ctrl+C -> 終了
     *
     * ではなく、
     *
     *   Ctrl+C -> handler() 実行
     *
     * となる。
     *
     * 図にするとこうである。
     *
     *   設定前:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     デフォルト動作 = 終了
     *
     *   設定後:
     *     Ctrl+C
     *       |
     *       v
     *     SIGINT
     *       |
     *       v
     *     handler()
     *
     * つまりこの1行が、
     * このプログラム全体の意味を変えている。
     */
    signal(SIGINT, handler);

    /*
     * while (1)
     *
     * 無限ループである。
     *
     * このループ自体には終了条件がない。
     * したがって通常の制御フローだけを見ると、
     * このプログラムは永遠に "." を出し続ける。
     *
     * しかし実際には Ctrl+C による SIGINT が飛んでくると、
     * そのたびに handler() が割り込んで呼ばれる。
     *
     * ここで学べる重要な点は、
     *
     *   「メインループはそのまま続いていても、
     *    シグナルハンドラは非同期に差し込まれる」
     *
     * ということである。
     */
    while (1) {
        /*
         * sleep(1);
         *
         * 1 秒スリープする。
         *
         * これにより "." の表示が 1 秒ごとになり、
         * 通常動作とシグナル割り込みの様子を観察しやすくしている。
         *
         * もし sleep() が無ければ "." が高速に出すぎて、
         * "@" の割り込みが見えにくくなる。
         *
         * ----------------------------------------
         * シグナルと sleep()
         * ----------------------------------------
         *
         * sleep() はシグナルで中断されることがある。
         *
         * つまり、sleep 中に Ctrl+C を押すと、
         *   1. SIGINT が届く
         *   2. handler() が呼ばれる
         *   3. sleep() が途中で戻る可能性がある
         *
         * このコードでは sleep() の戻り値を見ていないため、
         * 中断されたかどうかは特に処理していない。
         *
         * ただし学習上は、
         *
         *   「シグナルは計算中だけでなく sleep 中にも割り込む」
         *
         * という点が大切である。
         */
        sleep(1);

        /*
         * fprintf(stderr, ".");
         *
         * 通常動作の可視化として "." を 1 文字出す。
         *
         * これにより、
         *
         *   1秒ごとにプログラムが動いている
         *
         * ことが視覚的に分かる。
         *
         * また stderr を使っているので、
         * バッファリングで見えにくくなる可能性が比較的低い。
         *
         * 実行中の見た目はたとえば
         *
         *   ....@...@....
         *
         * のようになりうる。
         *
         * ここで
         *   "." = main ループの通常処理
         *   "@" = シグナルハンドラによる割り込み
         *
         * を意味する。
         */
        fprintf(stderr, ".");
    }

    /*
     * このコードは通常ここへ到達しない。
     *
     * 理由:
     *   while(1) が無限ループだからである。
     *
     * また、SIGINT を受けてもデフォルト終了ではなく
     * handler() を呼ぶようにしているため、
     * Ctrl+C を押してもそのまま継続する。
     *
     * つまりこのプログラムは、
     *
     *   「シグナルを捕まえても終了せず、反応しながら動き続ける例」
     *
     * として振る舞う。
     */
}
